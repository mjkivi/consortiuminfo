			<h5>Boston Law Firm And Technology Groups Team<br />Up To Fight Landmark Decision That Could<br />Undermine Hundreds Of Tech Standards</h5>
			<h6><em>"Friend of the Court" Brief Filed on Behalf of Six Major Standard Setting Organizations Seeks Rehearing of Rambus v. Infineon Appeal</em></h6>
			<p><strong>Boston, MA (March 12, 2003)</strong> &mdash; Boston-based law firm <a href="http://www.gesmer.com">Gesmer Updegrove LLP</a> today announced that it had filed a "friend of the court" brief on behalf of six major standard setting organizations (SDOs) in an effort to influence the outcome of Rambus v. Infineon, one of the most closely watched cases in the technology industry. The organizations supporting the brief boast more than 850 members, including the great majority of the most prominent technology companies in the world.</p>
			<p>Rambus v. Infineon has been followed with particular interest by the thousands of companies that participate in technical standard setting, because the trial court had concluded that Rambus, at the time a member of the Joint Electron Devices Engineering Council (JEDEC), had committed fraud by failing to disclose patent rights which it later asserted against adopters of a JEDEC memory standard. Infineon was one such adopter, and when it refused to pay royalties, Rambus sued.</p>
			<p>Infineon brought counterclaims, including one which alleged that Rambus had not only deliberately failed to disclose its patents, but that it had amended those patents to track the JEDEC standard as it developed. The trial court believed Infineon, and awarded it more than $7 million in costs and damages, based on the behavior of Rambus before and during the course of the litigation.</p>
			<p>However, on appeal, a three-judge panel of the United States Court of Appeals of the Federal Circuit &mdash; which has jurisdiction over all appeals from U.S. patent cases &mdash; held in a non-unanimous opinion on January 29, 2003, that Rambus had not committed fraud. On February 26, Infineon filed a motion petitioning the entire court to rehear the appeal.</p>
			<p>"When the Appeals Court's decision was announced, many were shocked, since the trial record seemed to indicate that even Rambus had believed that it had 'gamed' the system," said Andrew Updegrove, a partner with Gesmer Updegrove, who wrote the brief.</p>
			<p>"Moreover, the narrow analysis that the court applied in reviewing the infringement claims leads some to fear that participating in standard setting will now be more uncertain and difficult. Due to the many standard setting clients which we represent, we thought that it was important for the Court to better understand the consequences of its decision."</p>
			<p>The brief filed by Updegrove's law firm seeks to educate the court on the impact that the existing decision could have on the entire standard setting process.</p>
			<p>In order to demonstrate the seriousness of the situation, Updegrove's firm contacted its clients and other prominent SDOs, and assembled the group in less than a week. "We thought that the issues at hand were important enough that we decided to do this work on a pro bono basis," he said. "This made it much easier to pull such a comprehensive group together in a short time frame."</p>
			<p>David Schell, the President of Open GIS Consortium, one of the participating SDOs, stated, "It's difficult to underestimate the importance of standards in the modern world &mdash; they underlay all aspects of technology. Since standard setting is an entirely voluntary process, it's essential that the integrity of the system not be compromised, and that those who base their strategic decisions on the resulting standards know that what they agree on will be upheld by the courts. What is so destructive about the decision to overturn Rambus is that the voluntary standards process has become the de facto norm for creation of orderly technology markets. Challenging the basic premises of trust and fair play goes far beyond the righting of a dispute between two companies &mdash; it subverts the practical market assumptions that enable the IT industry to continue to function authentically.</p>
			<p>"Gesmer Updegrove, in filing its brief, has provided the technology community with a valuable service. Legal practice, particularly in the area of intellectual property rights, needs to evolve in step with the realities of rapid technology change and the consequent evolution of modern business practice." The 230 member Open GIS Consortium is based in Wayland, Mass.</p>
			<p>The six participating SDOs include IMS Global Learning Consortium, Inc. (<a href="http://www.imsglobal.org">http://www.imsglobal.org</a>/), OpenGIS Consortium (<a href="http://www.opengis.org">http://www.opengis.org</a>/), PCI Industrial Computer Manufacturers Group (<a href="http://www.picmg.org">http://www.picmg.org</a>/), and The Open Group (<a href="http://www.opengroup.org">http://www.opengroup.org</a>).</p>
			<p><em>For more information about the Rambus v. Infineon case, see </em><a href="/essentialguide/laws.php#rambus1">http://www.consortiuminfo.org/essentialguide/laws.php#rambus1</a> and <a href="/bulletins/#editorial">http://www.consortiuminfo.org/bulletins/#editorial</a></p>
