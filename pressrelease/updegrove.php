			<h5>Mass High Tech Grants Award to Andrew Updegrove<br />for Standard Setting Work</h5>
			<h6><em>Eighth Annual Mass High Tech All-Stars<br />Program Recognizes Innovation</em></h6>
			<p><strong>Boston, MA (October 27, 2003)</strong> &mdash; This year's Mass High Tech All-Stars Award in the legal category went to Andrew Updegrove, a founding partner of Gesmer Updegrove LLP (GU), a Boston-based law firm whose practice focuses on high tech clients. Updegrove was honored for his pioneering work in helping enable, launch and represent the organizations that set today's most vital information technology standards. He is also the founder and editor of ConsortiumInfo.org, the most comprehensive source of information on the Internet regarding consortia and standard setting. <a href="/">http://www.consortiuminfo.org/</a></p>
			<p>The Mass High Tech All-Stars Program recognizes New Englanders from all sectors of the technology community who help make the region a center of innovation. The program spotlights the region's most innovative leaders &mdash; those whose achievements are most likely to "affect how we work, live or play today and in the future". Updegrove was given the award for his groundbreaking work in helping create the structures and intellectual property policies upon which today's hundreds of standard setting consortia are based, and is only the fourth attorney to receive this award in the eight-year history of the Mass High Tech All-Stars program. Over the past fifteen years, Updegrove has worked with more standard setting consortia than any other attorney in the country.</p>
			<p>According to Jim Malone, Mass High Tech editor, "In new England's high tech world, the Mass High Tech All-Stars are those people who set the bar that much higher." Added Malone, "It is a pleasure to honor these outstanding members of New England's technology community and Mass High Tech wishes them further success, success that's sure to benefit everyone."</p>
			<p>Updegrove has previously been honored by being named one of the "Best VC Lawyers" in Boston by Digital Industry News. Besides working with standard setting organizations, he is counsel to many hundreds of startup companies and has negotiated hundreds of venture capital deals, angel financings, mergers and acquisitions on their behalf. In 2002, he launched www.consortiuminfo.org, the most detailed resource on the Internet regarding standards, consortia and standard setting, as well as the Consortium Standards Bulletin, http://www.consortiuminfo.org/bulletins/ , which provides monthly news on the same topics. ConsortiumInfo.org is visited by thousands of visitors from around the world, and is regularly used by all of the largest information technology corporations.</p>
			<div class="hr"></div><hr />
			<p><strong>About Gesmer Updegrove LLP</strong><br />Gesmer Updegrove LLP, is a Boston law firm that represents technology clients from the newest startups to Fortune 500 companies, venture capital funds and major universities. The firm's services include representation in the areas of business formation, venture capital and financing, mergers and acquisitions, intellectual property, Internet and electronic commerce, securities law, litigation, consortium and standard setting, employment law, licensing and taxation. The firm has helped form more than 55 standard setting consortia, including some of the largest and most influential such organizations in the world. For more information, see <a href="http://www.gesmer.com/practice_areas/consortium.php">http://www.gesmer.com/practice_areas/consortium.php</a></p>
