			<h5>Consortiuminfo.Org Announces Second Annual<br />News Sources Awards</h5>
			<h6><em>Awards recognize the news sources that best conveyed the role<br />of standards and open source in 2005</em></h6>
			<p><strong>Boston, MA (January 5, 2006)</strong> &mdash; ConsortiumInfo.org, the most comprehensive resource on the Internet for news and information on technology and communication standards, announced the winners of its second annual News Sources Awards. The Awards are made to the news services and journalists that best report on ITC standards and open source software news, and to the standards development organizations that were considered to be most newsworthy in 2005.</p>
			<p>The Awards are made in two categories: statistical and special recognition. The former are based on the number of articles per individual source selected for linking at the ConsortiumInfo.org <a href="/news/">Standards News Portal</a> from the thousands that were reviewed during the course of the year for that purpose, while the latter are based on the perceived value of the news and analysis that individual journalists provided to, and about, the open standards and open source communities.</p>
			<p>In announcing the 2005 Awards, ConsortiumInfo.org founder and Editor Andrew Updegrove stated: "We take great pleasure in bringing this recognition to the news services and individuals that have best conveyed to their readers the vital role that open standards and open source play in the modern world of information and communications technology."</p>
			<p>This year's Awards are as follows:</p>
			<p><strong>Statistical Awards</strong></p>
			<p><strong>1. Most Newsworthy Standard Setting Organization</strong> (Consortium, Accredited Standards Developer or Open Source Project)</p>
			<ul>
				<li><u>First Place</u>: Organization for the Advancement of Structured Information Systems (<a href="http://www.oasis-open.org/home/index.php">OASIS</a>)</li>
				<li><u>Second Place</u>: World Wide Web Consortium (<a href="http://www.w3c.org">W3C</a>)</li>
				<li><u>Third Place</u>: European Telecommunications Standards Institute (<a href="http://www.etsi.org/">ETSI</a>)</li>
			</ul>
			<p><strong>2. Best On-Line Coverage of ICT Standards by an On-Line News Service</strong></p>
			<ul>
				<li><u>First Place</u>: <a href="http://www.eweek.com/">eWeek.com</a></li>
				<li><u>Second Place</u>: <a href="http://www.zdnet.com/">ZDNet.com</a></li>
				<li><u>Third Place</u>: <a href="http://www.cnet.com/">CNET.com</a></li>
			</ul>
			<p><strong>3. Best Coverage of ICT Standards by an On-Line Journalist</strong></p>
			<ul>
				<li><u>First Place</u>: Martin LaMonica &mdash; <a href="http://www.news.com/">CNET.News.com</a></li>
				<li><u>Second Place</u>: Mathew Broersma &mdash; <a href="http://www.eweek.com">eWeek.com</a></li>
