			<p><strong>Best Columnist/Blogger </strong>(Commercial): David Berlind (ZDNet.com)<br /><strong>Best Community Site or Blog (Non-Profit):</strong> Pamela Jones &mdash; <a href="http://www.groklaw.net/">Groklaw.net</a></p>
