                                  <td valign="top"><p align="center" class="style7">Action is Taken In The Matter of Rambus on behalf of Standard Setting Organizations<br>
        representing over 8,600 member entities</p>
                                      <p class="style8"><strong>BOSTON, April 21, 2004 -</strong> Boston-based law firm Gesmer Updegrove LLP today announced that it had filed a &quot;friend of the court&quot; brief with the Federal Trade Commission urging the Commissioners to reverse the decision of an Administrative Law Judge (&quot;ALJ&quot;) earlier this year in favor of Rambus, Inc. The FTC investigation has figured prominently in one of the most closely watched cases in the technology industry. Eleven major standard setting bodies, as well as a standard setting joint venture, are parties to the brief. The combined membership of the standard setting bodies exceeds 8,600, including most major U.S. technology companies, as well as many government agencies, universities, and other entities.</p>
                                      <p class="style8">The brief was filed on behalf of the parties on a &quot;pro bono&quot; -- or fee-free -- basis by Gesmer Updegrove. &quot;The conduct that Rambus is alleged to have engaged in goes to the very heart of the integrity of the standard setting process,&quot; said Andrew Updegrove, a partner at Gesmer Updegrove and the author of the brief. &quot;As we are a national leader in forming and representing the organizations that set standards, we felt that it was our duty to stress the importance of this case to the FTC. Although we believe that the ALJ made other mistaken rulings, those failings will be addressed by the FTC's own Complaint Counsel. The urgent message that we sought to deliver is that admitted efforts to game the system must be punished. Without an enforceable duty to act in good faith, the standard setting process will collapse.&quot; </p>
                                      <p class="style8">The FTC action coincides with the ongoing battles between Rambus and various chip manufacturers. In <em>Rambus v. Infineon</em>, the case that has proceeded the farthest to date, first one side, and then the other, gained the advantage, with Rambus ultimately coming out the winner. Moreover, estimates of the industry-wide royalties at stake run as high as a billion dollars. But the impact of the case goes far beyond just the memory industry. If the FTC does not reverse the ruling of the ALJ, the process that sets the more than 100,000 standards that affect nearly every aspect of daily life in this country will be undermined. </p>
                                      <p class="style8">As noted in the brief: </p>
                                      <p class="style8">&middot; Voluntary standards, especially technology standards, are vital to the national interest, affecting almost all areas of modern life, safety and commerce.<br>
&middot; The Federal government is dependent on such standards: Congress has mandated the use of voluntary consensus standards by the Federal agencies whenever possible. <br>
&middot; The failure by the courts to protect the standard setting process would undercut the American economy and impair our international competitiveness. <br>
        - Without an enforceable duty of good faith, standards process participants would have more to gain by cheating than playing by the rules, and the standard setting process would collapse.</p>
                                      <p class="style8">Rambus will now have six weeks to respond to Complaint Counsel's brief and the briefs of the Friends of the Court. Brief oral arguments before the FTC by counsel to Rambus and the FTC Complaint Counsel are expected to be scheduled for late summer.</p>
                                      <p class="style8">ABOUT THE FTC ACTION AGAINST RAMBUS</p>
                                      <p class="style8">The underlying facts are as follows: Rambus Inc., which develops semiconductor memory technology, participated in the standard setting process of the Joint Electron Devices Engineering Council (JEDEC) in the early 90's. During the design process, Rambus did not disclose that it held patents and patent applications on designs included in the standards. When companies, including Infineon, implemented those standards, Rambus sued for patent infringement. Infineon counterclaimed, citing fraud, and a trial court found Rambus guilty. To the astonishment of almost all, the fraud verdict was overturned by a Federal District Appellate Court in January of 2003. In the meantime, the FTC had brought claims against Rambus as well. A trial based upon those claims took place during the summer of 2003, and the ALJ ruled in favor of Rambus in a 348 page Initial Decision on February 23, 2004. The FTC Complaint Counsel filed an appeal to the Initial Decision on April 16, 2004. The Amicus Brief filed by Gesmer Updegrove is in support of this appeal.</p>
