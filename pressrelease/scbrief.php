			<h5>Boston Law Firm, Industry Groups File Brief<br />With Supreme Court In Case That Could<br />Undercut Technology And Other Industries</h5>
			<h6><em>Brief is filed in Rambus v. Infineon on behalf of Standard Setting<br />Organizations representing over 8,600 member entities</em></h6>
			<p><strong>Boston, MA (August 13, 2003)</strong> &mdash; Boston-based law firm Gesmer Updegrove LLP today announced that it had filed a "friend of the court" brief with the United States Supreme Court in <em>Rambus v. Infineon</em>, one of the most closely watched cases in the technology industry. Ten major standard setting bodies, as well as several leading financial industry corporations and a standard setting joint venture, are parties to the brief. The combined membership of the standard setting bodies exceeds 8,600, including most major U.S. technology companies, as well as many government agencies, universities, and other entities.</p>
			<p>The brief was filed on behalf of the parties on a "pro bono" &mdash; or fee-free &mdash; basis by Lucash, Gesmer &amp; Updegrove. "<em>Rambus v. Infineon</em> goes to the very heart of the integrity of the standard setting process," said Andrew Updegrove, a partner at Gesmer Updegrove and the author of the brief. "As we are a national leader in forming and representing the organizations which set standards, we felt that it was our duty to acquaint the Supreme Court with the importance of the issues involved. This is hardly the right time for the courts to be undercutting processes that are crucial to American technology, Homeland Security and national competitiveness."</p>
			<p>The case is so significant that additional briefs were filed by the Attorneys General of 15 States and Puerto Rico, by the standard setting body whose process was involved, and by five semiconductor companies.</p>
			<p>The case history of <em>Rambus v. Infineon</em> has been a roller coaster, with first one side, and then the other, gaining the advantage. Moreover, estimates of the industry-wide royalties at stake run as high as a billion dollars. But the impact of the case goes far beyond just the memory industry. If the Supreme Court does not take the case, the process that sets the more than 100,000 standards that affect nearly every aspect of daily life in this country will be undermined.</p>
			<p>As noted in the brief:</p>
			<ul>
				<li>Voluntary standards, especially technology standards, are vital to the national interest, affecting almost all areas of modern life, safety and commerce.</li>
				<li>The Federal government is dependent on such standards: Congress has mandated the use of voluntary consensus standards by the Federal agencies whenever possible.</li>
				<li>The failure by the courts to protect the standard setting process would undercut the American economy and impair our international competitiveness.</li>
			</ul>
			<p>Gesmer Updegrove LLP, a Boston-based technology law firm, gathered the impressive group of participants within the 30-day period allowed by court rules. The decision whether to take the case may be made by the Supreme Court as early as October of this year.</p>
			<div class="hr"></div><hr />
			<p><strong>ABOUT RAMBUS V. INFINEON</strong><br />The underlying facts are as follows: Rambus Inc., which develops semiconductor memory technology, participated in the standard setting process of the Joint Electron Devices Engineering Council (JEDEC) in the early 90's. During the design process, Rambus did not disclose that it held patents and patent applications on designs included in the standards. When companies, including Infineon, implemented those standards, Rambus sued for patent infringement. Infineon counterclaimed, citing fraud, and a trial court found Rambus guilty. To the astonishment of almost all, the fraud verdict was overturned by a Federal District Appellate Court in January of this year. In the meantime, the Federal Trade Commission had brought claims against Rambus as well. A trial based upon those claims is currently taking place before an administrative law judge. In July, Infineon took the last defensive step available to it, and petitioned the Supreme Court to intervene.</p>
