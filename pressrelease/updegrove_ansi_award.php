			<h5>Andrew Updegrove Receives ANSI<br />2005 President's Award for Journalism</h5>
			<h6><em>American National Standards Institute Gives Award for his work at<br />ConsortiumInfo.org and in the Consortium Standards Bulletin</em></h6>
			<p><strong>Boston, MA (August 29, 2005)</strong> &mdash; ConsortiumInfo.org, the most comprehensive resource on the Internet for news and information on technology and communication standards, announced today that Andrew Updegrove, its founder and editor, would receive the 2005 President's Award for Journalism from the American National Standards Institute (ANSI). ANSI is the globally recognized non-profit organization that administers and coordinates the U.S. voluntary standardization and conformity assessment system, accredits U.S. standards development organizations, and represents the U.S. in the international standards organizations ISO and IEC.</p>
			<p>Andrew Updegrove is a high tech attorney and co-founder of the Boston-based law firm Gesmer Updegrove LLP. In 2002, he created <a href="/">www.consortiuminfo.org</a> (which is hosted by Gesmer Updegrove), and also launched the <em><a href="/bulletins/">Consortium Standards Bulletin</a></em>, a journal of "news, ideas and analysis" about standards that delivered its 31 st on-line issue this month. On being notified of the award, he responded, "In the <em><a href="/bulletins/">Consortium Standards Bulletin</a></em> I have tried to spread the message that technology standards are vitally important to our modern, networked world, and at ConsortiumInfo.org I have tried to provide the resources needed to understand, create and use them. It is gratifying to see these efforts recognized."</p>
			<p>The award is one of several that ANSI grants annually to recognize individuals "for their significant contributions to national and international standardization activities and ongoing commitment to their industry, their nation and the enhancement of the global voluntary consensus standards system."</p>
			<p>This is the first time in the five-year history of the journalism award that ANSI has recognized the work of someone that is not a professional, full-time journalist (previous winners include Brandon Mitchener of The Wall Street Journal Europe, and David Berlind, of Ziff-Davis ZDNet.com).</p>
			<p>In announcing the award, ANSI stated:</p>
			<p>"<a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a> has been awarded the President's Award for Journalism, which recognizes outstanding journalistic work that helps to illuminate the role that standardization and conformity assessment activities play in improving the health and safety of Americans, while strengthening the competitiveness of U.S. business in a global marketplace. Mr. Updegrove acts as editor and publisher of the <em>Consortium Standards Bulletin</em>, which offers a wealth of information on standards and standards-setting, both online and in print. <em>The Bulletin</em> and website, ConsortiumInfo.org, have helped to raise the profile of standards and highlight their importance to commerce and daily life, as well as encourage greater support for their use and development."</p>
			<p>The Leadership and Service Awards will be given at a ceremony in Washington, D.C. on October 5, in connection with World Standards Day events.</p>
