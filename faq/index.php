			<h1>Consortiuminfo.org FAQ</h1>
			<p><b>Q. What is a consortium?</b></p>
			<p><b>A. A consortium is a group of companies that join together to accomplish a specific goal.</b> In the technology industry, they most frequently join together to set standards to enable the development of new infrastructures (e.g., for the Internet and telecommunications), products (e.g., high definition television and wireless peripheral devices), software (e.g., Linux for business users) and services (e.g., web services). Most consortia also promote these new technologies, in order to educate potential customers and create demand. There are hundreds of consortia in operation today, supporting thousands of standards.</p>
			<p><b>Q. Who joins consortia?</b></p>
			<p><b>A. Thousands of companies are members of anywhere from a single consortium to scores of such organizations.</b> Members also include government agencies, non-profit research institutions and major universities. Membership in a given consortium can cost anywhere from several hundred dollars to several hundred thousand dollars, depending on how ambitious its goals are.</p>
			<p><b>Q. What is ConsortiumInfo.org?</b></p>
			<p><b>A. ConsortiumInfo.org is the most detailed and comprehensive resource on (or off) the Internet on the topics of standard-setting and the consortia that support this process.</b> Its attributes include: book-length content; many interactive features; hundreds of annotated links to standard setting consortia, laws, other web-based resources, articles and primary research materials. These features make it not only the most useful resource for those interested in its subject matter, but a new type of research site which has been designed to take advantage of the unique qualities of the Internet.</p>
			<p><b>Q. How does it do that?</b></p>
			<p><b>A. In a variety of innovative and interactive ways.</b> First, a reader can read "across" the major categories and gain a comprehensive and in depth knowledge about standard-setting and consortia. At the same time, someone interested in doing in-depth research can follow the links and subcategories down through successive levels to access laws, cases, articles and other types of materials and resources. In each and every case, a helpful annotation will inform the visitor of the nature of the new material, to make it simple to determine whether the visitor's time in reviewing that material will be rewarded. Hundreds of links, each of them annotated, can also take a visitor outside the site to a wealth of other resources indexed through ConsortiumInfo.org.</p>
			<p><b>Q. Who created this site?</b></p>
			<p><b>A. ConsortiumInfo.org was created by <a href="http://www.gesmer.com/">Gesmer Updegrove LLP</a>, a Boston-based law firm which serves emerging and established high-tech companies, venture capital firms, and major universities.</b> GU has also helped form and represented more than 60 standard-setting and promotional consortia, including some of the largest and most influential consortia in the world. GU believes that it has represented more standard-setting consortia than any other law firm in the country. Besides legal representation, it provides business consulting services to help companies structure consortia that will attract a wide membership, and to help them create strategies that lead to success.</p>
			<p><b>Q. Why did GU create ConsortimInfo.org?</b></p>
			<p><b>A. ConsortiumInfo.org provides a number of benefits to the firm, its clients and the industry at large.</b> First and foremost, it is a valuable informational resource for our clients. It is also a way to demonstrate our expertise to a worldwide audience by providing in-depth explanations of all of the important aspects of standard-setting and consortium formation and operation. Finally, creating and updating the site provides professional satisfaction to the attorneys at our firm who have invested a substantial part of their time gaining expertise in this area. We are pleased to make this free resource available to those involved in standard-setting and consortium activities around the world, whether or not they are (or ever become) our clients.</p>
