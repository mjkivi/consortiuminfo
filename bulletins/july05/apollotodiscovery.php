                <div align="center">
                  <p align="center" class="style10"><strong> <strong> FROM APOLLO TO DISCOVERY</strong> </strong><br>
                  </p>
                  <p align="left" class="style2"> Twenty-six years ago this week, human beings walked on another world for the first time, and the entire planet watched in awe. This month, the same nation that achieved that feat hopes to resume its ability to send its astronauts into space using its own launch systems. In between those two points in time lies a long period of dramatic robotic missions launched by the United States (and other nations), but stagnation in manned missions and in the development of new launch systems and enabling technologies. </p>
                  <p align="left" class="style2">In this issue we examine the role that standards can play in reinvigorating the global space program, as well as the challenges that lie in the way of achieving that results, particularly as regards the ability for the U.S. to assert leadership in setting those standards. </p>
                  <p align="left" class="style2">In our <strong><em>Editorial</em></strong>, we examine the important relationship between the exotic practice of &ldquo;Rocket Science&rdquo; and the far less glamorous pursuit of setting the standards that enable such science to pay off. </p>
                  <p align="left" class="style2">In our <strong><em>Feature </em></strong>article we present a detailed overview of the state of setting standards for space applications today, concluding with a review of a sobering report that details the dramatic needs for increased, and more cohesive, space standard setting for such purposes. The report also describes the substantial lead that Europe enjoys in this process over the U.S. We are grateful for the cooperation of senior managers of many of the leading space standards organizations in compiling the information for this article. </p>
                  <p align="left" class="style2">In our <strong><em>Trends</em></strong> Article we are pleased to present a detailed report on how standards are selected, developed and deployed by NASA. The article includes an extensive interview with Paul Gill, the Technical Standards Program Manager for NASA. </p>
                  <p align="left" class="style2">In this month&rsquo;s selection from what was formerly called the Standards Blog (and has now been renamed &ldquo;<strong><em>Consider This&hellip;</em></strong>&rdquo;), we take a look at the challenges presented by using standards in relativistic environments, both on earth and in space. </p>
                  <p align="left" class="style2">And finally, we introduce a new <strong><em>Standards Blog,</em></strong> which is the name we have borrowed for the more &ldquo;traditional&rdquo; blog that we launched this month at ConsortiumInfo.org to provide you with up-to-the-minute analysis of breaking standards news. </p>
                  <div align="left"><span class="style2">As always, we hope you enjoy this issue.</span> </div>
