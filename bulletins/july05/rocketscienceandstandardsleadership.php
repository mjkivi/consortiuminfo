              <p><strong><em>              </em></strong></p>
              <p align="center"><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITORIAL</em></strong></font><strong> </strong></p>
              <p align="center" class="style10"><strong> <strong> ROCKET SCIENCE AND STANDARDS LEADERSHIP</strong> </strong></p>
              <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove<br>
              </a></strong></font></p>
              <p align="left"> <span class="style2">NASA &ndash; the United States National Aeronautics and Space Administration &ndash; is a place where, literally, rocket scientists work. </span></p>
              <p align="left" class="style2"> The popular phrase &ldquo;rocket science&rdquo; conjures up a variety of concepts: brilliance, involvement with the ultimate in cutting-edge technology, and engineering challenges with no margin for error. And indeed, the practice of rocket science involves not only sending missions into rarified atmospheres, but also working in a rarified atmosphere of unique, rather than mass-market, design. Yet even in such limited-production environments, standards are important, and in some instances, even more important than they are in more pedestrian settings. </p>
              <p align="left" class="style2"> And while the popular perception of rocket science is one of Olympian discipline, practiced in secluded, secure locations, it relies on centuries of advances in disciplines as disparate as mathematics, materials science, and all types of engineering (chemical, hydraulic, structural and so on). Thus, while those at NASA and their brethren in space agencies around the world spend much of their time designing thrusters and orbital paths, they must also traffic in the ordinary world of screws, wire and sheet materials, and take an active interest in how standards are set for these more pedestrian items. True, when specified for use in space applications, those familiar items will be subjected to far more drastic stresses, strains, and expectations than would be placed upon them in almost any other setting. In consequence, exotic alloys rather than chrome steel may need to be specified for the screws that hold elements of a spacecraft together. But regardless of their composition, screws for household appliances and screws for space applications use the same standards for their threads and heads. </p>
              <p align="left" class="style2"> NASA employees must also spend a great deal of their time helping develop new types of standards as well &ndash; for communications between spacecraft and for other applications unique to aviation use, or to utilization in space. As a result, they help develop standards in myriad standards development organizations (some thirty in all) that set standards for all manner of individual items, assemblies, physical processes and information technologies, some of which do, some of which don&rsquo;t have unique space applications. </p>
              <p align="left" class="style2"> This was not always so, since traditionally NASA (as was the case with other government agencies) used so-called &ldquo;government-unique&rdquo; standards in much of its procurement activities, even when correlative standards already existed in the private sector. Thus, while NASA would participate in the development of those specifications, the number of settings in which that participation occurred was limited to a smaller number of standard setting organizations. But with the passage of the Technology Transfer and Advancement Act by Congress in 1995, all government agencies were directed to use public consensus standards (NASA had already moved independently in the same direction), and therefore the worlds of space procurement and general standard setting came to overlap to a greater degree than had ever existed before. </p>
              <p align="left" class="style2"> Beginning with the Apollo-Soyuz docking mission in July of 1975, another level of standards collaboration had became necessary, this time at the international level. With the increasing pressure to combine missions for political and budgetary reasons, more and more complex international space missions were planned, requiring increasing cooperation on standards among the space agencies of the world. With more and more nations participating at this level, the need for international cooperation in standard setting continues to increase. Inevitably, the same type of national and regional forces that sometimes complicate any other type of standard setting actively will arise in the domain of space standards. </p>
              <p align="left" class="style2"> As the number of useful disciplines to which earth-orbiting platforms can be dedicated has increased (such as telecommunications, earth science information gathering and weather forecasting), new entrants into the space industry have followed, first to build or utilize satellites, and later to compete in the market to launch such payloads. Today, the X Prize has helped foster a new crop of companies interested in space. These companies, unlike their large, government contract funded predecessors, are nimble and entrepreneurial, and represent the first effort by private industry to not only build, but also to design launch vehicles on their own initiative and at their own expense, using innovative concepts and non-traditional materials. </p>
              <p align="left" class="style2"> Almost five decades from the birth of the space age, there is therefore a broad range of stakeholders in the space industry, from military (spy satellites and weapons systems), to NASA (exploration and science), to industry (launch vehicles, telecommunications and GIS-based services) to the nascent travel and adventure sector (the X Prize contestant companies and, most recently, Virgin Air, as a first customer for Bert Rutan&rsquo;s now-demonstrated suborbital design). This increase in stakeholders has, not surprisingly, brought increasing convergence in the types of standards that need to interact in space applications, bringing additional earth-based standards (such as civil aviation safety regulations and commercial mapping standards) together with space-unique standards. </p>
              <p align="left" class="style2"> This increasing activity has resulted in the formation of new working groups not only in traditional standard setting organizations, such as the American Institute of Aeronautics and Aerospace (AIAA) and the aerospace technical committees chartered by ISO, but also efforts within information technology consortia, such as the Open Geospatial Consortium (OGC) and the Object Management Group (OMG), and the formation of entirely new, dedicated organizations, such as the Consultative Committee for Space Data Systems (CCSDS), which was formed by the space agencies of the various nations active in space in order to develop the standards needed to enable cooperation among multiple nations in the support of an in-process mission of a single agency. </p>
              <p align="left" class="style2"> Thus, there are many types of standards activities in operation today that interlock and interrelate, some of which may be thought of as &ldquo;first generation&rdquo; efforts that adapt aviation standards to space applications, others as &ldquo;second generation&rdquo; efforts that set space-unique standards, and finally &ldquo;third generation&rdquo; activities that seek to adapt modern commercial technologies (such as software architecture and geoinformation services) to take advantage of space based platforms. Unfortunately the number of such first generation standards is in many areas too numerous and incoherent, while there are far too few second generation standards in existence to create increased desire for standards of the third variety. </p>
              <p align="left" class="style2"> Like many of my generation, I wish that the promise of the Apollo program had been followed by similarly dramatic feats and technical advancement. Sadly, the rapid advances of the 1960s soon gave way to slow progress in the wake of fitful commitments in the United States, a collapsing economy in the Soviet Union, and the absence of a real scientific mission for the International Space Station effort that is siphoning funds from the space budgets of many nations in addition to the United States. </p>
              <p align="left" class="style2"> With a flat commercial launch market for the last several years (which is projected to continue for the indefinite future), stagnation has occurred in the U.S. in the realm of setting the standards that enable space missions as well. This is the warning issued in a recent report entitled &ldquo;The Future of Aerospace Standardization&rdquo;, delivered in January of this year by the Aerospace Industries Association. The authors of that report conclude that &ldquo;Industry, NASA, DoD and the FAA urgently need to work together to ensure the development of globally recognized standards that support both government and commercial space interests.&rdquo; Internationally, and in Europe in particular, the authors see greater and more coherent efforts taking place, leading to the possibility that America&rsquo;s leadership in space may erode. </p>
              <p align="left" class="style2"> Will the return of the U.S. Space Shuttle to active service represent a watershed event that will reverse reinvigorate the U.S. space program? Unfortunately, no one expects that to happen, as the remaining missions for the orbiter are limited to completing the International Space Station. Will the new U.S. commitment to return to the moon, establish a permanent base there, and press on to Mars mark a revolution in government support for space exploration? It is hard to find true believers in that outcome either, given the general budget deficits stretching into the indefinite future and the limited degree of funding that NASA has even initially been promised to achieve these ambitious goals. Perhaps the sound of &ldquo;The East is Red&rdquo; beaming from space once again, and this time from a Chinese lunar lander rather than a Soviet sputnik, will mark such a turning point. </p>
              <p align="left" class="style2"> More likely, the successful launch of Space Ship One, the low budget, privately funded effort to win the X Prize will mark that watershed. As with civil aviation in the century just ended (which received much-needed support in its critical early years through receipt of lucrative federal mail-carrying contracts), the future of space innovation and accomplishment is more likely to come from private industry, following on the heels of the publicly funded research, development and prototypes of the federal space program, and augmented by the individual design creativity and innovation of space-age entrepreneurs. Doubtless, a manned mission into orbit in a private sector vehicle is still many years in the future, but delivering small payloads into orbit, perhaps via rockets launched from high-altitude aircraft, may come much sooner. </p>
              <div align="left"><span class="style2">While standards inevitably will play one of the least glamorous supporting roles in any such renaissance of space utilization, they are scarcely the least essential element in making that reality possible. Unless the United States is willing to play (at best) a supporting role to Europe in creating the standards needed to support greater and more rewarding access to space, government and industry would be wise to hear and heed the wake up call delivered by the AIA report. </span></div>
