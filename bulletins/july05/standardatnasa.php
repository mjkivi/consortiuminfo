              <p align="center" class="style8"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">TRENDS:</font></em></strong></p>
              <p align="center"> <strong> <span class="style10">STANDARD SETTING AT NASA: AN</span></strong></p>
              <p><span class="style10"><strong> INTERVIEW WITH PAUL GILL</strong></span><span class="style10"><br>
                </span><br>
                <a href="http://www.gesmer.com/attorneys/updegrove.php" class="style2"> <strong>Andrew Updegrove</strong></a> </p>
              <p><br>
              </p>
              <table width="436" border="0" cellspacing="0" cellpadding="0">
                <tr>
                  <td width="436" valign="top" class="style2"><strong><em> Abstract: </em></strong><em>The United States National Aeronautics and Space Administration (NASA) maintains a diverse and extensive program of research, development, and testing that enables dramatic scientific, robotic and manned missions into earth orbit and beyond. These activities involve the use of over 3,400 standards derived from more than 50 standard setting organizations through a network of 11 major NASA Centers, and many additional supporting facilities. Until recently, each of these Centers was free to develop and adopt standards on a largely independent basis, most of which were government-unique. Over the past decade, NASA has increasingly transitioned from using government standards to using private sector standards, and the approval and management of standards within NASA has been placed under central authority. Today, NASA&rsquo;s use of standards is controlled by unique Standards Management System that maximizes efficiency and safety, and coordinates efforts across the Agency. This article briefly reviews this transformation, and then reproduces an in-depth interview with Paul Gill, Manager of the NASA Technical Standards Program, who answers questions relating to all aspects of NASA&rsquo;s use of standards, and its participation in the international standard setting process. </em></td>
                </tr>
              </table>
              <p align="left"><strong><em> <span class="style71">Introduction: </span></em></strong><span class="style2">NASA lives at the intersection of a number of worlds. There is the world of its own complex, encompassing research, development and testing centers, assembly and launch facilities, and administrative headquarters, all dedicated to performing highly exacting and unique tasks in the unforgiving spotlight of the press. Outside this immediate world lies the orbit of the myriad contractors that provide the material to perform the actual missions, and then the realm of the space agencies of other nations with which NASA increasingly collaborates. There is also the academic world of the universities that supply experiments and other payloads, and that of the Department of Defense (DoD), which is partially dependent on NASA&rsquo;s capabilities to accomplish its own programs. </span></p>
              <p align="left" class="style2"> And then there are the broader planes upon which play out the missions that are the heart and soul of NASA: manned missions into earth orbit, Great Observatories that peer into the depths of the cosmos to the very beginning of time, orbiting platforms that probe the secrets of our own planet, and increasingly sophisticated robotic missions to explore the planets and moons of our solar system, as well as comets and asteroids that visit from greater distances. </p>
              <p align="left" class="style2"> Finally, there is the world of those that scrutinize, criticize and (sometimes grudgingly) fund NASA: the press, you and I, and Congress. NASA operates under the type of public spotlight and microscope that immediately identify and magnify every failure and shortfall, but too often fail to report and applaud properly the many successes, large and small, that have been hard-earned by those that engage on our behalf in the pursuit of space exploration. </p>
              <p align="left" class="style2"> One of the tools employed by NASA to operate in such a fault intolerant environment is a carefully devised and deployed standards program which could profitably serve as a model for private industry. This system, designed and deployed in the last decade, provides a unified and cohesive regime of standards analysis, selection, development, cataloging and deployment. It also enables gathering and archiving lessons learned in the process, and makes them available to guide future efforts. </p>
              <p align="left" class="style2"> This article is intended to describe that system and its operations, and features a lengthy interview with Paul Gill, the NASA Technical Standards Program Manager. We begin by summarizing some of the hallmarks of the NASA standards system, and then follow with the complete text of that interview. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">NASA Overview:</span></em></strong> The National Aeronautics and Space Administration, more familiarly known as NASA, was created by Congress in 1958 as the successor to the National Advisory Committee for Aeronautics (NACA), which in turn had been created in 1915. The reconstitution of NACA to support missions into space was partially in response to the launch by the Soviet Union on October 4, 1957 of Sputnik, an event that caught the United States famously unprepared to match the capabilities of its arch Cold War rival. </p>
              <p align="left" class="style2"> At its commissioning, NASA was somewhat vaguely charged by Congress: &ldquo; to provide for research into the problems of flight within and outside the Earth's atmosphere, and for other purposes.&quot; Its initial purpose, however, was clear: to catch up, and surpass, the Soviets in the conquest of space. Over time, with the end of the Cold War and successive changes of administrations, NASA&rsquo;s mission was fundamentally modified on several occasions, and less dramatically altered on a more frequent basis. Today, its self-espoused mission is: &ldquo;to understand and protect our home planet, to explore the universe and search for life, and to inspire the next generation of explorers&rdquo; to which is wistfully added, &rdquo;... as only NASA can.&rdquo; </p>
              <p align="left" class="style2"> Most recently, NASA has been called upon to transform itself in response to two challenging demands: first, to trace and resolve the issues that lead to the Columbia tragedy, and second to fulfill the Bush administration&rsquo;s mandate to return to the moon, establish a base there, and then send a manned mission to Mars. Either of these challenges would be challenging in isolation, especially given the Agency&rsquo;s budgetary constraints and the many unmanned scientific projects that NASA also has in process. </p>
              <p align="left" class="style2"> The scope of completing these tasks is magnified by the breadth of the NASA system, which comprises 11 <a href="http://www.education.nasa.gov/edoffices/centeroffices/index.html">NASA Centers</a>, as well as the many additional facilities that NASA owns or works with around the country and the world in order to design, build, launch and monitor its missions. Some of these Centers, such as the Jet Propulsion Laboratory (managed by the California Institute of Technology, or CALTECH), Kennedy Space Center and Johnson Space Center are familiar to all, while others are less in the public eye, such as NASA&rsquo;s Ames, Dryden and Langley Research Centers. Until recent years, each of these Centers was free to develop and adopt standards independently. </p>
              <p align="center" class="style2"><img src="/bulletins/nasa.gif" width="600" height="363"></p>
              <p align="left" class="style2">&nbsp;</p>
              <p align="left"><strong><em> <span class="style71">Transformation:</span></em></strong><span class="style2"> The United States Government has been urging its agencies to become increasingly involved in the development and utilization of private sector consensus based standards for some time, most decisively with the passage of the National Technology Transfer and Advancement Act of 1995 (NTTAA) and the subsequent revision by the Office of Management and Budget (OMB) of its Circular A-119 in 1998 (see, <a href="http://www.consortiuminfo.org/bulletins/jan05.php#feature">A Work in Progress: Government Support for Standard Setting in the United States 1085 &ndash; 2004</a> ) Concurrent with the progress of the NTTAA through Congress, the Defense Department was proceeding with its own &ldquo;MilSpec Reform&rdquo; initiative, which had similar goals. </span></p>
              <p align="left" class="style2"> Under the NTTAA, the government agencies were required to use private sector standards in lieu of &ldquo;government unique&rdquo; specifications wherever feasible, and to report to Congress on their progress in doing so on an annual basis. Under OMB Circular A-119, the agencies were given more explicit advice, and permitted to use not only the standards created by accredited standards development organizations (SDOs), but also those created by non-accredited consortia, and necessary, even proprietary, de fact standards. </p>
              <p align="left" class="style2"> The issuance of OMB Circular A-119 roughly coincided with a complementary initiative within NASA to rationalize its usage of standards. That initiative was led by the Chief Engineer of NASA at the instruction of then NASA Administrator Daniel Goldin. Under that that initiative, a new Technical Standards Program was launched, which would manage all standards activities at all NASA Centers, bringing such activities under a single point of supervision for the first time. Such central control would (among other benefits) permit all standards to be centrally indexed for better usage by all Centers, as well as enable better coordination of which standards were in use, where the most appropriate standards could be obtained, and how standard setting organizations, both accredited and non-accredited (SSOs) could most efficiently be motivated to develop new standards that NASA would need to execute its mission. </p>
              <p align="left" class="style2"> The result has been a gradual decrease in usage by NASA of government-unique MIL-SPECS/STDS and an matching increase in its use of private sector standards. While that transition has not been as complete as in some other government agencies, nonetheless approximately 54% of all standards utilized by NASA over the past four years were derived from the private sector. The balance are MilSpecs (25.84%), NASA unique (Center Developed &ndash; 10.31% or NASA Preferred &ndash; 5.54%), or other government standards (4.39%) (see Appendix B). </p>
              <p align="left" class="style2"> As of this writing, NASA&rsquo;s standards program has adopted (or is in the process of adopting) some 3,400 standards, only 60 of which were developed by NASA internally for Agency-wide use (although there are other Center-unique standards in use as well). The process of transitioning to the fullest practicable use of private sector standards is continuing. In furtherance of that goal, almost 140 NASA employees take part in the standards development work of more than 30 SSOs today. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">Management innovation: </span></em></strong> The restructuring of the NASA standards program has also led to the creation of an internal Standards Management System that would be the envy of most private companies. This system provides NASA with a superior ability to plan, deploy and track its participation in standard setting and utilization of standards, and includes databases: </p>
              <div align="left">
                <ul>
                  <li class="style2"> that record the name of each NASA employee that participates in an SSO. The database is accessible to all other NASA employees, thus avoiding duplication, and increasing the ability of NASA to leverage the benefits of participation in SSOs. </li>
                </ul>
                <ul>
                  <li class="style2"> into which standards-related &ldquo;lessons learned&rdquo; throughout the Centers are logged, in order to maximize the value of experiences gained throughout the NASA system. The result has been increased quality control, and augmented efficiency. </li>
                </ul>
                <ul>
                  <li class="style2"> that tracks the status of every standard that NASA is helping develop, is interested in, or has already adopted. </li>
                </ul>
              </div>
              <p align="left" class="style2"><strong><em> <span class="style71">The Interview:</span></em></strong> Our interview with Mr. Gill is divided into several main topics and a variety of subtopics, collectively covering the history, external collaboration, internal management, and other aspects of the NASA standards program, beginning with in Overview of the program. </p>
              <p align="left"><strong> <span class="style10">I. Internal Operations </span></strong></p>
              <p align="left" class="style71"><strong><em> A. Overview </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> How did you come to work at NASA? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> I joined NASA after I graduated from Tuskegee University with a degree in Electrical Engineering. My early career was focused in the area of automated process applications in materials. About 8 years ago I joined the NASA Technical Standards Program of which I became Manager in 1999. </p>
              <p align="left" class="style2"><strong class="style71"><em> CSB: </em></strong> Are there any important ways in which the development of space application standards is different than standard setting for atmospheric aeronautic standards? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> There are a lot of similarities. I suspect the most important differences are in terms of standards being developed to accommodate the hostile environments in which space vehicles must operate versus aircraft. Also, the demands associated with designs for one of a kind mission contribute to the differences. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB</span></em></strong><span class="style71">: </span>What has the historical involvement of NASA been in standard setting? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Originally, most of the standards used by NASA were MIL-SPECS/STDS or developed by a specific NASA Center for their own use. With the exception of a few standards having to do with safety, essentially none were &ldquo;agency wide standards.&rdquo; NASA also used standards developed by organizations such as SAE <em> [ed: Society of Automotive Engineers, which also sets aeronautics standards],</em> ASTM<em> [American Society of Testing and Materials],</em> IEEE <em> [Institute of Electrical and Electronic Engineers]</em> , etc. and participated in many of the standards developing efforts of these organizations, as it does today. </p>
              <p align="left" class="style2"> Currently, NASA has only about 60 NASA developed unique standards designated for agency wide use. The rest are standards developed primarily by DoD and non-government organizations, both national and international. In addition, the Agency still uses Center developed unique standards that have been produced to meet the requirements of programs and projects assigned to them. (This is in the range of several 1000 documents.) These we are currently endeavoring to transform to agency-wide standards where appropriate. With the establishment of the NASA Technical Standards Program in the mid-1990s, this has been a priority objective. </p>
              <p align="left" class="style71"><strong><em> B. Program Coverage </em></strong></p>
              <p align="left" class="style2"><strong class="style71"><em> CSB:</em></strong> What facilities and entities are covered by the Technical Standards Program? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The NASA Technical Standards Program encompasses all the NASA Centers, NASA HQ, and JPL<em> [the Jet Propulsion Laboratory]</em> plus their allied facilities. Thus it is an &ldquo;Agency wide&rdquo; endeavor in support of all the Agency&rsquo;s programs and projects. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> How many different types of standards does the program cover? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Basically the Program addresses Standards, including specifications and handbooks, as they apply to the needs of the Agency for standards products. This encompasses all areas of engineering, including safety, information technology, data systems, etc. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> How many different technical areas does the NASA standards program cover? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The Program covers eleven categories of standards. The listing is provided in Appendix A. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> How many standards are included in the program today? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Currently there are over 3,400 NASA Preferred Technical Standards that have been either developed by the Agency or are non-NASA developed standards, including DoD, that have been &ldquo;adopted&rdquo; or pending adoption for use by the Agency. Of these standards only about 60 are currently NASA unique developed standards. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What percentage of the standards that you work with are &ldquo;space unique,&rdquo; as compared to general engineering standards (e.g., screw threads), and general aeronautical standards </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Only a small percent are &ldquo;space unique&rdquo;, mainly those developed by the Agency, AIAA, CCSDS, plus some of standards developed by other organizations. However, there is a large percentage of Center and Program developed standards that are used by the Agency&rsquo;s programs and projects. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What areas of standards that are not unique to space are of greatest interest to NASA (e.g., GIS standards) in performing its missions, as compared to building its vehicles and instruments? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Based on our metrics on standards usage, it would be standards associated with materials. Of the 3400+ NASA Preferred Technical Standards, approximately 45% are Materials Discipline oriented. </p>
              <p align="left" class="style71"><strong><em>C. Scope and Structure </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What is the scope of the NASA Technical Standards Program? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> There is an agency wide Technical Standards Program that encompasses not only the development of NASA unique standards products, but also provides the Agency with a &ldquo;one stop, one-shop&rdquo; source for standards, plus a source for locating standards related engineering lessons learned and applications notes on experiences in the use of a particular standard. In addition, a key objective of the Program is to improve engineering practices in the design and development practices for use on the Agency&rsquo;s programs and projects. </p>
              <p align="left" class="style2"><strong class="style71"><em> CSB:</em></strong> It appears that NASA is a standards world in microcosm, in part because it is a unique end user, in part because it does things that only a few other countries can do today, and in part because it is at the top of its own pyramid of contractors and facilities. Would you tell us how you and others at NASA went about designing your own standards system? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> To a large degree, your assessment is correct. The key to the design of the NASA Technical Standards Program was the attention given by the Administrator and the NASA Chief Engineer. This was important. The other major element that produced the viable program we have today is the NASA Technical Standards Working Group. This Group consists of a senior representative from each NASA Center, JPL, and several NASA HQ offices. They provide the necessary interfaces and guidance for the Program. Finally, the NASA Engineering Management Board provides oversight on the Program and its actions. Most recently, as a result of the Columbia Accident Investigation Board recommendations concerning the Agency having an independent Technical Authority, the delegation of responsibilities to Discipline Technical Warrant Holders has been a new development relative to enhancing the value and use of technical standards on NASA programs and projects. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Tell us about the structure of your program. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The structure of the NASA Technical Standards Program consists of the following major elements: (1) Development of NASA unique standards, (2) Adoption of non-NASA developed standards, (3) Agency wide Technical Standards System, and (4) the integration of engineering lessons learned and application notes on standards usage with appropriate technical standards. </p>
              <p align="left" class="style2"> The Agency establishes Engineering Standards Topic Working Groups to assist in the development of standards. It relies on the NASA Technical Standards Working Group for reviews of standards, coordination of Center involvements, etc. Thus, the Program is an integrated activity within the Agency. All of the Program&rsquo;s activities are conducted within the scope of the NASA Chief Engineer&rsquo;s responsibilities. </p>
              <p align="left" class="style2"> About a year ago we implemented a technical standards system usage survey. Currently it shows the following usage of the standards: (1) Requirements for Program/Project Development--24%, (2) In-House R&amp;D (Including Design, Analysis, Testing, Etc.)&mdash;29%, (3) Verification of a Contractor&rsquo;s Processes on Programs/projects&mdash;17%, (4) Acquisition of Parts or Materials&mdash;9%, (5) Evaluation of Proposal(s)&mdash;3%, (6) Education and Training&mdash;12%, and (7) Other uses&mdash;6%. These metrics on standards usage have provided NASA management with a better insight into the value of standards to the Agency. </p>
              <p align="left" class="style71"><strong><em> D. Transformation </em></strong></p>
              <p align="left" class="style2"> <span class="style71"><strong><em>CSB:</em></strong> </span>How did things operate before all NASA locations were placed under the same standards program? </p>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> Basically each NASA Center operated its own standards activity, using other government agency standards, those of non-government organizations, and developing their own standards to meet their specific requirements. At times, one Center would utilize the standards developed by other Centers where they met their needs. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What (or who) led to the formation of the Technical Standards Program? Did the Technology Transfer and Advancement Act of 1995 play a role? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The leadership for the formation of the NASA Technical Standards Program was by the NASA Chief Engineer, based on a directive issued in 1997 by the Administrator. With the advent of the Technology Transfer and Advancement Act of 1995, additional emphasis had been placed on the use by NASA of non-government standards and this emphasis continues today. </p>
              <p align="left" class="style2"><strong class="style71"><em> CSB:</em></strong> What were the major goals that the new way of doing things was intended to achieve? What problems was it intended to solve? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The major goal was to encourage the use of non-NASA standards where practical in the design, development, and operations of NASA programs. The exception being where a unique NASA requirement could not be met by the use of non-government standards. Then NASA would develop the necessary standard, sometimes in collaboration with a non-government standards developing organization. Also, NASA encouraged the participation of its employees in the committees responsible for the development of non-NASA standards. For example, we have nearly 140 NASA employees participating in the committees responsible for the development of non-government standards in standards developing organizations, both national and international. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Was the NASA transformation typical of what was going on in some other agencies at the same time, or was it unique? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG</span></em></strong><span class="style71">: </span>In 1994, the DoD instituted MilSpec Reform which was a major undertaking as you are aware. While not the same type of a transformation as was going on within NASA as it moved from a Center focused to an Agency focused standards program, some of the activities of the DoD initiative had similar effects. </p>
              <p align="left" class="style2"><strong class="style71"><em> CSB:</em></strong> What things were you able to do that an industry organization with many members might not be able to accomplish? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Probably the integration and linking of engineering lessons learned and application notes on standards usage to specific standards so the information would be readily available to NASA employees is the most obvious example. This has been an important accomplishment in that it has added to the number of NASA employees that have been made aware of engineering lessons learned in their area of work. In addition, it has provided a timely input on engineering experiences that have a bearing on the use of a particular standard plus provided information for use in the improvement of that standard. </p>
              <p align="left" class="style71"><strong><em> E. Infrastructure and Management </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> I&rsquo;m intrigued by the Standards Management System. How did that come to be developed, and how has it worked? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The Agency&rsquo;s Standards Management System mainly consists of the elements necessary to maintain a current status on all NASA standards under development, being processed for adoption, incorporation of engineering lessons learned links, provisions for NASA employees to propose new standards for development or adoption, maintaining databases on NASA employees participating in non-NASA standards developing organizations, national and international. One of the key elements of the NASA Technical Standards Program is the participation of Agency employees in the standards developing activities of about thirty non-NASA standards developing bodies. </p>
              <p align="left" class="style2"><strong class="style71"><em> CSB:</em></strong> What changes have you made since you launched the program? </p>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> I guess the most significant change has been to institute an Agency-wide awareness of the Program and its products. The metrics maintained for the Program illustrate the dramatic effect of this initiative. Second has been the proactive encouragement on the use of the Standard Update Notification System , which keep Agency employees informed when changes or updates occur on a standard they are using. This System has become a very important asset for the Agency. All procurements with a value of over $5M must have the standards specified registered in the Standards Update Notification to ensure they are current and notifications on changes are received in a timely manner. Changes to standards being used on a program/project can have major impacts on the safety, performance, reliability, and costs. Using out-of-date standards also exposes programs/projects to the risk of repeating those failures that led to the update of the standard. </p>
              <p align="left"><strong><em> <span class="style71">F. Achievements </span></em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> In what ways is the Standards Program essential to completing NASA&rsquo;s mission? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The Program functions under a directive issued by the Administrator as an integral part of the NASA Chief Engineer&rsquo;s Office reflects the importance of the NASA Technical Standards Program to the Agency&rsquo;s Mission. This responsibility is met through the elements of the Program I previously mentioned. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What are some of the things that you&rsquo;re particularly proud of? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> I suppose it would be primarily the development of a unique Integrated Technical Standards Initiative. It consists of these elements integrated into one system: (1) Agency wide Full-Text Technical Standards System, (2) Standards Update Notification System, and (3) Engineering Lessons Learned/Application Notes Linked to Technical Standards. </p>
              <p align="left" class="style2"> Another accomplishment was the Standards Awareness Initiative since it made essentially all of the Agency&rsquo;s engineering staff aware of the Program&rsquo;s products. Also, the pursuit of non-NASA developed standards for adoption as NASA Preferred Technical Standards. They now represent about 98% of the total number of NASA Preferred Technical Standards. In addition, we put in place an extensive metrics capability for the Program products, which has enabled managers in the Agency to become more aware on the importance and usage of technical standards. One of the results of these metrics was the revelation that NASA-developed standards are one of the top five downloads in the Agency out of the 110 standard developing bodies products available. </p>
              <p align="left" class="style71"><strong><em> G. Lessons Learned </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What have you learned (good and bad) that other standards development organizations could benefit from? How about other government agencies? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> On the good side, I suppose learning and appreciating the importance of providing one&rsquo;s &ldquo;customers&rdquo; what they need and in a timely manner. As for the bad, the thing that comes to mind is the problems we have experienced due to the lack of a common index of all SDO developed technical standards that is kept current and available to all users. </p>
              <p align="left" class="style2"> A few SDOs have had exclusive licenses with suppliers and that has sometimes created difficulties integrating them into our Integrated Technical Standards System. One must keep in mind that SDOs are basically &ldquo;monopolies&rdquo; with respect to the standards they produce. It is not like the user has several sources for the same standard they need. Overlap between SDOs is minimal as you know. </p>
              <p align="left" class="style2"> Obtaining feedbacks to the degree we would like on experiences from the users of standards within the Agency is another good lesson learned but we would like to see more feedbacks and are increasing our efforts in this regard. </p>
              <p align="left" class="style71"><strong><em> H. Traumatic Events </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Did the Challenger disaster in 1986 have an impact on standards programs at NASA? Were any new standards programs instituted in response, or changes made to existing protocols? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The Challenger disaster created an environment whereby all aspects of the design and development of space related hardware and systems were reviewed. Standards were no exception. There were no significant changes that I recall in existing standards used by the Agency, but I am sure some of the lessons learned were utilized in updates to some standards, both those developed by NASA and those by non-government organizations. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Did the more recent loss of Columbia result in any changes or new decisions? Have standards been viewed as a way to make future missions safer? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The loss of Columbia and the Columbia Accident Investigation Board&rsquo;s recommendations regarding standards becoming an important element of the independent Technical Authority within the Agency is probably the most significant recent development. As a result, standards have become more recognized in terms of importance for future mission&rsquo;s safety. This recognition has led to the Agency undertaking an initiative to identify &ldquo;Core Standards&rdquo; for various disciplines so they can be readily addressed in the development of new flight missions/projects. The core standard would reflect standards that are deemed important in a disciplinary area and must be considered by the program/project, thus ensuring their contents will not be overlooked in establishing design and development requirements. </p>
              <p align="left" class="style2"> [The Columbia tragedy also resulted in some new standards-related work, including] a number of new and updated testing and ET insulation process matters and practices. However, these are part of the manufacturing activities and have not yet come under Agency Standards documents as part of the NASA Technical Standards Program. Just what specific changes were made, I am not aware. </p>
              <p align="left"><strong> <span class="style10">II. External Operations </span></strong></p>
              <p align="left" class="style71"><strong><em> A. Participation by non-employees </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Are contractors involved in your internal standards program, or do they only implement the NASA-unique standards that you create? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Yes, NASA&rsquo;s contractors, especially its support contractors, are involved in the NASA Technical Standards Program activities. Feedback from its prime contractors also plays a role in the Program. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> If they are involved, how does that work? Is their participation elective, encouraged or required? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The involvement of support contractors are through the respective Center&rsquo;s contract processes. Their participation is both elective and encouraged; however, it is not a &ldquo;required&rdquo; involvement unless included in a contract scope assignment. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Do any standards development organizations (SDOs or consortia) participate in any way with your internal program, or does collaboration with them only occur when NASA representatives attend their meetings? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Collaboration essentially occurs when NASA representatives attend the meeting of SDOs or their standards developing committees. However, over 50 non-NASA standards developing organizations are involved in the Agency wide Technical Standards System by providing access to their standards products for use by the Agency on its programs and products. </p>
              <p align="left" class="style71"><strong><em> B. Other government agencies </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What is the interplay between defense applications and NASA applications? Does the Air Force, for example, participate fully in your programs, and/or the Defense Department, or is it just NASA? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> NASA has a long-standing record of collaborating with the Air Force in the area of space applications and we share our standards developments with them as they do with us. Exchanges on applications of mutual interest are achieved either directly or via our mutual participations in the various non-government standards developing organizations. NASA is a very significant user, for example, of MIL-SPECS/STDS in the design and development of its launch vehicles and spacecraft. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> How much do you work directly with other agencies? </p>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> We have not had any significant involvements with other Agencies relative to space standards activities other than the DoD. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What other U.S. government agencies are involved in space standards, if any (e.g., NIST)? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The primary involvement is by the Department of Defense (DoD) with, recently, the Department of Transportation and the Federal Aviation Administration. </p>
              <p align="left" class="style71"><strong><em> C. International Collaboration</em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Interoperability with other space programs dates back (at least) to the celebrated Apollo-Soyuz docking mission in July of 1975. Would you give us a brief history of how things proceeded from that starting point? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The Apollo-Soyuz docking mission produced the need for common interfaces between the two spacecraft and their associated elements. This need produced awareness for accommodating standards that would meet the needs of other nations. This resulted in the generation of common interfaces and standards, where needed, for use on the International Space Station and Space Shuttle. International standards emphasis has been one of the spin-offs of the interactions between the various nations having mutual space program interests. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What impact on interoperability standards strategy and programs did the beginning of the International Space Station have? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The International Space Station, with its many partners, reinforced the need for interoperability standards and, thus, international standards acceptable to all partners. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Which national space agencies are most involved in developing space standards? Which are the leaders? </p>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> I believe the United States would be considered the leader, with Russia, France, Japan, and now China beginning to provide inputs. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Which space agencies does NASA interact with the most? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Through NASA's participation in the ISO TC20 SC13 and SC14 standards developing activities, we interact with essentially all the other national space agencies to some degree. However, as you might suspect, most of our interactions are with the Europe, Russia, and Japan space agencies. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Does everyone more or less see things the same way? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Not necessarily. Each country has its own interests and needs. The European Community reflects the consolidated view of almost all European countries. In an international standards developing endeavor, each tries to put forth the respective interests of their country, which is understandable. To my knowledge, this has not created any significant problem for NASA with respect to its collaborative relations with other countries. </p>
              <p align="left"><strong><em> <span class="style71">C. Participation in SSOs </span></em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Please describe in overview the relationship between government agencies and private industry in your area of standards development (e.g., are the agencies the leaders or the followers?) </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> With regard to the area of space standards, there is a good relationship between the government agencies and private industry. However, I would say for space specific standards, NASA&rsquo;s direct and indirect involvements create the momentum for private industry actions at this time. As access to space becomes more focused with non-government endeavors, as has been the case with the aviation industry for example, I would expect to see a stronger industry leadership develop. </p>
              <p align="left" class="style2"><strong class="style71"><em> CSB:</em></strong> Which standards bodies are involved in setting standards that are unique to space applications? </p>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> The primary SSOs involved in setting standards that are &ldquo;unique&rdquo; to space applications as their primary function include: </p>
              <div align="left">
                <ul>
                  <li class="style2"> American Institute of Aeronautics and Astronautics (AAIA) </li>
                </ul>
                <ul>
                  <li class="style2"> United States Technical Advisory Group to the International Standardization Subcommittee for Space Data and Information Transfer Systems (ISO TC20/SC13) </li>
                </ul>
                <ul>
                  <li class="style2"> United States Technical Advisory Group to the International Standardization Subcommittee for Space Systems and Operations (ISO TC20/SC14) </li>
                </ul>
                <ul>
                  <li class="style2"> Consultative Committee for Space Data Systems (CCSDS) </li>
                </ul>
              </div>
              <p align="left" class="style2"> In addition, SAE, ASTM, AIA, ASME, and the Institute of Electrical Engineering in particular, all have significant standards developing efforts that support unique space applications. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Which non-government voluntary consensus standards organizations do you look to most? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Please see Appendix B for a table of the SDOs whose standards products the Agency uses most according to our metrics over the past four years. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Which organizations does NASA participate in, and how many personnel participate in SDOs and consortia? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> Please see Appendix C for A table providing a summary of this information. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> How often are existing SDOs and consortia not interested or qualified to undertake a new development project that you need to see get underway? Is this increasing or decreasing? Are there some areas where the absence of interested and qualified organizations is particularly problematic? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG: </span></em></strong> Most all SDOs will undertake the development of a new space related standard if sponsored to do so or if they ascertain there is an adequate &ldquo;market&rdquo; for the standards within the aerospace industry. For example, the Air Force recently sponsored the AIAA to develop five standards they need. So far, NASA has not sponsored the development of a space unique standard by a SDO. However, as noted earlier we are participants with a large number of SDOs in the development of standards having interest to NASA programs and projects. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Have you ever been involved with helping to start a new non-governmental standard setting organization? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> NASA was involved in helping the AIAA, CCSDS, ISO TC20 SC13 and SC14 get their standard developing programs started and continues to support them. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Are there some things that you think that non-governmental standards developers could do better? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> I believe having an open access to their standards products via a common index containing all the SDO standards products would help all users. Pricing is another matter that is not consistent among the SDOs. </p>
              <p align="left" class="style10"><strong> III. The Future </strong></p>
              <p align="left" class="style71"><strong><em> A. Private enterprise </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Have any of the new private space ventures begun participating in standards efforts? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG: </span></em></strong> Not to my knowledge. However, I would be surprised if some of their employees were not involved in some SDO standards developing committees. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Do you expect that NASA will learn useful things from these efforts (e.g., regarding composites) that might result in new standards? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> In all probability I believe we will. </p>
              <p align="left" class="style71"><strong><em> B. Trends and Challenges </em></strong></p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> What trends, challenges and issues do you see on the horizon for space standards? </p>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> More international collaboration will certainly develop. Challenges will be blending national interests into international interests, to the benefit of all concerned. </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> Is the existing global standard setting infrastructure up to the demands that you see ahead? If not, what&rsquo;s missing? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The existing and developing global standards structure seems to be positioning itself to address the needs of government and private space enterprises. These are not always the same, as you know, and I suspect the market place will determine just which global standards structure will be most productive. The recent sponsored action by the AIA &ldquo;The Future of Aerospace Standardization&rdquo; provides and excellent assessments of the key requirements for standard systems intended to support the global aerospace industry. (<a href="http://www.aia-aerospace.org/"> http://www.aia-aerospace.org</a>) </p>
              <p align="left" class="style2"><strong><em> <span class="style71">CSB:</span></em></strong> That report expressed the belief that Europe was creating standards more effectively than the U.S, and that the U.S. was in danger of being relegated to a &quot;follower&quot; rather than a &quot;leader&quot; role.&nbsp; What is your view on the conclusions and recommendations of that report, and on any plans being made to address them? </p>
              <p align="left" class="style2"><strong><em> <span class="style71">PSG:</span></em></strong> The United States standards development system is decentralized whereas the standards developing system in Europe is centralized. That within itself makes for a difference in the way standards products are developed. Personally, I believe whether the United States is losing its &ldquo;leadership&rdquo; role in the development of standards will be determined by the international market place. The more the SDOs in the United States reach out and include international cooperation and involvement in their standards development, as is being done by ASTM and SAE, for example, the less danger there will be in the United States loosing its &ldquo;leadership&rdquo; role is the development of standards. </p>
              <div align="left">
                <p><strong><em> <span class="style71">CSB:</span></em></strong> <span class="style2">What plans does NASA have for the future in the standards area? </span></p>
              </div>
              <p align="left" class="style2"><strong class="style71"><em> PSG:</em></strong> To increase its collaboration with SDOs, both national and international, to the degree practical. In particular, the NASA Technical Standards Program will move forward in supporting the President&rsquo;s initiative for a Space Shuttle replacement, return to the moon, and eventually go to Mars. </p>
