              <p align="center" class="style10"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">CONSIDER THIS: </font></em></strong></p>
              <p align="left" class="style56"> <strong> [][][] <strong> October 27, 2005</strong> </strong></p>
              <p align="left"><span class="style56">#33</span><strong class="style38"><em> Do Standards Matter? </em></strong></p>
              <p align="left" class="style2">Yes, that&rsquo;s a rhetorical  question.&nbsp; But like a lot of things we  &ldquo;know,&rdquo; it is sometimes interesting to ask ourselves what, exactly, supports  what we are sure we know.</p>
              <p align="left" class="style2">I recently asked myself that  sort of question when I was notified that I would receive a standards-related  award, and that I would be expected to say a few words by way of an acceptance  speech.&nbsp; Since I like to present ideas  in an &ldquo;out of the box&rdquo; fashion, I thought I would try this question out when I  stood in front of a room full of people who have spent most or all of their  professional careers developing standards.</p>
              <p align="left" class="style2">As hoped, it made an impact,  perhaps because I put into words what I suspect many of them had always sensed,  but which was too non-traditional for them to articulate.&nbsp; After all, standards are just dry, dusty,  boring specifications, are they not?</p>
              <p align="left" class="style2">I&rsquo;ve never thought so,  perhaps because I don&rsquo;t actually help create them.&nbsp; Instead, I help create the organizations that develop the  standards, and perhaps this enables me to focus more on the forest of meanings  and effects that standards represent, rather than on the pieces of paper  themselves.</p>
              <p align="left" class="style2">So here is what I said:</p>
              <p align="left" class="style2">I'm very pleased to be here  this evening to accept the 2005 ANSI President's Award for Journalism.&nbsp; As some of you may know, I am receiving this  award in recognition of my work at a website, ConsortiumInfo.org, and in a  monthly eJournal, the <em>Consortium Standards Bulletin</em>, now in its 33rd  issue.&nbsp; Each of these ventures is public  and free, and each addresses standards, standard setting (both SDO and  consortium-based), and the role of standards in society.</p>
              <p align="left" class="style2">You may wonder why, as a  full-time practicing attorney, I have spent over a thousand hours a year for  the past three and a half years in such a pursuit.&nbsp; There are two possible answers to that question.&nbsp; Since one reflects poorly on my mental  state, I will turn to the second, which is this:&nbsp; Because I believe that Standards Matter.</p>
              <p align="left" class="style2">Since I'm sure that all of  you already share that opinion, I would like to offer you three reasons that  may not have occurred to you before.</p>
              <p align="left" class="style2">The first reason I will  identify with the word &quot;Humanity.&quot;&nbsp; </p>
              <p align="left" class="style2">What I am suggesting by this  is that the type of standards that you and I work with are simply the latest  form of a tool that mankind has been creating since long before the dawn of  history.&nbsp; After all, what is a standard  but an abstraction that people voluntarily agree upon for a particular purpose.&nbsp; The first standards system may well have  been sign language: &quot;this sign means that thing,&quot; followed by spoken  words, then written words, then weights and measures, and eventually the types  of standards that we know and help create today &ndash;&nbsp; safety, performance and interoperability standards, and, most  recently, open source software &ndash; a new way to solve old problems.&nbsp; There is no question in my mind that there  will be other types of standards that will come into being in the future.&nbsp; </p>
              <p align="left" class="style2">It is hardly an exaggeration  to say that the creation of standards, like the creation of tools,  differentiates us from all other forms of life.&nbsp; To create standards, therefore, is inextricably intertwined with  the very concept of what it means to be human.</p>
              <p align="left" class="style2">The second reason I will  call &quot;Hope.&quot;</p>
              <p align="left" class="style2">What I wish to point out  with this word is that there is something important and unique about standard  setting:&nbsp; in what other process do  people from all backgrounds and from all nations come together to mutually  agree on matters of common concern, and then voluntarily bind themselves by  rules that restrict their freedom of action in order to make the world a more  productive and useful place?&nbsp; The United  Nations has had nowhere near the same success in its efforts, despite a vastly  greater budget and the official support of governments from all over the world.&nbsp; Surely there are lessons that national and  world governments can learn from the standard setting process that could have  great benefit to humanity as it struggles with the challenges of a shrinking  world.</p>
              <p align="left" class="style2">The last reason I will call  &quot;Service.&quot;</p>
              <p align="left" class="style2">There are on the order of a  million supported standards in the world today, and the value that they convey  is incalculable.&nbsp; With the extension of  the Internet and the Web to the third world &ndash; something that would be impossible  and unimaginable without standards &ndash; that value is about to multiply a hundred  fold, as everyone, everywhere, will gain equal access to the riches of science  and the arts, to unbiased accounts of history and current events, and to a  competitive education -- something that only twenty years ago would have been  an inconceivable goal to achieve even in the lifetimes of our children.</p>
              <p align="left" class="style2">And yet all of these  standards, old and new, have been created by a comparative handful of people,  unknown, unsung, and utilizing the most modest of resources.&nbsp; To be fortunate enough to be among those  that serve in this pursuit is, I believe, a privilege indeed.</p>
              <p align="left" class="style2">Thank you very much.</p>
