                  <div align="center">
                    <p align="center" class="style10"><strong>THE SMALL BLUE SPHERE</strong></p>
                    <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font><br>
                    </p>
                    <div align="center">
                      <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php"><br>
