                      <p align="left" class="style2">One of the most transformative  images of the 1960's was the first publicly released, color picture of the  earth as seen from lunar orbit.&nbsp; Like a  small, blue-green marble lost in the immense black void of space, this view of  earth became a metaphor in 1969 for many of the fragility of our planet, and a  timely totem for the then-emerging environmental movement.</p>
                      <p align="left" class="style2">The years that immediately  followed saw many victories for environmentalists in the United States,  including passage of the Environmental Protection Act in 1969 (which  established the Environmental Protection Agency), the extension and then  amendment of the Clean Air Act (1970 and 1977), and the enactment of the Clean  Water Act (1972), Safe Drinking Water Act (1975) and Resource Conservation and  Recovery Act, which established the so-called &quot;Super Fund&quot;  (1976).&nbsp;&nbsp; </p>
                      <p align="left" class="style2">The result of these and other  pieces of legislation was cleaner air and water, the remediation of many  notorious hazardous waste sites, the conservation of millions of acres of  federal and state lands, and increasing awareness of the negative impact that  human activities continue to have on our environment.</p>
                      <p align="left" class="style2">But the early momentum of the environmental  movement of the 1960s and 1970s has not been sustained uniformly throughout the  world, where Europe (for example), largely assigns a higher priority to  environmental concerns than does the United States, both absolutely as well as  philosophically.&nbsp; For example, in the  U.S., &quot;hard science&quot; is often demanded to prove that a given action  or substance will have a deleterious effect, while in Europe, the possibility  of such an effect may be sufficient to result in legislative action.&nbsp; These same divisions are present within the  United States itself, resulting in swings of policy when control of government  passes from one party to the other.&nbsp; The  most significant example of such a swing was the repudiation of the Kyoto  Protocol on global warming by the current administration, following signature  -- but not submission to Congress for ratification -- of the Protocol by the  Clinton administration, which was aware of significant opposition to the  Protocol in the Senate.</p>
                      <p align="left" class="style2">Historically, such variations in  policy might be of concern solely to the citizens of the nation involved.&nbsp; But increasingly, the actions of one nation  can impact the welfare of many, not only with respect to over-exploiting a  single common resource, but by degrading the very biosphere we all share as  well. </p>
                      <p align="left" class="style2">The extremely complex example of  global warming therefore marks a transition from an era during which the  &quot;tragedy of the commons&quot; was the primary international environmental  concern (i.e., when everyone shares in the ownership of a resource - such as  fish stocks -- there is a greater incentive for any single user to exploit it  than to conserve it).&nbsp; And yet efforts  to address even single-resource issues via international treaties have proven  to be difficult enough. </p>
                      <p align="left" class="style2">Nor is global warming the only  emerging area of common concern.&nbsp;  Currently, an initiative commissioned by the United Nations, known as  the World Summit for the Information Society, or WSIS, is concluding a two-year  process that includes examination of the question &quot;Who should govern the  Internet?&quot;&nbsp; Following the most  recent meeting in that process in Geneva in August, the near unanimous opinion  appears to be &quot;not the United States,&quot; at least as regards the root  directory of the Internet, which is currently under the indirect control of the  U.S. Department of Commerce.&nbsp; </p>
                      <p align="left" class="style2">From one perspective, the Internet  and the Web are simply a new virtual element of our environment, and in that  sense present parallel concerns to environmental issues.&nbsp; True, telecommunications treaties and commonly  agreed upon standards have always existed.&nbsp;  But their impact was largely internal to the countries involved -- if  you chose not to sign such treaties or adopt such standards, you lost the  benefits of the network, but did not harm the interests of others (other than  by taking your nation out of the network, either fully or partially).&nbsp; But he who controls the root directory of  the Internet can, literally, disconnect an entire nation.</p>
                      <p align="left" class="style2">As time goes on, issues that  require international cooperation are bound to multiply rather than diminish,  and today we have no comprehensive, binding (or even consensual and  demonstrably successful) process for dealing with them.&nbsp; </p>
                      <p align="left" class="style2">So what are we to do?&nbsp; </p>
                      <p align="left" class="style2">Sadly, there are four, seemingly  intractable problems to be solved.&nbsp;  Using global warming as an example, the first is identifying and  quantifying the problem to be addressed (is the atmosphere warming, and are we  indeed the cause?).&nbsp; The second is  agreeing on the priority to be assigned to the problem at hand (which element  should be given greater weight: the impact to the economy, or the impact to the  environment?).&nbsp; The third is the means  of addressing the risk (should emission controls fall equally on all nations,  or should third world countries be granted allowances to compensate for the  damage already done by those nations that have already achieved a modern stage  of development?)</p>
                      <p align="left" class="style2">And lastly, there is the hardest  question: will nations be willing to yield a measure of their historical  sovereignty in order to achieve effective solutions to global problems?</p>
                      <p align="left" class="style2">To date, the answer to that  question has been &quot;no,&quot; which has made reaching a decision on the  first three questions that much more difficult.&nbsp; Some 86 years after the founding of the League of Nations, there  is still no global body that is empowered to create a law that is binding upon  all.</p>
                      <p align="left" class="style2">If we assume that the answer to  the sovereignty issue will continue to be &quot;no&quot; for some time, then we  have no choice but to find a different way to solve problems such as global  warming, because we cannot wait to find a solution.</p>
                      <p align="left" class="style2">Perhaps a model for that solution  may be found in the standard setting process.&nbsp;  After all, what other system exists that is not only global, consensual  and successful, but involves the voluntary giving up of rights in order to  achieve a common good?&nbsp; This process, in  fact, has found a solution to the conundrum of the Tragedy of the Commons by  identifying benefits in the exploitation of opportunities that can only be  achieved by reaching consensus.</p>
                      <p align="left" class="style2">The magic of the standard setting  process is that it makes the risk of standing outside the process greater than  the costs and concessions required to participate.&nbsp; In consequence, not only do those whose participation is vital to  success willingly engage in the process, but they adopt the resulting standards  without the compulsion of laws as well. </p>
                      <p align="left" class="style2">To an extent, this is how the  treaty process has historically operated as well, but characteristically  sanctions for non-compliance have been required in order to achieve success,  because compliance may be seen to be more costly and disadvantageous than  non-compliance.</p>
                      <p align="left" class="style2">The lesson of the standard setting  process, then, is that it takes carrots as well as sticks to bring stakeholders  -- multinational corporations as well as nations -- not only to the table, but  to compliance as well, until that day when even more urgent changes force a  different answer to the sovereignty question.</p>
                      <p align="left" class="style2">Perhaps the WSIS process will  provide the crucible within which a first step in this direction may be taken,  as the standard setting process and nationalism meet in the same room, in an  effort to solve a common problem.</p>
