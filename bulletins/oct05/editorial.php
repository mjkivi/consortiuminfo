                          <td width="500" valign="top" class="style2"><p><em>The Earth was small, light blue, and so touchingly  alone,</em> <em>our home that must be defended like a holy relic.</em></p></td>
                        </tr>
                        <tr>
                          <td valign="top" class="style2"><div align="center">Soviet Cosmonaut Aleksei Leonov</div></td>
                        </tr>
                        <tr>
                          <td valign="top" class="style2">&nbsp;</td>
                        </tr>
                        <tr>
                          <td valign="top" class="style2"><em>We <u>are</u> as gods, and might as well get good at it.</em></td>
                        </tr>
                        <tr>
                          <td valign="top" class="style2"><div align="center">Purpose  statement, the Whole Earth Catalog</div></td>
                        </tr>
