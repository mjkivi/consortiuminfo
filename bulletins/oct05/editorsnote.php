                <p><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITOR'S NOTE:</em></strong></font></p>
                <div align="center">
                  <div align="center">
                    <p align="center" class="style10"><strong>GOVERNMENTS, SSOS AND  SOCIETY</strong></p>
                    <p align="center" class="style10"><strong>(PART III)</strong><br>
                    </p>
                    <p align="left" class="style2">This issue is the third in a series  exploring the evolving relationship between the private and public sectors, as  they confront the increasingly intractable challenges of a modern world.&nbsp; </p>
                    <p align="left" class="style2">This series began in August, with an  examination of the poor coordination that too often characterizes the standard  setting partnership between the government and private-sector in the United  States.&nbsp; That issue included a call to  optimize that relationship to national advantage.&nbsp; The September issue focused on a current drama unfolding in the  Massachusetts state government, involving both open standards and open source  software.&nbsp; The current experience of the  Commonwealth highlights the importance of these tools in the modern world, as  well as the significant role that governments can play in accelerating their  adoption by society at large.</p>
                    <p align="left" class="style2">In this issue, I look to the future, and  ask whether we will be able to meet new challenges (such as global warming)  that are of a type and magnitude that our existing political systems seem  inadequate to address, and which will require a variety of standards solutions. </p>
                    <p align="left" class="style2">This examination begins in the <em><strong>Editorial</strong></em>, which contrasts  the current need for universal cooperation to solve global issues with the slow  progress achieved to date towards that goal, and asks whether the consensus  based standard setting process might provide a better model for the future than  historical treaty negotiations.</p>
                    <p align="left" class="style2">This month&rsquo;s <em><strong>Feature Article</strong></em> looks far into the  future, suggesting what a long-term approach to solving global environmental  issues might resemble, as we become better able to monitor the effects of  regulations, and to utilize the resulting data to refine such restrictions  directed at environmental preservation and enabling sustainable  development.&nbsp; The result would be the  need for dynamic rather than static standards, capable of adjusting to new  information as it becomes available.</p>
                    <p align="left" class="style2">The <em><strong>Trends</strong></em> piece this month  looks back to the September issue, and provides a timeline of the ongoing  developments in the OpenDocument saga.&nbsp;  As these events demonstrate, some of the largest technology companies in  the world are continuing to press their standards-based advantage in an effort  to break Microsoft&rsquo;s dominance in office productivity software, providing a  unique opportunity to observe how a single standard can profoundly affect the  marketplace.</p>
                    <p align="left" class="style2">My <em><strong>Standards Blog</strong></em> selection for  this month looks in on the status of a different global governance story  relating to standards: the World Summit on the Information Society that will  conclude its originally contemplated work at a meeting in Tunis in  November.&nbsp; Going into that meeting, the  United States stands alone regarding the control of the root directory of the  Internet, and in this post, you can learn how you can log on and tell the U.S.  Ambassador to the process who you think should &quot;govern the Internet.&quot;</p>
                    <p align="left" class="style2">And finally, this month's <em><strong>Consider This</strong></em> tries to answer  the question of &quot;why standards matter&quot; in the greater context of the  societal concerns that have been the subject of this series of issues. </p>
                    <p align="left" class="style2">Next month, we will conclude this series on the intersection of  standards, government and society with an in-depth of review of what happens at  Tunis in the final WSIS meetings, and what the decisions made there may portend  for the future.</p>
                    <p align="left" class="style2"> As always, I hope you enjoy this issue. </p>
