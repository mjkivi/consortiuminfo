              <p align="center" class="style8"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">TRENDS:</font></em></strong></p>
              <p align="center"><span class="style10">OPENDOCUMENT VS.  OFFICE:<br>
                THE LINES ARE DRAWN</span> </p>
              <p align="center">&nbsp;</p>
              <p align="left"><span class="style2">If commerce can be theater, than certainly the current full-frontal assault on Microsoft Office by a significant number of the largest technology companies in the world is such, and political theater at that.  And while the OpenDocument office suite and then the format of the same name developed by OASIS have been under development for years, it was the announcement by Massachusetts that its Executive Agencies, for pragmatic reasons, would be required to use products supporting the OpenDocument OASIS Format that unleashed the corporate hounds in full cry in the hunt to tree Microsoft at last. </span>
              <p align="left"><span class="style2"> Since the last issue of the Consortium Standards Bulletin <a href="http://www.consortiuminfo.org/bulletins/sep05.php"> OpenDocument and Massachusetts:  the Commonwealth Leads the Way</a></a> scarcely a day has passed without some announcement, large or small, calculated to maintain the maximum momentum possible in the drive to break Microsoft's dominance in office productivity software, an immensely profitable line of business (at least for a vendor that can maintain an 85% market share). </span>
              <p align="left"><span class="style2"> The stakes, of course, are much greater than just stealing market share in a single product space.  If Microsoft's competitors can succeed with OpenDocument, then Microsoft will not only be weakened in this product space, but both open source software and open standards will gain credibility and appeal in the marketplace as well. </span>
              <p align="left"><span class="style2"> This brief space in time thus presents a unique opportunity to observe a variety of fascinating dramas unfold concurrently in rapid, real time, from the combined efforts of companies like Sun and IBM to press their advantage, to Corel's Quixotic effort to accomplish whatever Corel is trying to accomplish (itself an open question as of this writing), to Microsoft's public positioning regarding OpenDocument at the same time that may assume it to be pushing for a counterattack in the Massachusetts legislature. </span>
              <p align="left"><span class="style2"> And all of this has been made possible by the creation of a humble (if ambitious and complex) standard.  If the current efforts to unhorse Microsoft in the office productivity software marketplace are even partially successful, let it not be forgotten what was the nature of the engine of destruction that eventually accomplished a decades-long quest. </span>
              <p align="left"><span class="style2"> With that as prelude, here is a day-by-day chronology of the more significant developments in the last 30 days in the continuing OpenDocument saga.  As this issue goes to virtual press, the crescendo continues to build. </span>
              <p align="left"> <span class="style2"><strong>September 27: </strong>Long-time Redmond nemesis (and more recently partial ally) Sun Microsystems <a href="http://www.sun.com/smi/Press/sunflash/2005-09/sunflash.20050927.1.html"> releases StarOffice 8</a>, which supports the OpenDocument format, to much fanfare. </span>
              <p align="left"> <span class="style2"><strong>September 28:</strong> eWeek.com reporter Steven J. Vaughan-Nichols reports that Communications Manager for Corel WordPerfect Greg Wood <a href="http://www.eweek.com/article2/0,1759,1864843,00.asp"> has told him</a> that "While Corel won't commit to a date for adding OpenDocument to WordPerfect, the company made it clear that it is working towards that goal." </span>
              <p align="left"> <span class="style2"><strong>September 29:</strong> Sun <a href="http://www.itweek.co.uk/vnunet/news/2143045/sun-throws-patent-weight-behind"> pledges not to assert</a> any of its patents against OpenDocument implementers, thereby neutralizing question earlier raised by Microsoft. </span>
              <p align="left" class="style2"><strong>September 30:</strong> FoxNews.com <a href="http://www.foxnews.com/story/0,2933,170724,00.html"> posts a commentary</a> critical of OpenDocument by James Prendergast, Executive Director of Americans for Technology Leadership.  After a flood of objections from readers, Fox acknowledges that it should have disclosed that Microsoft was a founder of ATL.  Fox later posts <a href=" http://www.foxnews.com/story/0,2933,172063,00.html"> a sampling</a> of the comments received.
              <p align="left"><span class="style2"> <strong>October 3:</strong> Microsoft announces that its next release of Office, Office 12, <a href="http://www.techworld.com/applications/news/index.cfm?NewsID=4505"> will support Adobe PDF </a>, the second format (besides OpenDocument) that Massachusetts will allow to be used for saving documents.  Microsoft attributes decision to "customer demand," saying that it has received 120,000 requests a month for this change. </span>
                    <p align="left"><span class="style2"> <strong>October 4:</strong> Computerworld <a href="http://computerworld.com.sg/ShowPage.aspx?pagetype=2&articleid=2742&pubid=3&issueid=66"> reports </a> that a study of large government departments found, "for many sites, it is now 10 times cheaper to migrate to the new OpenOffice.org 2.0 than upgrading to Microsoft Office 12". </span>
                    <p align="left"><span class="style2"> <strong>October 10:</strong> I report at the <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1642"> Standards Blog</a> that Microsoft has indicated to me that support of OpenDocument is not impossible, likening the situation to PDF:  "For us this has been, and will continue to be a matter of evaluating the flow of customer requirements." </span>
                    <p align="left"><span class="style2"> <strong>October 10: </strong>Announcement of the formation of the <a href="http://opendocumentfellowship.org/Main/HomePage"> "Open Document Fellowship"</a> to support adoption of the OpenDocument format and products that support it.  A ZDNet.com article erroneously reports that OASIS is a co-founder of what is in fact a volunteer-supported. </span>
                    <p align="left"><span class="style2"> <strong>October 10: </strong>OASIS announces that it <a href="http://news.com.com/OASIS+submits+OpenDocument+as+standard/2100-7344_3-5892649.html"> submitted OpenDocument to ISO </a> on September 30 for adoption as a "Publicly Available Standard."  Achieving this status will make it much more likely that European and other governments will adopt it. </span>
                    <p align="left"><span class="style2"> <strong>October 13: </strong>OpenOffice.org, the developer of an open source office suite that has committed to support OpenDocument (and which supplied the precursor specification used by OASIS as a starting point for its development of OpenDocument) announces that the release of OpenOffice.org 2.0, which will support the OpenDocument format, <a href="http://www.desktoplinux.com/news/NS9034096132.html"> will be delayed</a>. </span>
                    <p align="left"><span class="style2"> <strong>October 17:</strong> David Berlind posts a c. 9,000 word investigative article at ZDNet.com examining whether Microsoft was taken by surprise by the Massachusetts OpenDocument decision.  The piece is entitled, <a href="http://news.zdnet.com/2100-3513_22-5893208.html"> Microsoft:  We were Railroaded in Massachusetts on ODF</a> </span>
                    <p align="left"><span class="style2"> <strong>October 18: </strong>In an <a href="http://www.betanews.com/article/Interview_Corel_on_Sun_Open_Standards/1129672161">interview at BetaNews.com</a>, Corel's Richard Carriere and Greg Wood promote WordPerfect's number 2 market position in office productivity software and criticize OpenDocument as "an unsupported standard" that it has no current interest in supporting. </span>
                    <p align="left"><span class="style2"> <strong>October 19: </strong>Corel is roundly criticized for the interview in <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1663">Shame on Corel</a> and at other sites that pick up the story.   Corel later shifts position (see October 25, below). </span>
                    <p align="left"><span class="style2"> <strong>October 20:</strong> OpenOffice.org <a href="http://www.openoffice.org/press/2.0/press_release.html"> announces release</a> of OpenOffice.org 2.0 </span>
                    <p align="left"><span class="style2"> <strong>October 25:</strong> David Berlind at ZDNet.com posts an email <a href="http://blogs.zdnet.com/BTL/?p=2064"> from Corel's Greg Wood </a> saying much nicer things about OpenDocument, but <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1679"> still not saying</a> whether, or when, it will support the format. </span> </span>
                    <p align="left"><span class="style2"> <strong>October 25: </strong>Dan Farber at ZDNet.com reports a conversation with Microsoft's Ray Ozzie, who makes eventual support by Microsoft sound <a href="http://blogs.zdnet.com/BTL/?p=2061"> much more likely</a>. </span>
                    <p align="left"><span class="style2"> <strong>October 25:</strong> InformationWeek <a href="http://www.informationweek.com/story/showArticle.jhtml?articleID=172900251"> confirms rumors</a> that Massachusetts Secretary of State William Galvin and State Senator Marc Pacheco are critical of OpenDocument and will hold a hearing regarding the Information Technology Division's OpenDocument policy. </span>
                    <p align="left"><span class="style2"> <strong>October 31: </strong>Massachusetts Secretary of State William Francis Galvin and State Senator Marc R. Pacheco hold a much-awaited hearing on the OpenDocument policy. </span>
                    <p align="left" class="style2"> The above is only a sampling of what has transpired since our last issue.  To see a more complete day-by-day selection of events and analysis and to follow the story as it continues to unfold, visit the OpenDocument subtopic heading at the ConsortiumInfo.org <a href="http://www.consortiuminfo.org/news/cat.php?CID=30">News Portal</a> and the <a href="http://www.consortiuminfo.org/newsblog/blogcat.php?CID=30"> Standards Blog</a>.</p>
