              <p align="center" class="style8"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">STANDARDS BLOG:</font></em></strong></p>
              <p align="left" class="style56"> October 29, 2005</p>
              <p align="center"> <strong> <span class="style10"><strong>Tell the Government What You Think 
                About the Future of the Internet </strong></span></strong></p>
              <p align="left" class="style2">For over a year I've been covering the United Nations sponsored <a href="http://www.itu.int/wsis/">"World Summit for the Information Society"</a> (WSIS) process, which hopes to bring the benefits of the Internet and the Web to all peoples everywhere, a very honorable and important goal.  Not surprisingly, this process has raised the question of "who should control the Internet?"  That question has become extremely emotional, with the United States government saying "Us!" as regards the root directory of  the Internet (which is currently under our control), and the rest of the world saying, in so many words, two words that I won't repeat here. </p>
              <p align="left" class="style2">Amazingly enough, on November 2, you'll have the opportunity on-line to tell the U.S. representative in this process who you think should hold the keys.  I'll tell you how later in this post. </p>
              <p align="left" class="style2">But first, a little background.  Who controls the Internet today? </p>
              <p align="left" class="style2">Right now, the answer is, "it depends on what aspect you're talking about."  If you're talking about the standards that enable the Internet, the answer is a number of non-profit, global consortia (e.g., the W3C and the IETF).  But if you're talking about the root directory of the Internet &ndash; the country codes (in one database) and the individual addresses given to individual computers (in another) -- then you're talking about the International Corporation for Assigned Names and Numbers, popularly referred to as <a href="http://www.icann.org/"> ICANN</a>. </p>
              <p align="left" class="style2">ICANN is, on the one hand, a non-profit that is headquartered in the U.S. and has an international Board.   But on the other hand, it is subject to the will of the National Telecommunications and Information Administration, which in turn is a unit of the U.S. Department of Commerce.  And there's the rub, because theoretically, someone in Washington could call someone in California at ICANN some day and tell him to turn off, oh, say, Iran or North Korea (not that they would, of course). </p>
              <p align="left" class="style2">So when the WSIS process began, the International Telecommunication Union (ITU), which might have, but didn't, bid for control of the Internet during it's chaotic early days, put the "control of the Internet" on the table (the ITU is an agency of the UN, and is also primarily responsible for organizing and running the WSIS process).  As a result, the Working Group on Internet Governance (WGIG) came into being, and discussion quickly zeroed in on control of the root directory as a matter of international concern. </p>
              <p align="left" class="style2">All that's happened in the past two years would fill a book (and doubtless some day will), but if you want to get quickly up to speed, here's how to go about it.  Start with this article from July of 2004, called <a href="http://www.consortiuminfo.org/bulletins/july04.php#feature">Who Should Govern the Internet?</a> Then go to the ConsortiumInfo.org News Portal <a href="http://www.consortiuminfo.org/news/cat.php?CID=25">WSIS/Internet Governance</a> subcategory, and you'll find c. 25 articles and blog entries from the past five months tracking what's been happening leading up to the final WSIS meeting in Tunis, including the recent PrepCon3 meeting in Geneva when even the European Union deserted the U.S.  A particularly good article by Kevin Murphy linked there describes how the current system works. </p>
              <p align="left" class="style2">In truth, this is a big and important issue, because although today it's America' condescending attitude regarding ICANN that is under the microscope, in the future, it will be other issues, including, perhaps, the future technical development of the Internet or the Web, whether content could be censored, and so on.  So what happens in Tunis matters to everyone, in the first world as well as the developing world, because it represents the beginning, and not the end, of the discussion on such issues. </p>
              <p align="left" class="style2">So with that as prelude, here's how you can let the U.S. government know who you thinks should "govern the Internet" in the future. </p>
              <p align="left" class="style2">The lead representative for the U.S. is <a href="http://www.state.gov/r/pa/ei/biog/46292.htm">Ambassador David A. Gross</a>, a Bush appointee who has served since August 2001 as the U.S. Coordinator for International Communications and Information Policy in the Bureau of Economic and Business Affairs.  If you go to an October 28 posting at the USINFO.STATE.GOV site called <a href="http://usinfo.state.gov/xarchives/display.html?p=washfile-english&y=2005&m=October&x=20051028142855cmretrop0.290539&t=livefeeds/wf-latest.html">Expanding Internet Access Must Remain World Focus at Summit</a> you'll see the current U.S., position.  And at the very end of the post, you'll find this: </p>
              <div align="left">
                <blockquote>&nbsp; </blockquote>
              </div>
              <blockquote><p align="left" class="style2">In a November 2 Internet chat, Gross will preview the upcoming WSIS summit and discuss his views on why the current governance structure is the best way to preserve the nature of the Internet as an innovative medium. </p>
                <p align="left" class="style2">Gross will be available to answer questions at 11 a.m. EST (1600 GMT). To ask a question or make a comment, please register at iipchat@state.gov. Questions and comments are welcome in advance of and during the November 2 program. </p>
              </blockquote>
              <p align="left" class="style2">So there you have it &ndash; your chance to make your point to the man who will make the U.S.'s points in Tunis November 16-18, or at least your chance to see how he answers the opinions of those who do choose to ask questions and make comments. </p>
              <p align="left" class="style2">Either way, you can follow how things progress here at the <i>Standards Blog</i>, as I will cover the run up to, and the outcome of, the Tunis meeting in detail.  It's likely that I'll also dedicate the November issue of the <a href="http://www.consortiuminfo.org/bulletins/">Consortium Standards Bulletin</a> to the same topic. </p>
