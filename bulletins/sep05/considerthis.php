                  <p align="left"> <span class="style2">Now we can see that for millennia, advances in storage technology led to improvements in attributes such as transferability (tablet to papyrus) or even cost per copy (printing press). But the types of data that could be stored remained constant and the attributes differentiating one medium from another were minor. The last such innovative leap in this sequence was the invention of the printing press. </span></p>
                  <p align="left" class="style2"> With the advent of modern technology, however, the number of storage methods and media began to proliferate. Once there were alternative media, input devices and output devices, all with different characteristics, there were real differences among them that favored one type of use over another. The result was choices &ndash; but also rapidly obsolete technology using physically unstable media &ndash; resulting in a radical decrease in the likelihood that data would survive throughout human (and technical) generations. </p>
                  <p align="left" class="style2"> In contrast, preservation of data used to be inevitable (at least to some meaningful degree), due to the durability of media, the existence of a sufficient continuum of recorded human knowledge (e.g., the Rosetta Stone), and a limited number of ways that information could be recorded (e.g., letters, symbols) which facilitated deciphering even if that continuum was broken. </p>
                  <p align="left" class="style2"> But what of our Apple Nano? Absent a proprietary interface and a continuing supply of iTunes format music, our elegant little toy will become only an output device. Absent a battery, it becomes merely an inscrutable, albeit an undeniably stylish, wafer. </p>
                  <p align="left" class="style2"> And what of modern media itself? Thousands of Hollywood movies and television kinescopes have already deteriorated beyond redemption. Your LPs were left at the Swap Shed at the town transfer station years ago. Cassettes? Not even your car has a cassette deck anymore. The expected life of a CD or DVD? Don't ask. Your personal data? On your hard drive (have you backed up your computer today?) and a variety of servers scattered around the country. Virtually all of the news of the last five years? Servers again. No paper. </p>
                  <p align="left" class="style2"> On the other hand, what of the works of Shakespeare, Avicenna, Thucydides, Mohammed, and Confucius? No matter how small your town, most or all of them will be found in the local library, each having been easily preserved, transcribed and handed down over the years, both in their original language as well as in myriad translations. </p>
                  <p align="left" class="style2"> The moral of the story is that with choices come responsibilities. Not necessarily on a personal level, but certainly as a society. It is in this sense that the recent bold decision of the Massachusetts Information and Technology Division (see: <a href="http://www.consortiuminfo.org/bulletins/sep05.php#feature">Massachusetts and OpenDocument: A Brave New World?</a>) represents a watershed in technologic civic responsibility, whether its implementation proves to be easy or painful. </p>
                  <p align="left" class="style2"> It&rsquo;s also just in time. For some hundred and fifty years now, we have enjoyed a heady florescence of media choices that have added immeasurably to the richness of our lives and the ability to express ourselves. But now we need to think more carefully about whether this richness will be preserved, and how. As technology continues to evolve, the difficulty of the task increases, rather than the opposite, unless we are willing to sacrifice some of the positives in exchange for more robust attributes such as longevity, transferability and ease of access over the long term. In the world of analog devices, the challenges were already difficult. In a digital world, absent rigorous interoperability standards and a new sense of discipline, it becomes impossible. </p>
                  <p align="left" class="style2"> In short, we need to begin to balance choices with responsibility, and to create the infrastructure necessary to preserve and pass on to our heirs what has been created during our lifetimes to the same extent (if not necessarily in the same way) as the information of the last generation was preserved for us by our own ancestors. </p>
                  <p align="left" class="style2"> And if we do not? (How did that song go&hellip;something like &ldquo;Thanks for the Memories?&quot;) </p>
