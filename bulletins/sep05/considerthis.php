                  <p align="left" class="style56"> <strong> [][][] <strong> September 29, 2005</strong> </strong></p>
                  <p align="left"><span class="style56">#32</span><strong class="style38"><em> Clay Tablets, iPods and Evo/Devolution</em></strong></p>
                  <p align="left" class="style2">The new Apple iPod Nano&#8482; is truly the perfect marriage of stylish modern design and cutting edge technology.  It weighs but 1.5 ounces, and, as one typically rhapsodic reviewer (struggling for a suitably expressive method of comparison) <a href=' http://www.pcmag.com/article2/0,1895,1857405,00.asp'>expressed it</a>, is only "five dimes long.&quot;  He went on to announce that the new Nano had received the PCMag.com "Editor's Choice&quot; award. </p>
                  <p align="left" class="style2">And it is rugged, too.  Two critics at the arsTechnica Website <i>(<a href='http://arstechnica.com/reviews/hardware/nano.ars/3'>&quot;Serving the PC enthusiast for over 6x10-2 centuries&quot;)</a></i> were also struggling for an angle for their review, and subjected a Nano to a series of increasingly violent "stress tests,&quot; failing to still its tiny, pristine voice even by driving a car over it -- twice. </p>
                  <p align="left" class="style2">The Nano is certainly, then, the <I>ne plus ultra</I> of storage technology: capable of storing massive amounts of data in the blink of an eye, of delighting its owner for up to 14 hours on a single charge, and even content to be physically abused in new and extreme ways for the amusement of jaded consumer electronic critics. </p>
                  <p align="left" class="style2">Or is it? </p>
                  <p align="left" class="style2">In some ways the Nano is vastly inferior from a technological perspective to one of the earliest means of storing data: the clay tablet. </p>
                  <p align="left" class="style2"> Consider this: the Nano is a perfect example of what might be called "simultaneous evo/devolution&quot; (as in &quot;two steps forward, two steps back&quot;).  For at the same time that it represents the (current) apotheosis of instant storage and reproduction technology, it is also representative of our recent plunge into an information black hole that is swallowing all newly-created data, potentially never to be found again. </p>
                  <p align="left" class="style2">The useful storage of information, after all, depends upon how you define "useful." For example, attributes of utility would normally include the following (in no particular order): ease of input, ease of output, cost, fidelity, longevity, portability and transferability (i.e., can the data be easily extracted and stored elsewhere?). </p>
