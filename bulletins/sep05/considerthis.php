                  <p align="left" class="style2"> For thousands of years, the type of comparison just performed would have been meaningless. Why? Because there were no choices of media that had very different attributes. And also because until the dawn of the information technology age, the state of the information recording art progressed slowly sequentially, rather than rapidly and in branching fashion. </p>
