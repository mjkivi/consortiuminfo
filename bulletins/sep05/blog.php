                  <p align="left" class="style2">Puritan divine John Winthrop  famously uttered those words (based on a scriptural passage in the Gospel of  Matthew) in a sermon in Boston in 1630. He also included them in a work  entitled &ldquo;On Christian Charity.&quot; His memorable phrase has been transplanted  into various settings, and employed for many purposes, ever since (Ronald  Reagan was particularly prone to quote Winthrop to illustrate his own vision of  the mission of America). </p>
                  <p align="left" class="style2">One is tempted to recall  Winthrop&rsquo;s cautionary words yet again in the context of the decision by the  Commonwealth of Massachusetts to require use of the OpenDocument format by all  of its Executive Agencies, becoming the first government of any size in the  world to embrace the OpenDocument format. </p>
                  <p align="left" class="style2">There are more than  superficial resemblances between calling for right conduct in public and right  conduct in information technology. Making IT transitions is always, to state it  frankly, a pain in the neck. One that is necessary on a frequent basis, to be  sure, but what CIO enjoys procuring, installing, learning (and worst of all)  training in connection with deploying a new IT resource? Governments,  therefore, can play a key role in getting a marketplace over the hump of  adopting new technology, especially where businesses are not sufficiently  motivated to do so for their own reasons. </p>
                  <p align="left" class="style2">In the case of  Massachusetts, there is something of the flavor of John Winthrop&rsquo;s messianic quest  as well. The Commonwealth, after all, was one of the most spirited states in  pursuing antitrust charges against Microsoft not so long ago, and one of the  most unwilling to fold its tents after the Department of Justice opted to  settle. And while all (including Microsoft) agree that the current decision has  nothing to do with the prior litigation, it nevertheless is an effort to strike  a blow for freedom of data.<br>
  &nbsp;<br>
                    Is it over-reaching to  recall Winthrop in such a context? Perhaps not, given the fervor of the open  source community. True, the Open Document format is a standard, not an open  source software product. But it has been instantiated in OpenOffice,&nbsp; an open source office suite, and the uptake  of other open source software will doubtless receive a solid push from the  decision of Massachusetts to support OpenDocument.&nbsp; And the goal of the new policy - independence from proprietary  lock-in - is the same in each case. </p>
                  <p align="left" class="style2"> But still, is that enough to invoke a comparison between Peter Quinn and John Winthrop? Perhaps it is. Consider the following press release from Open Source Victoria (as in Victoria, Australia), titled, <a href="http://www.linuxpr.com/releases/8128.html">OVA to States: Follow Massachusetts in Open Document Standards</a> (the states being referred to, incidentally, are those Down Under, and not in North America). In part, the press release reads as follows: </p>
                  <div align="left">
                    <blockquote>&nbsp; </blockquote>
                  </div>
                  <blockquote><p align="left" class="style2"> The Commonwealth of Massachusetts has announced the adoption of the OpenDocument XML file format as its preferred method for storing government documents&hellip;.Open Source Victoria calls on all&hellip;Australian states and government agencies to also adopt this format, as it is the only viable approach to ensuring guaranteed access to public sector documents and data in perpetuity. </p>
                  </blockquote>
                  <p align="left" class="style2"> John Winthrop would hardly know what to make of software, but doubtless he would be pleased to know that a shot fired in Massachusetts can still be heard around the world. </p>
