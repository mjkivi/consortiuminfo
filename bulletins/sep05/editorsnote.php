                <p><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITOR'S NOTE:</em></strong></font></p>
                <div align="center">
                  <div align="center">
                    <p align="center" class="style10"><strong>GOVERNMENTS, SSOS AND  SOCIETY</strong></p>
                    <p align="center" class="style10"><strong>(PART II)</strong><br>
                    </p>
                    <p align="left" class="style2">This month, I present the  second in a three-part series of issues on the interplay of government and the  private sector organizations that develop standards and open source software.</p>
                    <p align="left" class="style2">In the last issue, we looked  to the past, exploring both the discrete as well the overlapping domains in  which the public and private sectors have historically been active, and noted  the lack of overall coordination and cooperation between these independent but  parallel processes.&nbsp; In the Editorial to  that issue, I called for greater collaboration between these too-often  disconnected processes (requests to republish that editorial in both public and  private sector  publications have been granted).&nbsp; </p>
                    <p align="left" class="style2">In this issue, we look to  the present.&nbsp; Governments have often  been the developers or early adopters of important new standards, often because  their unique needs lead them to place a higher value on certain requirements  than does the private sector.&nbsp;&nbsp; A  current example of this phenomenon is the decision by the Information  Technology Department of Massachusetts to mandate the use of an important new  type of information technology resource - open data formats - by its Executive  Agencies.&nbsp; Massachusetts is the first government  in the world to impose this requirement on itself.&nbsp; The decision of the Commonwealth has the potential to inspire  other governments in the United States and throughout the world to follow its  example, and provides the focus for all of the articles that we bring to you  this month.<br>
  &nbsp;<br>
                      My analysis begins in the <em><strong>Editorial</strong></em>, which places the role of government in accelerating the adoption of  valuable new capabilities in historical perspective.&nbsp; In the 19th century, the federal governments of both  the U.S. and Great Britain accelerated the proliferation and interconnection of  many private railways into infinitely more valuable national networks by  imposing railway gauge standards.&nbsp; Absent  such action, the process of seamless integration would have taken far  longer.&nbsp; Similarly, the decisions by multiple  governments around the world today to adopt (and sometimes mandate) the use of  open source software and open standards can be expected to drive even  proprietary vendors to adopt strategies consistent with the new requirements of  this enormous market sector.</p>
                    <p align="left" class="style2">In the <em><strong>Feature Story</strong></em>, I  provide a detailed review of the amendments to the Enterprise Technology  Reference Model of the Commonwealth of Massachusetts that became effective on  September 21, 2005.&nbsp; These changes will require  the saving of all Executive Agency documents in either the OASIS OpenDocument  Format or Adobe PDF after January 1, 2007.&nbsp;  My report is based on interviews with representatives of the  Commonwealth, OASIS, Microsoft,&nbsp; Sun  Microsystems&nbsp; and the Association for  Competitive Technology, as well as the amendments themselves and a variety of  comments submitted by interested parties.</p>
                    <p align="left" class="style2">In my <em><strong>Consider This</strong></em> offering for September, I follow the challenges of information preservation  further, noting a troubling paradox: while the modern information age has made  great strides in augmenting the ease of inputting and saving data, clay  tablets, handwritten records, the Gutenburg printing press, and Edison&rsquo;s wax  cylinders still represent superior modes of long term archiving when compared  to any modern technology-based solution currently available.</p>
                    <p align="left" class="style2">And finally, in my <em><strong>Standards Blog</strong></em> entry for this month, I compare the role of Massachusetts in becoming the first  government in the world to require OpenDocument compliance with another  challenge, this one taken up by the Puritan colonists that founded the  Commonwealth hundreds of years before.</p>
                    <p align="left" class="style2">Finally, a note about next  month, when I will offer the third and final issue in this series on the  interplay between the private and public sectors.&nbsp; In that issue, I will examine some as-yet unappreciated and vital  challenges that we will face in the future, which will demand an unprecedented  degree of public and private collaboration in order to do what will need to be  done.&nbsp; Look for that issue -- <strong><em>&ldquo;Standards  for a Small Planet&quot;</em></strong> -- to arrive in October.</p>
                    <p align="left" class="style2"> As always, I hope you enjoy this issue. </p>
