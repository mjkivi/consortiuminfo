                  <p align="center"><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITORIAL</em></strong></font><strong> </strong></p>
                  <p align="center" class="style10"><strong> <strong> </strong>GOVERNMENTS AS ACCELERATORS </strong></p>
                  <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove<br>
                  </a></strong></font></p>
                  <p align="left" class="style2">There once was a time long ago when governments rightfully claimed a monopoly in standard setting.  When weights, measures and coinage became necessary tools for society, only the state had the power and the means to set such standards, and to provide the inspection and certification role needed to give credibility to these standards (a service governments still provide today). </p>
                  <p align="left"><span class="style2"> But with the Industrial Revolution, private industry began to set standards as well, in order to facilitate manufacturing and reassure potential customers of the safety of new products.  Government was quite content to allow industry to set standards for things like screw threads, and even safety-related standards for boilers and elevators when government was not sufficiently motivated to perform that job itself.  But government still reserved some areas of regulation to itself, in areas such as food and drug efficacy and purity, workplace safety, broadcast frequencies and various other domains where the exercise of the authority of the state was acknowledged to be necessary and proper. </span>
                  <p align="left"><span class="style2"> But just as industry sometimes assumed a standards task when government was disinclined to do so quickly enough to satisfy those seeking to access new commercial opportunities, governments have also intruded into private sector territory when its internal needs or its goals for society differed from those of the private sector (e.g., to set the standard distance between railroad rails in the United States and Great Britain, when the owners of individual railroads were not yet willing to give up the local travel monopolies that different rail gauges protected). </span>
                  <p align="left"><span class="style2"> Today we are witnessing another one of those times in history when government is choosing to act in this fashion.  This time, it is not rail transport, but the future of information technology that is in play.  The specific field of action is software, and the changes that governments are driving arise from their decision to adopt, and in some cases even mandate use of, open source software products and other products based on open standards rather than the proprietary products of individual vendors. </span>
                  <p align="left"><span class="style2"> This action on the part of governments arises from two factors: government's special role in society, and its evaluation of its own (sometimes unique) needs as the consumer of products and services. </span>
                  <p align="left"><span class="style2"> Both factors come into play with respect to the role of government as the long-term custodian of vast amounts of documents and data, resulting in concerns and demands that are not shared to the same extent by many other types of end-users.  Other motivations for governments around the world include a heightened awareness of security, a desire to avoid dependency on foreign-origin goods and services, and national competitiveness, depending on the level of government in question (local, state or national).  And, like all other end users, governments have legitimate concerns over costs of acquisition and ownership, abandonment, and control. </span>
                  <p align="left"><span class="style2"> Over the last five years there has been increasing integration of open source software into governments of all types around the world through individual procurement decisions on a product-by-product basis by public sector CIOs.  As far back as 2000, a two-week long <a href='http://developer.skolelinux.no/rapporter/dodfoss.pdf'> MITRE survey</a> found that the U.S. Department of Defense already employed 115 "FOSS&quot; (free and open source software) products in 251 different settings.  This incorporation of open source products was therefore largely a manifestation of government's acting to satisfy their own needs. </span>
                  <p align="left"><span class="style2"> But in the last several years, there has been an increasing trend by governments at all levels and in countries spread around the world to mandate the use of open source software products in fulfillment of their understanding of their obligations to the public at large. </span>
                  <p align="left"><span class="style2"> Given the vast purchasing power of governments, any continuation of this trend must necessarily have a profound impact on the IT marketplace, rearranging vendor intellectual property, development and licensing strategies. </span>
                  <p align="left"><span class="style2"> Of course, these preferences by governments would be infeasible if the requisite open source products, and the equally necessary open standards needed to realize the maximum benefit from such software, did not exist.  Happily, myriad parallel efforts have been proceeding at a rapid pace in the private sector, providing the Linux/Apache/MySQL/Perl-PHP-Python (LAMP) server stack, the OASIS OpenDocument office suite format standard (as implemented in the open source OpenOffice suite and other packages), and the FireFox browser, to name only a few. </span>
                  <p align="left"><span class="style2"> The result is that we are at one of those points in time, as in the early days of the railways, when governments have the opportunity and the motivation to push society at large across a crucial chasm in decisive fashion.  Interestingly, governments today are not mandating what vendors must conform to when selling to private sector customers.  Instead, governments (even when they pass rules mandating exclusive or preferred use of open source products) are simply saying what they are willing to buy.  Given the massive market power of government IT purchasing, however, any widespread continuation of this trend will be more than sufficient to have a profound and predictable impact on vendor development and licensing decisions. </span>
                  <p align="left"><span class="style2"> While government intervention is no more necessary today to preserve the forward momentum of open source software than it was to set railway gauges (which would have been addressed in the private sector eventually), such action will provide a valuable accelerating effect, injecting a note of reality into the marketplace that would otherwise manifest itself more slowly through the interplay of normal market forces.  If many states and national governments follow the lead of the Commonwealth of Massachusetts, for example, and require internal usage of products conforming to the OpenDocument OASIS Standard, Microsoft may well decide how to address backward compatibility issues with its products, if not in the initial release of Office 12, then certainly in an interim release not long after, in order to conform to support the OpenDocument Format. </span>
                  <p align="left" class="style2"> The end result of government acceleration of open source and open standards based IT products, as with the connection of all parts of a nation by fast railway and highway networks, will be an acceleration of opportunities for commercial vendors and individuals alike, an increase in the favorable network effects of more seamless and facile information communication and transport, and the augmentation of the stability and security of society.</p>
