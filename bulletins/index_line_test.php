				<h4>ABOUT THIS ISSUE:</h4>
				<!-- <h3>STANDARDS AND THE ENVIRONMENT</h3> -->
				<h3>Modern Times</h3>
				<p>Two and a half years ago I wrote an issue titled <a href="http://www.consortiuminfo.org/bulletins/may05.php">Revolution Time (Again)</a>. The premise of that effort was that the consortium-based IT industry was grappling with a number of new challenges (such as convergence and open source software licensing regimes) that a more formal structure might to some extent help address. After examining the creative ways in which consortia were rising to the occasion in my <a href="http://www.consortiuminfo.org/bulletins/may05.php#feature">feature article</a>, I closed with this observation:</p>
				<blockquote>Still, one cannot help but look back with some nostalgia to a time when the IT industry was a fully committed member of an acknowledged, global system, created for the express purpose of developing non-competing, rationally related, universally adopted standards. Perhaps a way can be found to create structures that would allow the IT industry to enjoy more of the benefits of such a system in the future, without once again becoming constrained by the lack of flexibility that led to the fragmenting of the IT standard setting world some twenty years ago.</blockquote>
				<p>Today, consortia are more established than ever in the ICT industry &mdash; and they still lack a central organization. Moreover, the standards world continues to offer only the most elementary ability to differentiate standards organizations (they are either accredited or they are not) or standards (they are either globally adopted or they are not).</p>
				<p>All of which would be fine, if we were still living in an age of railway gauges and screw threads, rather than global telecommunications networks, worldwide product monopolies, patent inequities between the developed and developing worlds, and the increasing popularity of open source software.</p>
				<p>To operate in these more complex modern times, I suggest in this issue that not only is a new global standards organization needed to serve the needs of the ITC industry, but that entirely new types of certification are needed as well &mdash; not for standards compliant products, but for standards organizations themselves.</p>
				<p>I make the case for these new structures in my <strong><em>Editorial</em></strong>, aptly titled <strong><em>Leaving Home for Good: It's Time for a Global Consortium Standards Organization</em></strong>. I examine the subject in much greater detail in this issue's <strong><em>Feature Article</em></strong>, identifying the areas in which new services are needed, and laying out a suggested framework for providing them.</p>
				<p>In this month's selection from the <strong><em>Standards Blog</em></strong>, I turn from the solution back to one facet of the problem, describing the collateral damage that occurred to SC 34, an important and (until recently) extremely productive ISO/IEC JTC 1 Committee, when Microsoft's OOXML specification came up for a vote. Sadly, the integrity of the standard setting system has taken a severe hit in that committee, as well as globally, where the voting in many National Bodies was marred by reports of undue vendor influence, the deliberate spreading of disinformation, and in one case, offers to reimburse business partners for standards body dues as an incentive to buy votes.</p>
				<p>In my <strong><em>Consider This</em></strong> piece I turn from technical to social standards, and reflect on a different kind of collateral damage to the integrity of another important process created to uphold standards: the American criminal justice system.</p>
				<p>And finally, I introduce a new feature in this issue, called the <strong><em>Monday Witness</em></strong>, which will focus on the success or failure of governments in upholding the standards we expect of them. Two selections are included in this issue: the first explains the premise for this series, and the second is an example of that premise applied to current events. Each issue I'll include a selection from this new topical series, which you will also be able to find at the <strong><em><a href="http://www.consortiuminfo.org/standardsblog/index.php?topic=3584">Standards Blog</a></em></strong>.</p>
				<p>As always, I hope you enjoy this issue.</p>
