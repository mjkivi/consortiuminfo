				<h4>STANDARDS BLOG:</h4>
				<h3>OOXML Payback Time as Global Standards<br />Work in SC 34 "Grinds to a Halt"</h3>
				<p class="center"><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></p>
				<h6>Date: October 16, 2007<br /><span class="views">Views: 18,615</span></h6>
				<img class="imgright" src="/images/odf.gif" alt="" width="50" height="50" border="0" />
				<p>One of the more egregious behaviors observed in the recent global vote on OOXML was the sudden and last minute surge to join not only various National Bodies just before they voted on OOXML, but also the relevant committee of ISO/IEC for the same purpose. At the latter level, <a href="http://www.consortiuminfo.org/standardsblog/article.php?story=20070831072521575">not one but two</a> unusual membership changes occurred. During the voting period, more and more countries joined SC 34, the committee within ISO/IEC's Joint Technical Committee 1 (JTC1) that addresses document formats, at the Observer (O) level. Then, in the final weeks and days before the voting closed, many of these new members as well as some longer term members suddenly upgraded their status to Principal (P) membership, thereby gaining greater influence in the final vote under the complex rules under which the committee operates (those rules are described in detail <a href="http://www.consortiuminfo.org/standardsblog/article.php?story=20070831151800414">here</a>).</p>
				<p>SC 34 is one of the more important and active committees in JTC1, and has a constant stream of standards under active consideration and balloting.  In anticipation of the OOXML vote, its membership surged &mdash; with 23 new National Body members, and the number of P members spiking by 11.   When almost all of the new members voted for adoption (most of those countries that were long term P members voted against adoption, with comments), many felt that the standard setting process had been abused.</p> 
				<p>Unfortunately, the damage has not stopped there: since the OOXML ballot closed on September 2, not a single ballot has received enough votes to count in this important committee. Why? Because the last minute arrivals to SC 34 are not bothering to vote.</p>
				<p>The resulting gridlock of this committee was as predictable as it is unfortunate. The extraordinarily large number of upgrades in the final months, and particularly in the final days, therefore seemed attributable not to an abiding investment and interest in the work of SC 34, but in the outcome of a single standards vote. That conclusion is now certain, given the voting performance of the upgraded members since they cast their votes on OOXML.</p>
				<p>The specific problem arises from the rules under which JTC1 committees operate, which are intended to ensure that specifications do not become approved unless there is sufficient interest in them, as well as adequate review, to merit issuance as global standards. One of these rules is that at every balloting stage, at least 50% of the P members eligible to vote must in fact return a ballot. Even this requirement, however, does not set a high bar, because a member is permitted to return a ballot of "Abstain," and inadequate review to form an opinion is accepted as a valid reason to abstain. As a result, returning a vote of "abstain" constitutes at best only the most minimal level of good citizenship.</p>
				<p>Sadly, even that level of responsibility has been lacking in the newly upgraded members, whose numbers have dramatically raised the number of P members required to vote in order to advance a standard towards final approval. While I'm told that 90% of committee votes have achieved the necessary 50% return in the past, the current numbers tell a far different story: the three most recent ballots (<a href="http://www.jtc1sc34.org/repository/0902.htm">SC 34 N 870</a>, <a href="http://www.jtc1sc34.org/repository/0909.htm">SC 34 872</a> and <a href="http://www.jtc1sc34.org/repository/0910.htm">SC 34 N 874</a>) have all failed because of P member apathy.   As I read the tallies at those links, only one recent P member responded to a single ballot, even after some votes had been reissued for a second or even a third time. Had it not been necessary to include the new P members in the calculations, the second two votes would have passed (the first related to establishing a liaison relationship with another organization, and not a standard).</p>
				<p>The result is that a very important committee has, in the words of its Secretariat Manager in frequent pleas to the non-responsive members, "ground to a halt." The impact is significant, since this is the committee that controls standards such as RELAX NG (ISO/IEC 19757 Part 2), Schematron (ISO/IEC 19757 Part 3) and Topic Maps (ISO/IEC 13250) &mdash; not to mention ODF (if will be interesting to see if participation increases when Microsoft's PDF-competing XML Paper Specification advances to SC 34 from Ecma, where it is currently in preparation).  It is also a committee that attracts top talent on behalf of its traditional members, such as Ken Holman, Jon Bosak, Murata Makoto, Steve Pepper, Patrick Durusau, Francis Cave, Martin Bryan, and Rick Jelliffe, to name only a few.</p>
				<p>It's a sad story, and one that at present does not have a happy ending in sight. For now, those that want to advance the cause can only grow increasingly frustrated.  If you want to sample the depth of that frustration, read the excerpts reproduced below from Secretariat Manager Ken Holman's plaintive weekly memos. And if you know anyone involved in standard setting in Malta, Cyprus, Lebanon, Cote d'Ivoire (and so on), do everyone a favor and ask them to consider either voting, or dropping back to Observer membership.</p>
