				<h3>Words, Standards and Torture: What's in a Name?</h3>
				<p class="center"><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></p>
				<h6>Date: October 14, 2007<br /><span class="views">Views: 3,076</span></h6>
				<blockquote class="openingquote"><strong>Tor · ture&nbsp;</strong><em>noun: the act of causing great physical or mental pain in order to persuade someone to do something or to give information, or as an act of cruelty to a person or animal &mdash; </em><a href="http://dictionary.cambridge.org/define.asp?key=83918&amp;dict=CALD">Cambridge Dictionaries Online</a><br /><br /><em>For the purposes of this Convention, the term torture means any act by which severe pain or suffering, whether physical or mental, is intentionally inflicted on a person for such purposes as obtaining from him or a third person information or a confession,&hellip;or intimidating or coercing him or a third person,&hellip;when such pain or suffering is inflicted by or at the instigation of or with the consent or acquiescence of a public official or other person acting in an official capacity. It does not include pain or suffering arising only from, inherent in or incidental to lawful sanctions. &mdash; </em><a href="http://www.ohchr.org/english/law/cat.htm">Part I, Article 1, Section 1, Convention against Torture and Other Cruel, Inhuman or Degrading Treatment or Punishment</a></blockquote>
				<p>On October 4, the <em>New York Times</em> <a href="http://query.nytimes.com/gst/fullpage.html?res=990CE3DD1130F937A35753C1A9619C8B63">broke the story</a> that the U.S. Justice Department had issued secret legal opinions approving interrogation techniques such as simulated drowning; concluding that such practices did not meet the legal definition of torture. On October 7, the <em>Times</em> ran an editorial titled <a href="http://query.nytimes.com/gst/fullpage.html?res=9C02EFD91238F934A35753C1A9619C8B63">On Torture and American Values</a>. The piece read in part as follows:</p>
				<blockquote>Once upon a time, it was the United States that urged all nations to obey the letter and the spirit of international treaties and protect human rights and liberties. American leaders denounced secret prisons where people were held without charges, tortured and killed. And the people in much of the world, if not their governments, respected the United States for its values.</blockquote>
				<blockquote>The Bush administration has dishonored that history and squandered that respect. As an article on this newspaper's front page last week laid out in disturbing detail, President Bush and his aides have not only condoned torture and abuse at secret prisons, but they have conducted a systematic campaign to mislead Congress, the American people and the world about those policies&hellip;.</blockquote>
				<blockquote>The White House could never acknowledge that. So its lawyers concocted documents that redefined ''torture'' to neatly exclude the things American jailers were doing and hid the papers from Congress and the American people. That allowed the White House to claim that it did not condone torture, and to stampede Congress into passing laws that shielded the interrogators who abused prisoners, and the men who ordered them to do it, from any kind of legal accountability.</blockquote>
				<p>Why I am I writing about this topic in something called "The Standards Blog?"</p>
				<p>Besides the obvious fact that every American must take personal responsibility for what the American government does in his or her name, there is this: perhaps the oldest standards of all are words. Most standards are, after all, otherwise arbitrary and meaningless things that become distinctive and valuable only because we agree upon what they are supposed to mean. There is nothing inherently significant about 60 watts as compared to 55, and 32 ounces of fluid has no greater cosmic significance than 31. Even the otherwise rational elements of the metric system are divided or derived from arbitrarily chosen physical coordinates, such as the distance from the poles to the equator.</p>
				<p>Words, like weights and measures, also have meaning and value only to the extent that we share the same understanding of what they mean. When we depart from those commonly understood meanings, we fail to communicate effectively, either innocently to mutual disadvantage, or deliberately with the intent to deceive. And sometimes, we do worse.</p>
				<p>When we play fast and loose with standards such as weights and measures (e.g., at a gas pump) or with the purity or composition of materials (as in manufacturing), we are likely to break the law. Laws, of course, are made up of words and not numbers.  Some of those words are given definitions in the laws in which they appear. This is because breaking the law can, and should, have severe consequences, and words used in laws must therefore be used precisely and consistently.</p>
				<p>In order for there to be laws capable of providing protection beyond the boundaries of a single country, the words used in laws must also be agreed upon internationally. But if, having reached such an agreement, individual nations can arbitrarily redefine the meaning of a word that is at the crux of a law, then the international system of law, and indeed the prospect of having any global system of laws at all, breaks down entirely.</p>
				<p>The Twentieth Century bore brutal witness to what a world without common and enforceable laws, based upon a fundamental and inviolable sense of human dignity and human rights, can look like. Already, the Twenty-first Century is demonstrating an all-too great willingness to assume the same face. Must this sad start be the prelude to an unbroken future of more of the same?</p>
				<p>If this century is not to be an unconscionable replay of the last, we must not only agree to agree &mdash; but also to continue to agree &mdash; upon those words upon which the global protection of human dignity and individual survival is based. No nation should be any more above the definition upon which a law is based, than above the law itself.</p>
				<p>When words become part of international law, it should be a violation of international law to unilaterally reinterpret them.  And when we allow our government to engage in such behavior without speaking up, we become complicit in the same illegal behavior.</p>
