				<h4>CONSIDER THIS:</h4>
				<h6>November 19, 2007</h6>
				<h3><span class="red">#51</span> On Guilt or Innocence, and the Space Between</h3>
				<p class="center"><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></p>
				<p>The Western world prides itself on its secular system of justice, rightly pointing to the promise of equal, humane justice for all as a hallmark of an enlightened society. And while individual nations, states or courts may from time to time fall short of their ideals, those that are subject to the rule of law never lose sight of their constitutionally guaranteed rights, nor differ much on the essential details of what equal protection under the law should mean.</p>
				<p>At the very heart of the concept of justice is the presumption of innocence until proven guilty. And who could fail to be comforted by the pristine, irreducible certainty that such a binary concept offers? Either a prosecutor meets, or does not meet, the legal standards established to define guilt: "beyond a reasonable doubt," in the case of a criminal charge, or "by a preponderance of the evidence," in the case of a civil suit. If the evidence as evaluated by a jury of one's peers, does not meet the appropriate standard, then one is, as a matter of law, declared innocent.</p>
				<p>And yet&hellip; Is it possible that the application of such a time-honored standard could harbor within its few words a danger to society itself? As a matter of fact, the answer is yes, although the devil lurks in the implementational details, and not within the rule itself.</p>
				<p>Consider this: what happens when someone is arraigned, and must stand before a judge accused of a crime? The answer is that the accused must enter a plea, and that plea is similarly binary: guilt, or innocence. There is no third choice, because after the district attorney has determined the charge, the law does not recognize any space between.</p>
				<p>And there's the rub, for how many connections between motive and action in life can be judged in so black and white a fashion, let alone the facts and emotions that may lie behind what may be a crime of passion? Unlike virtually all other situations where one is called upon the carpet, whether by a parent, a spouse, a supervisor or a friend, it is rarely wise to use the word "but" in court followed by a rendition of extenuating circumstances. Why? Because extenuating circumstances are usually only relevant as between levels of punishment for certain crimes, such as involuntary manslaughter and manslaughter.</p>
				<p>True, extenuating circumstances may be introduced in the sentencing phase, at least in those situations where the judge has discretion to decide the severity of the sentence and is not bound by mandatory sentencing rules. But since guilt or innocence is determined before sentencing becomes necessary, there is no opportunity to first argue that, notwithstanding admitted facts, one should not be found to be guilty at all, by virtue of extenuating circumstances. Instead, the accused invariably plays it safe, and pleads innocence rather than guilt.</p>
				<p>This gives rise to a number of unintended consequences, the most obvious of which is that someone that knows that she is guilty must nonetheless plead innocent in self-defense, because the risks of pleading guilty and not having the extenuating circumstances taken fully into account are too high. Worse yet, those that sit in court, or read about the case in the press, are treated to the spectacle of someone that may be clearly guilty being defended by an attorney who is demonstrably seeking to portray the facts as other than all suspect them to be, because that offers the only alternative to automatic conviction.</p>
				<p>When the attorney succeeds in this endeavor, an injustice of a different type occurs &mdash; a defendant that actually committed a crime escapes punishment entirely. But regardless of the outcome, when the defendant believes that the charge does not match the reality, she is left with a system that offers no middle ground other than a plea bargain, which places great power in the hands of the district attorney, while still leaving the defendant vulnerable to the judge that must accept the plea, and who retains the freedom to impose a stricter sentence than the DA recommends.</p>
				<p>The result is a travesty of justice that plays out in courtrooms throughout the country every day &mdash; defendants that might actually plead guilty to a lesser charge than the prosecutor is willing to accept, or wish that their attorney could present the full facts and circumstances as they are, but cannot afford to take the chance.</p>
				<p>The system also places both defendant and defense attorney in a hopeless position: the defendant is entitled to tell his attorney anything in confidence, but under the rules of ethics the attorney is not permitted to plead to a judge that her client is guilty if she knows her to be guilty in fact. The result? Either the client must lie to her attorney about her guilt, or the attorney must either lie to the judge, or ask to be relieved from the case. The binary system once again offers no middle ground.</p>
				<p>And, of course, even the jury is drawn into the same morass, because it is trapped by the strict definition of the elements of the crime as well. Faced with a compelling defendant and circumstances that the jurors to believe in their heart of hearts that justice would only truly be served by an acquittal, they are faced with an illogical and seemingly unfair choice: to convict someone who their common sense tells them is innocent, or to acquit a defendant that the law tells them is guilty.</p>
				<p>These types of paradox can only be damaging to the public perception of the rule of law. A system that starts on the highest moral ground possible finds its ethics undermined, renders cynical those who are subject to the system, and forces those who agree to defend the guilty, often for free, to compromise their own integrity by participating in a charade where they are forced to say one thing in court, even if they believe another in their hearts.</p>
				<p>In a better world, perhaps there would be not just a defense attorney to guard the rights of the accused and a prosecutor to protect the interests of society, but a third advocate to defend the integrity of the system itself. That person, acting as a mediator, would hear both the accused and the prosecutor and would be bound to maintain the disclosures of each in confidence from the other, but could use that knowledge to negotiate with defendant and DA alike in order to seek to arrange a plea and a proposed sentence that truly reflected the circumstances as well as the crime.</p>
