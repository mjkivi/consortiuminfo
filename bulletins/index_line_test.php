				<h4>MONDAY WITNESS:</h4>
				<h3>Introducing: "The Monday Witness"</h3>
				<p class="center"><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></p>
				<h6>Date: November 5, 2007<br /><span class="views">Views: 771</span></h6>
				<p>Regular readers will know that my interest in standards is not limited to those that help make information and communications technology work.  Over the years I've written about standards created to address concerns more directly relevant to the human condition, such as <a href="http://www.consortiuminfo.org/bulletins/sep06.php">human rights</a> standards, <a href="http://www.consortiuminfo.org/bulletins/july04.php">social responsibility</a> standards, and <a href="http://www.consortiuminfo.org/blog/">much more</a>. The world being what it is, I think that it's time I did so on a regular basis, and that's what this blog entry is all about.</p>
				<p>At the heart of many of my past non-technical writings lies a concern over whether the standards of conduct (personal, institutional, international) that we encounter in the modern world are adequate to protect ourselves, our children and our future.  All too clearly, the reality too often is that they are not.  Our environment continues to degrade, our governments often act in ways that we do not support, and we feel that we are powerless to make a difference.</p>
				<p>My own political consciousness was formed by the Viet Nam war and the civil rights movement, and the social protests that erupted in connection with those events.  It was in many ways a messy time, but one that was inspiring as well.  Thousands, and eventually millions, of citizens made their opinions known through protests and other public actions, sometimes at the expense of a night in jail, and sometimes at the cost of something worse.  Their efforts changed the future of our country in ways both immediate and long lasting.</p>
				<p>Unfortunately, one thing that did not prove to be long lasting was this new tradition of public activism.  Whether due to complacency, a greater sense of disengagement, or simply increasing social discomfort over rocking the boat, the voices of protest have continued to diminish in number, even in the face of events that in times past would have brought multitudes into the streets.</p>
				<p>One result, in my view, is that our government is less responsive to public opinion, even when polls show that the electorate no longer supports a failed policy.  Another seems to be a greater and greater schism between the liberal and conservative media.  When Americans could receive only three channels of television, those channels could not afford to alienate either the right or the left.  But with the recent explosion of media channels &mdash; cable, talk radio and on line &mdash; has come the opportunity to serve niche markets of all kinds.  Even many of the editorial columnists of the <em>New York Times</em> have abandoned any pretense of neutral presentation in favor of left-leaning stridency.  The <em>Wall Street Journal</em> reciprocates in kind from the right. In consequence, more and more people seek, and can easily find, news that tells them what they want to hear, rather than serious, neutral analysis intended to truly get to the bottom of things.</p>
				<p>The result seems to be that to the extent that people care at all, they care about being fed what they want to hear.  Worse, people in everyday life increasingly shy away from talking about politics and current events at all, because the risk of giving offense has increased, and the chance of having a productive give and take has plummeted.</p>
				<p>For me, this has been a cause of increasing frustration in the face of the ongoing horror of the Iraq war, ineffectual efforts to address global warming, and general avoidance of the issues of the day.</p>
				<p>What can anyone do, in such a situation?  Yes, there are organizations to which you can send checks, tiny demonstrations to join in that go unnoticed, elections in which to cast votes, and very, very occasionally, someone to vote for that might have some measure of real courage and integrity.  Little enough, it would seem.</p>
				<p>Little or not, I strongly believe that those of us that live in effective democracies must take responsibility for what our governments do in our names, and use whatever reasonable tools we have at our disposal to make a difference.  Even if all we can do is to bear public witness to what we believe is right.</p>
				<p>In my case, this blog is the tool that I control that can project my voice the farthest.  And unlike so many media channels today, its audience is not self-selected to be conservative or liberal politically.  What this tells me is that I have the opportunity, and perhaps the responsibility, to use this platform when appropriate not to tell people what to think, but to raise questions that need to be thought about, and encourage others to do the same.</p>
				<p>Accordingly, this is the first in a series of pieces that you can expect to appear on Mondays on an irregular basis, each introduced with the name "The Monday Witness."  The topics will vary, but the common theme will be to highlight instances of action and inaction in the world today that violate widely held standards of human decency.  Sadly, I won't be lacking for topics, and hopefully you'll react the way one reader did to a piece I posted a few weeks ago called <a href="http://www.consortiuminfo.org/standardsblog/article.php?story=2007101409294839">Words, Standards and Torture</a>:</p>
				<blockquote>As a regular reader of your blog, I come to your site for news and informed opinion on standards.</blockquote>
				<blockquote>Never the less, far from being put off by your post on torture, I thank you for that post.  As one of the more shameful (and certainly more under-reported!) stories of this year it is good to see those with an audience writing about it.</blockquote>
				<p>If you choose to read what I write in this series, spend a moment thinking what you, too, might do in some small way to make it harder for all of us to shirk responsibility for what is done in our names.  And by all means, share your comments on whatever I write.</p>
				<p>If you're game, I'll see you here next Monday.</p>
