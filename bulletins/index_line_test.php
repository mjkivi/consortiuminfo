				<h4>EDITORIAL:</h4>
				<h3>Leaving Home for Good: It's Time for a<br />Global Consortium Standards Organization</h3>
				<p class="center"><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></p>
				<p>Some twenty years ago, information technology vendors began opting out of the accredited standards system with increasing frequency in order to form organizations they called fora, alliances, and (most often) consortia. The reasons for the schism were several, but the development was remarkable in that the separatists presumed that standards could become ubiquitous whether or not they acquired the imprimatur of one of the "Big Is:" the International Organization for Standardization (ISO), the International Electrotechnical Commission (IEC), and the International Telecommunication Union (ITU). And they were right.</p>
				<p>Today, there are hundreds of consortia, and many of these organizations have achieved a size, work output, membership, influence and respect that equal that of their accredited peers. Along the way, the information and (to a lesser extent) communications technology industries have come to rely heavily upon consortia to supply their standards needs. But even as this parallel universe of standard setting has achieved respectability, an interesting trend has developed: more and more standards that have been created by consortia are being submitted to one of the "Big Is" for adoption.</p>
				<p>As in most things commercial, the reason for this cross-pollination has to do with the customer &mdash; some of which have a bias or (in the case of some governments) a mandate to utilize accredited standards where they exist. With so many standards organizations, both accredited and non, and thousands more standards being developed all the time, there is a logical reason to seek a means to differentiate good standards from those that are less so. Today, it is still the case that the only organizations available to vet standards on a global, representative basis are the Big Is.</p>
				<p>In a different setting, this situation might resolve itself through some sort of reunion of parent and wayward child. But the consortium movement is no prodigal son. Instead, consortia represent a predictable and healthy evolutionary forking of a well-established "one size fits all" system that had proven to be too restrictive to accommodate a faster moving industry looking for more flexible rules and a lighter weight process. But as the consortium movement matured and consortium standards proliferated, vendors sometimes wished that a global seal of approval could be obtained from one of the Big Is.</p>
				<p>In response, processes were created that permit (for example) consortia to submit successful standards to ISO/IEC JTC1, the Joint Technical Committee formed by ISO and IEC to address the needs of the IT industry, for approval as "Publicly Available Standards."</p>
				<p>But bridges such as this have proven to be imperfect, and susceptible to abuse. Currently, SC 34, an ISO/IEC JTC 1 committee, is grappling with DIS 29500 &mdash; an ECMA developed standard based upon Microsoft's OfficeOpen XML specification. Many harried participants in the review process have expressed the view that the "Fast Track" program being used to propel the submission through the adoption process (unsuccessfully) from start to finish in just six months was inappropriate for a specification that weighs in at more than 6,000 pages. Moreover, there have been widespread reports of disinformation, vendor pressure, and (in one case) even offers of financial reimbursement to business partners as incentives to join National Bodies in order to vote for approval.</p>
				<p>Even absent such extraordinary circumstances, the traditional infrastructure supports a range of industries that is so broad that IT standards consumers are left with less information about the standards they adopt than some would desire. Can a standard be implemented in open source software as well as proprietary products? Was it initially created through a truly open process, or was it processed by a small group of business allies? Can it be adopted on economically equal terms throughout the world, or is it biased towards developed nations? In short, what exactly does adoption by a Big I actually <em>mean</em>, and is that enough?</p>
				<p>The result, I believe, is that new types of global approvals are needed. Certifications of processes and organizations in more dimensions would allow standards consumers to make better choices among available alternatives, and would assist governments in particular in using their purchasing power to drive the types of commercial behavior that they think will best benefit their citizens. Instead of simply choosing between one standard that is ISO/IEC approved and one that isn't, a government could look for a standard that has been globally certified as meeting low energy consumption goals &mdash; and perhaps choose that standards instead. And as between two ISO/IEC adopted standards developed by two different organizations, the fact that one organization, but not the other, have been certified as meeting minimum standards of openness and vendor neutrality could make the final choice clear.</p>
				<p>Similarly, as between an ISO/IEC approved standard that does not require payment of a royalty and an equally well-conceived standard that may only be implemented for a fee, a government could specify the one that is both globally approved and royalty free. In so doing, a more level playing field would be created between the first-world nations whose vendors already own huge global patent portfolios and those emerging nations whose nascent industries may never be able to achieve parity in the patent race. First world nations would reap benefits as well, as new powers like China would thereby lose the incentive to create their own "home grown" standards.</p>
				<p>Should such new standards be created by the Big Is? They could, but I believe that it would be better for new structures to be created to devise categories, criteria and certifications that would be uniquely appropriate for ICT industries. These certifications need not supplant, but would indeed support, the existing accredited system, providing the customer with the ability to require compliance with standards that were approved by one global authority, the other, or in the appropriate case, by both. Vendors looking for the right organization to host a new standards project would have choices as well, as they would gain another way to differentiate their products. And well-run consortia would find value as well, by gaining a way to distinguish themselves from their less rigorous and vendor-neutral competitors as well.</p>
				<p>In short, I believe that the time is ripe for the consortium child to complete its passage from adolescence to full maturity and independence. With the addition of a new "Big I" to serve the needs of consortia and their members, one that could be uniquely designed for the discrete needs of ITC customers, the process of leaving home would finally be complete.</p>
