              <p><strong><em>              </em></strong></p>
              <div align="center">
                <p align="center" class="style10"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">CONSIDER THIS: </font></em></strong></p>
                <p align="left" class="style56"> <strong> [][][] <strong> August 23, 2005</strong> </strong></p>
                <div align="left"><span class="style56">#31</span> <span class="style103"><strong><em>How do you Define a Wilderness? </em></strong></span><span class="style2">There are many different tools that qualify (in my mind, at least) as standards. One of the oldest is the humble definition, a simple device that establishes the exact meaning of a given word so that both the speaker as well as the listener can understand that word in the same way. In its oldest form, a definition was the answer to a child&rsquo;s question &ldquo;What does that word mean?&rdquo; </span>
                </div>
                <p align="left"></p>
                <p align="left" class="style2"> Definitions can be simple and useful tools, or they can be powerful instruments of policy, because he who controls a definition has already won the first battle, and sometimes perhaps the war as well. </p>
                <p align="left" class="style2"> For example, consider this: what does (or should) the word &ldquo;wilderness&rdquo; mean? </p>
                <p align="left" class="style2"> Most of us would likely summon an image like this when hearing that word in a sentence: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> An area of undeveloped land affected primarily by the forces of nature; an area where humans are visitors who do not remain.<br>
                      <em><a href="http://www.google.com/url?sa=X&start=3&oi=define&q=http://highered.mcgraw-hill.com/sites/0070294267/student_view0/glossary_s-z.html"> highered.mcgraw-hill.com/sites/0070294267/student_view0/glossary_s-z.html</a></em></p>
                </blockquote>
                <p align="left" class="style2"> This is a contemporary definition, however, that reflects a modern appreciation of nature. A thousand years ago, when wolves roamed all over Europe and most people throughout the world lived in isolated communities separated by untamed terrain, wilderness had a far different connotation, and represented a place of fear and danger, rather than a desirable area to be preserved. In times gone by, the word would have been defined as follows: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> Area between cities, whether inhabited or not. Also called the Wilds.<br>
                      <a href="http://www.google.com/url?sa=X&start=17&oi=define&q=http://home.comcast.net/~TheGM/SE/glossary.html"> home.comcast.net/~TheGM/SE/glossary.html</a></p>
                </blockquote>
                <p align="left" class="style2"> As I write this, I am on a hiking and camping vacation in Northwestern Nevada, one of the emptiest parts of the United States. In an area 120 by 160 miles, there are only 12 ranches and a 575,000 acre wildlife refuge. Besides the ranch buildings, there is not a home, gas station, or (needless to say) cell phone tower or fast food emporium. In fact, there is not even a paved road, except for state route 140, a two-lane affair that cuts through one corner of the refuge. The land is stark, big and beautiful, with much wild game, no cultivation, and, on any given day, only a handful of people present. The silence is enduring, unmarred even by the sound of a plane. But does that make it wilderness? </p>
                <p align="left" class="style2"> The answer depends, of course, on the definition. </p>
                <p align="left" class="style2"> Almost all of this land is public, but this public land is also committed to specific uses. The majority is controlled by the Bureau of Land Management (BLM), which means (in these parts) that each of these acres has been leased to a rancher, in some cases several generations ago. These valuable rights to graze cattle have been handed down from generation to generation. Still, if you visit you will likely drive for many miles without seeing a single steer (they are likely at higher, cooler elevations at this time of year). But don&rsquo;t be deceived: all of the land through which you are traveling has been profoundly altered as a result of over a hundred years of grazing (as has been the case with virtually every other acre of land west of the Mississippi that is not cultivated or part of a city, town or National Park). </p>
                <p align="left" class="style2"> Once, this land was covered with grasses and wild flowers that retained and made the most of the fertile earth and the sparse rainfall of the Great Basin, and grew four feet high. Today, as a result of grazing and fire suppression, the land grows primarily what cattle won&rsquo;t eat. Characteristically, that means sagebrush, which periodic fires sparked by summer lightning would ordinarily suppress, and a sparse growth of cheat grass and a few other plants that grow sporadically in between the woody sagebrush bushes, provided that the land is not being over-grazed. Much of the soil has long-since washed away from most non-level areas, and the volcanic rock that forms the underlying geology of most of the state is often plainly visible through the cracks of the parched ground that remains. </p>
                <p align="left" class="style2"> Does that mean that this land is therefore undeniably not wilderness? To my surprise, I found a definition that clearly predates our modern usage of the word: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> (1.) Heb. midhbar, denoting not a barren desert but a district or region suitable for pasturing sheep and cattle (Ps. 65:12; Isa. 42:11; Jer. 23:10; Joel 1:19; 2:22); an uncultivated place. This word is used of the wilderness of Beersheba (Gen. 21:14), on the southern border of Palestine; the wilderness of the Red Sea (Ex. 13:18); of Shur (15:22), a portion of the Sinaitic peninsula; of Sin (17:1), Sinai (Lev. 7:38), Moab (Deut. 2:8), Judah (Judg. 1:16), Ziph, Maon, En-gedi (1 Sam. 23:14, 24; 24:1<br>
                      <em><a href="http://www.google.com/url?sa=X&start=6&oi=define&q=http://christianity.about.com/library/weekly/blT0003800.htm"> christianity.about.com/library/weekly/blT0003800.htm</a></em></p>
                </blockquote>
                <p align="left" class="style2"> For some, the argument might begin and end right there. Bring on the cows! </p>
                <p align="left" class="style2"> As I drove on a dirt road into this isolated tract, I happened to pass one of the few ranchers that live here, the owner of 20,000 acres of high, rugged mountains and broad and empty valleys. We had quite an interesting conversation and I learned a number of things that are germane to what a wilderness may, or may not be, depending upon your point of view. For one, he&rsquo;s found 16 of his bulls (much more valuable than cows) shot dead and left on the spot so far this year, presumably by ecoterrorists that want the land to return to it&rsquo;s original state. Clearly, their definition would run something like this: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> Land that, together with its plant and animal communities, is in a state that has not been substantially modified by, and is remote from, the influences of European settlement or is capable of being restored to such a state, and is of sufficient size to make its maintenance in such a state feasible.<br>
                      <em><a href="http://www.google.com/url?sa=X&start=9&oi=define&q=http://www.deh.gov.au/biodiversity/publications/strategy/gloss.html"> www.deh.gov.au/biodiversity/publications/strategy/gloss.html</a></em></p>
                </blockquote>
                <p align="left" class="style2"> He also told me that he is planning to spray his entire ranch with an herbicide that would kill the sage brush, and that he would then burn the terrain every five years or so to keep the sagebrush from reestablishing itself, so that his ranch could return to grasslands of a more traditional character. He was also enclosing a large area of his land to keep his cattle out, in an effort to encourage it to return completely to its original natural state. Even without such intervention, however, state wildlife officials estimate that his land already supports 75 antelope, 25 mountain sheep and several mountain lions (all animals that tolerate little human interference, and that we associate with wild, isolated, natural habitats), not to mention herds of deer and many other types of wildlife. He also hopes to share his land with more people, inviting them in as visitors. Clearly, to him, &ldquo;wilderness&rdquo; (if that is a word he would use at all) would have a more nuanced meaning, perhaps as follows: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> An area of land that has been least modified by modern technological society; the most intact and undisturbed expanses of our remaining natural landscapes. Also see Forest Management Zone.<br>
                      <em><a href="http://www.google.com/url?sa=X&start=11&oi=define&q=http://www.forest.nsw.gov.au/education/glossary/default.asp"> www.forest.nsw.gov.au/education/glossary/default.asp</a></em></p>
                </blockquote>
                <p align="left" class="style2"> When I moved on from his ranch and into the Charles Sheldon Antelope Range, I expected to see a significant difference in vegetation, since the Range has not been grazed by cattle in many decades. To my surprise, there was more, rather than less sagebrush, and as I hiked I often found that the juniper trees that were attempting to re-colonize part of their traditional habitat had been cut down. </p>
                <p align="left" class="style2"> I learned the reason for these discoveries from the only person that I met during the several days that I hiked and camped in the wildlife refuge: a fire marshal who I found parked with his rig and crew on a cliff overlooking a wide valley of the Range, pre-positioned over the area that an approaching thunderstorm was most likely to ignite with its lightning strikes. He told me that sagebrush provides excellent cover for sage grouse, deer and antelope (he was right &ndash; I repeatedly flushed deer and grouse as I hiked), and that the Range was being managed to maximize game production for hunters. As a result, the fires that would normally help maintain the natural mix of plant (and therefore animal) life were being suppressed as quickly and consistently as possible, so that sagebrush, and therefore this small number of (literally) target species, could flourish. </p>
                <p align="left" class="style2"> Further, the wild horses that roam this state in their thousands are viewed as unwelcome competition for available grass. More than a third of the herd in the refuge, some 860 animals, are culled each year (hopefully to be adopted rather than slaughtered, provided that sufficient new owners can be found and qualified). </p>
                <p align="left" class="style2"> Superficially, the wide and empty Range must look and feel to its hunters (provided that they are not aware of the area&rsquo;s historical flora and fauna) didn&rsquo;t think too hard about it) as if it conforms to the following definition, something to make you feel like a good steward of our common natural heritage: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> Land remaining in basically wild (i.e., undisturbed) condition, with few if any traces of human activities.<br>
                      <em><a href="http://www.google.com/url?sa=X&start=4&oi=define&q=http://www.nrdc.org/reference/glossary/w.asp"> www.nrdc.org/reference/glossary/w.asp</a></em></p>
                </blockquote>
                <p align="left" class="style2"> But once again, the reality is that while the refuge is an enormous, empty haunting landscape, in fact it is being managed like any pasture. </p>
                <p align="left" class="style2"> Not surprisingly, there is a legal definition of the word &ldquo;wilderness&rdquo; in the United States today, and this definition controls what type of most-wild landscapes will survive for us and our children to enjoy. It is with this statutory usage that the exact definition of &ldquo;wilderness&rdquo; becomes critical, as well as contentious. </p>
                <p align="left" class="style2"> Legal definitions for statutory and regulatory purposes rightly incorporate the public goals that are being served by the legislative effort. For example, if preservation of natural habitats is a desired goal, then criteria such as sustainability become important, since islands of habitat that are too small cannot sustain the full complement of their native species, which go locally extinct. Here is one non-government definition that incorporates this concern: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> An area of land generally greater than 1000 hectares that predominantly retains its natural character and on which the impact of man is transitory and, in the long run, substantially unnoticeable.<br>
                      <em><a href="http://www.google.com/url?sa=X&start=5&oi=define&q=http://www.borealforest.org/nwgloss13.htm"> www.borealforest.org/nwgloss13.htm</a></em></p>
                </blockquote>
                <p align="left" class="style2"> Similarly, if human enjoyment of wilderness is a goal, or if making critical decisions about what terrain will be preserved and what will not, then these criteria must be included as well. Here is an example: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> A part of our natural landscape that is sufficiently large and varied to constitute a more or less self-regulatory ecological unit, where human interference with the land, plants and animals is minimal, and where the beauty and character of the landscape has aesthetic, cultural or scientific significance.<br>
                      <em><a href="http://www.google.com/url?sa=X&start=12&oi=define&q=http://www.ccfm.org/ci/gloss_e.html"> www.ccfm.org/ci/gloss_e.html</a></em></p>
                </blockquote>
                <p align="left" class="style2"> What Congress eventually adopted as a definition of wilderness, as with any other piece of legislation, represented a balancing of the demands of competing interest groups. In this case, one of the issues that was in play was the ongoing struggle between those that wish to make economic use of public lands (typically these are people that live in proximity to the lands in question) and those that wish to enjoy the same lands first-hand while on vacation, or simply know that they are there and can read about them or see them on television (in the same way that fans of space exploration follow missions avidly, but do not expect to ever board a space ship). The great majority of these people live many hundreds of miles away. Naturally, as fate would have it, it&rsquo;s also true that the former group (and most public lands) are located in &ldquo;Red&rdquo; states, while the latter live in the &ldquo;Blue&rdquo; ones, making the debate even more difficult and spirited than might otherwise be the case. </p>
                <p align="left" class="style2"> The definition contained in the statute that was eventually passed in Washington is summarized by one source as follows: </p>
                <div align="left">
                  <blockquote>&nbsp;
                    </blockquote>
                </div>
                <blockquote><p align="left" class="style2"><em> Wilderness:</em> Areas designated by Congressional action under the 1964 Wilderness Act. Wilderness is defined as undeveloped federal land retaining its primeval character and influence without permanent improvements or human habitation. Wilderness areas are protected and managed to preserve their natural conditions, which generally appear to have been affected primarily by the forces of nature with the imprint of human activity substantially unnoticeable; have outstanding opportunities for solitude or for a primitive and confined type of recreation; including at least 5,000 acres or are of sufficient size to make practical their preservation, enjoyment, and use in an unimpaired condition; and may contain features of scientific, educational, scenic, or historic value as well as an ecological and geologic interest. <br>
                      <em><a href="http://www.google.com/url?sa=X&start=10&oi=define&q=http://www.reo.gov/general/definitions_n-z.htm"> www.reo.gov/general/definitions_n-z.htm</a></em></p>
                </blockquote>
                <p align="left" class="style2"> The Wilderness Act was indeed a great accomplishment that has resulted in the preservation of many priceless landscapes. But one (to me) regrettable result of the compromises required to achieve its passage is that federally protected wilderness land may (and invariably is) leased for grazing purposes. Of all categories of public land, only National Parks and Monuments are off limits for leasing &ndash; National Forests and all other types of federal land are fair game (with a small number of specific exceptions made for particular pieces of land) for grazing, logging and mining, subject to compliance with other relevant laws and regulations. As administrations change, rules are added and subtracted, depending on the goals of the then-current administration. A recent example is the barring of construction of new access roads for logging and other purposes on certain lands by the Clinton administration, which was later reversed by the current administration. </p>
                <p align="left" class="style2"> Given the profound effect that grazing has on ecosystems (and especially so in the dry areas where most federal land is found), this means that the landscape may be empty, beautiful and a joy to visit, but it will never approximate what the same land looked like in its original natural state, nor will it have the full richness of biodiversity that once existed and could, for the most part, exist again. Only in a National Park or Monument may you see that type of landscape beginning to reemerge (Sequoia National Park is a splendid example, especially when the desert flowers are in bloom). </p>
                <p align="left" class="style2"> But solitude is harder to achieve in such places, given the heavy usage that many of these parks receive and the non-wilderness infrastructure of roads, campgrounds and interpretive facilities that have been put in place to support such heavy visitation. In other words, the landscape may be more faithful to the original ecosystem, but it is in other ways less of a wilderness. </p>
                <p align="left" class="style2"> So how much can a definition matter? At times, a definition can matter greatly, because a definition is a standard upon which laws can be based that determine which precious resources will be preserved, and how, and what we and our children will be able to experience in the course of our lives. </p>
