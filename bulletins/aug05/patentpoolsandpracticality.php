              <p><strong><em>              </em></strong></p>
              <div align="center">
                <p align="center" class="style8"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">STANDARDS BLOG:</font></em></strong></p>
                <p align="left" class="style56"> August 9, 2005 </p>
                <p align="center"> <strong> <span class="style10">PATENT POOLS AND PRACTICALITY: <br>
                  </span></strong><span class="style10"><strong>WOULD YOU GIVE A PENNY FOR THE GUY? </strong></span><span class="style10"><br>
                  </span><br>
                  <strong><em> </em></strong></p>
                <p align="left"> <span class="style2">In times gone by, children in London would trundle scarecrow-like figures in wheelbarrows through the streets on November 5, and beg passersby for &quot;A penny for the Guy?&rdquo; The scarecrow was meant to represent <a href="http://en.wikipedia.org/wiki/Guy_Fawkes"> Guy Fawkes</a>, the ringleader of the 1605 Gunpowder Plot, who sought to annihilate both King James I and the British Parliament as well. You wouldn't actually get anything in return for your largesse, but a penny would keep the children happy, and away from worse mischief. </span></p>
                <p align="left" class="style2"> Cut to the present. A long-running drama has taken a new and relatively unusual turn that is worth noting. The saga to which I refer is the protracted (and ultimately unsuccessful) struggle waged within EPC Global to maintain a royalty-free environment for RFID standards. And that new turn is the announcement today that a patent pool is being formed by some 20 companies in order to establish royalty rates and manage royalty payments on implementations of EPC Global RFID standards. </p>
                <p align="left" class="style2"> The announcement was made in a <a href="http://home.businesswire.com/portal/site/google/index.jsp?ndmViewId=news_view&newsId=20050809005368&newsLang=en"> press release</a> with a headline that could win an award for prolixity: &quot;RFID Industry Group Proposes Patent Licensing Consortium; Group Invites RFID Patent Holders to Join; Modeled after MPEG-2 and DVD IP Licensing Programs; Conference Call Scheduled for Today at 12:30 p.m. ET.&quot; [Added Wednesday AM: An <a href="http://www.nytimes.com/2005/08/10/technology/10tags.html?"> article in the New York Times</a> today contradicts this headline, stating that Intermec Technologies -- which claims that it owns 145 RFID technology patents and is already in litigation involving some of them -- was not asked to join the new group.] </p>
                <p align="left" class="style2"> The press release tries to put the best face on the announcement that such a large group of patent owners will be charging a fee for the use of readers, and (more significantly) the billions of RFID tags that a mature industry could be expected to purchase per year. For example, the words &quot;patent pool&quot; are never used in the release (it speaks of the formation of a &quot;patent consortium&quot; instead). Similarly, the release states in part: </p>
                <p align="left" class="style2"> This consortium will be modeled after the successful patent licensing consortium formed and implemented around essential technologies in the MPEG-2 and DVD industries. As with those successful models, the RFID consortium is intended to provide a structured approach for holders of essential RFID patents to receive fair compensation for those patents, at a reasonable cost to the end-user, thus promoting rapid adoption of RFID. This approach offers competitive benefits by integrating complementary technologies and reducing transaction costs. It offers an effective alternative to time-consuming and expensive individual licensing agreements. </p>
                <p align="left" class="style2"> Is this all just self-serving spin, following on the failure of EPC Global to achieve a royalty free result? The answer will depend on whether you look at the world ideologically or pragmatically. </p>
                <p align="left" class="style2"> If you are in the former camp, and especially if you favor the open source model, then this is a great disaster, and the Dark Side has triumphed, because a new technology will be taxed. But let's look at the other side for a moment and see if the future is really all that bleak. </p>
                <p align="left" class="style2"> First, let's look at how a patent pool operates, when all owners of relevant patent claims agree to participate. When that happens, the following results can follow: </p>
                <div align="left">
                  <ul>
                        <li class="style2"> The participants decide on what royalty the market can bear without slowing adoption. Let's say that amount is one cent per RFID tag. </li>
                        <li class="style2"> The participants then agree on a formula for dividing that one-cent among them. </li>
                        <li class="style2"> They also agree on one licensing agreement, and appoint one licensing agent to administer the program. Implementers thus can have &quot;one stop shopping&quot; rather than having to negotiate agreements with multiple vendors. </li>
                        <li class="style2"> Because there is a single point of licensing, the terms really are RAND (reasonable and non-discriminatory), because there are no one-on-one negotiations and private deals between vendors and implementers. </li>
                        <li class="style2"> When other companies are awarded patents, they simply join the pool, and the royalty that implementers are charged doesn't go up. Thus, there are still rewards for continuing innovation, while the price remains unchanged. </li>
                        <li class="style2"> Because everyone is on the bus, the patent landscape is cleared, and all of the patent owners have an incentive to get behind the effort. </li>
                        <li class="style2"> Most significantly, there is less likelihood of a destructive battle between two competing standards, because everyone can get a piece of the action (possibly on different parts of the technology chain, with one company making their profit on tags, and another on readers), even though there is only one standard. </li>
                  </ul>
                </div>
                <p align="left" class="style2"> The result, therefore, can be rapid adoption of inexpensive technology, with no threat of a standards war, but with continuing R&amp;D and ongoing upgrading of the initial architecture. In other words, a new technology can enter the marketplace more quickly, more pervasively, and even more cheaply due to greater volume sales and price competition, and end-users can adopt that new technology with less risk. </p>
                <p align="left" class="style2"> Of course, in the best of all possible worlds, patent owners would make their patents available for free to achieve exactly the same result, and content themselves with a vast new market, competing with each other to offer valuable features and superior quality and service. </p>
                <p align="left" class="style2"> But we live in a different world, and it's not likely that we'll be able to do anything about that in the near future. For now, maybe a penny a tag for the Guy is a small price to pay to know that you're buying a VHS video player, and not a Betamax. </p>
