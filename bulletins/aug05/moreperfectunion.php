                      <p align="center" class="style10"><strong><strong><strong>TOWARDS A MORE PERFECT UNION</strong> </strong></strong><br>
                      </p>
                      <p align="left" class="style2">&nbsp;</p>
                      <p align="left" class="style2">In this issue I highlight the interdependencies of two systems that have much in common, but too often work in total isolation (at least in the United States): government and standard setting organizations (SSO's). Europe and China, among others, see standard setting as an extension of regional and national policy, and integrate these two systems closely, to national advantage. I believe that the United States government, SSO's and industry should recognize the value of greater collaboration as well, and work towards optimizing the relationship between these two allied, but too-often independent systems. </p>
                      <p align="left" class="style2"> In this month&rsquo;s Editorial, I note that the interconnections between government and SSO's will increase, as will the margin between success and failure in remaining competitive in the global marketplace. As a result, I call for more cooperation and dialogue between government and SSO's in the United States. </p>
                      <p align="left" class="style2"> In the Feature Article, I examine the ways in which government and SSO's are the same, and in which ways they are different, and offer suggestions on how the two can better work together for greater mutual benefit, and greater benefit to the end user. </p>
                      <p align="left" class="style2"> In this month&rsquo;s excerpt from the Standards Blog I depart briefly from this issue&rsquo;s theme, and describe how one mechanism for simplifying intellectual property matters &ndash; the patent pool &ndash; can be used to favorable effect in the software arena as well. </p>
                      <p align="left" class="style2"> In this month&rsquo;s excerpt from Consider This&hellip; I explore the relationship between standards (in this case definitions) and legislation by comparing the many popular definitions of &ldquo;Wilderness&rdquo; to that which was included in the 1964 Wilderness Act. </p>
                      <p align="left" class="style2"> I also have two announcements to make this month. In the first, I take pleasure in announcing that we have helped plan, and are co-sponsoring a seminar in Washington, D.C. on October 5, in conjunction with World Standards Week and the ANSI Annual Conference. The seminar will analyze the impact that the close coordination between government and SSO's in Europe and China is having on their national competitiveness, the specific areas in which that impact is being felt, and what can be expected in the future. </p>
                      <p align="left" class="style2"> I also take personal pleasure in announcing that the American National Standards Institute (ANSI) will be bestowing their 2005 President&rsquo;s Award for Journalism on me in recognition of the role that ConsortiumInfo.org and the Consortium Standards Bulletin have played in helping &ldquo;to raise the profile of standards and highlight their importance to commerce and daily life, as well as encourage greater support for their use and development.&rdquo; </p>
                      <p align="left" class="style2"> As always, I hope you enjoy this issue. </p>
