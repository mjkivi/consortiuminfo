                <p align="center"><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITORIAL</em></strong></font><strong> </strong></p>
                <p align="center" class="style10"><strong> <strong> </strong> <strong> A CALL FOR GREATER COLLABORATION </strong> </strong></p>
                <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove<br>
                </a></strong></font></p>
                <p align="left">&nbsp; </p>
                <p align="left"> <span class="style2">While standards are important tools, it is worth recalling that they coexist with another rule set that is in many ways similar, but which is created through a different process. That other set of rules, of course, comprises the many sets of local, state and federal laws and regulations that govern our lives. </span></p>
                <p align="left" class="style2"> When setting standards (or drafting legislation) in the breach, it is all too easy to forget about the other system of rules and skills. But the interconnections between these two systems (as explored in greater detail in this month&rsquo;s <a href="#feature">Feature Article </a>) are many, such as the incorporation of consensus standards into regulations (such as building codes). </p>
                <p align="left" class="style2"> By keeping this big picture in focus, it is easier to understand the often related roles that standards, laws and regulations play in the grand scheme of things, and therefore to do a better job of creating them. It can also enable the creation of more sophisticated and predictable business strategies. Similarly, by recognizing how much is held in common between consensus-based standards and legislated standards, those that create each type of tool may learn from the experiences of those that work in the other system. </p>
                <p align="left" class="style2"> Recognizing that voluntary and mandatory standards have much in common, and that they often serve the same ultimate goals, will be increasingly important in the future, as technology assumes an ever greater role in just about everything. If there is too little communication between those that set governmental policy and those that set standards, each is likely to work at cross purposes rather than towards achieving common goals. In today&rsquo;s highly competitive global marketplace, those nations that maintain the closest ties and achieve the greatest synergies between public and private standards efforts will doubtless gain a meaningful competitive advantage. Most knowledgeable sources would conclude, we believe, that the United States does not enjoy such an advantage today. </p>
                <p align="left" class="style2"> Traditionally, standards and government policy have intersected most often in areas such as trade (where nominally mechanistic standards can be used to favor local industry), procurement (where both government-unique and consensus standards may be used together), and health and public safety (where all manner of standards exist, some of which are created within one system, and some within the other). But now new intersections (or collisions, depending on your point of view) are emerging. For example, Internet governance has become a major focus of the ongoing World Summit on the Information Society (WSIS), which has created a Working Group on Internet Governance (WGIG) to consider what action, if any, should be requested in light of the United States&rsquo; continuing oversight of ICANN, which controls the global Internet root directory. </p>
                <p align="left" class="style2"> In fact, both governments as well as the standards bodies that maintain the standards, registries and protocols that enable the Internet to operate all wish to have an efficient, effective system. But each has separate concerns as well. Those that set Internet and Web standards have a technical focus and strong vendor involvement, while those that are concerned with Internet &ldquo;governance&rdquo; worry over who controls this essential resource, especially if it is another government. </p>
                <p align="left" class="style2"> And what of the end-user? On the standards body side, those that have technical expertise may participate in organizations such as the IETF, but someone who simply wants to use the Internet (and whose life is increasingly dependent upon how it operates) has no effective voice at all. In the WSIS process, the interests of the end-user (and particularly those in the Third World) are at the forefront, but the agendas of some participants may also include other goals that are not as admirable. And, as the transitory WSIS process demonstrates, there is currently no effective, standing, trusted system in place to permit both sides to discuss and agree on how to achieve the best results for humanity as a whole when standards and public policy intersect on a global basis. </p>
                <p align="left" class="style2"> Nor is this an isolated example. With increasing globalization, environmental pressures and international tensions, it will be particularly important for each person that plays a role in setting the rules and creating the tools that will control and enable our lives to understand how the pieces fit together. What, for example, will we agree that concepts such as &ldquo;sustainability&rdquo; mean, and who will create the laws, regulations and standards that will allow us to define, measure and achieve that goal on a case-by-case basis? Will there be communication among those that set policy and those that create standards, and how will that be achieved? </p>
                <p align="left" class="style2"> If we are to live in the type of world that we would wish, greater identification of the role of standards, and the interplay of laws and standards, will be important &ndash; as will greater understanding on the part of those that work in each system about the work and goals of the other. Perhaps greater compromises will be needed by each system in order to achieve the best results. Focusing greater attention on how this dual system can be optimized, and setting up well thought out channels for regular dialogue today will doubtless make creating the world that we hope to live in tomorrow more achievable. </p>
