                  <td class="style2"><em> &ldquo;The international language of commerce is standards. Adherence to agreed upon product or service specifications underpins international commerce, enabling trillions of dollars of goods to flow across borders, regardless of the spoken language of any business parties. The common acceptance of standards is fundamental to the success of robust, fair, and free trade.&rdquo; - </em> U.S. Secretary of Commerce Donald L. Evans. </td>

