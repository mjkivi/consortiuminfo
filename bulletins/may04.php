              <p align="center"><strong> <span class="style10">BREAKING DOWN TRADE BARRIERS: </span></strong></p>

              <span class="style10"><strong> AVOIDING THE CHINA SYNDROME</strong></span><br>

<blockquote><p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove <br>

</a></strong></font></p>

              </blockquote>

              <p align="left"><strong><em> <span class="style10">Background</span><span class="style2">:</span></em></strong><span class="style2"> One of the less stressful tasks that United States Secretary of State Colin Powell performed this March was to co-sign a letter addressed to Chinese Vice Premiers Wu Yi and Zeng Peiyan, protesting the imminent effectiveness of a Chinese technology standard. That standard would have prevented non-Chinese manufacturers from selling wireless-enabled devices in China unless they could secure the assistance of one of 24 Chinese companies that were licensed to utilize the new standard. </span></p>

              <p align="left" class="style2"> It&rsquo;s not often these days that Colin Powell takes time off from America&rsquo;s current international troubles to take an active part in a trade matter. But in this case, the potential stakes were huge, extending far beyond the commercial fortunes of U.S. chip vendors. Over 1.2 billion people live in China, and allowing the governmental gatekeepers of so vast a market to successfully erect a standards barrier would bode ill for U.S. trade not only during election year, but for long thereafter. </p>

              <p align="left" class="style2"> Moreover, China&rsquo;s action on the standard in question did not represent an isolated incident. Significantly, a 2004 General Accounting Office survey of U.S. companies with a presence in China ranked &ldquo;standards and certification&rdquo; as the most serious issue on an extensive list of World Trade Organization (WTO) commitment areas, ranking above even &ldquo;tariffs,&rdquo; and &ldquo;intellectual property rights&rdquo; -- despite extortionate value added taxes on some goods and the rampant piracy of intellectual property in China that together result in the payment and loss of billions of dollars a year. Equally significant, standards and certification issues had ranked number 20 in a similar survey conducted only two years before. Clearly, there had been a policy shift in Beijing that required responsive action. </p>

              <p align="left" class="style2"> The standard at issue is known as WLAN Authentication and Privacy Infrastructure, or WAPI, which is analogous in purpose to the IEEE 802.11 Wireless Fidelity (or Wi-Fi) standard. China&rsquo;s public objection to the IEEE offering involves the Wired Equivalent Privacy (WEP) security component of Wi-Fi, which China&rsquo;s Ministry of Information Industries pronounced last November to be inadequate. Due to this failing, only WAPI, a new standard approved by the Chinese Standardization Administration of China (SAC) was deemed to be adequate to enable wireless capability in products sold in China. WAPI was declared to be effective on December 1 of 2003, but the deadline for mandatory compliance was deferred to June 1, 2004. </p>

              <p align="left" class="style2"> If China was simply concerned about the adequacy of WEP, international businesses immediately pointed out, it could have addressed its concerns in a variety of traditional ways. Already, efforts at IEEE were at work to bolster Wi-Fi security, and China could have participated in those efforts. Or, it could have proposed its own standard for international adoption and use, or at least made it generally available for implementation. Instead, China not only mandated compliance with its domestic standard, but also refused to license the underlying technology to anyone but a select group of domestic companies. Foreign manufacturers could seek, but would not be guaranteed, a partner for purposes of offering their products to the Chinese marketplace. Moreover, western semiconductor manufacturers were concerned over the need to share their most valued design information with commercial partners in a nation renowned for loose respect for intellectual property rights, and non-existent government enforcement of those same rights. </p>

              <p align="left" class="style2"> In the months between the announcement of the WAPI standard and the letter signed by Secretary Powell (as well as by Secretary of Commerce Donald Evans and U.S. Trade Representative Robert Zoellick), opposition on the part of U.S. companies mounted dramatically. Notably, Intel, Texas Instruments and Broadcom each announced that it would not sell affected products in China after May 31. Intel CEO Craig Barrett visited Beijing in early April as part of a continuing dialogue with Chinese trade officials in an effort to resolve the standoff. </p>

              <p align="left" class="style2"><strong><em> <span class="style10">The Stakes</span>: </em></strong> After decades of comparative second tier status, the Chinese economy is now assuming enormous trade power, not only as an exporter, but as a potential importer as well. It is now the third largest trade partner for the United States, and its economy is growing at a far greater pace than that of any western nation. At the same time, it has an extremely cheap work force, a historically inward looking world view, and the type of state control that permits tight control of imports, if it chooses to exercise it. Finally, China now permits a sufficient degree of freedom to individuals to inspire the type of entrepreneurship that enables quick response to commercial opportunities as they arise. </p>

              <p align="left" class="style2"> There is great temptation for the government of China to exercise its central control by erecting a variety of trade barriers. Already, China imposes a punitive 17% value added tax on imported semiconductors &ndash; but rebates all but 3% of that tax when it is paid by a domestic manufacturer. In 2003, China imported over $2 billion in semiconductors, resulting in payments by U.S. manufacturers of $344 million in VAT charges. In March of this year, the United States filed its first fair-treatment complaint against China with the World Trade Organization (WTO) over the VAT issue. A hearing date on the complaint has not yet been announced. </p>

              <p align="left" class="style2"> The U.S. is ill-positioned to see trade shift further to the benefit of China. Already, the U.S. is running trade deficits at an all time high level ($124 billion of which was attributable to China alone in 2003), and manufacturing jobs continue to be siphoned off to low-wage markets in Asia. At the same time, China lacks an extensive network of land lines, and is the fastest-growing wireless market in the world. Until now, however, it has imported 85% of the semiconductors that it uses to enable wireless devices. </p>

              <p align="left" class="style2"> Thus, the wireless market represents, on the one hand, an opportunity to narrow the trade deficit between the U.S. and China. And on the other hand, it represents the potential for another dramatic loss of U.S. jobs, if China uses protective measures to help its domestic companies acquire the potential to compete both at home and abroad. The possibility of the latter alternative coming true, with or without protective measures, is real: Taiwanese companies, which already are heavily involved in setting up manufacturing facilities in China, grew their own semiconductor sales from $440 million in 1990 to $16 billion only ten years later, according to figures compiled by McKinsey. </p>

              <p align="left" class="style2"> The WAPI standoff was thus one that the U.S. could ill-afford to ignore. </p>

              <p align="left" class="style2"><strong><em> <span class="style10">The Resolution:</span></em></strong> At the same time, any single issue that is addressed between the governments of two countries can only be addressed in the context of all other current and long-term issues that concern those governments. In the case of global powers like the U.S. and mainland China, those issues include a complex web of ideological, human rights and geo-political topics, in addition to a host of trade matters. And dynamically, any negotiation involves not only the issue at hand, but also the avoidance of looking weak. Still, once the lines were drawn and the two parties were staring at each other across the conference room table, either China or the U.S needed to blink in order for the issue to be resolved. </p>

              <p align="left" class="style2"> Fortunately for the United States, it was China that blinked. </p>

              <p align="left" class="style2"> The setting was the 15 th session of the China-US Joint Commission on Commerce and Trade, an annual event held this year in Washington, D.C. Vice Premier Wu Yi led a 17 member delegation to that meeting, and announced China&rsquo;s decision on April 21 to back down after a day of intense negotiations. He also announced a willingness to crack down on intellectual property piracy. Specifically, China agreed to indefinitely postpone required compliance with WAPI, and announced its willingness to work with international standard setting organizations in an effort to agree upon a global 3G wireless standard. </p>

              <p align="left" class="style2"> As part of the accord announced, the Standards Administration of China will now work with the IEEE on further development of broadband standards, as well as continue to advance the WAPI standard. The IEEE, which is already seeking to beef up the security features of the Wi-Fi standard through inclusion of the 128-bit Advanced Encryption Standard in the 802.11i version of Wi-Fi, for its part has agreed to hold a meeting of its wireless working group in China. </p>

              <p align="left" class="style2"> But did China, in fact, blink in response to U.S. governmental pressure? </p>

              <p align="left" class="style2"> In the case of the accord reached in April, it is important to note not only what was agreed to, but what was left in dispute, and what may have transpired between closed doors that was not publicly disclosed. True, the WAPI standard was indefinitely shelved by China, but the VAT tax remains in place. The U.S., of course, will have a second chance to make progress on that score when the WTO schedules a hearing on that issue. In the meantime, of course, China&rsquo;s nascent semiconductor business will enjoy a pronounced price advantage, and U.S. semiconductor companies will continue to line the coffers of the Chinese government. </p>

              <p align="left" class="style2"> It is also worth noting that China itself was not in good field position to stick to its own deadline, if Intel and other manufacturers were indeed determined to stick by their guns. China has a true need and commitment to wireless deployment, and it already has a significant installed base of Intel Centrino-based equipment &ndash; all using the Wi-Fi standard if they have wireless capability. With its own deadline only six weeks away, China may have come to view the biggest potential loser of its own policy (at least in the near term) as itself. Ultimately, it may have been the U.S. semiconductor vendors that asserted the most persuasive pressure. </p>

              <p align="left" class="style2"><strong><em> <span class="style10">Conclusions:</span></em></strong> From the standards perspective, the most significant aspect of the Wi-Fi/WAPI affair is that a &ldquo;standards war&rdquo; was averted. There are already sufficient barriers to trade, and indeed, enough standards-based impediments in place already, without providing the basis for escalation. </p>

              <p align="left" class="style2"> When a situation as closely followed as this is resolved in favor of seeking a single global standard, the international standards infrastructure becomes stronger. Had the standoff ended with China sticking by its guns after the United States had engaged at the highest diplomatic levels, there would have been the potential for the U.S. to respond in like kind, or for other nations to more seriously consider erecting similar protective measures, given the failure of the U.S. to turn China&rsquo;s flank. </p>

              <p align="left" class="style2"> It is to be hoped that the WAPI episode will mark the beginning of a retreat from using standards as barriers to trade, rather than an augury of similar actions to come by China or other nations or alliances. In the end, the best hope for businesses and consumers everywhere lies in the realization by those setting trade policy that all have more to gain than to lose by supporting strong, universally adopted standards. </p>

