                <p align="left">&nbsp; </p>

                <p align="left"> <span class="style2">Government and standard setting coexist in the United States in a uniquely American way. Contrary to the practice in most countries, the development of standards in the U.S. is almost entirely a matter of private, rather than public, action. Even the American National Standards Institute (ANSI), which accredits American standards development organizations (SDOs) and represents the United States in ISO, the global standards body, is a non-governmental entity. </span></p>

                <p align="left" class="style2"> While this reality is wholly consistent with the American ethos of private enterprise, it also creates a need for close cooperation and effective communication between standard setting organizations (or SSOs, meaning both SDOs and consortia) and government. Why? Because there are some things that only governments can do, such as negotiating trade treaties with the governments of other sovereign states. As a result, SSOs sometimes need to recruit federal clout in support of commercial interests. And from government&rsquo;s perspective, standards have become so vital to global trade that government cannot secure some advantages for Americans without the active assistance of SSOs. </p>

                <p align="left" class="style2"> The importance of standards and the need for effective cooperation between the private standard setting sector and government is highlighted by two events that occurred in recent weeks. The first was the resolution of a much-watched standards face-off between China and U.S. chip manufacturers. (See our <strong><em>Trends</em></strong> article for this month: <strong><em><a href="#trends">Breaking Down Trade Barriers: Avoiding the China Syndrome</a></em></strong>). A compromise was reached between the two countries at the eleventh hour through intervention at the highest levels of government. The second was the release of a major report by the U.S. Department of Commerce (DOC), entitled &ldquo;Standards and Competitiveness &ndash; Coordinating for Results.&rdquo; (See our <strong><em>Feature Article</em></strong> this month, <strong><em><a href="#feature">U.S. Department of Commerce Releases a Major Standards Report</a></em></strong>) </p>

                <p align="left" class="style2"> In the case of the China standoff, Intel and other companies had announced their decision to discontinue shipments to China rather than comply with a Chinese government edict that would require all wireless-enabled products to meet its own home-grown wireless standard, beginning June 1. The edict went far beyond merely mandating a local standard over the globally popular IEEE 802.11 (Wi-Fi) standard, however. It also provided that a limited number of Chinese manufacturers would be licensed to build and certify to the Chinese standard. Hence, any foreign manufacturer would not only need to comply with the new standard, but partner with a Chinese company as well. </p>

                <p align="left" class="style2"> Following lobbying by American industry representatives, a letter was sent to Chinese Vice Premiers Wu Yi and Zeng Peiyan that was co-signed not only by U.S. Secretary of Commerce Donald Evans and U.S. Trade representative Robert Zoellick, but by Secretary of State Colin Powell as well, for added emphasis. At a meeting held on April 21 st, the Chinese finally agreed to indefinitely postpone the imposition of their standard, and to cooperate in the further development of a global standard. </p>

                <p align="left" class="style2"> The narrow avoidance of a standards trade war with China provided an appropriate backdrop for the release of the DOC report, which had been in the works since an initiative to counter trade barriers was announced by the DOC in March of 2003. Not surprisingly, the report is subtitled, &ldquo;Removing Standards-Related Trade Barriers Through Effective Collaboration.&rdquo; </p>

                <p align="left" class="style2"> The findings and recommendations of the report are in large part the product of 13 wide-ranging round table discussions hosted by the DOC with representatives of over 200 industry associations and SSOs from diverse industries. During those round tables, it was made clear that industry understandably looks to government to intervene to eliminate standards-based trade barriers. The report also found that, while many effective systems and interconnections between the federal government and SSOs are already in place, there are many ways in which the situation could be improved to national advantage. </p>

                <p align="left" class="style2"> Why would the support of standard setting by government be in need of a tune up? Perhaps it is that the vibrancy of voluntary, consensus standard setting in this country has made it easy for close cooperation between SSOs and government to be neglected. The federal government, after all, has enough to occupy its attention, while U.S. industry is always happier to self regulate than to be regulated. As a result of this dynamic, the two forces are more likely to effectively unite in a defensive posture when a threat arises, rather than to collaborate on an ongoing basis as part of a comprehensive program to optimize the competitiveness of U.S industry abroad. This can leave the U.S. at a disadvantage, when faced with the closely coordinated public/private programs that are active in countries such as China, and within alliances such as the European Union. </p>

                <p align="left" class="style2"> The results of the relatively tenuous connection between government and the standard setting infrastructure in the United States may not be all bad. Recently, U.S. manufacturers of water heaters found themselves in a no-win situation when a Department of Energy efficiency rule became effective, but the DOE had not yet created the mandatory test called for by the new rule to prove compliance. Only by applying for a compliance waiver for their products could the manufacturers continue to sell their wares. As of this writing, the DOE is still spending its time processing requests for waivers rather than completing the test. In the meantime, the efficiency standard languishes. </p>

                <p align="left" class="style2"> And in any event, standards should first and foremost be about ensuring qualities like utility, interoperability, safety and efficiency, and not about securing offensive or defensive advantages for national industries. In the best of all worlds (as compared to the one we actually inhabit), standards would exclusively be developed to create level playing fields, provide the best solutions for consumers, and to advance similarly laudable goals. And, in truth, the new Department of Commerce report focuses more on creating level playing fields than on substituting U.S.-advantaged standards in place of their foreign analogs. </p>

                <p align="left" class="style2"> We believe that the United States -- as well as every other country -- has far more to gain by promoting the adoption of the best standards and the breaking down of trade barriers than by playing standards games intended to favor domestic industry. Governments already have ample tools at their disposal to advance national trade interests besides proprietary standards and unreasonable conformance assessment practices. The over arching problem, of course, is that it is just as easy to counter one trade barrier with another. Soon standards can become solely tools to defeat competition, rather than to create intrinsic value. Ultimately, universally adopted standards not only guarantee better products, but make every nation&rsquo;s goods and services more suitable and attractive in a global market as well. </p>

                <p align="left" class="style2"> As observed in the opening line of Secretary of Commerce Evans&rsquo; introduction to the DOC report, &ldquo;The international language of commerce is standards.&rdquo; When individual countries lose sight of the accuracy of that proposition, there is a risk to us all that efficient global trade may disintegrate under the imposition of a Babel of local standards. </p>

