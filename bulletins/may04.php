              <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"></font></p>

              <p align="left"> <strong><em> <span class="style10">Introduction: &nbsp;</span></em></strong><span class="style2">It is not often that a press conference on standards draws a standing room only crowd in the nation&rsquo;s capitol. But on May 18, I watched as the custodial staff of the Department of Commerce (DOC) carried chair after chair into the Main Lobby of the Department of Commerce when just that occurred. Well over 100 press and industry representatives had gathered to hear Secretary of Commerce Donald L. Evans issue a report outlining the DOC&rsquo;s plan to defend U.S. trade by more aggressively supporting the U.S. voluntary standards process. Evans was joined on the dais by an appropriate supporting cast of prominent representatives of industry, standard setting organizations and government agencies. </span></p>

              <p align="left"> <span class="style2">The title of the DOC report: &ldquo;Standards and Competitiveness &ndash; Coordinating for Results&rdquo; was in some ways less informative than its subtitle: &ldquo;Removing Standards-Related Trade Barriers Through Effective Collaboration.&rdquo; That subtitle was aligned with the March 19, 2003 DOC press release in which the initiative leading to the release of the report was announced. The lead quote attributed to Evans in that press release reads in part as follows: </span></p>

              <blockquote>

                <p align="left"> <span class="style2">&ldquo;The Bush Administration remains committed to promoting competition and opening new markets for U.S. goods. Standards and testing are key to our international competitiveness. But more and more we are hearing that foreign standards and testing requirements are keeping our products out of foreign markets. This is the wrong approach that reduces efficiencies, limits competition and increases prices for the consumer goods.&rdquo; </span></p>

              </blockquote>

              <p align="left" class="style2"> The impetus for launching the initiative was an accumulation of events, from minor actions by individual countries to the broad-based integration of standards into commercial policy by the European Union. But the degree of attention that the release of the report commanded was likely augmented by the long buildup and timely resolution in late April of a well-publicized standards battle between China and the United States (see the following article, <strong><em><a href="#trends">Breaking Down Trade Barriers: Avoiding the China Syndrome</a></em></strong>). And certainly, the issue of global competitiveness is particularly relevant in an election year where employment growth and the preservation of manufacturing jobs are key factors. </p>

              <p align="left" class="style2"><strong><em> <span class="style10">What the Report Is</span>:</em></strong> The 31 page report (with appendices) has five substantive parts: </p>

              <div align="left">

                <ul>

                    <li class="style2"> A review of the eight-point charter of the initiative that led to the report. The points include five specific deliverables:

                      <ul>

                        <li> Commerce Standards Activity Assessment </li>

                      </ul>

                    </li>

                    <ul>

                      <li class="style2"> Training and Outreach Program</li>

                          <li class="style2">&ldquo;Best Practices&rdquo; Database </li>

                          <li class="style2"> Expanding the &ldquo;Early Warning System&rdquo; of trade barriers </li>

                          <li class="style2"> Appointment of a Standards Liaison within the International Trade Administration </li>

                    </ul>

                </ul>

                </div>

              <p align="left" class="style2"> As well as three more generalized goals: </p>

              <div align="left">

                <ul>

                    <ul>

                        <li class="style2"> Reinforcing Expertise in Key Markets </li>

                        <li class="style2"> Partnering with the President&rsquo;s Export Council on Standards Leadership </li>

                        <li class="style2"> Reaching out to U.S. Industry </li>

                    </ul>

                </ul>

                <ul>

                    <li class="style2"> A well-written overview of the &ldquo;highly decentralized&hellip; U.S. standards system.&rdquo; The overview summarizes both the constituent parts of that system (e.g., ANSI and various government agencies), as well as the way in which these bodies interact both domestically as well as with their counterparts internationally. </li>

                </ul>

                <ul>

                    <li class="style2"> An overview of current DOC standards-related activities, organized by the several DOC bureaus through which those activities are carried out, including the International Trade Administration (ITA); the National Institute of Standards and Technology (NIST); and the National Telecommunications and Information Administration (NTIA), among others. </li>

                </ul>

                <ul>

                    <li class="style2"> A summary of issues identified through industry input, principally gathered through 13 roundtable discussions held with a wide array of private-sector representatives, and from the formal responses to the notices posted by the DOC in the <em>Federal Register</em>. The issues are categorized under eight headings

                                            

                      <ul>

                        <li> Standards Issues </li>

                        <li>Conformity Assessment Issues </li>

                        <li>Regulatory Issues </li>

                        <li>ISO/IEC Concerns </li>

                        <li>Funding Issues </li>

                        <li>Trade Policy Issues </li>

                        <li>Training and Technical Assistance </li>

                        <li>Collaboration<br>

&nbsp;                        </li>

                      </ul>

                     </li>

                    <li class="style2">Over 50 discrete recommendations, grouped under 11 major headings, as discussed in greater detail below. </li>

                </ul>

                <strong><em> <span class="style10">What the Report is Not</span></em></strong><span class="style10"><strong><em>:</em></strong></span> <span class="style2">While the compilation of issues in the report is convincingly comprehensive and the recommendations are thoughtful, the report does not provide any surprising revelations, or propose any bold new initiatives. Rather, it offers a mix of thematic suggestions as well as specific actions that are intended to tune up and optimize the existing trade support system. Many of the recommendations focus on increasing coordination and communication within what it rightly refers to as the &ldquo;highly decentralized&rdquo; U.S. standards system. Other recommendations are externally directed, and intended (for example) to permit U.S. government and industry to identify foreign trade barriers more quickly, and confront them more effectively. </span></div>

              <p align="left" class="style2"><strong class="style10"><em> Principal Issues: </em></strong> A wide diversity of issues were identified during the information gathering phase of the DOC initiative. One way to group them, with some added observations, is as follows: </p>

              <ul>

                <li>

                  <div align="left" class="style2"><strong><em> <span class="style10">Non-Proprietary Systemic Issues:</span></em></strong> An &ldquo;overriding&rdquo; concern identified by many participants was &ldquo;the presence of multiple standards or standards differing from international norms in other countries.&rdquo; It is important to note that there are many reasons for this reality. While it is true that some countries set standards as deliberate barriers, others set standards (at least in part) for genuine local or regional perceived needs. Similarly, global standards are set through a consensual process, which by its nature is not as efficient or as coordinated as an enforceable process might be. Finally, proprietary forces at the corporate level are rife in the standard setting process, which often leads to the launching of competing standards based on purely commercial, as compared to national, agendas. Solutions &ndash; and the appropriateness of government to provide them &ndash; that are intended to limit the existence of competing standards will be very different when the causality is nationally based than when it arises from the ambitions of a group of multinational companies.</div>

                </li>

              </ul>

              <div align="left">

                <ul>

                    <li class="style2"><strong><em> <span class="style10">Proprietary Systemic Issues:</span></em></strong> International standards bodies are inherently based on the concept of one vote per country. But with the increasing incorporation of standards into European Union policy, and the expansion of the Union itself, some round table participants objected to the increasing power of bloc voting in the International Organization for Standardization (ISO) and the International Electrotechnical Commission (IEC). Any modification to such rules is arguably more properly the province for debate within ISO than the DOC. </li>

                </ul>

                <ul>

                    <li class="style2"><strong class="style10"><em> Mechanical Barriers:</em></strong> Not only standards, but conformity assessment can be the source of abuses. Many participants highlighted the insistence of some countries not only on testing, but on testing within their own borders, even where a product may have previously been certified as compliant to a standard at another location. In contrast to the previous category, this type of barrier seems uniquely suited for government, as compared to SDO, action. </li>

                </ul>

                <ul>

                    <li class="style2"><strong class="style10"><em> Standards of Objectivity:</em></strong> Some participants believed that certain regulations have no objective basis, and had not been determined in a transparent fashion. Of course, the objective basis for a standard or regulation is in the eye of the beholder, and some regulations (such as a number of European regulations with ecological dimensions) that seem either protectionist or baseless to some Americans appear eminently reasonable to the generally greener consciousness of &ldquo;Old Europe.&rdquo; Progress in this category again seems properly the province of government -- but the current administration has not assiduously cultivated the type of cooperative treaty relationships with Europe that would facilitate progress on this front. </li>

                </ul>

                <ul>

                    <li class="style2"><strong><em> <span class="style10">Lack of Government Funding Support:</span></em></strong> Standard setting in the United States by SSOs is inherently a low budget proposition relative to its importance, due to the lack of direct government support for that process. The ANSI accredited standards development organizations (SDOs) are feeling particularly at economic risk, given the recent holding of a federal court in <em>Southern Building Code Congress International v. Veeck. </em>The court in that case granted a builder the right of free access to a copyrighted standard referenced in a building code, raising the prospect that federal courts nationwide might mandate free access to some percentage of the thousands of standards currently being sold to underwrite the operations of SDOs. Certainly, what would be a modest infusion of cash by government standards would be an enormous influx of capital in the primarily virtual world of standards development.</li>

                </ul>

              </div>

              <div align="left">

                <ul>

                    <li class="style2"><strong><em> <span class="style10">Lack of Government International Support:</span></em></strong> Some participants believed that the federal government could be more forceful in supporting standards at the treaty level, and, in particular, should seek to hold foreign nations more closely to U.S. interpretations of the requirements of the World Trade Organization&rsquo;s Technical Barriers to Trade Agreement (WTO TBT). Other suggested actions included augmenting DOC outreach to foreign officials in an effort to educate them to treaty obligations. While government efforts could certainly be effective in this area, there should also be opportunities for direct action by American SDOs as well. </li>

                </ul>

                <strong><em> <span class="style10">The Recommendations</span></em></strong><span class="style10"><strong><em>:</em></strong></span><span class="style2"> The actions proposed in the report are the work of DOC staff members in consultation with DOC bureaus, based upon their own observations, the roundtable input, and formal submissions. The Recommendations themselves are a potpourri of activities large and small, ranging from &ldquo;Promote World Standards Week more visibly&rdquo; at the low end to the creation of a Standards Liaison post within the ITA at the more substantive end of the spectrum (the first Standards Liaison, Heidi Hijikata, has already been appointed). </span>

                <p class="style2"> A large number of recomendations involve improvement of domestic communication and coordination at specific points of contact, as well as particular types of information that should pass between stated committees, bureaus, SDOs and industry participants. Other coommunication recommendations are more generalized (e.g., &ldquo;Develop and promote consistent messages within DOC and the U.S., Government on key standards issues&hellip;&rdquo;). This is hardly surprising, and no doubt necessary in so decentralized a system. </p>

                <p class="style2"> Another large number of recommendations do not propose any new specific actions at all, and instead simply urging &ldquo;expanding,&rdquo; &ldquo;building upon&rdquo; and &ldquo;continuing&rdquo; existing programs. And the language used in certain specific recommendations indicates a degree of ambivilence that undercuts hopes for prompt action. </p>

                <p class="style2"> Some of the more interesting recommendations include the following: </p>

                <ul>

                  <li> <span class="style2">&ldquo;Explore opportunities&rdquo; to create a public/private task force to augment a DOC effort to identify and resolve standards-related barriers to trade as quickly as possible. Such an effort would &ldquo;include extensive collaboration with other agencies, such as the U.S. Trade Representative.&rdquo; The tentative language of this recommendation, however, raises questions over the DOC&rsquo;s commitment towards effective implementation. (Recommendation V.1.a) </span></li>

                </ul>

                <ul>

                  <li> <span class="style2">&ldquo;Organize a high-level Standards Trade Mission&hellip;probably to China or Europe, focusing on Promotion of U.S. Approach to standards and strengths of U.S. system for a specific sector.&rdquo; Such a mission would include representatives of SDOs as well as industry. The use of the words &ldquo;probably/or&rdquo; rather than &ldquo;and&rdquo; (without the &ldquo;probably&rdquo;) again indicates tentativeness. (Recommendation V.1.e) </span></li>

                </ul>

                <ul>

                  <li> <span class="style2">&ldquo;Provide basic training on standards and conformity assessment, including testing, labeling, accreditation, and related issues&hellip;&rdquo; to a wide variety of domestic and overseas trade staff. To the credit of the DOC, Evans announced at the press conference that this effort is already well under way. (Recommendation V.3.a) </span></li>

                </ul>

                <ul>

                  <li class="style2"> Intriguingly: &ldquo;Formalize training on standards development, conformity assessment, relevant trade agreements, and interagency process to DOC staff (spcifically NIST, NTIA and ITA).&rdquo; Given that these are the agencies that are most concerned with standards, such training would seem overdue &ndash; but better late than never. (Recommendation V.3.b) </li>

                </ul>

                <ul>

                  <li class="style2"> By seeking collaboration and funding opportunities with a diversity of other agencies and programs, including the Commercial Law Development Program (CDLP), the Middle east Partnership Initiative (MEPI)(State Department) grants, the Trade Development Agency (TDA), the United States Agency for International Development (USAID), and private-sector sources. (Recommendation V.4.b) </li>

                </ul>

                <ul>

                  <li> <span class="style2">&ldquo;Collaborate with ANSI on the revision of the National Standards Strategy&rdquo; (the NSS was originally created by ANSI in 2000). (Recommendation V.4.d) </span></li>

                </ul>

                <ul>

                  <li> <span class="style2">&ldquo;Evaluate early warning systems used by other U.S. Government agencies, specifically USDA&rsquo;s tiered early warning system.&rdquo; (Recommendation 5.c) </span></li>

                </ul>

                <ul>

                  <li> <span class="style2">&ldquo;Improve Interactions with China.&rdquo; Given that one out of five people alive today lives in China and the fact that China has been increasingly focusing on domestic standards in preference to international standards, it may not be surprising that China rates an entire category of the report to itself, with nine different recommendations. Most of the recommendations, however, are general rather than specific, and begin with words such as &ldquo;Support,&rdquo; &ldquo;Continue to monitor&rdquo; and &ldquo;Explore.&rdquo; The closest that any of the nine recommendations comes to launching a specific new activity is the last: &ldquo;Periodically assess how Chinese standards development and new standards affect U.S. exports and market access to key industry sectors.&rdquo; (Recommendation V.7.i)</span></li>

                </ul>

              </div>

              <p align="left" class="style2"><strong><em> <span class="style10"> <strong><em> Conclusions</em></strong>:</span></em></strong> <span class="style2">The DOC report is hardly a bold, visionary document. No significant new programs are proposed, and many of the recommendations relate to existing programs. But on the other hand, the DOC has already begun to actively implement the plan, with several of the more important recommendations already completed (e.g., the appointment of the first Standards Liaison) or well underway (such as the training of its overseas personnel on standards matters). And, in fact, there is little apparent need for the type of dramatic new initiatives that might play well in an election year but would have little practical purpose. </span></p>

              <p align="left" class="style2"> Instead, the report focuses on the very signficant forces already in place within the DOC and elsewhere inside the government. If this vast infrastructure is truly trained, optimized and deployed in support of a coherent standards policy, then the effects could be significant indeed. We have just seen the Bush administration engage with China at a high diplomatic level to break down an imminent standards barrier, so clearly the government is already aware of the stakes at issue when standards are used to close markets. And Phil Bond, the Under Secretary for Technology in the DOC, is not only clearly knowledgeable and engaged when it comes to standards, but also willing to project an active presence in SDO and consortium venues as proof that he recognizes the importance of standards to the national interest. </p>

              <p align="left" class="style2"> At the same time, it does not appear that there will be any fundamental shift in the relationship between the voluntary standard setting process in the United States and government. No mention is made of any direct funding for standard setting. Nor is there any proposal for any more formal structural change in the way that SSOs and government interact. Such a proposal might, for example, have involved holding an annual &ldquo;summit&rdquo; between representatives of SSOs and of the appropriate agencies and bureaus of government. Granting standards such a level of respect and attention might help preserve the degree of attention that they are enjoying under the report. </p>

              <p align="left" class="style2"> Whether the absence of structural change is for the better or worse in other respects, it does mean that the new commitment of government to support standard setting is more likely to wane rather than wax, once the threat of foreign, standards-based trade barriers eventually abates. For that reason, at least, its disappointing that the report did not take a bolder course. </p>

