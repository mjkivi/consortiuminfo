                <p> I first learned that I was acquiring a loyal following at the Yahoo! (and other) Rambus boards after I had the temerity to file our first pro bono brief in the case of Rambus v. Infineon, the case among several involving Rambus that is the farthest advanced in the courts. &quot;Pro bono,&rdquo; of course, means that we filed the brief without charge to anyone, and paid all of the filing and printing costs as well. Our brief urged that the Federal Circuit Court, to which Rambus had appealed its case after losing in a trial court, should reconsider its decision not to sanction Rambus for its bad behavior in the JEDEC standard setting process. </p>

                <p> In the trial court proceeding, it had been established that Rambus had destroyed evidence, and discovery also showed that Rambus had sought to covertly violate the JEDEC disclosure rules. A judge and jury agreed and severely sanctioned Rambus, but two out of three judges on a Federal Circuit Court panel later disregarded many of the jury's findings of fact, and imposed a patent analysis that many believe is wholly inappropriate to standard setting. The third judge on the panel roundly condemned Rambus for its conduct, and stated that the patent analysis used by the court was flawed and impractical as applied to the standard setting process. </p>

