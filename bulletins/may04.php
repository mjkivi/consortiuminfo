                <p> So where does all this lead? To the observation that while there are many investors who use message boards for completely legitimate purposes, there are others who gather there to yammer and trade profanity over, essentially, nothing. The same thing occurs in countless boards, on countless subjects -- political, social, and so on -- all over the Internet. It&rsquo;s sad that a technology that can share so much knowledge is also so often used to help pools of dysfunctional people clump together to ignore any information that doesn't match their financial self-interest, to abuse anyone who may disagree with their views, to assume that no one acts except for personal gain, and to stoke each other's prejudices. </p>

