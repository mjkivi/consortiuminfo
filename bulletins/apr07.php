				<p>The modern standards development infrastructure is largely the product of the industrial age, and evolved to address the needs of such an economy.  The advent of the Internet and the Web, and the continuing introduction of new ICT-based products and services in ever shorter product cycles, are exposing the fact that a system that retains strong roots in the 19th century is ill-suited to meet the demands of the 21st.   <br><a href="http://consortiuminfo.org/bulletins/pdf/apr07/feature.pdf">Print this article</a></p>
