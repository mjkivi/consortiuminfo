				<h4>FEATURED MEETING:</h4>
				<h3 class="red">AN OPEN INVITATION TO ATTEND</h3>
				<h3>GLOBAL  STANDARD  SETTING  2007:  AN  INTERACTIVE  DISCUSSION</h3>
				<p class="center"><strong>Hosted by Gesmer Updegrove LLP</strong><br>40 Broad Street, Third Floor<br>Boston, MA 02109<br>617-350-6800</p>
				<p class="center"><strong>June 20, 2007, 8:30 am</strong> &ndash; <strong>4:00 pm</strong></p>
				<p>On June 20, the third in a series of annual events focused on bringing together leaders of both accredited and non-accredited standards developers will be held, sponsored by the American National Standards Institute (ANSI).  </p>
				<p>ANSI represents U.S. interests in a number of important global standards organizations, and accredits those traditional standards development organizations (SDOs) that are formed to primarily benefit U.S. interests. Consortia, on the other hand, are not accredited by any national or international authority, and seek to set global standards, and many are headquartered in the United States.  Since SDOs are formed to represent national interests, while consortia are created to serve the interests of an international membership, the paths of these two communities cross constantly in the trenches, but almost never in a formal fashion, except through the many one-on-one liaison relationships established between SDOs and consortia that address the same or adjacent technical areas. </p>
				<p>Despite these differences, SDOs and consortia have far more in common, including the fact that their memberships characteristically overlap heavily.  They also share common concerns relating to intellectual property rights management, governance, member recruitment and retention, and promotion of their work product.  At the same time, each has competencies that the other usually lacks, and experience that its peers of both types could benefit from sharing.  The goal of this year's event will be to not only gain from another exchange of ideas, but also to explore avenues for ongoing communication on a more structured and frequent basis.</p>
				<p>This year's event has been designed to be highly interactive, and will feature brief panel presentations analyzing problems faced by all types of standard setting organizations (SSOs), followed by lengthier, facilitated discussions involving all attendees.  A final discussion period has been set aside to discuss ways in which consortia and accredited organizations can engage more frequently and productively to address matters of common concern.</p>
				<p><strong>Attendance: </strong> Participation is limited to senior management, Board members and other qualified representatives of both accredited and non-accredited SSOs, to ensure a high-level peer-to-peer exchange of issues and ideas.  There is no charge to attend, but space is limited and will be allocated on a first-come, first-served basis.</p>
