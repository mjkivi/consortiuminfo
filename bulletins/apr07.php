				<h4>STANDARDS BLOG:</h4>
				<h3>THE W3C, ACCESSIBILITY AND THE WEB:<br />A SHARED RESPONSIBILITY</h3>
				<p><span class="blogdate">Date:  April 13, 2007</span><br>Views: 1,753</p>
				<!--
				<blockquote><i></i></blockquote>
				-->
				<img class="imgright" src="/images/csb_200704_earth.jpg" alt="Earth / Globe" height="80" width="80" border="0">
				<p>Human beings have an astonishing capacity to take the most incredible innovations for granted almost as soon as they begin to enjoy them.  A less attractive feature of human nature is our ability to forget (and even not care) that others may not enjoy those same advantages. Sometimes, those that are disadvantaged in this way may indeed lose ground as we gain new conveniences and privileges.</p>
				<p>So it is with the Web, because those that are less fortunate may lose access to traditional services as they migrate to this new platform.</p>
				<p>As a result, I have tried to do my part to focus attention on a regular basis on Web and IT accessibility issues, in all their many forms (a recent example is <a href="http://www.consortiuminfo.org/standardsblog/article.php?story=20070307134445160">here</a>).  Happily, the media is paying closer attention to equal IT access, if only because advocates of the rights of  those with physical abilities have sought to make accessibility issues more visible. A recent example of such public attention was the focus in Massachusetts on accessibility in connection with the adoption of ODF.  More broadly, the <a href="http://www.itu.int/wsis/index.html">World Summit on the Information Society</a> (WSIS), a multi-year initiative launched under the auspices of the United Nations, has sought to promote Internet and Web accessibility on a global basis.</p>
				<p>To the general public, hardware and software economic roadblocks to Third World equality of access are easy to understand, and thus the worthwhile work of the <a href="http://www.laptop.org/">One Laptop Per Child</a> initiative justifiably receives wide attention.  But there are many other initiatives that have been, and continue to be, pursued largely outside public notice.  These projects address much more basic infrastructural challenges, and therefore appear less "interesting" to the general public. Yet without this important work, true global equality of Internet and Web access would not only be economically challenging to achieve, but technically impossible as well.</p>
				<p>These initiatives range from the <a href="http://www.consortiuminfo.org/standardsblog/article.php?story=20061017163856508">Unicode</a> (which seeks to encode, and therefore make machine readable and convertible, all current and historical character sets), to <a href="http://www.consortiuminfo.org/blog/blog.php?ID=46">language codes</a> (which identify the thousands of current and extinct languages for similar purposes), to the many <a href="http://www.consortiuminfo.org/standardsblog/article.php?story=2006072910244854&">accessibility initiatives</a> surrounding ODF.   I'm pleased that the Linux Foundation, of which I am a director, is also doing its part, through its Accessibility Working Group.  That initiative is dedicated to developing free and open accessibility standards to enable comprehensive universal access to computer systems, applications, and services.  And so on, up, down and across all of the many levels of IT infrastructure that continue to expand and become more complex on a daily basis.</p>
				<p>One of the leaders in such efforts has long been the W3C, which has launched accessibility projects of many types for much of its existence.  Unlike most large consortia, which tend to be driven primarily by commercial interests that focus on market opportunities rather than market equalities, the W3C has demonstrated a long-standing commitment to ensuring that the fruits of its labors are accessible to all, regardless of physical abilities, culture, language or geographic location.  This is crucial work, and is a tribute to the values that Tim Berners-Lee and others at the W3C have inculcated in that organization.</p>
				<p>The text that I've pasted in below is Robin Cover's extract from the <a href="http://www.w3.org/TR/2007/NOTE-i18n-html-tech-lang-20070412/">W3C announcement</a> (taken from Robin's invaluable <a href="http://xml.coverpages.org/newsletter.html">XML Daily Newslink</a> emails).  This item provides a good example of the quiet but important work that is ongoing at the W3C and a small number of other organizations, and the degree of technical attention that achieving universal accessibility requires.  Billions of people around the world will share in the benefits of work such as this &ndash; and yet few of those that benefit are likely to ever know to whom they owe a well-deserved expression of appreciation.</p>
				<p>Before moving on, I'd like to highlight one section of the W3C announcement, because it highlights the fact that you and I have an opportunity &ndash; and a responsibility &ndash; to do our part as well.  Enabling accessibility at the standards level will prove to be a wasted effort if Web site developers and owners do not take advantage of the good work of the W3C and others:</p>
				<blockquote>This document provides guidance for developers of HTML that enables support for international deployment.  Enabling international deployment is the responsibility of all content authors, not just localization groups or vendors, and is relevant from the very start of development.</blockquote>
				<p class="center">The W3C has done its part. Now it's our turn to do ours. </p>
				<p class="center">* * * * * * * * * * * * * *</p>
				<p class="center"><strong>Internationalization Best Practices: Specifying Language in XHTML and HTML Content</strong><br />Richard Ishida (ed), W3C Technical Report</p>
				<p>Members of the W3C Internationalization Core Working Group have published "Internationalization Best Practices: Specifying Language in XHTML and HTML Content" as a Working Group Note. The document is part of a series and written for HTML content authors working with XHTML 1.0, HTML 4.01, XHTML 1.1, and CSS. Specifying the language of content is useful for a wide number of applications, from linguistically sensitive searching to applying language-specific display properties. In some cases the potential applications for language information are still waiting for implementations to catch up, whereas in others, such as detection of language by voice browsers, it is a necessity today. </p>
				<p>On the other hand, adding markup for language information to content is something that can and should be done today.  Without it, it will not be possible to take advantage of any future developments. Applications already exist that can use information about the natural language (i.e., the human, non-programmatic language) of content to deliver to users the most relevant information or styling, based on their language preferences. The more content is tagged and tagged correctly, the more useful and pervasive such applications will become.  Language information is useful for things such as authoring tools, translation tools, accessibility, font selection, page rendering, search, and scripting.  These applications can't work, however, if the information about the language of the text is not available. Language information should therefore be specified for the page as a whole, and wherever language changes within the page. In the future there will be other applications for language information, driven by developments in technology. For example, implementations of the CSS3 ':first-letter' pseudo-element will need language information to apply correct styling.</p>
