				<h4>EDITOR'S NOTE:</h4>
				<h3>WHAT NEXT?</h3>
				<p>Mr. Natural, the eponymous hero of R.Crumb's <em>Zap Comix!</em> was wont to recommend, "<em>Always use the right tool for the job!</em>"  These are wise words, and ones that raise the question of whether the standard setting infrastructure we have today is the one we need for the future.</p>
				<p>That's the subject of this issue, as well as the question of what role, if any, government should play in helping to retool standard setting mechanisms to ensure that the needs of society are properly represented and addressed.  In some countries, government participation in such a reexamination would be expected and natural.  But in the United States, it would run counter to the historical role that government has played.  To date, Congress has acted through its regulatory powers primarily in areas of safety and health, and left information and communications technology (ICT) standards largely in the hands of private industry.</p>
				<p>But as the Internet and the Web play a larger and more important role in our lives, questions begin to arise over whether the public interest needs to figure more prominently in how these resources further evolve.  The recent public debate over "Internet equality" is one example of how this realization is beginning to dawn.  In some cases, as with open document formats, such ICT-based debates have involved standards as well.  </p>
				<p>I begin my analysis of these issues in my <strong><em>Editorial</em></strong>, which asks whether government should lead, follow, or simply get out of the way, and conclude that in at least some areas, governments will have little choice but to become involved.  I reach this conclusion because private industry is not sufficiently motivated to address the interests of those parties (such as consumers) that currently largely lack a seat at the standard setting table.</p>
				<p>In this month's <strong><em>Feature Article</em></strong>, I review the ways in which the current standard setting infrastructure, which evolved to meet the needs of an industrial economy, is inadequate to meet the needs of a world based increasingly upon ICT.  I suggest that this structure is a system under stress, and lacks an obvious means of evolving sufficiently to address the challenges that lie ahead.</p>
				<p>In my selection from the <strong><em>Standards Blog</em></strong> for this month I focus on a different and highly distributed weak link in the standards infrastructure &ndash; us.  Despite the fact that the World Wide Web Consortium (W3C) has developed a suite of standards able to render Web sites much more accessible to those with physical disabilities, the use of these standards to date is woefully low.  I regret that this is true for this site as well, and hope to be able to rectify this failing in the future.</p>
				<p>In my <strong><em>Consider This</em></strong> essay for this month I depart from this issue's theme to highlight the relationship between the imprecise natural language standards we use every day to describe variable qualities, such as shades of light and dark, and the more precise ones that standard setting organizations create for every variable, including even "slipperiness."</p>
				<p>And finally, I provide an announcement of a meeting that I am helping to organize and present in June, in an effort to address some of the issues discussed in this issue.  That meeting will be the third annual gathering of representatives of both consortia and accredited standard setting bodies.  The goal is to engage in a highly interactive exchange of ideas and experiences that will lead to a more structured and ongoing avenue of communication between these two essential arms of the standard setting infrastructure. </p>
				<p>As always, I hope you enjoy this issue.</p>
