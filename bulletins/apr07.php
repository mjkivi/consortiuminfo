				<h4>CONSIDER THIS:</h4>
				<p class="blogdate">April 31, 2007</p>
				<h3><span class="red">#48</span> The Slippery (and Colorful) Business of Standards</h3>
				<p>The recent announcement of a new standard for "slipperiness" reminded me not only of the seemingly infinite, and at times surprising, types of standards we find we cannot live without, but also of the linkage between language and standards.  </p>
				<p>The standard in question was developed by the Entertainment Services and Technology Association (ESTA), one of those worthy standards development organizations that may somehow have escaped your notice to date.  According to the <a href="http://www.esta.org/">ESTA Web site</a>, its core mission is "Building the Business of Show Business," and in support of that quest, its Floors Working Group developed what it calls <em>BSR E1.34-200x, Entertainment Technology - Measuring and Specifying the Slipperiness of Floors Used in Live Performance Venues.</em></p>
				<div class="calloutleft">
					<p>One might wonder whether it is more surprising that the world has existed so long without a way to measure the slipperiness of the floors of live performance venues, or that someone has now taken the time and effort to plug that remarkably small gap (odds are that your wonder may incline towards the latter).  </p>
				</div>
				<p>If one were so inclined, one might wonder whether it is more surprising that the world has existed so long without a way to measure the slipperiness of the floors of live performance venues, or that someone has now taken the time and effort to plug that remarkably small gap (odds are that your wonder may incline towards the latter).  Still, as noted in the announcement, "It is axiomatic that you can't manage what you can't measure," and if you're in the Business of Building Show Business, I guess it's quite plausible that you might find yourself in need of managing the slipperiness of live performance venues as well.</p>
				<p>All snarkiness aside (or mostly aside, anyway), the same announcement notes that slipperiness standards already exist to ensure that "firemen don't slip off fire engine steps and that pedestrians don't lose their footing."  Fair's fair, then, say I, and I for one feel compelled to agree that it was high time for <em>someone</em> to "deal with the concerns of performers trying to dance or to do some other unusual movement in front of an audience."  Viewed from that perspective, slipperiness is certainly something I'd want someone to be managing if I was at risk of tumbling arse over tin cups in front of a few hundred people.</p>
				<p>More to the point of this on-line venue, however, is the fact that standards provide a way to quantify individual perceptions, the better to intelligibly communicate and share those perceptions to achieve mutually desirable results.  And as this announcement indicates, while everyday language is adequate to categorize qualities in a very general way, it is rarely useful to do so in a highly precise manner.</p>
				<p>Still, natural language does use words in the nature of quantitative standards all the time.  If that sounds like an odd contention to make, then this would be an ideal time for me to invite you to Consider This:</p>
				<p>Language is by purpose descriptive, and successful in that regard in an analog sort of way.  Let's use a simple example to make this point, starting with one of the simplest of distinctions:</p>
				<p>&lt;<em>Black</em> or <em>White</em>&gt;</p>
				<p>Not much need for a standard to draw this absolute distinction.  But not much information can be conveyed by so narrow a range of alternatives, either, because we are restricting ourselves to a binary situation, offering only two choices.  So now let's expand the field of opportunity a bit, and change our field to read as follows:</p>
				<p>&lt;the range of <em>Black</em> to <em>White</em>&gt;</p>
				<p>Now we have an infinite number of gradations between the two binary extremes.  But how can we manage that range so that we can make use of this new opportunity?  One way, as noted in the slipperiness announcement, is by measuring it, but natural language doesn't lend itself well to that.  We can make some progress by using words of comparison, as in:</p>
				<p>&lt;<em>This</em> is darker (or lighter) than <em>that</em>&gt;</p>
				<p>That's a bit better, but only if all you want to know is whether A is darker or lighter than B, rather than just how light or dark A actually is.  If we want to know the latter, then we still need a broader choice of words to work with &ndash; and these can rightly be referred to as standards, broadly so called.  For example, we are already familiar with this range of descriptors:</p>
				<p>&lt;<em>Black, Dark Gray, Gray, Light Gray</em> and <em>White</em>&gt;</p>
				<p>While one might quibble that each individual element is "just the name of a color," when viewed together they can be seen to be the common language equivalents of, for example, the wattage of light bulbs, with words corresponding to numbers.  After all, the sequence of 40, 60, 75, 90 and 150 (the common light bulb wattages) provides exactly the same number of alternatives in bulbs as we used words to describe the range of shades above.  In the case of the shades from light to dark, though, we use words for cultural reasons to describe our visual perceptions rather than numbers - and thank heavens for that.</p>
				<p>If we were to use numbers, though, it might be more obvious to us that we are in fact using a performance standard to measure the amount of color we perceive when we look at something (black, after all, being the name we give to a surface that absorbs all of the frequencies of the visible light spectrum, while white is the "color" we see when all such light is reflected).</p>
				<p>For most purposes, the five words noted above will suffice.  And this is fortunate, because natural language becomes less useful when it is required to become more granular, because we lack a vocabulary of comparatives that is more than superficial (where does one go after "very light gray?").  In consequence, we may be forced to resort to metaphors, rather than comparatives, but that only helps us be a bit more specific at the extremes ( we know what is meant by the words "slow as a tortoise" and "fast as a bat out of hell," but metaphors don't arise naturally to fill in the space between).</p>
				<p>Still, if you were picking paint colors, you would find that for marketing purposes the paint manufacturer will have provided more or less arbitrary names for the dozens of shades of gray it offers, in addition to its own numerical lot numbers.  And it's likely that it will be those names, and not the lot numbers, that you will use to communicate your final selection to the paint clerk.  Anyone that has viewed the sometimes ludicrously lush names of paint samples might appreciate the virtues of these numerical identifiers, especially if they are sequential rather than arbitrary.  But it's still more likely that the conversation at home will run more to "Dear, don't you just love Festive Fuchsia?" rather than "No. 1094 is so what we want!"</p>
				<p>But Festive Fuchsia won't serve in all situations.  Instead, numerical scales have been developed to measure exactly the electromagnetic data that we more familiarly refer to as colors, and with far greater precision.  Among others, astrophysicists, meteorologists, and engineers all find cause to measure the amount of light absorbed or reflected by a given surface.  The ratio of light that is reflected in comparison to the total energy that strikes such a surface is expressed as its albedo (a term that can be used in relation to all types of electromagnetic energy, and not simply that which is represented by the frequencies that comprise the spectrum of visible light).  Measuring the albedo of a given surface can be useful in connection with everything from consumer product design to calculating the future progress of global warming, not to mention the possibility that remote planets might harbor life.</p>
				<p>Whether vernacular or scientific, and expressed in words or numbers, each methodology assigns a standard set of descriptors to communicate the amount and type of reflected light, and each is equally valid, so long as it serves the purpose of providing useful information.  </p>
				<div class="calloutleft">
					<p>One day, we received drawings in the mail from Brian Watson, our architect, showing a tile design.  We liked the design, but not the colors he had used.  The workman was arriving the next day to set the tile, but Brian hadn't included any color samples with the design from which we could make another selection.  What to do?</p>
				</div>
				<p>What to do, though, if you find yourself caught between these two methodologies, and in need of the precision of the scientific scale while having only words at hand?  This happened to my wife and I while we were remodeling our home a decade ago.  With the interior of our home consigned to a couple of construction dumpsters, we were living and working out of our vacation house on an island off the coast of Maine.  </p>
				<p>One day, we received drawings in the mail from Brian Watson, our architect, showing a tile design inset he was suggesting for the shower in the guest bathroom.  We liked the design, but not the colors he had used in the drawing.  The workman was arriving the next day to set the tile, but Brian hadn't included any color samples with the design from which we could make another selection.  What to do?</p>
				<p>We called Brian and tried to describe the colors we wanted in the abstract.  But lacking a commonly understood color vocabulary (a/k/a, a standard), we were getting nowhere, until Brian had an inspiration.  Like us, he had a six year-old child, which led to a sudden, mid-sentence interjection: "Do you have the complete set of Crayola crayons up there?"  Indeed we did, as did he.  In no time, we had agreed on Burnt Sienna (an original Crayola color, introduced in 1903) and Teal Blue (a shade of more recent vintage, added to the Binney &amp; Smith Big Box in 1990), and our southwestern motif inset was on its way to completion.</p>
				<p>All of which only goes to show that dealing with qualitative data can be a slippery business.  Unless, that is, you've got a standard even if it's only a Big Box of crayons. </p>
