				<a name="editorial" id="editorial"></a>
				<h4>EDITORIAL:</h4>
				<h3>CYBERINFRASTRUCTURE<br>AND THE PUBLIC INTEREST</h3>
				<p class="center"><a href="http://www.lpu.com/attorneys/updegrove.shtml">Andrew Updegrove</a></p>
				<p>Over the last several months I have spoken at conferences and symposia in places as widely dispersed as Washington and Cambridge, Beijing and New Haven.  In each case, the topic was the intersection of standards and the public interest, comprehending new concepts such as the "knowledge commons" and the increasing importance of "cyberinfrastructure."  The issues we discussed included government's responsibility to utilize appropriate standards to safeguard the future of public documents, and the best way to ensure that the promise of information and communications technologies (ICT) is fulfilled in developing nations.  These gatherings have been held under the auspices of institutions as diverse as the National Academies and the United Nations Development Programme, the Chinese Ministry of Commerce and the United States &ndash; European Commission Transatlantic Consumer Dialogue, and the Law Schools of Harvard and Yale Universities. </p>
				<div class="calloutleft">
					<p>Whether government should incline towards leading, following or simply getting out of the way [in standard setting] is a matter upon which there are likely to be strongly held differences of opinion.  How well government chooses among those roles, and how well it executes when it chooses to lead, will likely have a profound impact on our lives in the years ahead.</p>
				</div>
				<p>The fact that so many people are meeting in so many venues to discuss standards in non-technical contexts evidences the realization that something new and important is at work here.  And the fact that many of these conferences are taking place in academic and government venues suggests that people are still trying to figure out what it's all about.</p>
				<p>At the most basic level, what's under the microscope is the increasing influence and importance of the Internet and the Web.  Unlike previous telecommunications and information technology advances, each of which offered incremental improvements in one discipline or the other, the Internet and the Web represent not only giant leaps forward in both information and communications technologies, but also the convergence of both in a way that profoundly impacts the relationship of the individual to almost everything, including public information, employment opportunities, government services, and the most personal and private of information.</p>

				<p>As a result, the attention of government is commanded in almost every way: as a consumer of technology, as a provider of services to its citizens, as the guarantor of equal access and opportunity, as the regulator of telecommunications and content, as the ultimate arbiter of intellectual property rights, and so on, down through seemingly infinite and more subtle levels of impact.  It's all very complex, and government is only just beginning to wrestle with many of the consequences &ndash; such as "off shoring," a word, as well as a threat to domestic jobs, that would not exist without the Internet.</p>
				<p>At the heart of these new developments lie a variety of standards, from Internet numbers, names and protocols, to open document formats, to language and character codes.  In the past, when new ICT innovations emerged, the standards upon which they relied lay far below the level of political and public notice.  Now, they have often become the subject of public debate (there are open document bills pending in the legislatures of four US states) as well as international trade disputes (China continues to promote its WAPI standard for domestic use over WiFi) and even foreign relations (the ICANN dispute remains unresolved).</p>
				<p>More fundamentally, governments are beginning to realize that perhaps the Internet really has changed everything, at least for them, and that standards represent one of the elements they are going to have to deal with as they begin to grapple with what to do about their new responsibilities.  How will they deal with financial and medical data breaches?  What can they do to ensure that first responders will be able to communicate the next time that terrorists strike in the Homeland, and how will the refugees of the next Katrina be able to access their electronic medical records?  And what must governments do to ensure that public records will be available in fifty years, if they no longer maintain paper archives?   </p>
				<p>All of these questions, and many more, can only be solved (at least in part) through the development and management of standards.  But who will set those standards?  How can governments ensure that they are created?  Does it matter who sets them, and who should have a say in their creation?  Should government stand aside, or should they play a role in their development?  If government should be involved, should its role be as a leader, a participant, a catalyst, a funding source or a regulator?</p>
				<p>This dialogue is only beginning, and to date the United States government itself has most often been peripherally, and reactively involved.  In the case of ICANN, it has responded more politically than analytical.  With respect to first responder standards, the American National Standards Institute (ANSI) has perhaps acted more productively than the government agencies directly involved.  Data breaches?  Private industry has been more proactive, through the formation of consortia such as the Cyber Security Industry Alliance and, more recently, the PCI Security Standards Council.  Privacy?   Europe has acted more quickly and aggressively.  Open document standards?   Nothing is happening in Congress, but bills to mandate the use of open formats have been filed in four US States, and the Executive Agencies of Massachusetts began converting to open formats in January of this year.  Several national governments in Europe have, or are moving in the same direction.</p>
				<p>Whether government should incline towards leading, following or simply getting out of the way is a matter upon which there are likely to be strongly held differences of opinion.  It's also likely, though, that government will not have the luxury of opting for the third choice in some of the areas just mentioned.  How well government chooses among those roles, and how well it executes when it chooses to lead, will likely have a profound impact on our lives in the years ahead.</p>
				<p>Hopefully it will choose wisely, and execute well.</p>
