                <p><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITORIAL:</em></strong></font></p>
                <div align="center">
                  <div align="center">
                    <p align="center" class="style10"><strong>SUBSTANCE AND STRUGGLE</strong></p>
                    <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font></p>
                    <p align="left" class="style2">As in time past, I began my  preparations for this &quot;Year in Review&quot; issue by scanning the  individual news items (more than 700 in 2005) that I had culled from thousands  more for posting at the <a href="http://www.consortiuminfo.org/news">News  Portal</a> of this site.&nbsp; After  completing that task, I sat back and asked myself what single word might best  sum up the world of standards in 2005.&nbsp;  I was rather dismayed that the word that immediately sprang to mind, and  best seemed to fit, was &quot;struggle.&quot;</p>
                    <p align="left" class="style2">Indeed,  there was a great deal of contention, as well as outright confrontation,  everywhere one might care to look for it: within standard setting  organizations, such as IEEE, where rival factions pushed for adoption of their  favored flavor for the next standard in the 802.11 family of wireless  standards; between consortia, as in the battle to the death still raging  between the supporters of the HD-DVD and the Blu-ray next generation DVD formats;  between individual countries and the rest of the world, as with the ongoing  efforts by China to protect its domestic industries through standards within  the constraints established by the Act on Technical Barriers to Trade; and last  but not least, the struggle of just about everyone in the IT industry to cope  with the current flawed patent regime in the United States. </p>
                    <p align="left" class="style2">Of  course, there were successes, too, but while many so-called  &quot;successes&quot; may have resolved an issue (at least for a time), they  were really defeats of one side by another, with the public, positive  statements of the losing side smacking of face-saving efforts to put the best  spin on what was in fact a retreat.&nbsp;  Certainly this was the case when the deadlock between the United States  and most of the rest of the world over &quot;Internet governance&quot; was  broken just prior to the formal convening of the World Summit on the  Information Society in Tunis.&nbsp; In such  one-sided victories, it may be that a different resolution has merely been  delayed, rather than defeated.</p>
                    <p align="left" class="style2">As the  new year begins, then, it is worth asking this question: Has the world of  standard setting really grown to be more contentious, and if so, why?</p>
                    <p align="left" class="style2">I  suspect that it would be untrue to state that standard setting has suddenly  become more adversarial across the board.&nbsp;  But I expect that a case could be made that within the information and  communications technology (ICT) sector it has become more of a contact sport  over the course of the last several years, and that the last twelve months have  been particularly good examples of this willingness to mix it up.</p>
                    <p align="left" class="style2">Why  would this be true?&nbsp; Most obviously,  because ITC standards matter more now than ever, and in more ways than ever  before, both positively and negatively.&nbsp;  Standards can still create new product opportunities, but if all of the  patents that they infringe are owned (for example) in the West, then  manufacturers in the East (e.g., in China), may find themselves at a great  disadvantage, and desirous of creating standards of their own.&nbsp; </p>
                    <p align="left" class="style2">Similarly,  when a single manufacturer has a valuable monopoly (such as Microsoft, with its  Office productivity suite), then it is more likely to work against rather than  support a standard -- such as the OpenDocument OASIS format &ndash; that could  threaten that monopoly.&nbsp; And again, if  one country has control of a key component of a valuable ITC resource, such as  the root directories of the Internet (which are maintained under the  supervision of the United States Department of Commerce) and a go it alone  attitude to boot, then it's likely that such a country will exhibit a &quot;my  way or the highway&quot; response to requests for joint control.</p>
                    <p align="left" class="style2">Each of  these examples of contention has received ample coverage in the news this year,  along with far too many other stories of a similar kind.&nbsp; It is likely that 2006 will bring more of  the same. </p>
                    <p align="left" class="style2">Still,  there is reason for optimism, for 2005 also brought news of countervailing  actions in the marketplace, such as patent pledges by major IT vendors in  support of open source software development efforts, and the continuing  evolution of what many have come to refer to as Web 2.0, built not only upon  formal efforts such as the upgrading of the RSS and Atom syndication standards,  but also upon nonprofit projects (e.g., the Wikipedia), free tools (e.g.,  de.lic.ious bookmarks) and entrepreneurial ventures (such as www.flickr.com) &ndash;  not to mention the enthusiastic, organic and experimental currents that have  swirled around the Web, based upon these and thousands of other sites, blogs and  shared ideas.</p>
                    <p align="left" class="style2">And  finally, there is the underlying dynamic of standard setting, which is  this:&nbsp; compliance with standards is  essentially a voluntary act.&nbsp; Unlike the  political system, which bestows upon the majority the power of the state, and  therefore the ability to oppress a minority even over long periods of time, the  standards system is based upon a balance of self-interest and common  benefit.&nbsp; No player can push that  balance too far or for too long in its own direction before it begins to lose  more than it gains by way of market reaction.&nbsp; </p>
                    <p align="left" class="style2">Of  course, this dynamic does not restore equilibrium in real time, and only by  coincidence is any individual cog in the standards machine in equipoise at any  given moment.&nbsp; But overall, it is a  self-correcting system, and that is a very powerful wind to have at one's back.</p>
                    <p align="left" class="style2">So it is  that as 2005 closes, we may well look with trepidation towards the discrete  events of the year to come.&nbsp; But we can  also anticipate with fair confidence that at this time next year, notwithstanding  all of the gnashing of teeth and sleight of hand that may lie between now and  then, we will be looking back on another year during which accomplishments  outnumbered failures, and where for all of the pushing and pulling, the vast  majority continued to play within the system.</p>
