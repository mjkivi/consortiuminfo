              <p><strong><em>              </em></strong></p>
              <div align="center">
                <p><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">HERE AT CONSORTIUMINFO.ORG: </font></em></strong></p>
                <div align="center"><div align="center">
                  <p align="center" class="style10"><strong>OUR YEAR IN REVIEW </strong></p>
                  <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font></p>
                  <p align="left" class="style2">2005 was a busy year at  ConsortiumInfo.org as well as in the standards world at large.&nbsp; Here are a few highlights:</p>
                  <p align="left" class="style2"><strong><em>February 16:</em></strong>&nbsp; We were  pleased to announce the launch of the <a href="http://www.consortiuminfo.org/metalibrary/">Standards MetaLibrary</a> in  our <a href="http://www.consortiuminfo.org/bulletins/feb05.php">February issue</a>,  which was appropriately dedicated to the Study of Standards.&nbsp; What is the Standards MetaLibrary?&nbsp; Most succinctly, it is an expanding database  of categorized abstracts of serious articles (currently 1,038 in number) about  standards, in each case with links to the full text, which in most cases is  available free of charge.&nbsp; The Standards  MetaLibrary was created with the assistance of a generous grant from Sun  Microsystems.&nbsp; We described the content  of the Standards MetaLibrary in the formal <a href="http://www.consortiuminfo.org/bulletins/feb05.php#announcing">announcement</a> as follows:</p>
                  <div align="left">
                    <blockquote>&nbsp; </blockquote>
                  </div>
                  <blockquote><p align="left" class="style2">The  Standards MetaLibrary is not limited to material about standards in a narrow  sense. Rather, it focuses on the importance of standards to the modern world  and their impact on society, and how they are created, and by whom. The  materials included therefore address topics such as how the standard setting  process operates, how governments support this process and utilize standards,  the economic benefits of standards uptake, legal aspects of the use (and abuse)  of standards, and many other subjects that illustrate the role of standards in  the world today.</p>
                  </blockquote>
                  <p align="left" class="style2">By  year's end, use of the MetaLibrary was exceeding 18,000 a month.&nbsp; For more about what the Standards  MetaLibrary is and how to make use of it, see the <a href="http://www.consortiuminfo.org/metalibrary/faq/">FAQ page</a>.</p>
                  <p align="left" class="style2"><strong><em>March 29:</em></strong>&nbsp; At the end of March, the host of this site, <a href="http://www.gesmer.com/">Gesmer Updegrove LLP</a> hosted what the American  National Standards Institute (ANSI) called a <a href="http://www.ansi.org/news_publications/news_story.aspx?menuid=7&articleid=909">&quot;landmark  event&quot;</a>.&nbsp; As described in  greater detail in the ANSI press release that followed the meeting:</p>
                  <div align="left">
                    <blockquote>&nbsp; </blockquote>
                  </div>
                  <blockquote><p align="left" class="style2">A landmark event occurred in Boston,  Massachusetts, today as representatives from 21 consortia convened with members  of the voluntary consensus standards community for the largest known meeting of  the two constituencies. </p>
                    <p align="left" class="style2"> Hosted by  Gesmer Updegrove LLP and organized by the <a href="http://www.ansi.org/usss">U.S.  Standards Strategy</a> (USSS) Committee, the session was held specifically for  the purpose of information sharing and soliciting input from consortia on the  draft revision of the Strategy that is currently underway. The USSS (formerly  known as the <a href="http://www.ansi.org/nss">National Standards Strategy</a>)  serves as a strategic framework to help guide standards-related activities  impacting trade, market-access, emerging national priorities and more. </p>
                  </blockquote>
                  <p align="left" class="style2"> ConsortiumInfo.org publicized the  event, and its Editor, Andrew Updegrove (a member of the United States  Standards Strategy Committee), was responsible for gathering most of the  representatives of the consortium community.</p>
                  <p align="left" class="style2"><strong><em>June 28:</em></strong>&nbsp; The <a href="http://www.consortiuminfo.org/newsblog/">Standards Blog</a> was  launched.&nbsp; By January 3, 2006 the 103rd  blog entry had been posted, and monthly visits to the Standards Blog were  approaching 90,000.&nbsp; Rather than a  compendium of links to the news of the day, the Standards Blog attempts to  provide in-depth analysis of important events, and to follow important stories  relevant to the world of standards as they develop.&nbsp; The Standards Blog has most thoroughly covered the evolving story  in Massachusetts relating to the adoption of <a href="http://www.consortiuminfo.org/newsblog/blogcat.php?CID=30">open document  formats</a>, and in particular the OASIS OpenDocument format.&nbsp; Many of the most important developments in  the story were first reported in the Standards Blog, which has now been cited  as a source in more than a hundred main-stream news articles.</p>
                  <p align="left" class="style2"><strong><em>August 15:</em></strong>&nbsp; Andrew Updegrove, ConsortiumInfo.org Editor,  was presented with the <strong><em>ANSI 2005 President's Award for Journalism</em></strong>.&nbsp; As stated in the ANSI <a href="http://www.ansi.org/news_publications/news_story.aspx?menuid=7&articleid=1008">press  release</a>:</p>
                  <div align="left">
                    <blockquote>&nbsp; </blockquote>
                  </div>
                  <blockquote><p align="left" class="style2"><strong>Andrew  Updegrove</strong> has been awarded the  President's Award for Journalism, which recognizes outstanding journalistic  work that helps to illuminate the role that standardization and conformity  assessment activities play in improving the health and safety of Americans,  while strengthening the competitiveness of U.S. business in a global  marketplace. Mr. Updegrove acts as editor and publisher of the <em>Consortium  Standards Bulletin</em>, which offers a wealth of information on standards and  standards-setting, both online and in print. <em>The Bulletin</em> and website, <a href="http://www.consortiuminfor.org/">ConsortiumInfo.org</a>, have helped to  raise the profile of standards and highlight their importance to commerce and  daily life, as well as encourage greater support for their use and development. </p>
                  </blockquote>
                  <p align="left" class="style2"><strong><em>December:&nbsp; </em></strong>Andrew Updegrove was <strong><em>elected to the Boards of  Directors of both ANSI and the Free Standards Group</em></strong>, offering the  opportunity to gain greater insight into issues of interest to the  ConsortiumInfo.org audience.&nbsp; ANSI is the administrator  and coordinator of the U.S. voluntary standards system, includes more than 200  standards bodies as its members, and represents the interests of U.S. industry  internationally, including as the U.S. representative in global standards  bodies ISO and the IEC.&nbsp; Free Standards  Group, a nonprofit organization formed by the open source development community  and leading IT industry vendors to create the standards needed to prevent Linux  and other key open source software from fragmenting in the way that destroyed  UNIX interoperability. Its current standards include the Linux Standards Base  (recently adopted as a global ISO standard) </p>
                  <p align="left" class="style2"><strong><em>By the Numbers:</em></strong>&nbsp;&nbsp; At year's end, all ConsortiumInfo.org  resources were still free and available to the public, and more extensive --  and utilized -- than ever.&nbsp; Here are a  few examples: </p>
                  <div align="left">
                    <ul type="disc">
                      <li class="style2">Number of archived news items at the <a href="http://www.consortiuminfo.org/news/">Standards News Portal</a>: <strong>1761 </strong></li>
                      <li class="style2">Number of news items entered in 2005:&nbsp; <strong>751</strong></li>
                      <li class="style2">Number of articles in the <a href="http://www.consortiuminfo.org/metalibrary/">Standards MetaLibrary</a>:&nbsp; <strong>1,038</strong></li>
                      <li class="style2">Number of standard setting organizations and       open source projects in the <a href="http://www.consortiuminfo.org/links/">Consortium       and Standards List</a>:&nbsp; <strong>455</strong></li>
                      <li class="style2">Monthly visitors/page views (January 2005):&nbsp; <strong>19,905 / 83,091</strong></li>
                      <li class="style2">Monthly visitors/page views (December       2005):&nbsp; <strong>183,745 / 743,885</strong></li>
                    </ul>
                  </div>
                  <p align="left" class="style2">We look forward to  increasing the resources and services available at ConsortiumInfo.org in 2006,  including a re-launch of the Standards Blog on a new open source technology  platform that will permit it to become a fully-featured community site, and, we  hope, an expansion of the MetaLibrary, among other plans currently on the  drawing board.</p>
                  <p align="left" class="style2">As always, your comments and  suggestions are welcome, as are inquiries relating to how we can work with  other organizations towards achieving goals of mutual interest.</p>
