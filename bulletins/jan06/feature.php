                <p><span class="style8"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">FEATURE ARTICLE</font></em></strong><strong> </strong></span></p>
                <div align="center">
                  <div align="center">
                    <p align="center" class="style10"><strong>2005: THE STANDARDS YEAR IN REVIEW</strong> </p>
                    <blockquote>
                      <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font></p>
                    </blockquote>
                    <p align="left" class="style2"><strong><em>Introduction:</em>&nbsp; </strong>2005 was a busy standards year &ndash; far busier than can  be summarized in one article, or even one issue of this journal.&nbsp; The year had both high and low points,  victories as well as losses, and stories that simply went sideways or sputtered  out entirely.</p>
                    <p align="left" class="style2">In many ways, the year was  characterized by struggle, as noted in this issue's <strong><em><a href="#editorial">Editorial</a>,</em></strong> in part because it was also beset by increasing complexity on many fronts:  patent thickets grew thicker while the United States patent system became  increasing recognized for contributing to the problem; nations and regions  became more skilled at incorporating standards into their commercial  strategies; major corporations did as well; and with the increasing success of  open source software came friction between the simple concept of Free and Open  Software and the more complex licensing practices of traditional standard  setting participants.</p>
                    <p align="left" class="style2">But the flip side of the  coin is that these same complexities often arose from the fact that the  importance of standards is becoming better recognized.&nbsp; With this realization of value and  importance, for better or worse, comes greater incentive for commercial and  governmental interests to play the standards game for maximum benefit.</p>
                    <p align="left" class="style2">In this third Annual Review  of the year in standards, we will therefore not attempt to summarize every  major event or story of the year just ended.&nbsp;  Instead, I will memorialize what I believe to have been the most  newsworthy highs and lows of the last twelve months, so that readers now and in  the future can remember the best &ndash; and the worst &ndash; that 2005 had to offer.</p>
                    <p align="left" class="style2"><strong>1<em>&#9658;Standards Story of the Year:&nbsp; </em><span class="style114">Massachusetts and OpenDocument</span></strong> <br>
                      There was little doubt in my mind what to recognize as the 2005 IT  standards story of the year.&nbsp; It  involves a visionary (and now toppled) leader in the person of State CIO Peter  Quinn, who announced his resignation to his employees on Christmas eve; a  battle between the largest IT companies in the world, as companies like IBM and  Sun saw an opening to break Microsoft's ownership of the office productivity  software market; infighting among politicians in a scrabble for control of  state IT procurement; the future of not only open standards but of open source  software as well; the need for software vendors &ndash; and standards developers &ndash; to  serve the accessibility needs of those with disabilities; the preservation of  historical record; and whether you or I will be able to access our individual  past in the future.</p>
                    <p align="left" class="style2">My coverage of this saga began at the <a href="http://www.consortiuminfo.org/news/cat.php?CID=30">News Portal</a> on  September 1st,and at the <a href="http://www.consortiuminfo.org/newsblog/blogcat.php?CID=30">Standards Blog</a> on September 17th.&nbsp; I dedicated the <a href="http://www.consortiuminfo.org/bulletins/sep05.php">September issue</a> to  the story, and have continued to follow it in all three of these venues ever  since &ndash; as I will in 2006, since the story is far from over.</p>
                    <p align="left" class="style2">The  twists and turns of this story are too Byzantine to be summarized in this  article, but you can access the full database of news stories and blog entries,  both already existing and as new material is added in the future, <a href="http://www.consortiuminfo.org/news/cat.php?CID=30">here</a>.</p>
                    <p align="left" class="style2"><strong>2<em>&#9658;Open Source Story of the Year:</em>&nbsp; <span class="style114">The  Embrace of Open Source by Big IT</span></strong></p>
                    <p align="left" class="style2">While 2005 was not the first  year in which big information technology companies invested heavily in Linux  (IBM most prominently passed through that knothole years ago), 2005 was the year  in which the floodgates opened, with many companies making public  pronouncements as well as private strategic decisions intended to boost  customer confidence and interest not just in Linux, but in open source  alternatives to proprietary software products in general.</p>
                    <p align="left" class="style2">The most dramatic evidence  of this trend was a series of public announcements by major patent owners that  they would not assert their patents against Linux, and in some cases, against  any open source software product.&nbsp; Those  announcements were intended to allay customer concerns relating to the  consequence of porous patent laws, and to underline the marketing message that  the open source train was gathering steam, having already left the station.</p>
                    <p align="left" class="style2">The first announcement was  made by IBM, which on January 10 <a href="http://www.ibm.com/news/us/en/2005/01/patents.html">pledged</a>:</p>
                    <div align="left">
                      <blockquote>&nbsp; </blockquote>
                    </div>
                    <blockquote><p align="left" class="style2">&hellip;to open  access to key innovations covered by 500 IBM software patents to people and  groups working on open source software. The pledge applies to any individual,  community, or company working on or using software that meets the Open Source  Initiative (OSI) definition of open source software.&nbsp; This is believed to be the largest pledge ever of patents of any  kind and represents a major shift in the way IBM manages and deploys its  intellectual property portfolio.</p>
                    </blockquote>
                    <p align="left" class="style2">IBM's  pledge was followed rapidly by (often curiously reminiscent) announcements by  other multinational IT vendors.&nbsp; Sun  Microsystems, for example, <a href="http://www.sun.com/smi/Press/sunflash/2005-01/sunflash.20050125.2.html">announced</a> a scant 15 days later:</p>
                    <div align="left">
                      <blockquote>&nbsp; </blockquote>
                    </div>
                    <blockquote><p align="left" class="style2">&hellip; the largest single release of  patent innovations into the open source community by any organization to date,  marking a significant shift in the way Sun positions its intellectual property  portfolio&hellip;. [S]aid Scott McNealy, Chairman and CEO, Sun Microsystems,  Inc. &quot;The release of more than 1,600 patents associated with the Solaris  OS far eclipses any other vendor's contribution. Today represents a huge  milestone for Sun, for the community, for developers and for customers.&quot; <br>
                        A  more modest pledge was later made by Computer Associates, which was reported on  March 3 to be planning a pledge of its own (it made its formal commitment in  support of IBM's pledge on September 7, involving <a href="http://www3.ca.com/press/PressRelease.aspx?CID=73958">14 patents</a> of  its own).&nbsp; Nokia made its own pledge in  support of Linux <a href="http://www.internetnews.com/dev-news/article.php/3508146">in May</a>.</p>
                    </blockquote>
                    <p align="left" class="style2">Other  actions of many types followed.&nbsp; In  August, Novell announced that it would open source its flagship <a href="http://www.infoworld.com/article/05/08/03/HNnovellopensuse_1.html">Suse  Linux</a> software, and even less directly involved vendors shifted position  towards a world in which open source software might become even more  powerful.&nbsp; Intel, for example, announced  in May that it had formed in internal <a href="http://news.com.com/Intel+forms+internal+open-source+group/2100-7344_3-5702371.html">open  source group</a>.&nbsp; In April, it was even  announced that Microsoft had <a href="http://www.eweek.com/article2/0,1759,1791085,00.asp">extended an olive  branch</a> to the open source community (although no watershed change in its  public strategy followed its call for a &quot;for a sit-down to discuss how the software giant can better work with  the open-source world.&quot;)</p>
                    <p align="left" class="style2">By  the end of the year, it seemed certain that the open source genie would never  be tricked back into the proprietary bottle, if only because too many  proprietary vendors were themselves too committed to any longer back away from  their public commitments. </p>
                    <p align="left" class="style2"><strong>3<em>&#9658;Standard of the Year: </em><span class="style114">XML</span>&nbsp; </strong> </p>
                    <p align="left" class="style2">If there was a standard of  the year, surely it was XML.&nbsp; But not  just XML (which achieved W3C Recommendation status on February 10, 1998), but  all of its schematic and other children &ndash; whether supported by independent  organizations (such as XBRL international, which has adapted&nbsp; XML for public accounting reporting  purposes), organizations within organizations (such as LegalXML, a Member  Section within OASIS, which in turn supports a myriad of XML-related  activities), less formal multi-member forums, and even individual companies,  university labs and government agencies.</p>
                    <p align="left" class="style2">In fact, there are few  information domains in which XML cannot be useful &ndash; and in most cases, has  already been put to use - from human resources (<a href="http://www.consortiuminfo.org/links/detail.php?ID=240">HR-XML Consortium (HR-XML)</a>, to  sports data (<a href="http://www.consortiuminfo.org/links/detail.php?ID=465">SportsML</a>).&nbsp; This trend continued in 2005, with the  formation of new groups, both formal and informal, including the <a href="http://www.bio-itworld.com/newsitems/2005/sept2005/09-29-05-news-hupo">International  Molecular Exchange</a> (Imex), which was formed to &quot;jointly curate and  exchange data&quot; in the five publicly available databases that are based  upon the Molecular Interaction XML Interchange standard developed for  proteomics use.</p>
                    <p align="left" class="style2">Look  for more activities of all types involving XML in 2006 (and after).</p>
                    <p align="left" class="style2"><strong>4<em>&#9658;Standards Developer of the Year (Organization):</em> <span class="style114">OASIS</span></strong></p>
                    <p align="left" class="style2">By almost any measure, OASIS  was the standards development organization that found itself (by design or  otherwise) in the middle of the most action in the IT sector.&nbsp; All in the same year, it:</p>
                    <div align="left">
                      <ul type="disc">
                        <li class="style2">Continued to play a central role in the       development of Web services and other e-business standards</li>
                        <li class="style2">&nbsp;Completed a nearly three year struggle to achieve consensus       on one of the first formal intellectual property policies to offer an       elective rules track to create standards that would be friendly to open       source software implementations (OASIS was then <a href="http://www.consortiuminfo.org/bulletins/pdf/mar05/editorial.pdf">condemned</a> for its trouble in a letter signed by 29 open source advocates, who wanted       OASIS to mandate use of the open source-friendly terms for all of its       Technical Committees)</li>
                        <li class="style2">Adopted and released eight new standards,       including version 1.0 of the OpenDocument format in May, providing the       first open office productivity application format (and the basis for the       commercial and political struggle in <a href="http://www.consortiuminfo.org/bulletins/sep05.php">Massachusetts</a> that I selected as the 2005 Standards Story of the Year).</li>
                        <li class="style2">Launched six new Technical Committees to address       a wide array of new needs..</li>
                      </ul>
                    </div>
                    <p align="left" class="style2">While some other consortia  and accredited standards developers may have had more projects in motion in  2005, it was OASIS that, through a perfect storm of commercial and political  forces, found itself to be at the center of more of what was newsworthy in 2005  than any other organization.<br>
  &nbsp;<br>
  <strong>5<em>&#9658;Standards Developer of the Year (Country):</em>&nbsp; <span class="style114">China</span></strong></p>
                    <p align="left" class="style2">In April, we dedicated an  issue of the <strong><em>CSB</em></strong> to the <a href="http://www.consortiuminfo.org/bulletins/apr05.php">rising power</a> of  China, which has deployed its formidable resources to the creation of what may  be the best funded and staffed standards infrastructure that the world has ever  seen.&nbsp; While that infrastructure appears  to still be finding its voice in the established bureaucracy of global standard  setting, the influence that China can be expected to assert will be  substantial, given the size of the market subject to its control.</p>
                    <p align="left" class="style2">China has even more reasons  to flex its standards muscles than most countries, given the preponderance of  patents in the West that currently forces it to function as the cut-rate job  shop for the high-priced occidental brands that reap the margins generated by  the sweat of Chinese brows.&nbsp; If China  can create and enforce its own standards without violating the World Trade  Organization's Technical Barriers to Trade Act, it will do so, adding another  weapon to an already formidable commercial armamentarium. </p>
                    <p align="left" class="style2">Already, there have been  major international repercussions over China's efforts to require compliance  with a home-grown wireless standard.&nbsp;  That issue has only been partially resolved, and additional tests are in  the offing involving Chinese home-grown standards for 3G cell phones, DVD  players and video/video compression.</p>
                    <p align="left" class="style2">Stay tuned.&nbsp; You'll be hearing more on this story in 2006  as well.</p>
                    <p align="left" class="style2"><strong>6<em>&#9658;Intellectual Property Issue of the Year:</em>&nbsp; <span class="style114">The ITC Patent Mess</span></strong></p>
                    <p align="left" class="style2">To paraphrase an old saying,  &quot;Nobody doesn't love to hate the U.S. patent system.&quot;&nbsp; And well they might, as multiple events in  2005 demonstrated.</p>
                    <p align="left" class="style2">On the litigation front,  multiple high-profile lawsuits ground on that many considered to involve  patents that should never have been issued, while new threats emerged involving  patents that are also popularly thought to be invalidly issued.&nbsp; </p>
                    <p align="left" class="style2">An example of continuing  litigation was the ongoing effort by a one-employee company called Eolas  Technologies to collect on a multi-hundred million dollar judgment against  Microsoft involving the Internet Explorer browser &ndash; a threat that had earlier  inspired a plea to the U.S. Patent and Trademark Office (PTO) by the W3C,  signed personally by Tim Berners-Lee, to void the patent in question.&nbsp; An example of a new threat is the contention  of another small company, this time based in North Carolina and called  Scientigo, which claims that use of <a href="http://www.technologyreview.com/BizTech/wtr_14885,311,p1.html">XML</a> (first released in 1998) would infringe its patent.</p>
                    <p align="left" class="style2">At the same time, endemic  cries against the formation and funding of new patent &quot;trolls&quot; (i.e.,  companies formed for the sole purpose of purchasing, and asserting, patents  against operating companies) reached the pages of even the <a href="http://www.post-gazette.com/pg/05257/571396.stm">Wall Street Journal</a>.&nbsp; One such company is Acacia Technologies,  which has acquired 120 patents in a variety of technical areas and has brought  suit already against companies large and small, including Intel.&nbsp; Another, named Thinkfire, is venture backed  and was founded by former Microsoft chief technology officer Nathan  Myhrvold.&nbsp; Thinkfire licenses patents it  has purchased, and manages those owned by others as well.</p>
                    <p align="left" class="style2">At the heart of the matter  is the long-held belief that patents in the U.S., and particularly software  patents, are <a href="http://www.consortiuminfo.org/bulletins/nov03.php">too  easy to get and too hard to challenge</a>, thereby enabling the owners of  patents that should never have been issued to put innocent software developers  and end-users to the choice of paying up or facing an uphill &ndash; and extremely  expensive &ndash; battle in court to challenge those patents.&nbsp; A bill is currently in Congress to address  the situation, but the opinions on the specific changes to be made to existing  law are as fiercely held as they are diverse.</p>
                    <p align="left" class="style2">Meanwhile, 2005 saw a  pitched battle in Europe over whether to permit the award of software patents  at all.&nbsp; By years end, the question had  been overwhelmingly answered in the <a href="http://msnbc.msn.com/id/8473537/">negative</a>,  at least for now.&nbsp; And in this country,  various initiatives were launched in support of open source software, while  awaiting whatever relief, if any, Congress might provide.&nbsp; One such initiative was an information  resource launched by Open Source Development Labs (OSDL) to create an <a href="http://www.infoworld.com/article/05/11/15/HNonlinepatents_1.html">on-line  patent commons reference library</a>.&nbsp;  More ambitiously, an <a href="http://opensource.sys-con.com/read/152384.htm">open invention network</a> was formed by IBM, Philips, Sony, Red Hat, and  Novell to actually buy patents that might be infringed by Linux users, in order <em>not</em> to assert them.</p>
                    <p align="left" class="style2">As the year  ended, it appeared that the best that could be hoped for would be the eventual  passage of a patent reform bill that would be likely to leave everyone equally  unhappy.</p>
                    <p align="left" class="style2">All in all, no  one was happy with the U.S. patent system in 2005 &ndash; except the trolls.</p>
                    <p align="left" class="style2"><strong>7<em>&#9658;International (Non?) Event of the Year:&nbsp; </em><span class="style114">WSIS</span></strong> </p>
                    <p align="left" class="style2">Perhaps the only thing that  was less popular in 2005 than U.S. patent laws was the U.S. position on a  question that came to be phrased, &quot;Who Should Govern the Internet?&quot;</p>
                    <p align="left" class="style2">As I reported for months at  the ConsortiumInfo.org <a href="http://www.consortiuminfo.org/news/cat.php?CID=25">News Portal</a> and  explored in depth in the <a href="http://www.consortiuminfo.org/bulletins/nov05.php">November</a> issue of  the <strong><em>CSB</em></strong>, the controversy focused on whether the U.S. would (as  earlier promised) or would not (as it announced in June) relinquish control of  the Internet Council on Assigned Numbers and Names (ICANN).&nbsp; Internationally, pressure and angst mounted  following the U.S. backtracking, while domestic determination not to give in  rose in equal measure.</p>
                    <p align="left" class="style2">By the time the second  plenary meeting of the World Summit on the Internet Society, a multi-year  endeavor commissioned by the United Nations and administered by the  International Telecommunication Union, convened in Tunis, virtually all other  topics had been eclipsed by the Internet governance controversy.</p>
                    <p align="left" class="style2">At the last minute, a  compromise (of sorts) was reached that largely gave the U.S. what it wanted &ndash;  continuation of the status quo &ndash; while granting those that wanted a global  resource to be administered collectively only a new &quot;Internet Governance  Forum&quot; that might &ndash; or might not &ndash; grow into something meaningful.</p>
                    <p align="left" class="style2"><strong><em>Summary:</em></strong>&nbsp; All in all,  the Year in Standards was contentious and clamorous, and by turns both regressive  and progressive, infuriating and encouraging -- much like the year in domestic  and international relations.&nbsp; It's a  safe bet that we can look forward to more of the same in 2006, as few of the  stories highlighted above have run their course, nor have the underlying  conditions, attitudes and strategic interests that fueled their heat abated.</p>
