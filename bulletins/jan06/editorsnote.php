                <p><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITOR'S NOTE:</em></strong></font></p>
                <div align="center">
                  <div align="center">
                    <p align="center" class="style10"><strong>LOOKING BACKWARD, LOOKING FORWARD</strong><br>
</p>
                    <p align="left" class="style2">Rather incredibly - to me  &ndash;the <strong><em>Consortium Standards Bulletin </em></strong>begins its fourth full year of  publication with this issue.<br>
  &nbsp;<br>
                      Much has changed in the  intervening time.&nbsp; But it's also  surprising how much has not, as you can see from the editorial in the December  2002 Inaugural Issue, which was titled &quot;IPR Policies: a Call to (Lay Down)  Arms.&quot;&nbsp; That editorial refers to private litigation  involving alleged abuses of the JEDEC standards process by memory technology  company Rambus, and an investigation into the same course of conduct that was  then about to be launched against Rambus by the Federal Trade Commission.&nbsp; <img src="/bulletins/jan06/dec02.png" alt="December 2002" width="358" height="326" align="left"></p>
                    <p align="left" class="style2">While Rambus settled one  suit against Infineon last year, its suits against a number of other companies  continue to grind on, as does the FTC proceeding.&nbsp; And the terms of IPR policies continue to be the source of as  much, or more, contention than ever.&nbsp;  Were we to run our first editorial today with only factual updating, it  would still (regrettably) make as much sense now as it did then.</p>
                    <p align="left" class="style2">But times have changed while  the CSB has been bringing you the news, and in some cases, for the better.&nbsp; In that same editorial I called for the  following: </p>
                    <blockquote>
                      <p align="left" class="style2">What's needed is for the industry to have a  &quot;constitutional convention&quot; to set a standard policy - a policy which  will have alternative terms for a limited number of situations where  alternatives are truly needed, but also a well articulated rationale for each  alternative, stating where and why that alternative is appropriate. Its time  for the industry to compromise once, and agree many times thereafter. Once this  is accomplished, the industry can get back to the far more important business  of standard setting, rather than arguing about how to do it. </p>
                    </blockquote>
                    <p align="left" class="style2">Indeed, an ABA subcommittee  has now been toiling for more than a year to create just such a document.&nbsp; In another favorable development, a number  of companies have begun advocating for more stringent standard setting  disclosure rules &ndash; and even for early discussion of licensing terms, which the  U.S. Department of Justice has signaled it would find to be pro-competitive under  the antitrust laws, if properly managed.&nbsp;  The reason?&nbsp; Even IT companies  with the largest patent portfolios have come to realize that good standards can  be worth even more to them than the licensing revenue of their own  patents.&nbsp; </p>
                    <p align="left" class="style2">So as we look forward to  2006, there are reasons for encouragement as well as frustration.&nbsp;&nbsp; But in this issue, we look backward, and  present what I think were the most significant events, issues organizations  (and more) from the year just ended.</p>
                    <p align="left" class="style2">This issue's <strong><em>Editorial</em></strong> expands on this evaluation, concluding that while 2005 was a difficult year  marred by a lack of cooperation in all too many venues, ultimately the  successes outnumbered the failures, regardless of the amount of stomach lining  consumed in the process.</p>
                    <p align="left" class="style2">Recognizing the futility of  summarizing all of the important stories, or even trends, of 2005, this month's <strong><em>Feature Article</em></strong>&nbsp; focuses  instead on what I believe to have been most newsworthy in the year just ended.</p>
                    <p align="left" class="style2">This issue also includes my  annual <strong><em>News Sources Awards</em></strong>, this time in several additional  categories as well.&nbsp; I've appreciated  the good work of the hundreds of standards organizations, open source projects,  news services, individual journalists, community sites and bloggers that have  helped together to get out and analyze the news, and this is my way of  providing some small measure of recognition to those that I thought did the  best job of raising the profile of standards, and explaining to their readers  why standards matter.</p>
                    <p align="left" class="style2">Next, you will see <strong><em>Our  Year in</em></strong> <strong><em>Review,</em></strong> being a brief overview of some of the  things that have been happening this year here at ConsortiumInfo.org, followed  by this month's selection from the <strong><em>Standards Blog</em></strong> (relating to  our Standards Story of the Year).&nbsp; The  issue closes with a less serious entry from <strong><em>Consider This&hellip;</em></strong></p>
                    <p align="left" class="style2">All in all, it's been an  extremely active year in the world of standards, and therefore an equally busy  year at ConsortiumInfo.org.&nbsp; In light of  the expanded activities at the site, I've decided to set the <strong><em>CSB</em></strong> calendar for 2006 at ten issues instead of twelve, omitting the August and  December issues.&nbsp; This will provide time  at this end to catch up on other site projects, not to mention extra time with  my family, separate from my ever-present wireless-enabled laptop.</p>
                    <p align="left" class="style2">So as you read this issue  looking backward, I hope that you are looking forward to the New Year, and to  whatever may be in store for us.&nbsp;  Whatever that may be, I'll personally look forward to doing my best to  investigate it, think about it, and report on it to you here at the <strong><em>CSB</em></strong>.</p>
                    <p align="left" class="style2">As always, I hope you enjoy  this issue.&nbsp; </p>
