                <p align="center" class="style10"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">CONSIDER THIS: </font></em></strong></p>
                <p align="left" class="style56"> <strong> [][][] <strong> December 31, 2005</strong> </strong></p>
                <p><span class="style56">#35</span><strong class="style38"><em> Objectivity and my Secret Life as Simon Hoggarts </em></strong></p>
                <p align="left" class="style2">What has come to be known as the &quot;blogosphere&quot; has now been in  existence long enough to spawn  untold millions upon millions of personal  Websites, each of which presents the thoughts and observations of its  author.&nbsp; As one might expect, those  thoughts and observations are (to say the least) varied.&nbsp; To enter into the blogosphere is therefore  to enter into the very collective consciousness of humanity.</p>
                <p align="left" class="style2"> Which can be a rather disorienting experience, especially if as one  looks in, one sees one's own self looking back at them.&nbsp; Or, worse yet, someone looking back at them  that doesn't look like them, but someone else suspects <em>is</em> them.</p>
                <p align="left" class="style2"> As happened to me a few days ago, as recorded in my blog.&nbsp; Consider this&hellip;</p>
                <p align="left" class="style2"> * * * * * * *</p>
                <p align="left" class="style2">New Years Day weekend is not a time favored by those that would toss  their news into the pond of public awareness (unless they wish their news to  disappear leaving as few public ripples as possible).&nbsp; There being little worthwhile news to report, this post will  therefore not deal with the news of the day. </p>
                <p align="left" class="style2"> For  the same ripple reason, I will instead use this opportunity to reveal a  startling personal fact. </p>
                <p align="left" class="style2"> I  have learned, through a seemingly reliable source, that I am actually <a href="%20http://www.guardian.co.uk/Columnists/Archive/0,5673,-13,00.html%20">Simon Hoggart</a>, London <em>Guardian</em> political columnist and BBC Radio 4 personality. </p>
                <p align="left" class="style2"> Could  this indeed be so, you ask?&nbsp; Yes,  although it came as a complete shock to me as well. Although I have lived in  England in the past and read the <em>Guardian</em> on many occasions, I was  heretofore unaware that I had not only read, but written, some of what I had  previously enjoyed.</p>
                <p align="left" class="style2"> This  state of pleasant ignorance ended when an alert reader brought a certain blog  post to my attention. That entry was posted by one Jon Levell in his thought  provoking blog, <a href="http://www.coralbark.net/blog/">Irregular Verbiage</a>, aptly  subtitled (the most recent entry is dated November 24th), &quot;Sporadic  Musings.&quot; </p>
                <p align="left" class="style2"> It  seems that in that most recent Musing, Jon was disposed to muse over the  Massachusetts Open Document saga. But his musing did not stop there. Instead,  he culminated with this final, coy muse: </p>
                <div align="left">
                  <blockquote>&nbsp; </blockquote>
                </div>
                <blockquote><p align="left" class="style2"> In  other news, if you are obsessively reading info on the debate about open  standards (and specifically whether public organisations like the government of  Massachusetts or the European Commission should mandate that their employees  use then) then a key source of info is the Standards Blog. What I find most  intriguing is that you never see the Standards Blog's Andy Updegrove and the  Guardian's/Radio 4's Simon Hoggart in a room together. </p>
                </blockquote>
                <p align="left"><img src="http://www.coralbark.net/blogphotos/newsquiz_hoggart.jpg" alt="hoggart" width="247" height="165"></p>
                <p align="left" class="style2">Hmm.  He had a point. To the best of my knowledge, I <em>had</em> never been seen in  the same room as Guardian/Radio 4's Simon Hoggart; at least, to be objective,  unless I <em>was</em> Simon Hoggart. </p>
                <p align="left" class="style2"> While  some (Microsoft Office spokesman Alan Yates Microsoft springs to mind) might  question my objectivity, in fact I have tried to be scrupulously objective in  my reporting of facts and in any analysis of legal language that I offer on  line, while preserving the right to express the opinions that I form as a  result of what I observe. I strive to maintain a reputation for objectivity not  only for its own sake, but in order that my opinions will be given credibility  and weight, as well as the facts I report. </p>
                <p align="left" class="style2"> Thus  I feel that I cannot shy away from the challenge that Mr. Levell has obliquely  presented. After all, perhaps the greatest test of objectivity begins when one  looks into the mirror. </p>
                <p align="left" class="style2"> I  therefore take it as obligatory that I investigate Mr. Levell's disturbing  suggestion that I am &quot;Guardian/Radio 4's Simon Hoggart&quot;, rather than  mild-mannered Massachusetts attorney and standards wonk Andy Updegrove, and  that I do so with as much diligence as I would a contention that Mr. Yates (for  example) is in fact Presidential Press Secretary Scott McClellan (perhaps I'll  return to that intriguing possibility in a future blog entry). </p>
                <p align="left" class="style2"> With  this as prelude, here are the significant facts that I have uncovered, as well  as my ultimate conclusion on the question, after balancing all ambiguities against  my own sense of identity: </p>
                <p align="left" class="style2"> 1.  Mr. Hoggart was born on May 26, 1946. I was born on November 9, 1953. Close  enough for government work. </p>
                <p align="left" class="style2"> 2.  Mr. Hoggart has an <a href="http://news.bbc.co.uk/1/hi/uk/4051777.stm">entry</a> in the Wikipedia, and I do not.  Pure coincidence, or careful research on the part of the Wikipedia staff,  notwithstanding the recent challenges to the accuracy of the on-line  encyclopedia? </p>
                <p align="left" class="style2"> 3.  According to that same entry, Mr. Hoggart is alleged to have had a brief affair  with <a href="http://news.bbc.co.uk/1/hi/uk/4051777.stm">Kimberly Quinn</a>. I do not recall  ever&hellip;ahem, being in the same room as Ms. Quinn, but, on the other hand, I have  no recollection of ever being in the same room as Mr. Hoggart, so these two  pieces of data appear to cancel each other out. </p>
                <p align="left" class="style2"> 4.  Mr. Hoggart chairs BBC 4's weekly show, <a href="%20http://www.bbc.co.uk/radio4/comedy/newsquiz.shtml">The News Quiz</a>, a delightfully  erudite, humorous and occasionally pedantic show which I have often enjoyed. I  have some small pretensions to being both erudite and humorous, while my  partners regularly assure me that I am pedantic in the extreme. </p>
                <p align="left" class="style2">5.  Mr. Hoggart is the author of, among other works, a compendium of absurd  Christmas Letters, titled <a href="%20http://www.westminsterbookshop.co.uk/images/475/1843544741.jpg">The Hamster that Loved Puccini</a>. I  am forced to admit that I am fully capable of giving (and perhaps have given)  such a name to a book, should I ever write/have already written one. </p>
                <p align="left" class="style2"> 6.  I have not been able to contact Mr. Hoggart by telephone to put the question to  him/me, but each time I placed the call, <em>his phone was invariably busy!</em> </p>
                <p align="left" class="style2"> But  enough of this. </p>
                <p align="left" class="style2"> Clearly,  I have been unable to disprove the suggestion of Mr. Levell that I am one and  the same with the Guardian's/Radio 4's Simon Hoggart. Therefore, resolving all  doubts against my own bias that I am, at least in this regard, unique, and  assuming that Mr. Levell is a responsible practitioner of the blogging trade  and has some evidence to support his implied allegation, I must, with regret,  surrender my identity. </p>
                <p align="left" class="style2"> Finally,  a note to Mr. Levell. </p>
                <p align="left" class="style2"> I  see from the CV that you have posted at your site that you have earned a Ph.D  in Theoretical Particle Physics. Having myself (or, as the case may be, Mr.  Hoggart's self) at best only a muddled and imperfect knowledge of quantum  theory, perhaps you can explain, for Simon's and my benefit and in the manner  of the late lamented Douglas Adams, the seeming duality of existence that  I/he/we/whoever are currently experiencing. Or, as the question was more  succinctly put by the <a href="%20http://www.firesigntheatre.com/funway.html">Firesign Theatre</a>, in their 1969  appreciation album, <a href="%20http://www.whyaduck.com/cards/all-hail.jpg">All Hail Marx [and] Lennon</a>,  &quot;How can you be in two places at once, when you're not anywhere at  all?&quot; </p>
                <p align="left" class="style2"> Mr.  Levell, Simon's and/or my identity are at your mercy. We await your response. </p>
                <p align="left" class="style2"> Please muse on that. </p>
