                <p><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">AWARDS:</font></em></strong></p>
                <div align="center"><div align="center">
                  <p align="center"> <strong> <span class="style10"><strong>STANDARDS NEWS SOURCES OF THE YEAR </strong> </span></strong></p>
                    <p align="center"><strong><em> </em></strong><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font><br>
                    </p>
                    <p align="left" class="style2">For the second year in a row, I would like to recognize  those news sources that we believe did the best job of bringing  standards-relevant information and analysis to the on-line world.&nbsp; As I stated in last year's awards article:</p>
                  
                    <blockquote><p align="left" class="style2"> To state  the obvious, standards news reportage does not receive the type of attention  and awards that movies and Broadway shows (or even advertisements) attract.  While the American National Standards Institute (ANSI) annually bestows its  President&rsquo;s Award for Journalism to an individual reporter, we are aware of no  official recognition given to those standards organizations that release the  most noteworthy news, or the media that provide the most thorough and  discriminating specific coverage of the standards world. </p>
                    </blockquote>
                    <p align="left" class="style2">This year, I am expanding the  categories of news providers we would like to recognize to include not only  news services and standards organizations, but also individual journalists, bloggers  and community site editors as well. </p>
                    <p align="left" class="style2"><strong><em>Methodology:</em></strong>&nbsp; The awards that appear below are of two types.&nbsp; </p>
                    <p align="left" class="style2">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><em>Statistical Awards:</em></strong>&nbsp; As was the case last year, this set of awards was determined  solely by reviewing and tabulating those articles and press releases that I  selected to present and link to at the ConsortiumInfo.org <a href="http://www.consortiuminfo.org/news/">News Portal</a> during 2005 &ndash; more  than 700 items in all, out of the thousands of articles and press releases I  reviewed in the course of the year.&nbsp; </p>
                    <p align="left" class="style2"> The  larger sample set from which these 700 plus items were chosen was harvested  through three methods:</p>
                    <div align="left">
                      <ol>
                        <li class="style2">Use  of a series of standards and open source focused Google Alerts, most of which  were in place throughout the year, but some of which were added in response to  new stories as they developed as the year progressed</li>
                        <li class="style2">Press  releases sent directly to ConsortiumInfo.org by many of the 455 standards  organizations and open source projects listed at the ConsortiumInfo.org <a href="http://www.consortiuminfo.org/links/">Consortium and Standards List</a> (if your organization does not currently send press releases to  ConsortiumInfo.org, please find out how to so <a href="http://www.consortiuminfo.org/commcenter/#grpr">here</a>).</li>
                        <li class="style2">My  own use of the Web and all it has to offer.</li>
                      </ol>
                    </div>
                    <p align="left" class="style2"><strong><em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Special Awards:</em></strong>&nbsp;  Because the database used above does not include some of the other  sources of information upon which many of us rely (e.g.,&nbsp; blogs are under-represented at the News  Portal &ndash; something I plan to correct this year), the second set of awards is  more subjective, in that it is limited to those sources of which I have become  aware by various means, and which I personally found to be most useful in  finding, understanding, and presenting the news this year.</p>
                    <p align="left" class="style2">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <span class="style113">Sample Set:</span> During the measuring period, I included news  received from hundreds of sources based in many countries around the world that  in most instances fell into one of the following three groupings: </p>
                    <div align="left">
                      <ol>
                        <li class="style2"> Standard setting organizations, open source projects and other  non-profit sources.</li>
                        <li class="style2"> News portals, on-line journals, on-line versions of magazines and  newspapers, and other for-profit media outlets.</li>
                        <li class="style2"> Blogs and community sites. </li>
                      </ol>
                    </div>
                    <p align="left" class="style2">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <span class="style113">Selection Criteria:</span> In order to be chosen for  posting at the News Portal, a news item must fall into one or more of the 22  categories of news we cover at our portal and shown at left.</p>
                    <p align="left"><img src="/bulletins/jan06/news.png" alt="news" width="156" height="465" align="left"><span class="style2">In one  single category (New Consortia) inclusion is automatic, but in all others, it  is also necessary to have additional qualities that I believe make a story not  only newsworthy, but relevant and interesting to the ConsortiumInfo.org  audience as well. <br>
                      What  makes a standards story relevant and interesting (at least to me)?&nbsp; The following are some of the criteria I  look for in a story and/or in its presentation: </span></p>
                    <div align="left">
                      <ul>
                        <ul>
                          <li class="style2">&middot; Does it recognize the       role of a standards or open source organization, or of a standard or of       open source software, in the story?</li>
                          <li class="style2">&middot; Does it relate to       information or communications technology; if not, does it relate to some       other standards area that has more than purely commercial relevance? </li>
                          <li class="style2">&middot; Does it relate to how we live, work or transact       business? </li>
                          <li class="style2">&middot; Does it illustrate the importance of standards or       open source to society? </li>
                          <li class="style2">&middot; Does it help a reader understand what standards       or open source are all about? </li>
                          <li class="style2">&middot; Will the standard, software event in question       have a major impact beyond its purely technical ramifications? </li>
                        </ul>
                      </ul>
                    </div>
                    <p align="left" class="style2">Additional  criteria for certain categories appear below.</p>
                    <p align="left" class="style2"> My  particular thanks go to the individuals that have chosen to write such stories,  and to the media that have agreed that these stories deserve to be made  available to the world (please keep up the good work). </p>
                    <p align="left" class="style2"> Without  further ado, here are the 2005 ConsortiumInfo.org News Sources of the Year  Awards.</p>
                    <p align="left" class="style2"><strong>I.&nbsp; Statistical Awards</strong><br>
                        <strong>1<em>&#9658;Award:</em><span class="style114">&nbsp; Most  Newsworthy Standard Setting Organization </span></strong><br>
                        <strong><em>(Consortium, Accredited Standards Developer or Open  Source Project) </em></strong><br>
                      The  following awards do not indicate which standards organizations were most  prolific in generating new (or amended) standards, but rather which ones  received the most attention in the media as a result of the work that they  performed during the year.&nbsp; The items  counted included both press releases issued by the organization as well as  inclusions in the title or lead sentences of on-line articles.</p>
                    <div align="left">
                      <ul type="disc">
                        <li class="style2"><strong><em>First Place:</em>&nbsp; <span class="style114">Organization for the Advancement of Structured       Information Systems</span> </strong>&nbsp;<a href="http://www.oasis-open.org/home/index.php">(OASIS)</a> -&nbsp; 47 Items <em>[Last year: Third Place]</em></li>
                        <li class="style2"><strong><em>Second Place:&nbsp; </em><span class="style114">World Wide Web Consortium</span> <a href="http://www.w3.org/">(W3C)</a></strong> - 31 Items <em>[Last       year: Second Place]</em></li>
                        <li class="style2"><strong><em>Third Place:</em></strong>&nbsp; <strong class="style114">European Telecommunications       Standards Institute</strong> <a href="http://www.etsi.org/">(ETSI)</a> - 9 items <em>[Last       year: Did not Place]</em></li>
                      </ul>
                    </div>
                    <p align="left" class="style2">Each  of these organizations not only performed much good work in 2005, but was also  the focus of stories that highlighted the impact that standards have on  society, or which reflected other forces acting in the marketplace, such as the  difficulty of reconciling the rights of patent holders with the standard setting  process.&nbsp; </p>
                    <p align="left" class="style2"> While  the W3C was not the focus of much controversy in 2005, OASIS was more than  usually in the news in 2005 as a result of two events.&nbsp; The first was its adoption of a new  intellectual property rights policy early in the year, which was intended to be  more &quot;open source friendly,&quot; but which was not as warmly welcomed in  that community as hoped (see our March 2005 issue, <a href="http://www.consortiuminfo.org/bulletins/mar05.php">What Does  &quot;Open&quot; Mean?</a>).&nbsp; The second  event was a story that is continuing into 2006, and involves a struggle over  the proposed adoption by the Massachusetts Executive Agencies of the  OpenDocument OASIS Format standard for archival purposes (see our September  issue, <a href="http://www.consortiuminfo.org/bulletins/sep05.php">Massachusetts  and OpenDocument: the Commonwealth Leads the Way</a> and the many Standards  Blog posts that are collected <a href="http://www.consortiuminfo.org/newsblog/blogcat.php?CID=30">here</a>).</p>
                    <p align="left" class="style2"><em>[Disclosure:&nbsp; OASIS is a client of the author and his law  firm]</em></p>
                    <p align="left" class="style2"><strong>2&#9658;<em>Award:</em>&nbsp;<span class="style114"> Best On-Line Coverage of ICT  Standards by an On-Line News Service </span></strong></p>
                    <div align="left">
                      <ul type="disc">
                        <li class="style2"><strong>First Place: <a href="http://www.eweek.com/" class="style114">eWeek.com</a> 55       stories </strong>Items <em>[Last year: Third Place]</em></li>
                        <li class="style2"><strong>Second Place:&nbsp; <a href="http://www.zdnet.com/" class="style114">ZDNet.com</a> -</strong> 50 stories (includes       ZDNet U.S. U.K and Australia) Items <em>[Last year: Did not Place]</em></li>
                        <li class="style2"><strong>Third Place:&nbsp; <a href="http://www.cnet.com/" class="style114">CNET.com</a></strong> 36 Items <em>[Last       year: Second Place]</em></li>
                      </ul>
                    </div>
                    <p align="left" class="style2">Immediate runners-up include  InfoWorld (25), BusinessWire (20) and TechWorld (17). &nbsp;The rankings also appear to reflect in part  the number of journalists published by each news service (eWeek:&nbsp; 21; ZDNet: 19; and CNET: 20) that appreciate  the role of standards.</p>
                    <p align="left" class="style2">The relative level of awareness of  standards by some news agencies in comparison to others is made more obvious by  the names and numbers of some of those services that did not rate highly, as  follows: Computerworld (13), InformationWeek (12), and IDG News Service  (10).&nbsp; </p>
                    <p align="left" class="style2"> To an extent, when more than  one news service reported on the same story (as was often the case), the  rankings also reflect which article appeared most quickly.&nbsp; However, where two reports on the same story  appeared on the same day, the item i chose to list was selected on a random  basis (with the result that the effect should roughly cancel out over time &ndash; at  least among those news services that got to the Web with their stories first).</p>
                    <p align="left" class="style2"><strong><em>3</em>&#9658;<em>Award:</em>&nbsp; <span class="style114">Best Coverage of ICT Standards by an On-Line Journalist</span></strong></p>
                    <p align="left" class="style2"> This year I would also like  to recognize the individual journalists that were most aware of the role of  standards, open source software and the organizations that create them, and  took the time to present that role to their readers.&nbsp; As was the case with news services, while the same story was  often reported by more than one journalist, only one report was usually  included at the News portal.&nbsp; </p>
                    <div align="left">
                      <ul type="disc">
                
                        <li class="style2"><strong><em>First       Place: </em><span class="style114">Martin LaMonica</span></strong>- <a href="http://www.news.com/">CNET.News.com</a>- 15       Stories </li>
                        <li class="style2"><strong><em>Second Place: </em><span class="style114">Matthew Broersma</span></strong>- <a href="http://www.zdnet.co.uk">ZDNet UK</a>14 Stories</li>
                        <li class="style2"><strong><em>Third Place (tie):</em></strong>&nbsp; <strong class="style114">Ingrid Marson  </strong><a href="http://www.zdnet.co.uk">ZDNet UK</a> and<strong class="style114"> Steven Vaughn-Nichols</strong>-- <a href="http://www.eweek.com">eWeek.com</a>10 Stories each</li>
                        </ul>
                    </div>
                    <h2 align="left" class="style2">II.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Special Awards</h2>
                    <p align="left" class="style2"><strong>1&#9658;<em>Award:</em> <span class="style114">Best Columnist/Blogger (Commercial)</span></strong></p>
                    <p align="left" class="style2">Online journalism has become increasing brief and  stylized, with attendant loss of value.&nbsp;  Not only are most on-line stories limited to one to two screens of text  (using whatever space is left between the advertising), but prevailing rules of  journalism dictate that a good deal of what space is available will be spent in  a very predictable way: headline, subtitle, opening sentences, expansion of  theme, quote from source consistent with story line, statement of counter  position, closing quote from source counter to story line or &quot;good closing  quote&quot;.&nbsp; </p>
                    <p align="left" class="style2">The result is that while print journals abound with  longer, more analytical pieces, in-depth on-line analysis is increasingly  becoming the province of bloggers and those specialty sites that place fewer  constraints on their authors.</p>
                    <p align="left" class="style2">It will be no surprise to many that as the recipient  of this first award I have selected:</p>
                    <p align="left" class="style115">David Berlind (ZDNet.com)</p>
                    <p align="left" class="style2">David has previously been honored by the American  National Standards Institute (ANSI), which presented him with its Presidents  Award for Journalism in 2003.&nbsp; Unlike  most journalists, he was an IT professional before becoming a professional  writer.&nbsp; His <a href="http://blogs.zdnet.com/BTL/">Between the Lines</a> blog (a venue shared  with co-author Dan Farber) has consistently covered not only open standards and  open source, but more recently the interplay, and sometimes clash, between  those two disciplines.&nbsp; I haven't agreed  with David on every position that he has taken (the opposite is also true), but  his commitment and passion for covering open standards and open source is  unquestioned, as has been his contribution towards bringing important issues to  light and into public dialogue.</p>
                    <p align="left" class="style2"><strong><em>2&#9658;Award</em>:&nbsp; <span class="style115">Best Community Site or Blog (Non-Profit)</span></strong></p>
                    <p align="left" class="style2">Blogs  and community sites that focus on particular domains or issues have become an  increasingly important route by which news can reach the public that would  otherwise be neglected by, or unknown to, the formal media.&nbsp; In addition, many provide skilled analysis,  as well as an opportunity for additional facts and opinion to be aggregated and  consolidated.</p>
                    <p align="left" class="style2">It  will (again) be no surprise to many that I would like to honor, as the  recipient of the first award in this category:</p>
                    <p align="left" class="style115"><strong>Pamela Jones - <a href="http://www.groklaw.net/">Groklaw.net</a></strong></p>
                    <p align="left" class="style2">Almost  immediately following her first posting on May 16, 2003, Pamela has had an  enormous and loyal following.&nbsp; Her  ongoing dedication to investigative journalism in defense of Free and Open  Source Software is well-known to friend and foe (most notably, SCO) alike.&nbsp; More recently she has supported a variety of  open standards-related causes as well, including by championing the efforts of  Peter Quinn and the Information Technology Division of Massachusetts to mandate  use of the OpenDocument format by the Executive Agencies of the Commonwealth  for archival purposes.&nbsp; Pamela's  indefatigable work at Groklaw &ndash; and her less visible but equally tireless and  substantial efforts behind the scenes to marshal data, develop sources and  influence outcomes &ndash; have earned her the well-deserved respect and trust of the  entire open source community.</p>
                    <p align="left" class="style2"><strong><em>Conclusion:</em></strong>&nbsp; This year saw what I believe to be a noticeable increase in  standards awareness in ICT news coverage, as well as in the number of news  outlets (both general as well as technical) reporting on open source  topics.&nbsp; I hope that this trend will  continue, and take pleasure in bringing some small measure of recognition to  those services and individuals that have taken it upon themselves to acquaint  their readers with the role that these important tools play in the modern  world.</p>
