              <div align="center">
                <p align="center" class="style10"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">STANDARDS BLOG: </font></em></strong></p>
                <p align="center" class="style10">WHAT PRICE PUBLIC SERVICE?</p>
                <p align="center"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font></p>
                <p align="left" class="style2">For the last three and a  half months, I have been reporting at the <a href="http://www.consortiuminfo.org/newsblog/">Standards Blog</a> on the  unfolding story of Massachusetts CIO Peter Quinn's effort to adopt and  implement a policy that would ensure that important state documents will remain  available to the public indefinitely.&nbsp;  That policy mandates the use of &quot;open document format&quot;  standards by the Executive Agencies of the Commonwealth, and additionally  approves two tools for that purpose: the Adobe PDF format, and the OpenDocument  OASIS format.</p>
                <p align="left" class="style2">What began as a simple  effort by a responsible public servant to perform his job to the best of his  ability, unfortunately, degenerated this fall into the launching of political  attacks in the Massachusetts Senate on his authority, and to unsubstantiated  (and soon disprove) accusations regarding his personal integrity being raised  on the front page of the Boston <em>Globe.</em></p>
                <p align="left" class="style2">During this same time  period, I have written a total of 52 (to date) lengthy blog entries, some  investigative, some analytical, and some informational on this story.&nbsp; The entries that begin and end this article,  and the details included in between, document, sadly, what can happen when a  dedicated public servant runs afoul of political and commercial interests that  might suffer as a result of the implementation of a useful public policy.</p>
                <p align="left" class="style2">The first blog entry below  reports on the results of an investigation instigated by inquiries made by the  Boston <em>Globe</em>, as earlier reported <a href="http://consortiuminfo.org/newsblog/blog.php?ID=1771">here</a> in an entry  entitled &quot;City on a Hill or Tammany Hall?&quot;</p>
                <p align="center" class="style2">* * * * * * *</p>
                <p align="left" class="style56"><strong>December 10, 2005&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
                <p align="center" class="style2"><strong>Quinn Cleared in Travel  Investigation (But Will the <em>Globe</em> be Cleared?)</strong> </p>
                <p align="left" class="style2"> The Boston <em>Globe </em>reports today that Peter Quinn has been <a href="http://www.boston.com/news/local/massachusetts/articles/2005/12/10/review_backs_trips_by_technology_chief/">cleared</a> in an investigation into  whether his travel to trade conferences had received proper approval. The  article reads in part as follows: </p>
                <blockquote>
                  <p align="left" class="style2">The  Romney administration's chief technology officer did not violate  conflict-of-interest standards or other rules when he took 12 out-of-state  trips to attend conferences during the past year without obtaining the written  approval of his boss, according to a review by the governor's budget chief&hellip;. </p>
                  <p align="left" class="style2"> Also,  Fehrnstrom said that while sponsors of many of the conferences included  computer software companies, Quinn had assured Trimarco that none of the firms  are currently state vendors or are bidding on state business.</p>
                </blockquote>
                <p align="left" class="style2"> The article also confirms Quinn's statements that his  superior, Eric A. Kriss, who was then secretary of administration and finance,  had not only verbally approved each of the trips in advance, but had told him  that completing any paperwork in connection with the travel was unnecessary,  &quot; because he felt that the reason that the regulation had been put in  place originally -- the fiscal crisis of the mid-1990s had cut out all  state-funded travel -- had expired.&quot; It was the completion of this  paperwork (only) that had been called into question, and not the travel itself. </p>
                <p align="left" class="style2">As noted in an <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1771">earlier entry</a> entitled &quot;City on a Hill or Tammany Hall?&quot; Massachusetts has an  opportunity to showcase its innovation in technology through Quinn's policy  proposal, or to attack the messenger as part of an internal squabble. The same  point is implied in a quote in today's <em>Globe</em>, and has been made in  letters to the Commonwealth by both IBM and Sun Microsystems: </p>
                <blockquote>
                  <p align="left" class="style2"> ''I  knew of every trip that Peter was taking, and I approved them all,&quot; Kriss  said. ''He was in demand at most of these [conferences] because of the path  towards [open format] that we were taking. People in other states were anxious  to hear about the Massachusetts experience.&quot; </p>
                </blockquote>
                <p align="left" class="style2"> So ends one sordid little story in the long unseemly saga of  Massachusetts politics. But the story does not answer all of the questions that  this incident raises, some of which may be much more significant than Peter  Quinn's travel documentation: what caused this fruitless inquiry to be launched  in the first place, and was the reporting of the <em>Globe</em> responsibly  conducted? </p>
                <p align="left" class="style2">In fact, the investigation was instigated in response to  inquiries to the State Government made by the <em>Globe</em> itself, as it  reported on <a href="http://www.boston.com/news/local/articles/2005/11/26/romney_administration_reviewing_trips_made_by_technology_chief/">November 26</a>. Both articles were  written by <em>Globe</em> staff writer Steven Kurkjian. As noted in my earlier  City on a Hill entry, the inquiries were made by <em>Globe</em> staff writer  Steve Kurkjian concurrent with moves by Senator Pacheco and others in State  Government to curtail Quinn's ability to set rules for proper management of the  IT needs of the Executive Agencies for which he is responsible. </p>
                <p align="left" class="style2">The questions that remain are, why did the <em>Globe</em> make  these inquiries, and did it conduct the degree of investigation prior to  reporting the story that is consistent with responsible journalism?<br>
  &nbsp;<br>
                  The reason these questions are relevant is because a paper  can either be the source of invaluable information that might not otherwise  come to light, or it can be used by enemies of a person &ndash; or of a policy &ndash; that  are currently under attack. When a controversy is in process, I believe that it  is incumbent upon a journalist to be especially cautious regarding sources that  can be expected to have an axe to grind, and to subject those allegations to  particular scrutiny before proceeding to make them public. </p>
                <p align="left" class="style2">In this case, what did the <em>Globe</em> do before going to  press with the story? According to today's story, &quot;phone calls to Kriss's  home [in connection with the earlier story] seeking comment at the time were  not returned.&quot; The story does not say for how long the reporter tried to  contact him, or whether it was known whether Kriss was even in town on  Thanksgiving weekend, or why the <em>Globe</em> felt that it needed to rush the  story to print before making further efforts to learn whether it indeed had a  story to report at all. </p>
                <p align="left" class="style2">Andy Oram, an O'Reilly Media editor did manage to reach  Kriss on the day that the first <em>Globe</em> story came out. Here's what the <em>Globe</em> reporter <a href="http://www.oreillynet.com/pub/wlg/8566">would have learned</a> if he had held  his story until he was able to do the same: </p>
                <blockquote>
                  <p align="left" class="style2"> I  managed to reach Quinn's former boss, Eric Kriss, which the Globe did not.  (Choosing to break a story over Thanksgiving weekend, when protagonists are on  vacation and government offices that could answer questions are closed,  definitely does not contribute to clarity.) Kriss, whom I know because he's  contacted me with a book idea earlier, pointed out that: </p>
                  <blockquote>
                    <p align="left" class="style2"> - Most of Quinn's trips occurred after Massachusetts made  the decision to adopt OpenDocument. There is no possibility that the trips  would influence the decision that had already been made. <br>
                        <br>
                      - While some two-way communication occurs at any conference--and is beneficial  to the public--the primary purpose of the trips were to let Massachusetts  government tell the rest of the world what it was doing. <br>
                      <br>
                      - Far from being junkets, these trips were normally squeezed in on weekends  around his normal duties and represented a contribution of his free time to the  community. </p>
                  </blockquote>
                </blockquote>
                <p align="left" class="style2">In other words, the reporter would have learned exactly what  the investigation would learn: that there had been no impropriety, and that  there was no story to report. </p>
                <p align="left" class="style2">The more troubling question is, why did Kurkjian wake up one  morning and decide to look into Quinn's travel documentation at all, among all  of the other stories and areas of inquiry available to him at that time? </p>
                <p align="left" class="style2">That's something I'd very much like to know. As it happens,  the <em>Globe</em>, like a number of other papers, has an ombudsman, and his name  is <a href="http://www.boston.com/news/globe/editorial_opinion/oped/ombudsman/chacon_bio/">Richard Chac&oacute;n</a>. His email address  is ombud@globe.com, and his number is (617)929-3020; he also has a separate  voicemail number: (617)939-3022. </p>
                <p align="left" class="style2">Today I sent him the following questions by email: </p>
                <blockquote>
                  <p align="left" class="style2">Dear Mr. Chac&oacute;n: </p>
                  <p align="left" class="style2">I refer you to two stories  by Steve Kurkjian, one appearing on November 26 and one on December 9, relating  to the travel documentation of Peter Quinn, the CIO of the Commonwealth.  Currently, as reported by the <em>Globe</em> in several stories, Mr. Quinn, and a  policy that his department, the Information Technology Division, are being  criticized by Senator Pacheco, Secretary Galvin and Microsoft, among others.  They are also being applauded by a large number of people, companies and  governments around the world. </p>
                  <p align="left" class="style2">In the first of the two  stories I refer to above, titled <a href="%20http://www.boston.com/news/local/articles/2005/11/26/romney_administration_reviewing_trips_made_by_technology_chief/">Romney administration reviewing trips made by  technology chief</a>, Mr. Kurkjian reports that he made inquiries into  the travel of Peter Quinn which resulted in an investigation being launched by  the State into the same topic. </p>
                  <p align="left" class="style2">In the second story, titled <a href="http://www.boston.com/news/local/massachusetts/articles/2005/12/10/review_backs_trips_by_technology_chief/">Review backs trips by technology chief No  conflict found for aide</a>, also by Mr. Kurkjian, Mr. Quinn is reported  to have been cleared. The article also reports that he was cleared through the  testimony of a person, Eric Kriss, who Mr. Quinn had told Mr. Kurkjian prior to  November 26 could substantiate his story. </p>
                  <p align="left" class="style2">Mr. Kurkjian, however, went  to press over the Thanksgiving weekend with a story that was damaging to Mr.  Quinn's reputation before meeting with success in reaching Mr. Kriss. Had he  done so, as indicated by today's story, he would have found that there was no  need for an investigation, and indeed, no story to report at all. </p>
                  <p align="left" class="style2">Given the controversy  surrounding the possible displacement of Microsoft products by the policy that  Mr. Quinn proposed, I believe that it is important for you to address the  following questions, in order to determine whether the <em>Globe</em> was used by  opponents of Mr. Quinn or his policy to pursue the ends of any such opponents,  and if so, whether the <em>Globe</em> followed appropriate journalistic practices  in order to avoid this outcome: </p>
                  <p align="left" class="style2">1.&nbsp; Did Mr. Kurkjian decide to look into Mr.  Quinn's travel on his own, or was this suggested to him by someone? </p>
                  <p align="left" class="style2">2.&nbsp; If there was a source, who was that source? </p>
                  <p align="left" class="style2">3. If there was a source,  was that source inside the Massachusetts government or outside? </p>
                  <p align="left" class="style2">4.&nbsp; If the source was inside the government,  were the disclosures made to Mr. Kurkjian made in violation of any State  policy? </p>
                  <p align="left" class="style2">5. If the source was  outside the government, did the source have any affiliations that would lead  him or her to have an interest in the disparagement of Mr. Quinn? </p>
                  <p align="left" class="style2">6.&nbsp; Why did Mr. Kurkjian not wait to run the  story until he was able to reach Mr. Kriss, who Mr. Kurkjian knew could confirm  or disprove the basis for the story? </p>
                  <p align="left" class="style2">7. Was this story, and the  investigation behind it, in compliance with the <em>Globe's</em> policies? </p>
                  <p align="left" class="style2">Given the importance of the  information technology policy that underlies this story, which is literally  being watched from around the world and has generated hundreds of articles in  the world press, I hope that you will undertake to answer these questions. </p>
                  <p align="left" class="style2">By way of disclosure, I  have been reporting on this story for several months at my blog, and have  reproduced this letter there in an entry that will be read by thousands of  visitors over the next several days. The address of that entry is: <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1805">http://www.consortiuminfo.org/newsblog/blog.php?ID=1805</a> </p>
                  <p align="left" class="style2">I am also an attorney, and  one of my clients is the Organization for the Advancement of Structured  Information Systems (OASIS), which developed the OpenDocument standard that  lies at the heart of the discussions that are going on within the State House.  However, I am neither authorized nor acting for, nor am I being compensated by,  that organization in connection with this email or any of the entries at my  blog. </p>
                  <p align="left" class="style2">Thank you for your time and  consideration. </p>
                  <p align="left" class="style2">Andrew Updegrove, Editor<br>
                    ConsortiumInfo.org</p>
                </blockquote>
                <p align="left" class="style2">On December 12, Robert Chacon responded to me as follows:</p>
                <blockquote>
                  <p align="left" class="style2"> Mr.  Updegrove, </p>
                  <p align="left" class="style2"> Greetings and many thanks for your  detailed note. I think you raise some reasonable questions about the Globe's  stories concerning Peter Quinn (I had many of the same ones when I read  Saturday's story that he was cleared in the investigation). </p>
                  <p align="left" class="style2"> I don't have immediate answers for  you (or your blog readers, many of whom have sent their own similar notes after  having read your page). I can tell you at the outset that Stephen Kurkjian is  one of the most experienced, professional and ethical journalists that I have  ever known. Nevertheless, I do believe that some of these questions deserve  answers - from Steve and his editors. </p>
                  <p align="left" class="style2"> I have already sent a note to Steve  asking for a chance to talk about the stories. I will offer a more detailed  reply when I have some answers. </p>
                  <p align="left" class="style2"> Regards,<br>
                    Richard Chacon </p>
                </blockquote>
                <p align="center" class="style2"><strong>* * * * * * *</strong><br>
                </p>
                <p align="left" class="style2">On Christmas Eve, Peter Quinn sent an email to all of  his employees, telling them that he would resign as the CIO of Massachusetts,  effective January 9, 2006.&nbsp; His email  read in part as follows:</p>
                <blockquote>
                  <p align="left" class="style2"> Over the last several months, we have been through some very  difficult and tumultuous times. Many of these events have been very disruptive  and harmful to my personal well being, my family and many of my closest  friends. This is a burden I will no longer carry.</p>
                  <p align="left" class="style2">It is also readily apparent that I have become a  lightning rod with regard to any IT initiative. Even the smallest initiatives  are being&nbsp; mitigated or stopped by some  of the most unlikely and often uniformed parties. I view these circumstances  quite troubling because the good work laid out by the IT Commission is slowly  being strangled and brought to a halt. And the last thing I can let happen is  my presence be the major contributing factor in marginalizing the good work of  ITD and the entire IT Community.</p>
                </blockquote>
                <p align="left" class="style2">News of Peter's resignation was first reported on December 27 by me at  the <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1863">Standards  Blog</a> and by Pamela Jones at <a href="http://www.groklaw.net/article.php?story=20051227113652154">Groklaw</a>.&nbsp; In my entry, I reported:</p>
                <blockquote>
                  <p align="left" class="style2">According to Kriss, who met with Quinn on December 21st, the personal  attacks, and especially the unfounded (and quickly disproven) charges  publicized by the Boston <em>Globe</em>, played a major part in reaching his  decision.</p>
                </blockquote>
                <p align="left" class="style2">The articles in the press that followed largely focused on the impact  that Peter Quinn's resignation would have on the fortunes of the OpenDocument  format and its proponents, and whether or not Governor Romney, widely assumed  to be grooming himself for a run for the presidency, would withdraw his support  for ODF in favor of the XML Reference Schema that Microsoft had offered to  ECMA, a European standards body, in response to the success of ODF.</p>
                <p align="left" class="style2">In response, I posted the following entry at my blog.</p>
                <p align="center" class="style2"><strong>* * * * * * *</strong> </p>
                <p align="left" class="style56"><strong>December 29, 2005&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
                <h1 align="center" class="style2">Thanks, Peter; Hail and Farewell</h1>
                <p align="center" class="style2"><em>The best way to predict the  future is to invent it.</em><br>
                  - Computer scientist and visionary Alan Kay</p>
                <p align="left" class="style2"><br>
                  For  the last three and a half months I've been covering the ongoing debate and, for  want of a better phrase, &quot;related events&quot; surrounding the decision by  the Information Technology Division of Massachusetts (ITD) to mandate use of  the OpenDocument format (ODF) for archiving Executive Agency documents. The architect  and public spokesperson for that policy has been Peter Quinn, the head of the  ITD, and the CIO of the Commonwealth. </p>
                <p align="left" class="style2"> Like  everyone else who has been covering this story, whether on one side or the  other, I've felt free to use Peter's name and report on his actions as if they  were public property. Which, in a way, is what one's name and actions become &ndash;  even legally, as regards the threshold for proving libel -- when one accepts a  public position. And while I have invariably been complimentary, and even protective  (in the case of the <em>Globe</em> article questioning his travel records), I've  never asked Peter's permission to keep putting his name out there, day after  day and week after week, thereby doing my small bit to help keep him pinned  down at the center of a controversy. </p>
                <p align="left" class="style2"> Now,  as you know, Peter has decided to resign his position, in part to permit the  work of the ITD to move on unhindered, and in part to allow his and his  family's life to do the same thing. I can't begrudge him the decision, but I'm  very sorry to see him go, especially as a result of the reasons that led to  that decision. </p>
                <p align="left" class="style2"> I  can't say that I know Peter. I've interviewed him once, heard him speak or  testify three times, and exchanged a few brief emails with him over the past  several months. But I have admired him, and I'd like to take a few minutes to  say why. </p>
                <p align="left" class="style2"> First  of all, I've been working with standards organizations for 18 years, and those  organizations have created hundreds of ITC standards during that period. But  most of those standards, while important, were not significant in any larger  sense. Most were simply the cleverly created tools that were needed to take the  next incremental step in the march of technical progress.</p>
                <p align="left" class="style2"> ODF,  though, is a standard that <em>matters</em>, even though very few people yet  appreciate it. Unless ODF (or another mechanism capable of achieving the same  end) becomes universally adopted, much of the information of today's world will  not only fade from view as time passes &ndash; it may literally disappear for all  practical purposes, leaving little or no trace. Peter not only realized this,  but he committed to do something about it. </p>
                <p align="left" class="style2"> Second,  I've always admired the leaders and visionaries that see things before the rest  of us, and have the courage to show the way. It's both risky and lonely to be  out in front. The enemies, doubters and flaks always outnumber the friends and  those that &quot;get it,&quot; and too often it's those that follow that get  the credit rather than those that took the heat. </p>
                <p align="left" class="style2"> And  finally, I've always admired people who did the right thing simply because it  was the right thing to do, regardless of whether it would be easy or hard,  popular or controversial, personally beneficial or costly to one's career.  There are all too few of us that ever rise to that challenge, especially (it  seems) in public life. </p>
                <p align="left" class="style2"> Unfortunately,  I and others in the blogosphere and the traditional media will need to mention  Peter a bit longer, as the consequences of his resignation play out. I hope  that he will understand and not begrudge our actions too much, in hopes that  his goals will be more likely to be achieved as a result. </p>
                <p align="left" class="style2"> But  we should keep it short. It's the least we can do for someone who expected no  personal benefit from doing something that needed to be done for the benefit of  all, and who took the fall for doing so &ndash; not because what he wanted to do was  wrong, but because what was good for all was threatening to a few, and because  those few were powerful. </p>
                <p align="left" class="style2"> So  as Peter steps back from the spotlight, I'd like to express my gratitude to  him, and wish him well. </p>
                <p align="left" class="style2"> To  everyone else, I'd like to suggest that the best thing we can do for Peter is  to give him some peace and privacy. And we should do everything we can to  achieve what he set in motion - for our benefit as well as in recognition of  his efforts. It's the least we can do for someone who tried to make a  difference &ndash; and did. </p>
                <p align="left" class="style2"> So  thanks, Peter. Hail and farewell. </p>
                <p align="center" class="style2"><strong>* * * * * * *</strong> </p>
                <p align="left" class="style2"><strong><em>Epilogue:</em></strong>&nbsp; As of this  writing, the Globe's ombudsman, Robert Chacon, has not yet publicized the  results of his investigation.&nbsp;  Hopefully, that will happen soon.&nbsp;  But happily, on January 3, I was able to report in an <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1887">exclusive story</a> that the Massachusetts administration, at least for now, remains committed to  perpetuating Peter's hard-won effort to protect the history of a government  that could have supported him far better than it did. </p>
