                <p align="center" class="style10"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">CONSIDER THIS: </font></em></strong></p>
                <p align="left" class="style56"> <strong> [][][] <strong> November 30, 2005</strong> </strong></p>
                <p align="left"><span class="style56">#34</span><strong class="style38"><em> The way ain't far, but you can't get there from here </em></strong></p>
                <p align="left" class="style2">The  title above is the punchline for an ancient bit of rural humor.&nbsp; It's the answer the local gives, after due  deliberation, to the city guy in the car who's asking for directions.&nbsp; But it often seems like an apt description  of the likelihood of reaching agreement between two firmly entrenched points of  view.</p>
                <p align="left" class="style2">How  can it be so often the case that two intelligent people can have firm &ndash; and  diametrically opposed &ndash; opinions on the same issue?&nbsp; </p>
                <p align="left" class="style2">This  situation is so common that we simply take it for granted, especially as  election times approach.&nbsp; But perhaps if  we spent more time answering this question, we might find it easier to achieve  consensus.</p>
                <p align="left" class="style2">Of  course, there are many reasons why people disagree, ranging from simple  misreading of relevant facts to outright bigotry.&nbsp; In fact, the process of forming an opinion involves a series of  independent steps and processes, and any one of them can lead to divergent  results.</p>
                <p align="left" class="style2">For example, there is the  gathering and confirmation of facts, followed by deciding which are relevant  and which are not, after which one places them in a pattern that is meaningful,  to which one then applies intelligence, past experience and creativity to  arrive, finally, at an opinion.&nbsp; And  that is an over simplification (or, for a thinking person, should be).</p>
                <p align="left" class="style2">The following is an example  of how two radically different results on the importance of a single sentence  can be reached, simply by choosing which frame of reference to use in reaching  a judgment.&nbsp; The sentence is as follows:</p>
               
                <blockquote><p align="left" class="style2">Microsoft  irrevocably covenants that it will not seek to enforce any of its patent claims  necessary to conform to the technical specifications for the Microsoft Office  2003 XML Reference Schemas posted at  http://msdn.microsoft.com/office/understanding/xmloffice/default.aspx (the  &ldquo;Specifications&rdquo;) against those conforming parts of software products. </p>
                </blockquote>
                <p align="left" class="style2">Does this sentence  constitute a radical departure from historical Microsoft intellectual property  rights policy, or a smokescreen meant to imply much, but promise little?&nbsp; The answer is that it depends upon the frame  of reference, and the situation, in which you ask the question.&nbsp; </p>
                <p align="left" class="style2">Consider the following:&nbsp; On November 22 and 25 I posted two blog  entries on this same topic.&nbsp; The first  was written and posted the day that Microsoft announced&nbsp; the &quot;covenant not to sue&quot; from which the  sentence above is excerpted.&nbsp; That  covenant relates to the current version of its XML Reference Schema, and the  covenant was issued as part of an effort by Microsoft to head off a challenge  to its flagship (and extremely profitable) Office software suite by competing  products based upon the OpenDocument Format (ODF), an OASIS standard that benefits  from a covenant not to sue on the part of Sun Microsystems.</p>
                <p align="left" class="style2">This first post was based primarily on a comparison of the Microsoft covenant, in the context of a yet-to-be-granted adoption of the XML Reference Schema by Ecma, a European standards body, with the Sun covenant, in the context of the already issued OASIS standard.  You can read the lengthy line-by-line analysis, which was titled "Microsoft's Format Covenant Fails Comparison Test with Sun's" <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1762">here</a>, but for current purposes we'll simply skip forward to my final conclusion, and the questions to which that conclusion led me:</p>
                
                <blockquote><p align="left" class="style2">The upshot is that the Sun  covenant is far superior in several important respects to the Microsoft pledge. </p>
                  <p align="left" class="style2">This raises the question:  Why? Certainly, Microsoft must have expected that a comparative analysis like  this would be done almost immediately, so it must have had a strong incentive  to match the Sun covenant as closely as possible, and it decided not to. Again:  Why? </p>
                  <p align="left" class="style2">There are three  possibilities. The first is that it has evil entrapment plans afoot, but I  really don't think that is likely to be the case, and certainly not in each  instance, since it would be rightly pilloried for doing so. The second is that  it hasn't gotten far enough through the knothole to bring itself to go as far  as Sun did. </p>
                  <p align="left" class="style2">The third is that it has  made the calculated decision that this is as far as it needs to go to obtain  the objective that it is trying to achieve, which is to head off ODF at the  pass. </p>
                  <p align="left" class="style2">Which is it? My guess is  that it's a combination of 2 and 3. I'm told by those I know in Microsoft that  making such a covenant was a difficult and contentious decision internally, and  it would be tough to sell internally more than the absolute minimum necessary  to arguably do the job. </p>
                  <p align="left" class="style2">Will it be enough? We'll  find out. But on my review, there's a lot of light standing between the two  covenants which will provide plenty for people to talk about. Microsoft knows  its customers well, though, and it&rsquo;s the customers ultimately that will say  whether Microsoft bet its hand conservatively and still won, or didn't act  boldly enough to walk away with the pot. </p>
                  <p align="left" class="style2">Given that Microsoft has  already collected some endorsements from European governments in favor of its  formats, backed by the Ecma announcement (its no coincidence that Ecma is a  European standards developer), its clear that has already test marketed its bid  with its customers. </p>
                  <p align="left" class="style2">The bid now passes across  the table. Who's going to make the next bet? </p>
                </blockquote>
                <p align="left" class="style2">Lots of people found  that pretty convincing, and the right mode of analysis.&nbsp; But was it?&nbsp;  Three days later, I took a crack at it from another perspective in a  post titled &quot;The Microsoft Covenant Reexamined,&quot; which you can read  in full (if you are so inclined) <a href="http://www.consortiuminfo.org/newsblog/blog.php?ID=1769">here</a>. I  explained the basis for this second analysis as follows: </p>
                
                <blockquote><p align="left" class="style2">Some of my friends at  Microsoft thought that [my first analysis] was an unfair way to present the  covenant. By comparing the Microsoft covenant to Sun's, I was necessarily  ending up with a &quot;glass half full&quot; result. Instead, they thought I  should have provided an analysis of the covenant solely on its own merits. </p>
                  <p align="left" class="style2">This strikes me as a  fair request, so in order to present a more balanced picture, in this post I'll  look at the Microsoft covenant from the following perspective: how much better  is the new covenant than the old 2003 XML Reference Schema license? At the end  of this entry, I'll then try and reconcile the two analyses and see where we  come out. </p>
                </blockquote>
                <p align="left" class="style2">This time, my  analysis came out rather differently, because now I was asking a different  question as the first step in reaching a final conclusion.&nbsp; The analysis therefore concluded as follows:</p>
               
                <blockquote><p align="left" class="style2">So  is the new covenant a significant improvement? There is no question that it is,  and Microsoft therefore deserves credit for moving its IPR position so  substantially. Hopefully it will take the same posture with other patents in  its portfolio in the future, where significant standards opportunities arise. </p>
                </blockquote>
                <p align="left" class="style2">Of course, that still left the task of reconciling the two  analyses in order to attempt to come to a final, valid conclusion, but that&rsquo;s  not really the point of this essay (if you&rsquo;re curious, though, you can read the  last part of the second blog entry). </p>
                <p align="left" class="style2"> There  are two morals to be drawn from this simple exercise.&nbsp; The first one is that it's very easy to adopt a single analytical  approach (especially if it is sympathetic to your personal viewpoint), and  then stick with it.&nbsp; But once you become  locked into a given perspective, it may become difficult or impossible to agree  with someone else on an answer, unless they have adopted the same  perspective.&nbsp; It may also make it  extremely unlikely that you will come up with the &quot;right&quot; answer  yourself.</p>
                <p align="left" class="style2"> The  second moral is that while perspectives are wonderful tools for examining a  problem in order to appreciate certain dimensions of an issue, by definition  they sacrifice a degree of objectivity in exchange for heightened perceptivity,  much as intense light may highlight certain subtle features that might  otherwise escape notice, but will also wash out colors, yielding a distorted picture.</p>
                <p align="left" class="style2"> Ultimately,  it doesn't matter which end of the telescope you look through.&nbsp; Either way, things will appear larger or  smaller than they really are when you use your unaided vision.&nbsp; The trick is in reconciling the images to  get at the truth.</p>
                <p align="left" class="style2"> Sadly,  all too often we take the easy way out in an argument, and simply conclude that  you can't get to an answer from here.</p>
