                  <div align="center">
                    <p align="center" class="style10"><strong>POLICY AND TECHNOLOGY IN TUNIS</strong><br>
</p>
                    <p align="left" class="style2">This month's issue completes a four-part series of CSBs dedicated to examining the relationship between government at all levels and private sector standard setting.  The previous issues focused on the frequent lack of coordination between the public and private sectors (<a href='http://www.consortiuminfo.org/bulletins/aug05.php'> Government and SSO's: Optimizing the System</a>, in August), the role that governments can sometimes play in accelerating the uptake of standards (<a href='http://www.consortiuminfo.org/bulletins/sep05.php'>Massachusetts and OpenDocument:  The Commonwealth Leads the Way</a>, in September), and on the increasingly complex standards challenges that governments and industry will need to cooperatively address in the future <a href='http://www.consortiuminfo.org/bulletins/oct05.php'> (Standards for a Small Planet</a>, in October).</p>
                    <p align="left" class="style2">This month, the subject is  once again global standard setting, and the focus is on the second plenary meeting  of the World Summit on the Information Society (WSIS), which was held in Tunis,  Tunisia earlier this month.&nbsp; The WSIS  process is dedicated to bridging the Digital Divide between the First and the  Third World, and has set a goal of bringing the Internet &quot;within  reach&quot; of half of the world's peoples by 2015.&nbsp; Despite this worthy goal, much of the time and effort of the 175  nations involved has been squandered thus far in a wrangle over the question  &quot;Who should govern the Internet?&quot;</p>
                    <p align="left" class="style2">In my <strong><em>Editorial</em></strong>,  I reflect on the fact that while the governance issue has been finessed for  now, the United States has (once again) stepped on many international toes in  order to maintain its hegemony over the root zone of the Internet, which it  currently controls via its oversight of the Internet Corporation for Assigned  Number and Names (ICANN).&nbsp; Perhaps some  day what goes around will come back around in a way that we find less than desirable.</p>
                    <p align="left" class="style2">This month's <strong><em>Feature  Article</em></strong> provides an in-depth review of the WSIS process, describing how  it came into being, what it sought to accomplish, how the ICANN impasse arose,  and how it was resolved (at least for now).&nbsp;  The article closes on a more hopeful note than the Editorial, hypothesizing  that with time the ICANN episode will be seen as an irrelevant diversion.</p>
                    <p align="left" class="style2">This month's <strong><em>Blog </em></strong>entry  was written on the closing day of the WSIS Summit in Tunis, and highlights some  of the party lines that each of the various camps involved sought to spin to  the press to put the best gloss on the governance compromise that was reached  on the eave of the Summit.</p>
                    <p align="left" class="style2">Of course, in another sense  what this issue of the <strong><em>CSB</em></strong> is really about is the challenge of  achieving consensus among those with widely divergent, and strongly held  beliefs.&nbsp; Accordingly, my <strong><em>Consider  This</em></strong> piece  explores some of the problems with getting to  &quot;yes&quot; when perspectives are very different.&nbsp; To make my points I return to a story that was featured through  the last two issues, and which is continuing to play out in Massachusetts: the  face-off between the supporters of the OASIS OpenDocument format, and  Microsoft, which has pledged to offer its XML Office Schemas to Ecma, a  European standards organization, as a standard.&nbsp; The piece contrasts two equally valid modes of analysis that can  be applied to Microsoft's XML &quot;covenant not to sue&quot; to reach very  different conclusions, as an example of why consensus is so hard to achieve.</p>
                    <p align="left" class="style2">Next month, you can look  forward to our annual wrap up of the year's most significant standards stories,  as well as our recognition of those journalists whose work was most  consistently informative during the year.</p>
                    <p align="left" class="style2">As always, I hope you enjoy  this issue.&nbsp; </p>
