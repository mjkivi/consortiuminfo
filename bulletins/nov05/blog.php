                <p align="center" class="style8"><strong><em><font color="#006646" size="2" face="Arial, Helvetica, sans-serif">STANDARDS BLOG:</font></em></strong></p>
                <p align="left" class="style56"> November 18, 2005</p>
                <p align="center"> <strong> <span class="style10"><strong>Spin Sets in on Internet Governance Compromise</strong> </span></strong></p>
                <p align="center"><strong><em> </em></strong></p>
                <p align="left" class="style2">So the logjam on Internet governance has broken. The  question is, who blinked - the U.S. or everyone else? </p>
                <p align="left" class="style2">The issue of &quot;who should rule the Internet&quot;  attracted increasing coverage after the EU reversed polarity and abandoned  support for the U.S. position on this issue. That occurred not long ago, at the  last formal meeting (called PrepCon3) preceding the vast summit meeting that  came to order on Wednesday, bringing more than 19,000 participants together in  Tunis, Tunisia, to plan a global &quot;information society&quot;. </p>
                <p align="left" class="style2">The question at hand has been whether ICANN, the Internet  Corporation for Assigned Numbers and Names, would retain its control of the  root zone of the Internet, and whether it would remain subservient to the  U.S. government, via the supervision of the American Department of Commerce.  Once the EU dropped its support of ongoing U.S. hegemony, it left the battle  lines starkly drawn, and for all intents and purposes, the U.S. was on one side  of the line, and on the other side was everyone else. </p>
                <p align="left" class="style2">The last-minute desertion by the EU left the US with only  two options: agree to a compromise of its control in advance of the Summit, or  engage in a very public confrontation with multiple opponents in front of those  19,000 attendees, not to mention under the scrutiny of an intently watching  world, served by legions of media looking for juicy stories where there would  be few to be found.&nbsp; But the U.S. did  have one advantage, and that was a big one: current control of the root zone of  the Internet.&nbsp; If it wished to dig in,  there was very little that anyone could do about it.</p>
                <p align="left" class="style2">There were some reports heading into the final weekend  before the Tunis Summit that attempts were in process to break the impasse. And,  sure enough, on the first day of the Summit it was announced that a concordat  had indeed been signed the night before that would take the Internet governance  issue off the table before the Summit began its debate. But if one side  blinked, which one was it? </p>
                <p align="left" class="style2">One of the first articles released spun the compromise as a  defeat for the U.S., bearing the head the story <a href="http://www.pcadvisor.co.uk/news/index.cfm?newsid=5313">WSIS: US caves over internet monopoly </a>.  That article opened as follows: </p>
              
                <blockquote><p align="left" class="style2"> The  US has agreed to consult with other governments over top-level domain names in  a major shift in the US policy on control of the internet, EU officials  confirmed on Wednesday....Under the deal, struck late on Tuesday night, all  parties agreed that &quot;no government should have the last word on another  country's top level domain&quot;, said Martin Selmayr, a spokesman for EU  Information Society Commissioner Viviane Reding, who attended the meeting. </p>
                </blockquote>
                <p align="left" class="style2"> But contrary to that headline, the &quot;caving&quot; by the  US and the victory of the rest of the World appears to be more token than  real: the same article indicates that the primary compromise made by the U.S.  was simply that the American Department of Commerce would consult with a  country before taking any action affecting its domain identifier. Hardly  revolutionary stuff, that. </p>
                <p align="left" class="style2">But if the U.S. didn't cave, does that mean that the  opposition did? 
                  That's the way it looked to the Business Standard, in India,  in an article posted yesterday called <a href="http://www.business-standard.com/common/storypage.php?storyflag=y&leftnm=lmnu5&leftindx=5&lselect=1&chklogin=N&autono=205973">Internet Control</a>. As the Business  Standard saw it: </p>
               
                <blockquote><p align="left" class="style2"> The  rhetoric centred on control of the Internet turned out to be just hot air,  after a compromise was hammered out at the World Information Summit in Tunis.  The US remains in unilateral control. Status quo has been maintained with  respect to technical functionality as well as regulatory power. While a  multinational body, the Internet Governance Forum (IGF) consisting of national  governments, corporations and NGOs, has been created, this has &ldquo;no oversight  function and would not replace existing arrangements, mechanisms, institutions  or organisations, but involve them and take advantage of their expertise&rdquo;. The  forum &ldquo;would have no involvement in day-to-day or technical operations&rdquo;.  Translated, the IGF will act like a scaled-up version of the typical Internet  forum. It will allow frustrated users to rant and rave, or to offer useful  suggestions. But these suggestions will not be binding. </p>
                </blockquote>
                <p align="left" class="style2"> So it is that as details of the horse trading and next steps  on Internet governance are finding their way into the media, each article  manifests its own spin on what happened behind closed doors, and what will  happen next. <br>
                  Here in its entirety, for example, is the official (and  self-congratulatory) EU <a href="http://europa.eu.int/rapid/pressReleasesAction.do?reference=IP/05/1433&format=HTML&aged=0&language=EN&guiLanguage=en">press release</a> on the compromise,  which claims credit for putting back together what it had helped to put asunder  only a few weeks before at PrepCon3: </p>
              
                <blockquote><p align="center" class="style2"><strong> EU brokers deal on progressive internationalisation of  Internet<br>
                    governance at Tunis World Summit </strong></p>
                  <p align="left" class="style2"> EU  brokers deal on progressive internationalisation of Internet governance at  Tunis World Summit </p>
                  <p align="left" class="style2"> A  worldwide political agreement providing for further internationalisation of  Internet governance, and enhanced intergovernmental cooperation to this end,  was brokered at the World Summit on the Information Society in Tunis last  night. The compromise text agreed was based largely on EU proposals presented  in the discussions since June. As a first important element of the agreement, a  new international Internet Governance Forum (IGF) will be created to deliberate  among governments, the private sector and civil society at large in a  multi-stakeholder policy dialogue related to Internet Governance. A first  meeting of this Forum will be convened by the Secretary-General of the United  Nations by the second quarter of 2006 and take place in Greece. The texts  agreed in Tunis also include language that will allow for enhanced cooperation  among governments, on an equal footing, on public policy issues. Such  cooperation should include the development of globally applicable principles on  public policy issues associated with the coordination and management of  critical Internet resources. This cooperation will make use of relevant  international organisations. There was also a consensus in Tunis yesterday that  countries should not be involved in decisions regarding another country&rsquo;s Top  Level Domain, thus meeting requests made, in particular, by the EU in the  negotiations. </p>
                  <p align="left" class="style2">&ldquo;I  welcome the texts now agreed in Tunis. They pave the way for a progressive  internationalisation of Internet governance&rdquo;, commented Viviane Reding, EU  Commissioner for Information Society and Media, who is leading the Commission  delegation in Tunis. &ldquo;This agreement was possible because of the strong belief  of all democratic nations that enhanced international cooperation is the best  way to make progress towards guaranteeing the freedom of the Internet around  the globe and also to enhance transparency and accountability in decisions  affecting the architecture of the Web. The fact that the EU spoke with one  voice in Tunis, and had stood by its case for more cooperation on Internet  governance in the run-up to the summit, certainly strongly influenced this  positive agreement&rdquo;. </p>
                  <p align="left" class="style2"> The  text finalised last night reflects a consensus of all participants of the Tunis  summit. It will now be officially adopted by the Heads of State or Government,  or their representatives, in the course of the World Summit on Information  Society that officially starts today and will last until Friday. For the  Commission, the days to come will focus on gaining the support of other nations  for the EU&rsquo;s policy of investing in Information and Communication Technology,  as a means to overcome the &ldquo;digital divide&rdquo;. In addition, the Commission will  reiterate its position on the need to safeguard human rights, and in particular  freedom of speech, in order to build a truly global Information Society. </p>
                </blockquote>
                <p align="left" class="style2"> As news continues to trickle out, I'll report on it here,  and will post the full text of the principles that were agreed to as well. </p>
