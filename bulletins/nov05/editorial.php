                <p><font color="#006646" size="2" face="Arial, Helvetica, sans-serif"><strong><em>EDITORIAL:</em></strong></font></p>
                <div align="center">
                  <div align="center">
                    <p align="center" class="style10"><strong>NOT WITH A BANG, BUT A WHIMPER</strong></p>
                    <p align="center" class="style10"><font size="2" face="Arial, Helvetica, sans-serif"><strong><a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove</a></strong></font></p>
                    <p align="left" class="style2">In December of 2003, the representatives of 175 nations met  in Geneva with the avowed purpose of bringing the blessings of the Internet to  all peoples, everywhere.&nbsp; The name given  to the endeavor was the World Summit for the Information Society (WSIS), and its  sponsor was the United Nations.&nbsp; Last  week, the agents of 174 nations and their retainers and camp followers  reassembled, some 19,000 strong, in Tunis, Tunisia to report on what progress  had been made against achieving the grand vision to which they had pledged  their efforts two years before.&nbsp;&nbsp; Sadly,  that progress appears to amount to very little, although the benefits that  universal connectivity can bring grow richer by the day.</p>
                    <p align="left" class="style2">Nowhere was the record of non-achievement more visible than  with respect to making progress on a topic that was originally called  &quot;Internet governance,&quot; but which eventually narrowed to the single  question of &quot;Who should govern the Internet.&quot;&nbsp;&nbsp; More specifically, the argument concerned  who should control the Internet Corporation on Assigned Numbers and Names  (ICANN), which was created in 1988 by the United States Department of Commerce  to administer the country codes and site addresses upon which the Internet is,  in part, based.&nbsp; </p>
                    <p align="left" class="style2">Although ICANN's governance structure includes a degree of  international representation, it is ultimately under the control of the  Department of Commerce, which abruptly announced less than five months before  the Tunis Summit that it would not relinquish control of ICANN in 2006, as had  earlier been promised.&nbsp; The result was  an uproar that eventually culminated in even the European Union deserting the  American camp just a few weeks before the Summit.</p>
                    <p align="left" class="style2">In many ways, the ICANN conflict represented one of those  prismatic issues that appear to be discrete, but under closer scrutiny can be  broken into a spectrum of equally contentious matters, any one of which would  be challenging to resolve.&nbsp; All at the  same time, opinions relating to the &quot;go it alone&quot; attitude of the  United States, the inefficient bureaucracy of the United Nations, the power of  multi-national corporations, the demands of technology versus governance ideals  for civil society, and much more were brought into conflict, and all without an  established mechanism to break the impasse.&nbsp; </p>
                    <p align="left" class="style2">So complex were the forces at work and so firm the opinions,  that three quarters of the way between the Geneva and the Tunis Summits a  meeting held to reach a consensus position on Internet Governance resulted in  not one, or even two proposals, but four.</p>
                    <p align="left" class="style2">The difficulty in arriving at a solution that was truly  acceptable to all on ICANN may not be surprising, because to some extent the  ICANN issue was more symbolic than material.&nbsp;  The actual role of ICANN is limited and in the main quite technical and  administrative.&nbsp; But the potential  damage and mischief that could be caused by its mismanagement or manipulation  is great.&nbsp; This led the U.S. government  to focus on the importance of maintaining stability, and much of the rest of  the world to focus on the inequity of a single nation asserting the right to  unilaterally control what has become an essential global resource.&nbsp; Ultimately, the issue became binary &ndash; in  order for one side to win, the other would have to lose, and there proved to be  no middle ground of acceptable compromise.&nbsp; </p>
                    <p align="left" class="style2">Given that the Internet is at the very heart of the WSIS  process, the rancor generated over the ICANN issue came to overshadow almost  everything else that was under discussion, and perhaps undermined the  effectiveness of the entire process.&nbsp;  Ultimately, the common wisdom that &quot;possession is nine tenths of  the law&quot; resulted in the formation of a weak, international &quot;Internet  Governance Forum&quot; that might, or might not, grow into a more significant  governing body in the future.&nbsp; </p>
                    <p align="left" class="style2">As with the failure of the U.S. Senate to approve American  participation in the League of Nations in 1919, this new assertion of  unilateralism so early in the WSIS process may weaken the ongoing influence of  the WSIS initiative, just as it certainly diverted energies from more fruitful  goals during the second phase of the  process.</p>
                    <p align="left" class="style2">For now, a Band-Aid has been placed on the issue of who  will&nbsp; &quot;govern the Internet,&quot;  but beneath the thin film of the Tunis &quot;compromise&quot; the wound  continues to fester.&nbsp; Over time, it can  be assumed that more rather than fewer information and communications  technology (ITC) issues will arise that have global significance, and it will  not be tolerable to 175 nations to be beholden to one for their control  forever.</p>
                    <p align="left" class="style2">The ultimate irony is that the Internet (and the Web) are  built not on national power, but on technical standards.&nbsp; ICANN is merely the entity that employs some  of those standards to provide addresses for countries and Websites.&nbsp; If the standards can be set by consensus,  why cannot a way be found to manage their use by the same means?</p>
                    <p align="left" class="style2">Ultimately, the United States would be better served by  working to conceive such a consensus-based organization than by seeking to lock  up the Internet in an American basement.</p>
                    <p align="left" class="style2">After all, the next core element of the ITC infrastructure  may be developed in some other country's basement.&nbsp; And then the shoe will be on the other foot.</p>
