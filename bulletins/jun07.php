			<h4>CHAPTER 6</h4>
			<h3><span class="capSpace">Current Events</span></h3>
			<div class="hr"></div><hr />
			<p><em>While the emergence of standards issues in the popular press has historically been a novelty, a number of forces are converging today that have brought both discrete standards as well as broader standards issues into the public eye.  An example of the former upon which I have written frequently involves &ldquo;open document formats,&rdquo; which are XML&ndash;based formats that make it possible to create documents that can be read and revised by any compliant software application with a high degree of fidelity.  Absent the utilization of such standards, our ability to read today&rsquo;s documents in the future will be in question.</em></p>
			<p><em>The first such standard to reach the marketplace was the OpenDocument Format, or ODF, an OASIS&ndash;developed standard that has now been adopted globally by ISO/IEC JTC1.  More recently, Microsoft has made available the XML&ndash;based formats (which it calls Office Open XML, or OOXML) upon which its Office 2007 productivity suite is based.  Microsoft contributed the specification for its formats to Ecma, a European&ndash;based standards organization, which adopted OOXML in 2006.  That specification is now being considered by ISO/IEC JTC1 as well, in a process that is receiving wide attention.</em></p>
			<p><em>What launched this competition into the public eye was the decision of the Information Technology Division of Massachusetts in 2005 to specify open formats for procurement by the State's Executive Agencies, and to recognize ODF but not OOXML for that purpose.  The resulting controversy is reviewed in detail in the first article in this chapter, while the consequences for the CIO that developed the policy are reviewed in the second.  This year, bills were introduced (and defeated) in five US states to require the use of such formats, and similar efforts have been launched abroad.  As of this writing, the saga is far from over.</em></p>
			<p><em>The third article in this chapter turns to the unprecedented challenge presented by global warming, a harbinger of the ongoing resource and environmental issues that will face humanity in the future as population continues to grow.  Challenges such as these will demand the development, deployment and international enforcement of a wealth of standards in order to craft and enforce necessary solutions &ndash; a process for which no existing global political system now exists.</em></p>
			<p><em>My last article closes this chapter on a somber note, reminding us that governments are held to standards as well.  History after all often provides examples that could guide us more safely through the dangers of the modern world &ndash; if only we heed them.  It can therefore be illuminating to compare current events &ndash; and more significantly current decisions &ndash; to past decisions and their consequences.  Sadly, the question I asked over three years ago in the title to the last article in this chapter has been all too conclusively answered in the affirmative.</em></p>
