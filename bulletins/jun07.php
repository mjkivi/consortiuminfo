			<h2>June &ndash; July 2007<br /><span class="volumeinfo">Vol VI No 6</span></h2>
			
			<!-- ISSUE -->
			<h3>THE STANDARDS OF OUR TIMES<br /><span class="smaller">50<sup>th</sup> Issue Anthology &ndash; A ConsortiumInfo.org eBook</span></h3>
			<!-- INDEX ITEM -->
			<p><span class="section">Preface:</span> <span class="subsection"><a href="#preface" title="Section" accesskey="3">Thoughts on Fifty Issues (and the Future)</a></span>
				<br />In December of 2002 the first issue of the Consortium Standards Bulletin was delivered electronically. It's now c. 600,000 words, 350 stories and features, and four and half years later. That seems like a good time to reflect on the past, present and future of standard setting, and on this standards “Journal of News, Ideas and Analysis” as well.</p>
			<!-- INDEX ITEM -->
			<p><span class="section">Introduction:</span> <span class="subsection"><a href="#introduction" title="Section" accesskey="4">Who Cares About Standards?</a></span>
				<br />Over three million people open more than eight million page views at this site per year.  The access logs for ConsortiumInfo.org demonstrate how broad and inclusive the global interest in standards is &ndash; and provide some surprises as well.</p>
			<p><span class="subtitle">Consider This: <a href="http://www.consortiuminfo.org/bulletins/oct05.php#considerthis" title="Section" accesskey="3">Do  Standards  Matter?</a></span> (October 2005)</p>
			<!-- PART -->
			<p><span class="part">Part One: <br />Standards Development and Standards Stakeholders</span></p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 1 &ndash;</span> <span class="subsection"><a href="#chapter1" title="Section" accesskey="5">Goals: Standards Developers and Why They Participate</a></span>
				<br />Dimensional, safety and other standards implemented in physical products have been pervasive since the nineteenth century, but truly open systems are a recent innovation in the world of information technology. The selections in this chapter explore how this transition from a proprietary to an open-standards&ndash;based information infrastructure came about, why those who are most skillful at participating in standards development can benefit from this transition, and how participants can best evaluate and participate in standards development organizations. </p>
			<p><span class="subtitle">Editorial: <a href="http://www.consortiuminfo.org/bulletins/jan04.php#editorial" title="Section" accesskey="3">IT  Standards  Come  of  Age</a></span> (January 2004)</p>
			<p><span class="subtitle">Trends: <a href="http://www.consortiuminfo.org/bulletins/jun03.php#trends" title="Section" accesskey="3">Darwin,  Standards  and  Survival</a></span> (June 2003)</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/oct06.php#feature" title="Section" accesskey="3">Participating  in  Standard  Setting  Organizations:  Value  Propositions,  Roles  and Strategies</a></span> (October 2006)</p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 2 &ndash;</span> <span class="subsection"><a href="#chapter2" title="Section" accesskey="5">Process: Standards Developers and How They Participate</a></span>
				<br />Affecting the final form of standards can often have great strategic value to vendors, in part because of the proliferation of patents in many areas of standardization. The result has been a rise in gamesmanship in standard setting, the simultaneous launch of competing standards, outright &ldquo;standards wars&rdquo; between feuding vendor groups, and the rise of the &ldquo;patent troll&rdquo; and the &ldquo;submarine patent.&rdquo; The articles in this chapter explore the difference between beneficial &ldquo;standards competitions&rdquo; and truly destructive standards wars; the tension between open source software licensing terms and standards organization IPR policies; what happens when standards participants violate the rules; and the current debate over whether standard setting organizations should permit or require the so called &ldquo;<em>ex ante</em>&rdquo; disclosure of IPR prior to final adoption of a standard.</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/mar06.php#feature" title="Section" accesskey="3">Standards Wars: Situations, Strategies and Outcomes</a></span> (March 2006)</p>
			<p><span class="subtitle">Editorial: <a href="http://www.consortiuminfo.org/bulletins/may06.php#editorial" title="Section" accesskey="3">Meeting  in  the  Middle</a></span> (May 2006)</p>
			<p><span class="subtitle">Story Update: <a href="http://www.consortiuminfo.org/bulletins/sep06.php#update" title="Section" accesskey="3">FTC  Reverses  Itself,  Finding  That  Rambus  Created  an Unlawful  Monopoly</a></span></p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/jun06.php#feature" title="Section" accesskey="3"><em>Ex  Ante</em>  Disclosure:  Risks,  Rewards  Process  and  Alternatives</a></span> (June 2006)</p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 3 &ndash;</span> <span class="subsection"><a href="#chapter3" title="Section" accesskey="5">Outcomes: Standards Development and the End User</a></span>
				<br />While the accredited standards development process recognizes the right of all &ldquo;stakeholders&rdquo; (i.e., all those affected by standards as well as those developing them) to have a say in the standards development process, achieving that goal can be challenging.  The selections in this chapter focus on the impact of disproportionate stakeholder representation, and on the ways in which broader participation in standard setting by all those affected might be achieved over time.</p>
			<p><span class="subtitle">Trends: <a href="http://www.consortiuminfo.org/bulletins/july05.php#trends" title="Section" accesskey="3">Standard  Setting  at  NASA:  an  Interview  with  Paul  Gill</a></span> (July 2006)</p>
			<p><span class="subtitle">Editorial: <a href="http://www.consortiuminfo.org/bulletins/apr06.php#editorial" title="Section" accesskey="3">The Affecting, the Affected and the Internet: Solving the Stakeholder Conundrum</a></span> (April 2006) </p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/apr06.php#feature" title="Section" accesskey="3">Standards  Stakeholders:  Who  Should,  and  Who  Does,  Set  Standards?</a></span> (April 2006)</p>
			<p><span class="subtitle">Trends: <a href="http://www.consortiuminfo.org/bulletins/feb04.php#trends" title="Section" accesskey="3">A Look into the Future:  Introducing the Personal Datasphere</a></span> (February 2004)</p>
			
			<!-- PART -->
			<p><span class="part">Part Two: <br />Standards and Government</span></p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 4 &ndash;</span> <span class="subsection"><a href="#chapter4" title="Section" accesskey="6">Standards and International Trade</a></span>
				<br />Globally adopted standards can create larger markets for domestic goods, but also subject domestic industry to foreign competition.  In response, some interests have at times sought the adoption of global standards abroad to open foreign markets to domestic products, while at the same time erecting standards-based barriers to keep foreign products out of domestic markets. The World Trade Organization, among other mechanisms, has been utilized to secure the benefits of open standards and to prevent the erection of such barriers &ndash; with varying success.  The articles in this chapter examine the decisions being made in China today on these topics, and the ways in which US standards policy might be revised to address modern realities.  The final piece reflects the additional complexities that can intrude upon standards&ndash;based trade policies when cultural identities are at stake.</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/apr05.php#feature" title="Section" accesskey="3">The  Yin  and  Yang  of  China&rsquo;s  Trade  Strategy:  Deploying  and  Aggressive  Standards  Strategy  Under  the WTO</a></span> (April 2005)</p>
			<p><span class="subtitle">Trends: <a href="http://www.consortiuminfo.org/bulletins/jan05.php#trends" title="Section" accesskey="3">Reimagining  a  National  Standards  Strategy</a></span> (January 2005)</p>
			<p><span class="subtitle">Standards Blog: <a href="http://www.consortiuminfo.org/bulletins/oct04.php#blog" title="Section" accesskey="3">Soy  Sauce,  Kimchi  and  the  Golden  Rule</a></span> (October 2004)</p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 5 &ndash;</span> <span class="subsection"><a href="#chapter5" title="Section" accesskey="6">The Role of Government in Standard Setting</a></span>
				<br />Governments wear many hats in the world of standards. All at the same time, they are standards developers through the exercise of their regulatory power, standards consumers when they purchase goods and services, standards influencers when they participate in standard setting and issue procurement requests, and standards policy makers when they facilitate international trade. In this chapter, I contrast the approaches taken by national governments, examine the recent history of standards policy development in the United States, and offer thoughts on how such policies, and government interaction with industry, could be optimized in the future.</p>
			<p><span class="subtitle">Trends: <a href="http://www.consortiuminfo.org/bulletins/apr05.php#trends" title="Section" accesskey="3">Top  Down  or  Bottom  Up?  A  Tale  of  Two  Standards  Systems</a></span> (April 2005)</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/jan05.php#feature" title="Section" accesskey="3">A  Work  in  Progress:  Government  Support  for  Standard  Setting  in  the  United  States:  1980 &ndash; 2004</a></span> (January 2005)</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/aug05.php#feature" title="Section" accesskey="3">Understanding  the  Roles  and  Optimizing  the  Relationships  of  Government  and  SSOs</a></span> (August 2005)</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/feb07.php#feature" title="Section" accesskey="3">The  Role  of  Government  in  ICT  Standardization</a></span> (February 2007)</p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 6 &ndash;</span> <span class="subsection"><a href="#chapter6" title="Section" accesskey="6">Current Events</a></span>
				<br />Standards, government and public opinion are intersecting more frequently as information technology becomes more essential to participation in society, and as environmental challenges require complex international solutions based upon monitoring of emissions and pollutants.  The controversies that can arise when vested commercial interests are challenged by government efforts to protect public documents (quickly taken up by the competitors of the incumbent), and the lack of existing global governmental mechanisms to address environmental issues, are explored in this chapter.</p>
			<p><span class="subtitle">Feature: <a href="http://www.consortiuminfo.org/bulletins/sep05.php#feature" title="Section" accesskey="3">Massachusetts  and  OpenDocument:  A  Brave  New  World?</a></span> (September 2005)</p>
			<p><span class="subtitle">Standards Blog: <a href="http://www.consortiuminfo.org/bulletins/jan06.php#blog" title="Section" accesskey="3">What  Price  Public  Service?</a></span> (January 2006)</p>
			<p><span class="subtitle">Editorial: <a href="http://www.consortiuminfo.org/bulletins/oct05.php#editorial" title="Section" accesskey="3">The  Small  Blue  Sphere</a></span> (October 2005)</p>
			<p><span class="subtitle">Standards Blog: <a href="http://www.consortiuminfo.org/bulletins/apr04.php#blog" title="Section" accesskey="3">Is  Iraq  &ldquo;Another  Viet  Nam?&rdquo;</a></span> (April 2004)</p>
			
			<!-- PART -->
			<p><span class="part">Part Three: <br />Standards and Society</span></p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 7 &ndash; </span> <span class="subsection"><a href="#chapter7" title="Section" accesskey="7">Standards and Everyday Life</a></span>
				<br />Standards are everywhere around us.  But as with so many other aspects of modern life, we are rarely aware of them, or appreciate their importance.  In this chapter, I provide three examples of standards areas of importance, each of which is addressed through a different consensus&ndash;based process, and each of which affects a different area of modern life.</p>
			<p><span class="subtitle">Consider This: <a href="http://www.consortiuminfo.org/bulletins/aug05.php#considerthis" title="Section" accesskey="3">How  do  You  Define  a Wilderness?</a></span> (August 2005)</p>
			<p><span class="subtitle">Consider This: <a href="http://www.consortiuminfo.org/bulletins/sep05.php#considerthis" title="Section" accesskey="3">Clay  Tablets,  iPods  and Evo/Devolution</a></span> (September 2005)</p>
			<p><span class="subtitle">Standards Blog: <a href="http://www.consortiuminfo.org/bulletins/nov06.php#considerthis" title="Section" accesskey="3">A  Level  Playing  Field</a></span> (November 2006)</p>
			<!-- INDEX ITEM -->
			<p><span class="section">Chapter 8 &ndash;</span> <span class="subsection"><a href="#chapter8" title="Section" accesskey="7">Standards in the Broader Sense</a></span> (February 2005)
				<br />Standards come in all types, and can evolve as well as be developed through formal processes.  The less&ndash;regimented standards of the former sort are both pervasive as well as useful, and illustrate the various tradeoffs made in the creation of formal standards as well.  In this chapter I use a few everyday examples to make the point, ranging from electric organs, to political leanings to index funds.</p>
			<p><span class="subtitle">Standards Blog: <a href="http://www.consortiuminfo.org/bulletins/feb05.php#blog" title="Section" accesskey="3">How  Strict  a  Standard?  In  Praise  of  the  Hammond  B&ndash;3  Organ  </a></span> (February 2005)</p>
			<p><span class="subtitle">Consider This: <a href="http://www.consortiuminfo.org/bulletins/jun06.php#considerthis" title="Section" accesskey="3">Standards,  Evolution  and  Open  Source  </a></span> (June 2006)</p>
			<p><span class="subtitle">Standards Blog: <a href="http://www.consortiuminfo.org/bulletins/sep04.php#blog" title="Section" accesskey="3">Predictability  and  Standards  Denial  </a></span> (September 2004)</p>
			
