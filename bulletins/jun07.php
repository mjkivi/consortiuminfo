			<div class="hr"></div><hr />
			<p><em>Clearly, the future (unless we contrive to terminate our own involvement in it) will see us become even more dependent on technology, and therefore on standards.  In some ways, we are addressing this certainty with forethought, but in other ways we are being heedless of the risks inherent in our current course of conduct.</em></p>
