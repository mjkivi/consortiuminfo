			<p><em>Lastly, I focus on the long term.  In the final two articles, I explore both the dangers as well as the promises of the Web.  The risk I identify relates to the reality that as the breadth of digitized historical information increases, we will become more beguiled by the ease of on&ndash;line research.  The inevitable result is this: that which is not converted to digital form will quietly and inevitably tend to disappear from view.  In making this transition, we may carelessly lose all of the culture, literature and history that does not make the transition from written to digitized form.  </em></p>
			<p><em>In the more pleasant category of promises, I celebrate the opportunities for new scientific and cultural revelations and riches that may flow from Web enabled global collaboration.</em></p>
