			<h4>CHAPTER 3</h4>
			<h3><span class="space"><em>Outcomes:</em></span><span class="capSpace"> <br />Standards Development<br />and the End User</span></h3>
			<div class="hr"></div><hr />
			<p><em>When it comes to standards, all are affected, but few are self-chosen to do the actual development.  </em></p>
			<p><em>While the accredited standards development system recognizes the right of all &ldquo;stakeholders&rdquo; (i.e., all those affected by standards, as well as those developing them) to influence the standards development process, achieving the goal of full stakeholder representation can be challenging, due to the cost and effort required to participate in a meaningful fashion, and the lack of interest on the part of some classes of stakeholders (such as most consumers) to do so.  In the non&ndash;accredited system, many consortia try to bring commercial end&ndash;users into their organizations, but with varying success, and only a small number of such organizations seek to address the needs of non&ndash;technical and non&ndash;commercial interests at all.</em></p>
			<p><em>The selections in this chapter focus on this reality and its implications from a number of perspectives.  The first, an interview with the Technical Standards Program Director for NASA, provides a striking example of how end users can be very influential in ensuring that the standards they need are available to them, assuming they are willing to dedicate the resources required.  The next article identifies the various classes of stakeholders that have significant interests in standards, and the levels and manners of their current participation, while the third explores some of the ways that broader participation might be achieved utilizing the Wikis and other Web based tools that are becoming increasingly effective in helping on-line collaboration communities grow.   </em></p>
			<p><em>The final article uses the concept of what I call the &ldquo;personal datasphere&rdquo; to illustrate why it is important for all stakeholders to become more involved in determining which standards are developed and implemented in areas of high individual impact as we become ever more dependent upon information technology.</em></p>
