			<h4>CHAPTER 8</h4>
			<h3><span class="capSpace">Standards in the Broader Sense</span></h3>
			<div class="hr"></div><hr />
			<p><em>The concept of the consensus–developed and consensus–adopted standard is one of the broadest and oldest constructs of human existence, ranging from precise instantiations, as in technical specifications, to more general, but no less important and pervasive tools, such as codes of ethics and conduct. Each is the product of a variously strict or organic consensus process, and each usually operates in the field on a voluntary, opt–in basis. In large part, the value of standards derives from this very consensus – the standard must be reasonably agreeable to all, or it will not become widely adopted.</em></p>
			<p><em>The first standards were prehistoric, and included spoken and sign languages, followed by more abstract tools such as weights, measures and coinage.  In this chapter, I explore several types of standards in the broader sense, each of which has become either broadly adopted, or which can be observed in human behavior.  In doing so, I also offer some observations on why standards work and how, as well as the decisions that we make when deciding what types of standards we wish to deploy.</em></p>
			<p><em>My first example is the Hammond B&ndash;3 Organ.  You may not recognize the name, but you've been hearing the instrument itself all your life, and would recognize its unique sound in an instant.  The B-3 also illustrates the effect of striking balances in the level of detail a standard dictates: every organ has a keyboard and various other attributes in common, but the unwritten, de facto standard of &ldquo;organ&ndash;ness&rdquo; allows endless innovation in design – in contrast to the much stricter standard that dictates the design of a closely related instrument – the piano.</em></p>
			<p><em>My next example turns to both nature and politics.  Evolution is a natural standardization process, by means of which biological design specifications arise and are implemented as species.  So also with the behavioral characteristics that the resulting organisms demonstrate &ndash; including, I suggest, conservative and liberal viewpoints.</em></p>
			<p><em>Lastly, I look to the rather aberrational way in which, as individuals, we often choose not to make use of otherwise perfectly useful standards &ndash; such as calorie-based diets and stock market index funds &ndash; choosing instead to defy reality and the odds in a (usually futile) effort to somehow beat the system.</em></p>
