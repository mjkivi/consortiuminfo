			<a name="chapter1" id="chapter1"></a>
			<h4>CHAPTER 1</h4>
			<h3><span class="space"><em>Goals:</em></span><span class="capSpace"> <br />Standards Developers<br />and Why They Participate</span></h3>
			<div class="hr"></div><hr />
			<p><em>Dimensional, safety and other standards implemented in physical products have been ubiquitous since the nineteenth century advent of railway gauges, screw threads and steam boilers.  In the world of information technology, however, proprietary system vendor strategies have prevailed until recent times, limiting the degree to which interoperability standards have been developed and utilized.   As a result, vendor claims relating to &ldquo;open systems&rdquo; were more often wistful or promotional than factual during the early decades of the information age.</em></p>
			<p><em>With the convergence of information and communications technology and the success of the Internet and the Web, however, the value of global networks has become demonstrable, increasing demand for the types of truly open and interoperable systems that can allow end users to efficiently realize that value.  The most nimble and adaptable vendors have seized the market opportunities offered by this newly networked world, and changed their strategies to answer the call for open systems.  The result has been a dramatic change in not only systems architectures and vendor strategies, but also the realization that (for example) selling services and adopting open source software development practices can provide a less risky business model than developing and selling proprietary products.  </em></p>
			<p><em>The selections in this chapter explore how this transition from proprietary systems to an open&ndash;standards&ndash;based information infrastructure came about, and the competitive advantages that those who are most skillful at participating in standards development can realize.  The last article is a primer on how stakeholders can best evaluate and participate in standards development organizations. </em></p>
