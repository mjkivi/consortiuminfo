			<h4>PREFACE</h4>
			<div class="hr"></div><hr />
			<h3>Thoughts on 50 Issues (and the Future)</h3>
			<!-- ************* -->
			<p>In the Spring of 2002 I decided to build a new Web Site that I would call ConsortiumInfo.org.  The ambitious mission statement I wrote for myself was (and continues to be) as follows:</p>
			<blockquote>The goal of ConsortiumInfo.org is to be the most comprehensive source of information on the Internet regarding standards, standard setting, and open source software, and on the role that these essential tools play in business and society.</blockquote>
			<p>The resource I had in mind would showcase my law firm&rsquo;s and my expertise in setting up and representing standard setting organizations, and would provide a means by which potential clients could, through on-line searches, become aware of our existence and proficiency. Further to that goal, I began by writing a series of long topical pieces that were eventually collected under the heading <a href="http://www.consortiuminfo.org/essentialguide/">The Essential Guide to Standard Setting Organizations and Standards</a> (Russ Schlossbach, an associate with our firm, researched and wrote the very detailed and useful <a href="http://www.consortiuminfo.org/laws/">Law, Cases and Regulations Guide</a> material under the supervision of my partner, Lee Gesmer).</p>
			<p>But what began as a marketing&ndash;oriented effort soon morphed into something of an obsession, providing an outlet through which I could vent my creative energies and share my ideas on a variety of standards-related and societal topics.  As a result, I quickly decided to add what became the most complete list of information and communications technology standard setting organizations on the Web (the <a href="http://www.consortiuminfo.org/links/">Standard Setting Organization and Standards List</a>), and then a <a href="http://www.consortiuminfo.org/news/">News Portal</a> that I update daily, which now hosts over 3,200 articles in its archive.  In order to facilitate research as well as the teaching of standards courses in colleges and universities, I added the <a href="http://www.consortiuminfo.org/metalibrary/">Standards MetaLibrary</a>, which contains the abstracts of over 1,000 categorized articles on standards related topics.  And two years ago I added the <a href="http://www.consortiuminfo.org/standardsblog/">Standards Blog</a>.  Since then, I&rsquo;ve written 315 blog entries (about 600,000 words), and the blog is visited by as many as 100,000 readers a month.  Other site features added during the last five years can be found in the tab bar above and in the detailed <a href="http://www.consortiuminfo.org/sitemap/">Site Map</a>.</p>
			<p>This journal was launched about six months after ConsortiumInfo.org went live, with the delivery of the inaugural issue in <a href="http://www.consortiuminfo.org/bulletins/dec02.php">December 2002</a>.  Since then, I have written another 49 installments, containing altogether 350 editorials, articles and other features, and comprising another c. 600,000 words.  Given that an average length book has 60,000words, these issues could be bound into 10 full&ndash;length books, each of about 200 pages.  Bind up the blog entries as well, and you would have another 10.  Altogether, quite a lot of &ldquo;news, ideas and analysis.&rdquo;</p>
			<p>After this amount of output, it seemed a good time to take stock of what I&rsquo;ve written in the past, and how best to proceed in the future.  Compiling and presenting this anthology issue is one result of that process.  Undertaking an overall re&ndash;assessment of this publication was another.  Accordingly, with this issue you will notice several changes.  Most obviously, you'll see that I've adopted a new and more current presentation that I think provides a cleaner look and a more readable format.    </p>
			<p>You'll also note that this issue is identified as a two&ndash;month rather than a single&ndash;month issue .  Over the past four and a half years I have succeeded in writing 10 to 12 issues a year.  But with the launch of my blog, as well as an increase in speaking and travel obligations, I find that it's necessary for me to cut back to a more realistic and less exhausting schedule.  Beginning with this issue, I will be converting to a bi&ndash;monthly, six&ndash;issue per year schedule.</p>
			<p>The final change that you will notice is that I&rsquo;ve renamed the publication itself, retiring the name <em>Consortium Standards Journal</em> in favor of a new, and I think more appropriate, name <em>Standards Today</em>. The original title was in fact a compromise choice, meant to harmonize with a now&ndash;discontinued publication of my law firm (the <em>Technology Law Bulletin</em>). It quickly became less descriptive, as both the scope as well as the depth of the material I wrote steadily increased. The new name, I believe, is more representative of what readers now find here, and what they can look forward to reading in the future.</p>
			<p>Which brings us back to what you will find in this issue.  Long time&ndash;readers will know that I write about the broader impact of standards on society rather than on the narrowtopic of technical specifications alone.  It is this broader significance that makes the phenomenon of standards fascinating to me, as it meaningfully incorporates economics, history, linguistics, political theory, and much more.  From this perspective, we can see that the pervasive practice of standard setting throughout history is in some sense intrinsic to what it means to be human.  I have tried to capture the relevancy of standards in all their many forms to our lives today in the title to this eBook:  <em>The Standards of Our Times</em>.</p>
			<p>Tracing the common thread of standards as it weaves through humanity&rsquo;s existence, from pre&ndash;history through recorded time and into the space age, offers a fascinating journey that can tell us much about who we are.  My goal in making the selections included in this 50th Issue Anthology is to retrace a few favorite steps on my own journey down that path, and to help readers (old and new) appreciate the relevance of standards to society and their own lives.  I will be very pleased if it achieves that purpose. </p>
			
