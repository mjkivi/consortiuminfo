			<h4>CHAPTER 7</h4>
			<h3><span class="capSpace">Standards and Everyday Life</span></h3>
			<div class="hr"></div><hr />
			<p><em>There are many essential elements in the infrastructure that supports modern life of which most people are blissfully unaware.  One of these is standards, due to the pervasiveness of their use in every aspect of product design, safety, health care, services, and much more.  But standards arise not only in the dry and traditional areas of technical specifications, but in endless other areas as well.</em></p>
			<p><em>In this chapter, I illustrate a few common examples of standards of importance, each of which is developed through a different consensus&ndash;based process, and each of which affects a different area of modern life.</em></p>
			<p><em>The first example comes from the legislative process, which must constantly create technically precise definitions &ndash; standards in their own right &ndash; upon which legislation and regulations can then be based.  The example I choose to illustrate this area of standardization is the federal definition of &ldquo;Wilderness.&rdquo;</em></p>
			<p><em>In my second example I illustrate the importance of considering all design requirements before settling on an important standard.  To make this point, I turn to the iPod &ndash; that wonder of modern technology that allows us to effortlessly download, carry in our pockets, and listen to volumes of music that would have been unimaginable only a few years ago.  The format used to store this music, however is proprietary, and the device itself is fragile, and follows on the heels of many now obsolete and largely abandoned recording technologies.  Will iPod&ndash;based electronic formats be as accessible in five thousand years as documents based on another format and vehicle &ndash; the clay cuneiform tablet &ndash; are today?  The answer is more obvious than the reason why we assign so little importance to data survival and future access in product &ndash; and standards &ndash; development today.</em></p>
			<p><em>And finally, I turn to sports to illustrate how universally standards of all types are needed to establish a level playing field (both metaphorically and otherwise) for competitive sports.</em></p>
