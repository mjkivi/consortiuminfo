			<div class="hr"></div><hr />
			<p><em>The use of standards is integral to the concept of international trade, in both the negative as well as the positive sense.  When uniform standards are globally adopted and implemented, worldwide markets are created, as well as worldwide competition for goods and services.  The positive effects for consumers include broader choices and lower prices.  But those same effects can place domestic industry &ndash; and the jobs of consumers &ndash; at a disadvantage to foreign industries with lower fixed labor and other costs.</em></p>
