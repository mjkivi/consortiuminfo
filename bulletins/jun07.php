			<h4>CHAPTER 5</h4>
			<h3><span class="capSpace">The Role of Government in Standard Setting</span></h3>
			<div class="hr"></div><hr />
			<p><em>Governments wear many hats in the world of standards.  All at the same time, they are standards developers through the exercise of their regulatory power in areas such as public safety (sometimes creating standards, and sometimes referencing private industry standards), standards consumers (when they utilize standards in contracting and purchase off the shelf standardized products), standards influencers (when they participate in standard setting, and when they select which standards to specify in procurement), and standards policy makers (in international trade).</em></p>
			<p><em>While these roles are common to governments all over the world, different governments have adopted different strategies in practice, ranging from directing standards development on a national basis to leaving the creation of standards primarily to the private sector.  Similarly, some governments have integrated standards closely into national trade strategies, while others have been primarily responsive to calls for assistance from industry when commercial interests cannot achieve their goals without government support.</em></p>
			<p><em>In this chapter, I first review the differences in policy adopted by two nations with diametrically opposed standards strategies: China and the United States.  In the next, I review in detail the substantial changes that the US federal government has made in its approach to standards development and usage over the past quarter century.  In the next article, I focus more particularly on the interaction of the US government and information and communications technology during the same time period.  And in the fourth and final article, I turn from the factual to the potential, and examine how industry and government might best work together in the US to their mutual advantage in the future. </em></p>
