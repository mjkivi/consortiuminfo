			<a name="chapter2" id="chapter2"></a>
			<h4>CHAPTER 2</h4>
			<h3><span class="space"><em>Process:</em></span><span class="capSpace"> <br />Standards Developers<br />and How They Participate</span></h3>
			<div class="hr"></div><hr />
			<p><em>There was a time not so long ago when standard setting was a largely collegial and leisurely process that was engaged in by standards organization member representatives who grew to know each other well.  As standards became more essential in the information and communications technology industries, however, they also came to have greater strategic value.  With that value has come the opportunity for standards development participants to gain advantage over their competitors through influencing the specific standards, and standards elements, that are finally adopted and implemented.</em></p>
			<p><em>At the same time, the number of patents awarded in many areas of standardization has grown exponentially.  The result is that patent owners may at some times wish to withhold the right to license &ldquo;necessary claims&rdquo; under their patents from those that wish to implement a standard, and at other times may actively seek to ensure that such licenses will be needed, due to the substantial royalties and strategic benefits that can be gained when a patent would be infringed by an important standard.</em></p>
			<p><em>The results of these trends include more gamesmanship in standard setting, the simultaneous launch of competing standards, outright &ldquo;standards wars&rdquo; between feuding vendor groups, and the rise of the &ldquo;patent troll&rdquo; (i.e., a non-vendor patent owner that seeks only to license, and not implement, its patents), and the &ldquo;submarine patent&rdquo; (a patent revealed, often by a patent troll, only after the market has become &ldquo;locked in&rdquo; to the standard that infringes the patent).  Not surprisingly, these behaviors have led to a desire among some vendors to flush out potentially significant patent claims (usually referred to as intellectual property rights, or simply IPR) early in the standards development process.</em></p>
			<p><em>This chapter highlights these issues in depth.  The first article explains the difference between beneficial &ldquo;standards competitions&rdquo; and truly destructive standards wars, and the strategies that standards development participants use in navigating these waters.  The second explains a current hot topic in standard setting: whether or not standard setting organizations should adopt policies that would permit or require the so called &ldquo;ex ante&rdquo; disclosure of IPR prior to final adoption of a standard.  The third article provides an update and a historical review of the most-watched litigation of the decade involving standards development: the private suits and Federal Trade Commission prosecution of semiconductor design company Rambus Incorporated, each of which alleged the &ldquo;gaming&rdquo; of the technical process by Rambus.  The final article focuses on another challenge that the standard setting process is grappling with today: the increasing popularity of open source software licensing and development, and the difficulties inherent in reconciling the demands of this process with the IPR policies needed to make the standards development process work.</em></p>
