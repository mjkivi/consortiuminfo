                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><strong><em>I. Introduction</em>:</strong> &nbsp;</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">One of the cardinal frustrations of evolutionary scientists is the inability to speed up the evolutionary clock in order to observe species changes in process. In fact, the Holy Grail of anyone in this field is to stumble upon and document a new species in the process of branching off of an existing one. Such a quest is more realistic for those who actively observe the standards world, since the pace of change is infinitely more rapid. If fact, an example of &quot;standards process speciation&quot; has recently occurred with the advent of the open source community solution for some types of interoperability issues. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Of course, the more pervasive manifestation of evolution is gradual adaptive change - the less dramatic series of adjustments resulting from the fact that those individuals that are best equipped to play at the game of life are most successful in passing their genes along to their progeny.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Commercial entities are captive to the same dynamics, but with a difference: if they can figure out the course of the future better than their competitors, they will be more likely to thrive and pass increased value along to their shareholders. One of the few ways to glimpse - and even influence - some aspects of the future is through the exercise of an active, informed and coherent standards strategy. Given that reality, it is surprising that there has been comparatively little study of standard setting. Hence, there is little broad understanding of its dynamics, and a failure by many companies to fully exploit the competitive advantages that a carefully crafted standards strategy can secure. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">In the <a href="#survey">preceding article</a>, we report on the results of a detailed survey completed by three companies that are heavily invested in the standard setting process. While the number of respondents to the survey is not statistically meaningful, the companies involved are major participants in the standard setting process and their influence is significant (both by their participation in many organizations at the Board level, as well as by example). As a result, a number of interesting and useful observations can be drawn from their responses to the survey. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">And more intriguingly, a number of recommendations and predictions can be extrapolated from the same data, when conjoined with first-hand experience. These recommendations can assist IT companies be more successful in the Darwinian struggle for success.</font></p>
                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><strong><em>II. Observations and Predictions</em></strong></font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">If one rises above the operations of any individual SSO or participant and looks at the reality of standard setting today, some clear trends and their probable future courses begin to emerge. Here are some of the more significant:</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>The level of central control over standard setting participation is not great at many companies, but will increase</em></strong>. Despite the level of importance of standard setting to technology companies, and the risk that valuable IPR may be compromised by participation, oversight of participation has not historically been strict at many companies. However, oversight today is much more centralized than just a few years ago, as a result of a number of well-publicized IPR cases involving participation and conduct in standard setting. Once a centralized review process has been put in place for IPR purposes, its capabilities can be extended in other ways to bring greater coherence and efficiency to a company-wide standards strategy. Companies would be wise to make use of such enhanced capabilities.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>The focus on IPR policies will become even greater. </em></strong>While the trend toward tighter supervision over IPR policies will certainly continue, achieving meaningful internal control over participation by company representatives in SSOs indicates a significant challenge, especially for large corporations with operations in many countries. Due to the practical impossibility of day-to-day legal supervision of hundreds (and even thousands) of engineering staff that act as representatives in myriad SSOs, the only pragmatic way to avoid jeopardizing valuable IPR is to focus on an SSO's IPR policy - as well as its process mechanics - in order to lower the risk inherent in substantially unmonitored participation. Policies which are poorly articulated and which do not require clearly documented assertions regarding IPR, for example, present a higher potential for errors and disputes.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Vendors disproportionately participate in - and therefore disproportionately control - many SSOs (and especially consortia). This may have a negative impact (for all concerned). </em></strong>Not only do vendors tend to join more SSOs, but they often join at higher membership levels and consequently pay the lion's share of the budgets of such SSOs through their dues. In many organizations, they also take the lion's share of the board seats. Even if there is no actual intention to weight results in favor of vendors, there can be a negative impact due to lack of diversity of interest and experience in the high-level decision making that charts the SSO's course. Vendors would therefore be well advised to welcome diversity of members and opinions rather than exploit their numeric and economic influence. Otherwise, the standards offered to the market by the SSO may be unattractive to other vital constituencies, and fail to become widely adopted.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Only a subset of companies actively starts new SSOs. As a result, these companies are likely to be more influential in standard setting and enjoy a competitive advantage</em></strong>. It is a simple truth that the founders of a new SSO will usually have a disproportionate impact on the initial work product than those that come afterwards. Hence, even if influencing the final work product is not a goal, that impact will likely nonetheless exist. This will not always be good. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Accredited SSOs and consortia can both be &quot;gamed.&quot;</em></strong> It is necessary to thoroughly understand how an organization works prior to joining in order to judge how &quot;open&quot; it truly is. And after joining, it is even more necessary to understand how an SSO works in order to be aware of - and protest - actual abuse. Those companies that train their participating personnel most thoroughly will be able to defend their interests more effectively.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Even companies that make huge financial and human resource commitments to standard setting participation often do not follow a rigid process for vetting participation.</em></strong> This manifests itself not only at the application stage, but throughout the period of membership. Not infrequently, through personnel changes, a company &quot;forgets&quot; that it is a member of an organization, ceases participating, and yet may even pay up when it receives a membership renewal notice. The result is not only financial inefficiency, but also loss of opportunity.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Despite the proliferation of SSOs, IT companies still find that they need to start new ones.</em></strong> This is in some ways a commendable as well as a regrettable condition. One of the reasons that consortia became popular some 20 years ago was because a market that evolves as rapidly as the IT space requires rapid responses. By evolving an alternative process in addition to the already established accredited organizations, industry was able to react more flexibly and quickly, when nimbleness and speed were needed. However, forming a new organization has its own risks: starting up takes time and money, and the results are dependent on multiple variables, including the success of recruitment, the ability to set an effective process in place, and avoiding proprietary influences. There is much to be said for the existence of organizations such as W3C, OASIS and OpenGIS, which host (and welcome) many processes under one roof that are consistent with an over-arching and coherent strategic mission.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>IT standards are becoming more effective and important. </em></strong>With the advent of the Internet and technologies such as wireless, an increasing number of products are becoming dependent upon available, timely and effective standards. Those companies that have an Office of Standards are more likely to be able to realize such realities, articulate them to decision makers, recommend effective actions, and supervise execution, thus conveying a competitive advantage over companies that are less standards-aware.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>The importance of standard setting is not sufficiently recognized at the institutional level. </em></strong>The fact that most companies do not include standard setting as a budget line item may indicate that its value is insufficiently understood at the management level. This may be due to cultural factors, such as the separation between engineering and sales (and the fact that many peoples' eyes glaze over at the mere mention of the word &quot;standards&quot;). Institutional recognition of the importance of an effective standards strategy, however, is an essential first step towards mounting a coherent competitive plan.</font></p>
                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><strong><em>III. Recommendations to Companies </em> </strong></font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">The observations above suggest that those who most actively participate in standard setting do so to gain strategic advantages. If it is true that pursuing an effective standards strategy can secure strategic advantage (and the major commitments by leading IT vendors to standard setting indicates that they believe this to be so), then those of their competitors who are less active or skillful in this area may be expected to suffer. This should be reason enough for those companies that have not incorporated standards participation into their strategic plan to consider such a course of action. The following are some specific recommendations to consider when performing such an evaluation.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Recognize at the corporate level that effective standards participation is mission critical, and incorporate standards into overall strategy. </em></strong>In order to compete effectively, standards need to be integrated into central strategy, rather than be relegated to the status of an engineering-level pastime. Once standards planning is included in strategy, it is highly advisable to create a centralized standards office to effectively coordinate and manage disparate standards efforts in order to reap maximum advantage.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>If standards in a given area are mission critical, then allocate resources accordingly.</em></strong> While a small company cannot afford a Standards Office, a large one can. (A small company can still retain knowledgeable advisors to assist its standards efforts, however.) Participation in standard setting fulfills many functions: influencing outcomes, gaining advance knowledge of important standards, gaining access to training for key personnel, and so on. The proliferation of regional, as well as national, SSOs has made the game more complex, and experienced, dedicated staff is needed to understand how the many pieces fit together. Such individuals are in very short supply, but there is no substitute for hands-on experience.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>The level of experience of standards participants is often not high.</em></strong> Hiring, training and deploying skilled personnel to participate in SSOs therefore represents a competitive advantage. This opportunity exists at the macro level (in setting overall corporate standards strategy), as well as the micro level (in technical committees and on SSO boards) by influencing outcomes. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Those companies that do not favor forming new SSOs should consider becoming early members of new SSOs that they believe are likely to succeed.</em></strong> This recommendation stems from the observations that those who do form SSOs have the advantage of setting its technical agenda and are likely to disproportionately control the SSO for some period of time. Once such a group of companies has forced the issue by starting a new SSO, it may be strategically prudent for the founding companies' competitors to join as quickly as possible in order to ensure the neutrality of the organization and its eventual output. Companies that do not normally favor starting new SSOs would therefore be wise to actively monitor the activities of companies that are known for forming new SSOs, and act promptly in technical areas where their strategic interests are at stake. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Companies should make it known that they are willing to be founding members of new SSOs.</em></strong> This is the logical extension of the same precept. Most new SSOs go through a pre-public planning phase. During that phase, they will commonly agree upon the governance structure, and perhaps the IPR policy as well, of the new organization. There is therefore a strategic advantage in being invited to become a founder in order to influence these outcomes, even if forming the new SSO would not otherwise be deemed desirable. It is better to be invited to the table at the earliest stage and be able to make an early assessment of the likely success or failure of the organization, than to be forced into a reactive, less influential posture after the new organization is announced.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Approval of joining and renewing memberships should be subject to more strict central approval.</em></strong> At present, many companies err on the side of decentralized business (and even legal) approval for SSO participation. While this is sensible in that management of the business unit involved may be best able to understand the advantages of membership, it makes it more difficult to weigh alternatives when opportunities exceed budgets, and may result in an incoherent (and even internally competitive) overall standards program. While the common practice of combining central approval based upon the recommendation of a senior &quot;champion&quot; provides a partial solution to this problem, a more complete solution would include centrally created and distributed parameters and criteria for participation, conjoined with a standard evaluation schema against which opportunities can be measured. While too mechanical an application of such a schema would be unwise, using a checklist as a tool to effectively present and evaluate opportunities should improve overall results. A shorter form with a similar purpose should accompany renewal applications. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>With the increasing importance of standards, the costs of non-participation are becoming higher.</em></strong> In order to be competitive, end-users as well as vendors need to be increasingly knowledgeable about new standards that affect their enabling technology, even if they are less interested in influencing outcomes. Participation in many SSOs can be useful in advance planning and effective training.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Markets can be not only created but accelerated through collaboration.</em></strong> Increasingly, new product and service opportunities are dependent on not only the development of standards, but on the rapid adoption of those standards. SSOs can be effective on both fronts. The cost to a founder of forming a new SSO - even as a significant sponsor - is often trivial in comparison to the opportunity.</font></p>
                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><strong><em>IV. Recommendations to SSOs</em></strong></font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Recruitment pays. </em></strong>Many under-resourced consortia invest comparatively little effort in seeking members. However, the indications of this survey are that even the largest corporations can be recruited by effective efforts. Accordingly, allocating funds to recruitment (including using dedicated staff or third party service providers) may be highly cost effective.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>IPR policies are crucial.</em></strong> An SSO today cannot afford to have an out-of-date IPR policy. Not only may a defective policy result in commercial disputes, but it will make recruiting and retention of the most active members difficult, or even impossible.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Neither SDOs nor Consortia are &quot;better.&quot;</em></strong> Members care about results, and not labels. SSOs of all types need to understand - and serve - their customers. Like any other commercial enterprise, they cannot do so unless they thoroughly understand the customer and its needs.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Remember that management members are employees and not owners.</em></strong> SSOs should be about service, and not self-perpetuation. While there is nothing wrong with creativity, entrenchment is invariably a negative. An SSO needs to be able to dispassionately tell the difference between prudently adapting to meet evolving market and technical realities and a reluctance to merge or disband when its day in the sun has passed.</font></p>
                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><strong><em>V. Conclusions</em></strong></font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Socrates is said to have once famously observed that: &quot;The unexamined life is not worth living.&quot; Given the importance of standard setting to the modern commercial world, it is curious that there is as little examination of the process of standard setting as there appears to be. When one observes the degree of activity and commitment of the largest technology vendors to the standards process, it becomes evident that an IT player that does not examine (or worse, even have) a standards strategy may eventually find that it no longer has a commercial life to live.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">While such a statement may seem to smack of hyperbole, consider this: the faster the pace of technological change, the narrower the margin for error, the greater the reward to those who guess right, and the more severe the economic punishment of those who guess wrong. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Corporate managers would do well to recall their Darwin. Standards strategy is too important to survival to be left to the engineers alone.</font></p>
                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><strong><em>VI. Further Reading</em></strong></font></p>
