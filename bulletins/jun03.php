                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><em><strong>I. Introduction</strong></em>: &nbsp;</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">If you were to create a database of the members of standard setting organizations (SSOs), you would find that some large IT companies are members of an astonishing number of such collaborative efforts - 150 or more. You might then want to ask questions like: How do IT companies like these decide in which organizations to become involved, and how do they monitor whether to stay involved? What can other companies learn from their more heavily invested peers, and what can the standard setting organizations themselves learn from these existing and potential members? </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">In order to find out the answers to these questions, we turned to the Directors of Standards of three of the most active participants in all types of SSOs. The companies surveyed include two of the most influential IT vendors in the world (<em><strong>Hewlett-Packard</strong></em> and <em><strong>Sun Microsystems</strong></em>) and, for balance, a non-technology vendor with over $40 billion in 2002 revenues whose businesses are heavily dependent on technology, but is not itself principally a technology vendor. [The non-technology vendor company requested that its name not be disclosed, and is referred to below as &quot;Company #3&quot;]. </font></p>
                  <p align="left"><font size="3" face="Arial, Helvetica, sans-serif"><em><strong>II. Survey Methodology</strong></em></font> </p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Instructions</em></strong>: The survey questions were intended to address several areas of inquir and were grouped accordingly. Each respondent was permitted to decline answering questions where confidentiality or other concerns arose, but all three respondents answered almost every applicable question. Precision in numerical data was not required where exact data was unavailable or unduly burdensome to access.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Survey Participants</em></strong>: Each company respondent has recognized the importance of standards and of coordinating its standard setting activities by appointing a Director of Standards, and each gave its Director of Standards permission to respond to the survey. The individual respondents are some of the most experienced standards experts in the IT industry; each has decades of experience in the world of technology and standard setting and speaks from a depth of experience gained through participation in many of the industry's most important SSOs. They are:</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Hewlett-Packard</em></strong>: <strong>James Bell</strong> is the <em><strong>Director of Standards and Industry Initiatives</strong></em> for Hewlett-Packard Company. In that capacity, he is responsible for leading the development and execution of a focused strategy for effective, coordinated participation by HP businesses in industry consortia and standards organizations. He also represents HP on the Governing Board of The Open Group and serves on the Advisory Council and Advisory Board of the WorldWide Web Consortium. During 1995 and 1996, he served for fifteen months as President and CEO of X/Open, President and CEO of the Open Software Foundation, and after their merger, President and CEO of The Open Group.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>Sun Microsystems</em></strong>: <strong>Carl Cargill</strong> is Sun's <em><strong>Director of Standards</strong></em>, and manages Sun's standardization strategies, activities, and portfolio. He has been directly involved in standardization for nearly twenty years, and has written widely (including two books) on standards topics. He has also testified before Congress on the topics of consortia in standardization and before joint hearings held by the Federal Trade Commission and the Department of Justice on the necessity for Royalty Free IPR rules for consortia. Among other standards organization positions, he serves on the W3C Advisory Board and the Board of Directors of the Open GIS Consortium, and is the Chairman of the Governing Body of The Open Group.</font></p>
