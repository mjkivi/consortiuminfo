                    <p><font size="2" face="Arial, Helvetica, sans-serif"><strong> <font size="3">WHAT STANDARDS ORGANIZATION SHOULD I JOIN?</font><br>
                            <br>
                            <a href="http://www.gesmer.com/attorneys/updegrove.php">Andrew Updegrove </a></strong></font></p>
                    <table width="54%" height="22" border="0" align="center" cellpadding="0" cellspacing="0">
