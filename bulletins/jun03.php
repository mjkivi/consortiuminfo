                    <p>&nbsp;</p>
                  </div>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">How important are standards? Anyone from a plumbing inspector to an IT director would readily answer &quot;Very.&quot; Go down one level deeper into the subject of standard setting, and several more intriguing questions will suggest themselves: How important is participating in standard setting? And to whom? And why?</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Judging by the hundreds of technology standard setting organizations in existence and the thousands of companies that choose to join them, the answer to the first of these questions (How important?) would seem to be obvious: &quot;Very,&quot; once again. Divining an accurate response to the next question (To whom?) requires a bit more effort, although with enough time and patience, one could compile a database by simply visiting enough consortium and SDO websites and logging in the membership lists posted there. Answering the last question (Why?), however, requires going directly to the source. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Just why exactly do companies (as well as government agencies, academic institutions and others) participate in IT standards groups? And what is the answer to one last question - how do they decide in which specific organizations to participate?</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">The largest IT companies participate in scores - and sometimes hundreds - of standard setting bodies. The investment of such companies in dues, travel budgets, dedicated personnel and internal management costs can run well into 8 figures. Smaller companies may be involved in fewer organizations, but their investment - both in cash as well as in human resource terms, and the strategic consequences of the standards-based decisions that they make - can be just as significant.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Even the largest IT company is not likely to join every available organization. And even if its resources permitted it to do so, there are strategic decisions to be made where more than one alternative is available - at this time two years ago, should an IBM or an HP have decided to support Bluetooth, or HomeRF or Wi-Fi (or, hedging its bets, all three)? Inevitably, solving this dilemma would have involved guessing the answers to additional questions, such as: Which approach or group is most likely to succeed? Which technology would provide the best &quot;fit&quot; for the potential member's existing products and services? Which other companies (both friend and foe) have endorsed which endeavor? And so on.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">In this issue of the CSB we look at how companies should evaluate which organizations to join, and also report on how some of the largest IT companies actually go about the process. The survey completed by the organizations confirms what we have observed over time - that there is less centralized direction in evaluating standard setting participation than one might expect. Inevitably, this leads to more questions: Is a lack of a centralized business evaluation process a good thing, permitting quick intuitive reactions to a rapidly developing marketplace? Or is it simply the case that even companies that have made a strong commitment to standard setting have not yet gotten around to bringing order to the process of evaluating them? And, in actual practice, does it turn out to make any difference?</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Today, there are few answers to these questions. While there is anecdotal information available, to this author's knowledge there is little or no empirical data on questions such as why specific types of companies join specific organizations; whether companies are consistent in their evaluative techniques; and whether using one set of selective criteria over another is likely to yield a better long-term result. If more such information were available, two positive results could be achieved: companies could more efficiently and effectively engage in standard setting activities, and the organizations in which they participate could better tailor their offerings to the members that they serve.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Today, there is also no central location for such information, nor any general venue within which best practices can be discussed in a manner not specific to a single technology.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">A founding goal of the <em><strong>Consortium Standards Bulletin</strong></em> and its companion resource site, <a href="/">ConsortiumInfo.org</a>, is to obtain and aggregate information about how standard setting is done, in order to help develop and share the type of &quot;best practices&quot; that will make the global standard setting infrastructure itself more efficient and productive. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">One way of collecting the information that can lead to such best practices is through surveys. In this, our first year of publication, we have presented a number of surveys: examining which organizations should set web services standards (<a href="/bulletins/may03.php#featured"> The Role of Web Services Standards Bodies: In Their Own Words</a> ), reviewing what types of news consortia release - and how much of it is really &quot;news&quot; (<a href="/bulletins/mar03.php#featured">How Consortia Tell the News: A Survey of Over 150 Consortia and SDOs</a>), and in this issue, how major companies evaluate which organizations to join (<a href="#featured">SURVEY: MAJOR STANDARDS PLAYERS TELL HOW THEY EVALUATE STANDARD SETTING ORGANIZATIONS</a>).</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">We hope that you find these surveys interesting and useful. If so, you may find that you wish that more respondents had contributed data to the effort. If you are involved in standard setting at your company or in a consortium and would be willing to participate in future surveys, we would welcome your assistance. The greater the participation, the greater the value of the results that we can report back to you.</font><font size="2" face="Arial, Helvetica, sans-serif"> </font></p>
