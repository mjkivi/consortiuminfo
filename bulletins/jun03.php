                        <td><div align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><em><strong>Abstract: </strong>Thousands of companies are members of hundreds of standard setting organizations (SSOs). The methods such companys use for selecting these organizations are largely a mystery to those outside this small circle and, in fact, there seems to be little centralized order to the process that some employ. Yet companies often make significant investments in standard setting, with costs to the most active players running well into 8 figures annually. Making more data available regarding &quot;best practices&quot; decision-making would help all standard setting participants select organizations more wisely and gain the most from their memberships. At the same time, providing more information about member needs and selection methods would permit SSOs themselves to recruit more successfully and provide more effective - and valued - services.</em></font></div></td>
