                    <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>A. <u>What is your level and manner of participation?</u></em></strong> While all three organizations are heavily invested in standard setting, both of the technology vendors participate far more heavily (by a factor of five) over the non-technology vendor (HP and Sun each being involved in 150 SSOs or more, while Company #3 is involved in only 25 to 30). This should not be surprising, since technology vendors need standards to create product opportunities, while non-technology vendors most often join SSOs in order to influence standards development for their particular businesses and learn how to deploy standards effectively. Such end-users of standards are also less likely to be interested in promoting the adoption of standards. </font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Both Sun and HP are frequently recruited by SSOs soliciting their financial and technical support, receiving as many as one serious solicitation per week (HP: approx. 40 per year; Sun: 1 per week average). These solicitations appear to be productive for both sides, since the likelihood of success in the recruiting effort is relatively high (HP gives serious consideration to about 20 SSOs a year, while Sun takes an active interest in about 12). In fact, HP is recruited by almost every organization that it joins (unless it is a founder). Sun, on the other hand, identifies about 50% of the organizations that it chooses to join before it is solicited, indicating a more proactive approach. Despite the size and prominence of Company #3, it joins SSOs as a result of solicitation only 15% of the time, indicating that SSOs may be neglecting recruitment opportunities in the end-user community (See responses to question C.3).</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">The likelihood of success in recruiting a given company, however, relies in part on its philosophical beliefs about how standard setting should be conducted. Both HP and Sun are responsive to newly formed SSOs, while Company #3 prefers to work with existing SSOs, in order to limit the proliferation of new, untested (and possibly redundant) organizations. This difference in opinion also surfaced when the questions turned to founding new SSOs. Both HP and Sun actively participate in founding new SSOs (HP: about 15 a year; Sun: about 3, with this number likely to rise in the future), and each is considering starting several new SSOs at this time. Company #3, on the other hand, is not likely to help found a new organization.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">What leads a company to tackle the cost, effort and uncertainty of founding a new SSO, rather than look to an existing organization? Bell cites the need for &quot;Focus, speed, and seeking members with shared objectives.&quot; Cargill offers three reasons: first is a bias against accredited SSOs (an opinion not expressed by the other two respondents), which he feels often compromise the integrity of results in order to achieve consensus. The second is the lack of an existing organization able, or willing, to undertake the project. He states the third reason candidly:</font></p>
                  <blockquote><p align="left"><font size="2" face="Arial, Helvetica, sans-serif">You can start an SSO to counter another SSO - not a nice thing, but sometimes politically expedient. This leads to standards wars, which can be ugly, however, and therefore this is a last resort. </font></p>
                  </blockquote>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Finally, Cargill notes a fourth alternative, which has become increasingly popular: starting an open source project, with no formal consortium structure at all.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>B. <u>What types of organizations do you join?</u></em></strong> All three organizations join all types of SSOs. Each participates in both consortia as well as accredited organizations, and each is involved in national, regional, and global organizations. Sun, for example, participates in both European and Asian regional organizations, as well as in accredited national bodies in Britain, Canada, Norway, Sweden and Ireland. Europe's strong efforts to create standards on a regional basis was clearly reflected in a question asking the respondent to give &quot;notable examples&quot; of regional bodies in which the respondent companies participate: Six European bodies were named, and three US organizations.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong><em>C. <u>How do you make your decisions procedurally?</u></em></strong> While each company has centralized the process of participation approval to a degree, there are many variations in how the process is carried out. In the case of HP, business approval is needed before an application is considered by the standards office. At Sun central clearance is required, but Cargill indicates that it is difficult to enforce this rule. He estimates that 30% of the applications for new memberships escape central review. Like HP, the business decision to participate is made first (at the business unit level) before the approval of the central standards office is sought. At Company #3, new guidelines for evaluating SSO membership applications are now being reviewed. The guidelines &quot;emphasize the need for a clear business case, management approval, legal review, and Standards Office coordination.&quot;</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">None of the three organizations have an overall policy guiding the types of organizations to join (e.g., by business area, type of organization, etc.), nor any formal checklist against which each organization is judged. At Sun, for example, the consideration of a new application by the standards office occurs on a case-by-case basis, with any identified concerns being resolved through discussion.</font></p>
                  <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">All three organizations indicate the need for an application for membership to be supported by a &quot;champion&quot; in order for it to be approved. At Sun, that champion usually needs to be a Vice President or a Senior Technical Fellow, representing an informal, but nonetheless effective, internal filtering process as a precondition for central approval.<br>
