                    <p align="left"><strong> II. About the Standards MetaLibrary </strong></p>
                    <p align="left"><a name="ii1"></a><strong>1. What is a MetaLibrary? </strong></p>
                    <p align="left">Here is one particularly apt definition: </p>
                    <p align="left"><strong><em> Metalibrary:</em></strong><em>An (abstract) metalibrary is the entire collection of a society's data, information, and techniques, together with the means by which it is stored, accessed, and communicated. (Rick Sutcliffe, The Fourth Civilization, 1998)</em></p>
                    <p align="left"> Just as &ldquo;meta information&rdquo; is &ldquo;information about information&rdquo; and not useful data in its own right, and &ldquo;metatags&rdquo; are hidden codes that help you find a webpage with what you&rsquo;re looking for, a &ldquo;metalibrary&rdquo; is a means to an end, and not the end in itself. Instead, it is a very targeted and useful tool for finding specific information. As the volume of material on the Web continues to expand faster than the power of search engines, on-line metalibraries will become more and more useful. </p>
                    <p align="left"><strong> <a name="ii2"></a>2. What is the subject matter of the Standards MetaLibrary? </strong></p>
                    <p align="left"> Not surprisingly, the Standards MetaLibrary is concerned with standards, but not in a narrow sense. In fact (for now, at least), the Standards MetaLibrary does not contain technical articles at all. Instead, it focuses on the importance of standards to the modern world and their impact on society, and how they are created, and by whom. Why? Because just about everything we own or use, from cell phones to light bulbs, is affordable and usable rather than expensive and useless because of standards. There are over a million supported standards today, created by many hundreds of non-profit standards development organizations. </p>
                    <p align="left"> The materials that we index in our MetaLibrary therefore are concerned with how standards are set, how governments support them, the economic benefits of standards, legal aspects of their use (and abuse), and many other topics that can help us study the role of standards in the world today. </p>
                    <p align="left"><strong> <a name="ii3"></a>3. What is the purpose of the Standards MetaLibrary? </strong></p>
                    <p align="left"> We believe that standards and standard setting are crucial to the modern world for a broad variety of reasons. Most obviously, they are necessary to make things work. Today, standards enable not only mechanical devices, electronics and telecommunications, but also revolutionary new networks such as the Internet and the Web &ndash; unprecedented tools that for the first time can bring knowledge and opportunity directly to the isolated and the poor everywhere around the world. Less obviously, the creation of voluntary, consensus standards is an example of how humanity can work together for the common good &ndash; a capability that is all too rarely demonstrated, but always sorely needed (See: <a href="http://www.consortiuminfo.org/bulletins/dec04.php#editorial">A Seasonal Standards Message</a>) </p>
                    <p align="left"><strong> <a name="ii4"></a>4. Why was the Standards MetaLibrary created? </strong></p>
                    <p align="left"> The Standards MetaLibrary is the latest module of the <a href="http://www.consortiuminfo.org/">ConsortiumInfo.org website</a>. Our hope is that the Standards MetaLibrary will not only facilitate, but also encourage outcomes such as: </p>
                    <div align="left">
                      <ul>
                        <li> The serious study of the importance of standards </li>
                        <li> The creation and promotion of &ldquo;best practices&rdquo; in standards creation </li>
                        <li> The proliferation of course offerings in academia on standards and their creation </li>
                        <li> Greater understanding of standards and standard setting in government, resulting in more coherent and coordinated support of standards </li>
                      </ul>
                    </div>
                    <p align="left"><strong> <a name="ii5"></a>5. What is ConsortiumInfo.org? </strong></p>
                    <p align="left"> ConsortiumInfo.org is the most detailed and comprehensive resource on (or off) the Internet on the topics of standards and standard setting. All resources at the site are free and available to all visitors, and are provided as a public service by their author. The site includes a wealth of features and information, including: </p>
                    <div align="left">
                      <ul>
                        <li> The <strong><em><a href="http://www.consortiuminfo.org/essentialguide/">Essential Guide to Consortia and Standard Setting</a></em></strong>, a detailed overview of all important aspects of standard setting </li>
                        <li> The <strong><em><a href="/news/">Standards News Portal</a></em></strong>, which is updated daily </li>
                        <li> The <strong><em><a href="http://www.consortiuminfo.org/links/">Standard Setting Organization and Standards List</a></em></strong>, which is a regularly updated &ldquo;MetaList&rdquo; of over 450 standard setting organizations with descriptions of, and links to each organization </li>
                        <li> The <strong><em><a href="/bulletins/">Consortium Standards Bulletin</a></em></strong>, a free monthly eJournal on standards and their importance </li>
                        <li> The <strong><em><a href="/blog/">Standards Blog</a></em></strong>, a unique examination of the role of standards in society </li>
                      </ul>
                    </div>
                    <p align="left"><strong> <a name="ii6"></a>6. Who created the Standards MetaLibrary? </strong></p>
                    <p align="left"> The Standards MetaLibrary, ConsortiumInfo.org and the Consortium Standards Bulletin were created by Andrew Updegrove, a partner at the Boston law firm of Gesmer Updegrove that has represented more standards development organizations than any other law firm in the country. Gesmer Updegrove has sponsored the site since its inception, and provides hosting and web development services as well as other support. For more about the firm, see: <a href="http://www.gesmer.com">http://www.gesmer.com</a>. The MetaLibrary was created with the assistance of a generous grant from Sun Microsystems, Inc. For more about Sun&rsquo;s commitment to standards, see: <a href="http://www.sun.com/software/standards/overview.html" target="_off">http://www.sun.com/software/standards/overview.html</a></p>
                    <p align="left"><strong> <a name="ii7"></a>7.</strong><strong> What can we look for next at the Standards MetaLibrary? </strong></p>
                    <p align="left">Our work program for 2005 includes the following at this time: </p>
                    <div align="left">
                      <ul>
                        <li> Continuous addition of more articles as they are located </li>
                        <li> Abstracting and linking of legislation, policy papers and other primary resource materials </li>
                        <li> Compilation of a master bibliography, comprising all sources listed in the bibliographies of articles referenced in the Standards MetaLibrary </li>
                        <li> Acting as a host for serious works of authorship </li>
                        <li> Providing Amazon.com links to all in-print books listed in the master bibliography </li>
                      </ul>
                    </div>
