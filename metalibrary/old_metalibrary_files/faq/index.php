                    <p align="left"><strong>I. Using the Standards MetaLibrary </strong></p>
                    <p align="left"><strong> <a name="i1" id="i1"></a>1. How do I search for information in the Standards MetaLibrary? </strong></p>
                    <p align="left">The MetaLibrary has a number of features intended to make it easy and useful to use, beginning with the categories and subcategories that can be found at its home page. You may also use the search window on every page to search the entire MetaLibrary by keyword or author name. When you use this feature, the results will be displayed in the order of frequency of their viewing by site visitors, with the most popular articles towards the top. Over time, this will demonstrate which articles visitors have found to be most useful. When you select a subcategory from the Standards MetaLibrary home page, you may chose to sort the entries either alphabetically or by frequency of viewing. </p>
                    <p align="left"><strong> <a name="i2"></a>2. What citation format does the Standards MetaLibrary use? </strong></p>
                    <p align="left"> All entries are displayed in MLA citation format. </p>
                    <p align="left"><strong> <a name="i3"></a>3. What else should I know? </strong></p>
                    <p align="left">We have a few predictable disclosures and rules you should bear in mind: </p>
                    <div align="left">
                      <ul>
                        <li> All abstracts reproduced at this site and the full text of the articles themselves (which are hosted at other sites) are owned by their respective authors, publishers or other copyright holders. You may not copy or use any of these materials except as permitted by their copyright owners. </li>
                        <li> We have not reviewed any abstract or underlying article for accuracy, currency or originality. As with any other type of library, these materials are presented &ldquo;as is.&rdquo; </li>
                        <li> While we have applied certain standards in considering what material to include in the MetaLibrary, that material includes non-peer reviewed as well as peer reviewed work. Consequently, you should apply your own appropriate standards in selecting which sources to rely upon, or cite, in conducting your research using the Standards MetaLibrary. </li>
