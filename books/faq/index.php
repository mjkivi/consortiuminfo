      <td width="2%" align="left" valign="top"><p align="center"><strong> <br>
        FREQUENTLY ASKED QUESTIONS<br>
      </strong><strong> ABOUT THE <em> STANDARDS BOOKSTORE</em></strong></p>
        <p><strong></strong></p>
        <p><strong> <span class="style11">I. Ordering and Shipping </span></strong></p>
        <p class="style11"><strong> Do you fulfill and ship orders? <br>
        </strong>No, we utilize affiliate relationships with various book publishers and well-known resellers, who fulfill and ship the orders. We comb through their enormous catalogs of books to find only those related to standards and standards-setting, so you don&rsquo;t have to. </p>
        <p class="style11"><strong> Is your site secure?<br>
        </strong>Since we are an affiliate site, we do not get involved with financial transactions. Once you&rsquo;re ready to place your order, you&rsquo;ll be at the site of the publisher or reseller. All of the sites that we affiliate with are well known, established businesses. </p>
        <p class="style11"><strong> How do your prices compare to those of other bookstores?<br>
        </strong>We pass along the largest discount that each affiliated publisher allows affiliate sites like us to offer. </p>
        <p><span class="style11"><strong> What should I do if I cannot find the book I need? <br>
        </strong>If you know of a book on a standard or standard-setting that we do not have listed, please <a href="mailto:bookstore@consortiuminfo.org">let us Know</a></span><span class="style12">. </span><span class="style11"> If we can locate the book, we will add it to the store&rsquo;s list of available books. Please include as much information about the book as possible. </span></p>
        <p class="style11"><strong> How do I return a book? <br>
        </strong>To make a return, or to inquire about order(s) you may have placed, please contact the bookseller through whom you placed the order. </p>
        <p class="style11"><strong> II. About The Standards Bookstore </strong></p>
        <p class="style11"><strong> How is the Standards Bookstore different from other bookstores on the Internet? <br>
        </strong>With a few exceptions, we don&rsquo;t carry any books that other sites don&rsquo;t also carry, but we do make it much easier for you to find what you want. This is because we only carry books on information and communications technology, and have taken the time to index them carefully in a variety of useful ways. For example, you can search: </p>
        <ul>
          <li class="style11"> by application category and subcategory (e.g., Web or security) </li>
          <li class="style11"> by standard (e.g., Web services or Secure XML) </li>
          <li class="style11"> by keyword (e.g., Addressing or RSA) </li>
          <li class="style11"> by title, author, or ISBN number </li>
        </ul>
        <p class="style11"> Each standard/category is defined at the top of the page, to help you find books on exactly the subject you are interested in. And each definition ends with a link to the appropriate Wikipedia entry (the Wikipedia is a great free, volunteer, collaborative encyclopedia project). </p>
        <p class="style11"><strong> Why should I buy books from the Standards Bookstore? <br>
        </strong>The Standards Bookstore helps to support other aspects of the ConsortiumInfo.org site, including the <a href="/bulletins/">Consortium Standards Bulletin</a> and <a href="/news/">news</a> sections, all of which are free and publicly available. As the only resource of its kind on the Internet, ConsortiumInfo.org is a valuable tool for use by those wanting to learn more about standards, those that participate in standard setting, and those that wish to do serious research on standards and standard-setting. By purchasing books through the ConsortiumInfo.org Standards Bookstore, you help to ensure that this great resource will continue to provide a wealth of information for current and future users. </p>
        <p class="style11"><strong> III. About ConsortiumInfo.org </strong></p>
        <p class="style11"><strong> Why does it say <em>ConsortiumInfo.org Standards Bookstore</em>? <br>
        </strong>The Standards Bookstore is hosted by ConsortiumInfo.org, the most detailed and comprehensive resource on (or off) the Internet on the topics of standard-setting and the consortia and other organizations that support this process. Its features include: a <a href="/news/">News Portal</a>, the <a href="/essentialguide/">Essential Guide to Consortia and Standard Setting</a>, an exhaustive <a href="/links/">Standard Setting Organization and Standards List</a> an <a href="/bulletins/">eJournal</a>, and much more. See the <a href="/">Home Page</a> for full details. </p>
        <p class="style11"><strong> What&rsquo;s the deal with ConsortiumInfo.org?<br>
        </strong>ConsortiumInfo.org is non-commercial website owned by Andrew Updegrove, an expert on consortia and standard setting, and is sponsored by Gesmer Updegrove LLP, a Boston law firm that has represented more consortia than any other law firm in the country. The site is a free public resource. To learn more about it, see <a href="/aboutsite/">About this Site</a>. To learn more about Gesmer Updegrove, see <a href="http://www.gesmer.com/practice_areas/consortium.php">http://www.gesmer.com/practice_areas/consortium.php</a> </p>
        <p class="style11"><strong> How can I get the most out of ConsortiumInfo.org?<br>
        </strong>There are a number of ways, but these are some that people like best: </p>
        <ul>
          <li class="style11"> get a <a href="/subscribe/">free subscription</a> to our monthly eJournal, the <a href="/bulletins/">Consortium Standards Bulletin</a> </li>
          <li class="style11"> keep up to date every day on the latest news on standard setting and standards-based products by bookmarking our <a href="/news/">News Portal</a></li>
          <li class="style11"> or you can use our <a href="/rss/">RSS Feed</a> instead </li>
        </ul>
        <p class="style11"><strong> How can I help out? <br>
        </strong>Thanks for asking! Here are some ideas: </p>
        <ul>
          <li> <span class="style11"><a href="mailto:bookstore@consortiuminfo.org">tell us</a> what you think about the Standards Bookstore, and how we can make it better </span></li>
          <li class="style11"> tell your friends about ConsortiumInfo.org </li>
          <li class="style11"> invite them to subscribe to the Consortium Standards Bulletin and take advantage of the other site features </li>
          <li class="style11"> tell them about the Standards Bookstore, and suggest that they make use of it </li>
          <li class="style11"> <a href="mailto:bookstore@consortiuminfo.org">let us know</a> about new consortia or other information you think we might be interested in </li>
        </ul>
