            <li class="unnamed1">Title- This is the headline of the news item</li>
            <li class="unnamed1">Source- This is the source of the story (Examples: CNN.com, The Cover Pages) </li>
            <li class="unnamed1">Display Date- This is how the date will be displayed on the page. You can enter the date in any format here. (Examples: February 20, 2004, 20 February 2004, 2/20/2004).</li>
