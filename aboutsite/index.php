			<p style="margin-top:0px"><strong>A. Why this Site Exists</strong></p>
			<p>Consortia, as well as the formal Standards Development Organizations ("SDOs") that predate them, have an unquestioned place in the creation of valuable technical specifications, standards and reference software. They are also a major source of the verification test suites and compliance testing tools that allow vendors to more cost effectively ensure that their products comply with standards.</p>
			<p>More recently, thousands of open source software projects, some existing only online, and others with the same types of structure, staffing and budgets as consortia, have been created. These organizations create not standards to facilitate the creation of interoperable software, but the software itself. And today, software is the nervous system that drives almost everything, whether it be a computer, a scientific instrument, a cell phone, a consumer electronics device, or just about anything else we use today.</p>
			<p>Together, these diverse organizations &mdash; consortia, SDOs and open source projects &mdash; are the source of much of the technical "DNA" that drives the infrastructure upon which our ever-more complex, technology-based modern world relies. That DNA goes beyond mere standards, and in recognition of this fact, we have coined a new word to describe its elements. That word is "commonalities."</p>
			<p>What is a "commonality?" As we define it, a commonality is:</p>
			<ul class="indent1">
				<li>Whatever tool(s) we need<br /><br /></li>
				<li>That we need to agree on and create<br /><br /></li>
				<li>In order to do what we agree needs to be done</li>
			</ul>
			<p>It is in recognition of the vital role that the organizations that create commonalities play, and in order to facilitate their work, that we have created this site. We refer to these organizations as "Commonality Organizations," or "COs."</p>
			<p><strong>B. The Role of this Site</strong></p>
			<p>More specifically, this site is intended to:</p>
			<ul class="indent1">
				<li>Serve as both a comprehensive as well as an in-depth repository of information regarding COs and their work, enabling the uninitiated as well as the serious researcher to learn more about how these organizations operate and what they create<br /><br /></li>
				<li>Provide a clearinghouse where those who are actively involved in CO activities can share information regarding recent developments, just released standards and other work product, and the launching of new initiatives<br /><br /></li>
				<li>Provide a single point of access where the press, analysts, government and academia can visit to find out what is going on in the world of standards, open source and other commonalities<br /><br /></li>
				<li>Provide a neutral forum in which all participants can air ideas, issues and viewpoints, in order to find common causes and seek needed solutions.</li>
			</ul>
			<p>In short, we are seeking to provide <strong>the definitive portal for the CO community</strong>.</p>
			<p><strong>C. What this Site Contains and Why</strong></p>
			<p>We have chosen to include a variety of features at this site that are not available elsewhere, either at all, or to the same extent. For example, we are have created a public database of succinct, categorized descriptions of all known SDOs, consortia and consortium-model open source projects in the areas of information and communications technology, together with links to their sites and the pages where their standards (or other work product) may be accessed. Our <a href="/links/">Standard Setting Organization and Standards List</a> is by far the most complete list of its kind in existence today, and we are adding to it on a constant basis as new organizations are formed.</p>
			<p>Similarly, while there is a growing body of academic and business literature addressing standard setting, SDOs, consortia and open source projects, we are aware of no site that aggregates this content. Our answer to that need is the <a href="/metalibrary/">Standards MetaLibrary</a>, which includes more than a thousand indexed abstracts of on-line articles, each with a link to the full text (which is in most case available at the host site without charge). The Standards MetaLibrary was created with a generous grant from Sun Microsystems (for more information see the <a href="/metalibrary/learnmore.php">MetaLibrary FAQ</a>).</p>
			<p>Further to the same goal, we have authored detailed essays on the major topics that would be of interest to those that actually participate in the organizations that create standards and other commonalities. These essays are grouped in at <a href="/essentialguide/">The Essential Guide to Standard Setting Organizations and Standards</a>. If all of these essays are read "horizontally" they will provide a complete overview of consortia and standard setting. Where the subject matter is complex, as with laws and regulations, the relevant essay can also be read "vertically" by utilizing the embedded links to access additional levels of detail. These links will take the more serious reader to annotations of the underlying laws and cases, and eventually to the full text of the laws and cases themselves.</p>
			<p>We also function as an aggregator and presenter of current news in an ordered and analytical fashion. Every week, we review hundreds of news stories and press releases, and on a daily basis present the most significant news at our <a href="/news/">Standards News Portal</a> and, on a more selective and detailed basis, at the <a href="/standardsblog/">Standards Blog</a>. Monthly, we provide a full presentation of news, ideas and analysis in our award winning, free, monthly eJournal, the <a href="/bulletins/">Consortium Standards Bulletin</a></p>
			<p><strong>D. What this Site can Become</strong></p>
			<p>Over time, we hope to make this site not only the most comprehensive and useful source of news, information, research material and analysis on the Internet on the topics of COs and commonalities, but a robust forum for discussion and action. By visiting ConsortiumInfo.org on a regular basis, users will be able to stay abreast of all news, trends, issues and concerns in the worlds of standards and other commonalities.</p>
			<p>We believe that the world of consumers generally, and the technical community in particular, can benefit from having a trusted source of information and a forum for the exchange of ideas involving standards and other commonalities. By providing a common, public forum concerned with the creation, theory, use and significance of commonalities, we hope to encourage the study of these important tools as a discipline.</p>
			<p>More importantly, a common forum can help standards and other commonalities become better recognized for what it they are &mdash; essential elements in building the increasingly vital technological infrastructure upon which communications, industry and society itself is based. We hope that this site will help government, the press and other arbiters to better appreciate the importance of standards and other commonalities, and to become more likely to include them in their thinking, planning and reporting.</p>
			<p><strong>E. What You can Do to Help</strong></p>
			<p>We hope that those who visit this site will assist us by offering content, comments, news and information that would be of interest to others that use this site. By visiting the Communications Center, you can:</p>
			<ul class="indent1">
				<li>Add us to your press release distribution list<br /><br /></li>
				<li>Find out how to link your site to ours, and vice versa<br /><br /></li>
				<li>Provide us with information to link to a relevant article or paper that you have written or know about<br /><br /></li>
				<li>Get a free consortium news feed to add to your site<br /><br /></li>
				<li>Make suggestions (or give critical comments) to help us improve this site</li>
			</ul>
			<p>Please regard ConsortiumInfo.org as your site, and help us create as dynamic and useful a resource as possible. But whether you decide to be a passive user or an enthusiastic contributor, we hope that ConsortiumInfo.org serves you well.</p>
			<p><strong>F. The Future of this Site</strong></p>
			<p>Since this site was launched in 2002, a wide variety of changes have already occurred in the world of commonalities. To identify only a few, in these few years:</p>
			<ul class="indent1">
				<li>Open source consortia have proliferated and become an integral part of the world of technology<br /><br /></li>
				<li>The United Nations has begun to concern itself with "Internet Governance"<br /><br /></li>
				<li>Standards disputes have become significant events in international trade<br /><br /></li>
				<li>Standards policies have become important elements in national and regional strategies<br /><br /></li>
				<li>The United States patent system has come under increasing attack for its inability to facilitate, rather than impede, the development and use of standards and open source software</li>
			</ul>
			<p>We have tried to cover each of these developments in detail, as we will continue to cover tomorrow's issues. We hope that you will visit us often as we do.</p>

