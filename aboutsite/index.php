						<p style="padding-bottom:10px;"><strong>What is a consortium?</strong></p>
						<p>A consortium is a group of companies that join together to accomplish a specific goal. In the technology industry, they most frequently join together to set standards to enable the development of new infrastructures (e.g., for the Internet and telecommunications), products (e.g., wireless devices and high definition television), and services (SaaS and Web Services). Most consortia also promote these new technologies, in order to educate potential customers and create demand. There are hundreds of consortia in operation today, supporting thousands of standards.<br /><br /></p>
						<p style="padding-bottom:10px;"><strong>Who joins consortia?</strong></p>
						<p>Thousands of companies are members of anywhere from a single consortium to scores of such organizations. Members also include government agencies, non-profit research institutions, trade associations, major universities and individuals. Membership in a given consortium can cost anywhere from several hundred dollars to several hundred thousand dollars, depending on how ambitious its goals are.</p>
